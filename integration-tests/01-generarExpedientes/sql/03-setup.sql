SELECT CNT.CNT_ID
FROM CNT_CONTRATOS CNT
  LEFT JOIN CPE_CONTRATOS_PERSONAS CPE ON CNT.CNT_ID = CPE.CNT_ID AND CPE.BORRADO = 0
  LEFT JOIN CEX_CONTRATOS_EXPEDIENTE CEX ON CNT.CNT_ID = CEX.CNT_ID
