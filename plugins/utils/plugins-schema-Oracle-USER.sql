CREATE TABLE PLC_PLUGIN_CONFIG
(
  PLC_ID                   NUMBER(16)           NOT NULL,
  PLC_PLUGIN_CODE             VARCHAR2(50 CHAR)    NOT NULL,
  PLC_KEY             VARCHAR2(50 CHAR)    NOT NULL,
  PLC_VALUE             VARCHAR2(50 CHAR)    NOT NULL,
  VERSION                  INTEGER              DEFAULT 0                     NOT NULL,
  USUARIOCREAR             VARCHAR2(10 CHAR)    NOT NULL,
  FECHACREAR               TIMESTAMP(6)         NOT NULL,
  USUARIOMODIFICAR         VARCHAR2(10 CHAR),
  FECHAMODIFICAR           TIMESTAMP(6),
  USUARIOBORRAR            VARCHAR2(10 CHAR),
  FECHABORRAR              TIMESTAMP(6),
  BORRADO              NUMBER(1,0)                    DEFAULT 0 NOT NULL,
  CONSTRAINT PK_PLC_PLUGIN_CONFIG PRIMARY KEY (PLC_ID)
);

CREATE SEQUENCE S_PLC_PLUGIN_CONFIG START WITH 1 NOCYCLE CACHE 100;