CREATE TABLE BATCH_JOB_INSTANCE  (
	JOB_INSTANCE_ID numeric(31)  PRIMARY KEY NOT NULL,  
	VERSION numeric(31),  
	JOB_NAME VARCHAR(100) NOT NULL , 
	JOB_KEY VARCHAR(2500)
) 
go

CREATE TABLE BATCH_JOB_EXECUTION  (
	JOB_EXECUTION_ID numeric(31)  PRIMARY KEY  NOT NULL,
	VERSION numeric(31),  
	JOB_INSTANCE_ID numeric(31) NOT NULL,
	START_TIME TIMESTAMP DEFAULT NULL, 
	END_TIME TIMESTAMP DEFAULT NULL,
	STATUS VARCHAR(10),
	CONTINUABLE CHAR(1),
	EXIT_CODE VARCHAR(20),
	EXIT_MESSAGE CLOB,
	constraint JOB_INSTANCE_EXECUTION_FK foreign key (JOB_INSTANCE_ID)
	references BATCH_JOB_INSTANCE(JOB_INSTANCE_ID)
) 
go
	
CREATE TABLE BATCH_JOB_PARAMS  (
	JOB_INSTANCE_ID numeric(31) NOT NULL ,
    TYPE_CD VARCHAR(6) NOT NULL ,
	KEY_NAME VARCHAR(100) NOT NULL , 
	STRING_VAL VARCHAR(250) , 
	DATE_VAL TIMESTAMP DEFAULT NULL,
	LONG_VAL numeric(31) ,
	DOUBLE_VAL decimal,
	constraint JOB_INSTANCE_PARAMS_FK foreign key (JOB_INSTANCE_ID)
	references BATCH_JOB_INSTANCE(JOB_INSTANCE_ID)
) 
go
	
CREATE TABLE BATCH_STEP_EXECUTION  (
	STEP_EXECUTION_ID numeric(31)  PRIMARY KEY  NOT NULL,
	VERSION numeric(31) NOT NULL,  
	STEP_NAME VARCHAR(100) NOT NULL,
	JOB_EXECUTION_ID numeric(31) NOT NULL,
	START_TIME TIMESTAMP NOT NULL , 
	END_TIME TIMESTAMP DEFAULT NULL,  
	STATUS VARCHAR(10),
	COMMIT_COUNT numeric(31) , 
	ITEM_COUNT numeric(31) , 
	CONTINUABLE CHAR(1),
	EXIT_CODE VARCHAR(20),
	EXIT_MESSAGE CLOB,
	constraint JOB_EXECUTION_STEP_FK foreign key (JOB_EXECUTION_ID)
	references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
) 
go
	
CREATE TABLE BATCH_STEP_EXECUTION_CONTEXT  (
	STEP_EXECUTION_ID numeric(31) NOT NULL ,
    TYPE_CD VARCHAR(6) NOT NULL ,
	KEY_NAME VARCHAR(1000) NOT NULL , 
	STRING_VAL VARCHAR(1000) , 
	DATE_VAL TIMESTAMP DEFAULT NULL ,
	LONG_VAL numeric(31) ,
	DOUBLE_VAL decimal ,
    OBJECT_VAL BLOB,
	constraint STEP_EXECUTION_CONTEXT_FK foreign key (STEP_EXECUTION_ID)
	references BATCH_STEP_EXECUTION(STEP_EXECUTION_ID)
) 
go

