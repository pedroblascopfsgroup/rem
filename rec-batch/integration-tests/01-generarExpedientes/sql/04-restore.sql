
MERGE INTO CEX_CONTRATOS_EXPEDIENTE A
USING CEX_CONTRATOS_EXPEDIENTE_BACK B
ON (A.CEX_ID = B.CEX_ID)
WHEN MATCHED THEN
UPDATE SET A.BORRADO = B.BORRADO
/



