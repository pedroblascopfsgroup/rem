ALTER TABLE ACU_ACUERDO_PROCEDIMIENTOS DROP COLUMN CNT_ID;

CREATE SEQUENCE S_ACC_ACUERDO_CONTRATO;

CREATE TABLE ACC_ACUERDO_CONTRATO (
   ACC_ID                NUMBER(16)                      NOT NULL,        
   ACU_ID                NUMBER(16)                      NOT NULL,
   CNT_ID                NUMBER(16)                      NOT NULL,
   VERSION                      INTEGER               DEFAULT 0 NOT NULL,
   USUARIOCREAR                 VARCHAR2(10 CHAR)               NOT NULL,
   FECHACREAR                   TIMESTAMP(6)                    NOT NULL,
   USUARIOMODIFICAR             VARCHAR2(10 CHAR),
   FECHAMODIFICAR               TIMESTAMP(6),
   USUARIOBORRAR                VARCHAR2(10 CHAR),
   FECHABORRAR                  TIMESTAMP(6),
   BORRADO                      NUMBER(1)             DEFAULT 0 NOT NULL,
   CONSTRAINT PK_ACC_ACUERDO_CONTRATO PRIMARY KEY (ACC_ID),
   CONSTRAINT FK_ACC_ACU FOREIGN KEY (ACU_ID) REFERENCES ACU_ACUERDO_PROCEDIMIENTOS (ACU_ID),
   CONSTRAINT FK_ACC_CNT FOREIGN KEY (CNT_ID) REFERENCES CNT_CONTRATOS (CNT_ID)
);
