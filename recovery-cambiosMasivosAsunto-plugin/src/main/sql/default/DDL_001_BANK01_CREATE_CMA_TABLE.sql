WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= 'BANK01'; -- Configuracion Esquemas
    V_ESQUEMA_M VARCHAR2(25 CHAR):= 'BANKMASTER'; -- Configuracion Esquemas
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
BEGIN	

    DBMS_OUTPUT.PUT_LINE('******** CMA_CAMBIOS_MASIVOS_ASUNTOS ********'); 
    DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.CMA_CAMBIOS_MASIVOS_ASUNTOS... Comprobaciones previas'); 
    V_SQL := 'SELECT COUNT(1) FROM ALL_CONSTRAINTS WHERE TABLE_NAME = ''CMA_CAMBIOS_MASIVOS_ASUNTOS'' and owner = '''||V_ESQUEMA||''' and CONSTRAINT_TYPE = ''P''';
    EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
    -- Si existe la PK
    IF V_NUM_TABLAS = 1 THEN	
      V_MSQL := 'ALTER TABLE '||V_ESQUEMA||'.CMA_CAMBIOS_MASIVOS_ASUNTOS 
                  DROP PRIMARY KEY CASCADE';		
			EXECUTE IMMEDIATE V_MSQL;  
			DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.CMA_CAMBIOS_MASIVOS_ASUNTOS... Claves primarias eliminadas');	
    END IF;
		-- Comprobamos si existe la tabla   
    V_SQL := 'SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = ''CMA_CAMBIOS_MASIVOS_ASUNTOS'' and owner = '''||V_ESQUEMA||'''';
    EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
    -- Si existe la borramos
    IF V_NUM_TABLAS = 1 THEN 
			V_MSQL := 'DROP TABLE '||V_ESQUEMA||'.CMA_CAMBIOS_MASIVOS_ASUNTOS CASCADE CONSTRAINTS';
			EXECUTE IMMEDIATE V_MSQL;
			DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.CMA_CAMBIOS_MASIVOS_ASUNTOS... Tabla borrada');  
    END IF;
    -- Comprobamos si existe la secuencia
		V_SQL := 'SELECT COUNT(1) FROM ALL_SEQUENCES WHERE SEQUENCE_NAME = ''S_CMA_CAMBIOS_MASIVOS_ASUNTOS'' and sequence_owner = '''||V_ESQUEMA||'''';
		EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS; 
		if V_NUM_TABLAS = 1 THEN			
			V_MSQL := 'DROP SEQUENCE '||V_ESQUEMA||'.S_CMA_CAMBIOS_MASIVOS_ASUNTOS';
			EXECUTE IMMEDIATE V_MSQL;
			DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.S_CMA_CAMBIOS_MASIVOS_ASUNTOS... Secuencia eliminada');    
		END IF; 
    DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS... Comprobaciones previas FIN'); 


    --Creamos la tabla y secuencias
		DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS...');
    V_MSQL := 'CREATE SEQUENCE ' || V_ESQUEMA || '.S_CMA_CAMBIOS_MASIVOS_ASUNTOS';
		EXECUTE IMMEDIATE V_MSQL; 
		DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.S_CMA_CAMBIOS_MASIVOS_ASUNTOS... Secuencia creada');
    V_MSQL := 'CREATE TABLE ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS (
    CMA_ID NUMBER(16) NOT NULL
    ,SOL_ID NUMBER(16) NOT NULL
    ,ASU_ID NUMBER(16) NOT NULL
    ,DD_TGE_ID NUMBER(16,0) NOT NULL
    ,USD_ID_ORIGINAL NUMBER(16) NOT NULL
    ,USD_ID_NUEVO NUMBER(16) NOT NULL
    ,FECHA_INICIO TIMESTAMP NOT NULL
    ,FECHA_FIN TIMESTAMP
    ,REASIGNADO NUMBER(1) DEFAULT 0 NOT NULL
    ,VERSION                        INTEGER        DEFAULT 0                     NOT NULL
    ,USUARIOCREAR                   VARCHAR2(10 CHAR) NOT NULL
    ,FECHACREAR                     TIMESTAMP(6)   NOT NULL
    ,USUARIOMODIFICAR               VARCHAR2(10 CHAR)
    ,FECHAMODIFICAR                 TIMESTAMP(6)
    ,USUARIOBORRAR                  VARCHAR2(10 CHAR)
    ,FECHABORRAR                    TIMESTAMP(6)
    ,BORRADO                        NUMBER(1)      DEFAULT 0
					)';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS... Tabla creada');

    V_MSQL := 'CREATE UNIQUE INDEX ' || V_ESQUEMA || '.PK_CMA_CAMBIOS_MASIVOS_ASUNTOS ON ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS
					(CMA_ID)';
	EXECUTE IMMEDIATE V_MSQL;
    V_MSQL := 'ALTER TABLE ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS ADD (
                CONSTRAINT PK_CMA_CAMBIOS_MASIVOS_ASUNTOS PRIMARY KEY (CMA_ID),
				CONSTRAINT FK_CMA_SOL_ID FOREIGN KEY (SOL_ID) REFERENCES ' || V_ESQUEMA_M || '.USU_USUARIOS (USU_ID),
				CONSTRAINT FK_CMA_USU_ID_ORIGINAL FOREIGN KEY (USD_ID_ORIGINAL) REFERENCES ' || V_ESQUEMA || '.USD_USUARIOS_DESPACHOS (USD_ID),
				CONSTRAINT FK_CMA_USU_ID_NUEVO FOREIGN KEY (USD_ID_NUEVO) REFERENCES ' || V_ESQUEMA || '.USD_USUARIOS_DESPACHOS (USD_ID),
				CONSTRAINT FK_CMA_DD_TGE_ID FOREIGN KEY (DD_TGE_ID) REFERENCES ' || V_ESQUEMA_M || '.DD_TGE_TIPO_GESTOR (DD_TGE_ID),
				CONSTRAINT FK_CMA_ASU_ID FOREIGN KEY (ASU_ID) REFERENCES ' || V_ESQUEMA || '.ASU_ASUNTOS (ASU_ID)
                )';						
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.PK_CMA_CAMBIOS_MASIVOS_ASUNTOS... Creando PK');	
    DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.CMA_CAMBIOS_MASIVOS_ASUNTOS... OK');	

--CREATE INDEX CMA_CAMBIOS_MASIVOS_ASU_IDX1 ON CMA_CAMBIOS_MASIVOS_ASUNTOS (CMA_ID);

    -- Si NO existe la COLUMNA, la crea
    DBMS_OUTPUT.PUT_LINE('******** MEJ_REG_REGISTRO ********'); 
    DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.MEJ_REG_REGISTRO... Comprobaciones previas'); 
    V_SQL := 'SELECT COUNT(1) FROM SYS.ALL_TAB_COLUMNS WHERE COLUMN_NAME=''CMA_ID'' AND TABLE_NAME=''MEJ_REG_REGISTRO'' AND OWNER=''' || V_ESQUEMA || '''';
    EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
    IF V_NUM_TABLAS = 0 THEN	
      V_MSQL := 'ALTER TABLE '||V_ESQUEMA||'.MEJ_REG_REGISTRO ADD (CMA_ID NUMBER(16) NULL)';		
			EXECUTE IMMEDIATE V_MSQL;  
			DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.MEJ_REG_REGISTRO... Campo CMA_ID creado');	
    END IF;
    DBMS_OUTPUT.PUT_LINE('******** MEJ_REG_REGISTRO ********'); 

    
    
EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE;   
    
END;
/

EXIT;