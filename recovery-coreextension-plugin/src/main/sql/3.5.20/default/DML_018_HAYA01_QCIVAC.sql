WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	
    V_TEXT1 VARCHAR2(2400 CHAR); -- Vble. auxiliar
    V_ENTIDAD_ID NUMBER(16);

    --Valores en DD_QCI_QCIVAC
    TYPE T_TIPO_QCI IS TABLE OF VARCHAR2(150);
    TYPE T_ARRAY_QCI IS TABLE OF T_TIPO_QCI;
    V_TIPO_QCI T_ARRAY_QCI := T_ARRAY_QCI(
      T_TIPO_QCI('I1', 'ADQ.ALQUILER:IVA SOPORTADO GENERAL DEDUCIBLE', 'ADQ. PARA ALQUILER: IVA SOPORTADO TIPO GENERAL DEDUCIBLE'),
      T_TIPO_QCI('I2', 'ADQ.ALQUILER:IVA SOPORTADO REDUCIDO NO DEDUC', 'ADQ. PARA ALQUILER: IVA SOPORTADO TIPO REDUCIDO NO DEDUCIBLE'),
      T_TIPO_QCI('I3', 'ADQ.ALQUILER:IVA SOPORTADO GENERAL NO DEDUC', 'ADQ. PARA ALQUILER: IVA SOPORTADO TIPO GENERAL NO DEDUCIBLE'),
      T_TIPO_QCI('I4', 'ADQ.ALQUILER:ITP (INMUEBLES NO SIT EN CANARIAS)', 'ADQ. PARA ALQUILER: ITP (INMUEBLES NO SITUADOS EN CANARIAS)'),
      T_TIPO_QCI('I5', 'ADQ.ALQUILER:IGIC SOPORTADO GENERAL DEDUC', 'ADQ. PARA ALQUILER: IGIC SOPORTADO TIPO GENERAL DEDUCCIBLE'),
      T_TIPO_QCI('I6', 'ADQ.ALQUILER:IGIC SOPORTADO TIPO GENERAL NO DEDUC', 'ADQ. PARA ALQUILER: IGIC SOPORTADO TIPO GENERAL NO DEDUCIBLE'),
      T_TIPO_QCI('I7', 'ADQ.ALQUILER:ITP (INMUEBLES SITUADOS EN CANARIAS)', 'ADQ. PARA ALQUILER: ITP (INMUEBLES SITUADOS EN CANARIAS'),
      T_TIPO_QCI('I8', 'ADQ.ALQUILER:IVA SPRTDO SUPERREDUCIDO NO DEDUCIBLE', 'ADQ. PARA ALQUILER: IVA SOPORTADO SUPERREDUCIDO NO DEDUCIBLE'),
      T_TIPO_QCI('N1', 'EXISTENCIAS:IVA SPRTDO TIPO REDUCIDO NO DEDUCIBLE', 'EXISTENCIAS: IVA SOPORTADO TIPO REDUCIDO NO DEDUCIBLE'),
      T_TIPO_QCI('N2', 'EXISTENCIAS:IVA SPRTDO TIPO GENERAL NO DEDUCIBLE', 'EXISTENCIAS: IVA SOPORTADO TIPO GENERAL NO DEDUCIBLE'),
      T_TIPO_QCI('N4', 'EXISTENCIAS:IGIC SPRTDO GENERAL NO DEDUC ', 'EXISTENCIAS: IGIC SOPORTADO TIPO GENERAL NO DEDUCIBLE'),
      T_TIPO_QCI('N5', 'EXISTENCIAS:IVA SPRTDO SUPERREDUCIDO NO DEDUC', 'EXISTENCIAS: IVA SOPORTADO TIPO SUPERREDUCIDO NO DEDUCIBLE'),
      T_TIPO_QCI('X3', 'EXISTENCIAS:IGIC SPRTDO GENERAL DEDUCIBLE ', 'EXISTENCIAS: IGIC SOPORTADO TIPO GENERAL DEDUCIBLE'),
      T_TIPO_QCI('X4', 'EXISTENCIAS:ITP (INMUEBLES SIT EN CANARIAS) ', 'EXISTENCIAS: ITP (INMUEBLES SITUADOS EN CANARIAS)'),
      T_TIPO_QCI('S4', 'EXISTENCIAS:IVA SOPORTADO GENERAL DEDUCIBLE', 'EXISTENCIAS: IVA SOPORTADO TIPO GENERAL DEDUCIBLE'),
      T_TIPO_QCI('S6', 'EXISTENCIAS:ITP (INMUEBLES NO SIT EN CANARIAS', 'EXISTENCIAS: ITP (INMUEBLES NO SITUADOS EN CANARIAS')
      
      
    ); 
    V_TMP_TIPO_QCI T_TIPO_QCI;

BEGIN	
    
    -- LOOP Insertando valores en DD_QCI_QCIVAC -----------------------------------------------------------------
    
    DBMS_OUTPUT.PUT_LINE('[INICIO] '||V_ESQUEMA||'.DD_QCI_QCIVAC... Empezando a insertar datos en el diccionario');
    FOR I IN V_TIPO_QCI.FIRST .. V_TIPO_QCI.LAST
      LOOP
        V_MSQL := 'SELECT '|| V_ESQUEMA ||'.S_DD_QCI_QCIVAC.NEXTVAL FROM DUAL';
        EXECUTE IMMEDIATE V_MSQL INTO V_ENTIDAD_ID;
            V_TMP_TIPO_QCI := V_TIPO_QCI(I);
        --Comprobamos el dato a insertar
        V_SQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.DD_QCI_QCIVAC WHERE DD_QCI_CODIGO = '''||TRIM(V_TMP_TIPO_QCI(1))||'''';
        EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
        IF V_NUM_TABLAS > 0 THEN				
          DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.DD_QCI_QCIVAC... Ya existe el DD_QCI_CODIGO '''|| TRIM(V_TMP_TIPO_QCI(1)) ||'''');
        ELSE
          V_MSQL := 'INSERT INTO '|| V_ESQUEMA ||'.DD_QCI_QCIVAC (' ||
                      'DD_QCI_ID, DD_QCI_CODIGO, DD_QCI_DESCRIPCION, DD_QCI_DESCRIPCION_LARGA, VERSION, USUARIOCREAR, FECHACREAR, BORRADO)' ||
                      'SELECT '|| V_ENTIDAD_ID || ','''||V_TMP_TIPO_QCI(1)||''','''||TRIM(V_TMP_TIPO_QCI(2))||''','''||TRIM(V_TMP_TIPO_QCI(3))||''','||
                      '1, ''FASE_1238'',SYSDATE,0 FROM DUAL';
              DBMS_OUTPUT.PUT_LINE('INSERTANDO: '''||V_TMP_TIPO_QCI(1)||''','''||TRIM(V_TMP_TIPO_QCI(2))||'''');
          EXECUTE IMMEDIATE V_MSQL;
        END IF;
      END LOOP;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('[FIN] '||V_ESQUEMA||'.DD_QCI_QCIVAC... Datos del diccionario insertado');
   

EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;
/
EXIT;