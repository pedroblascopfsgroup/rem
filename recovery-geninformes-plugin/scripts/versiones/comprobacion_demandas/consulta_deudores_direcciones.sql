select PER.PER_DOC_ID, PER.PER_APELLIDO1, PER.PER_APELLIDO2, PER.PER_NOMBRE,
(SELECT DIR.DIR_ID FROM DIR_DIRECCIONES DIR INNER JOIN DIR_PER DPE ON DPE.DIR_ID=DIR.DIR_ID WHERE DPE.PER_ID=PER.PER_ID AND DIR.BORRADO=0 AND ROWNUM=1) "DIR_ID"
from prc_per ppe
inner join per_personas per on ppe.PER_ID=per.PER_ID
where per.BORRADO=0 and
ppe.PRC_ID=126010;


SELECT DIR.DIR_DOMICILIO, DIR.DIR_CODIGO_POSTAL, DIR.DIR_MUNICIPIO, PRV.DD_PRV_DESCRIPCION 
FROM DIR_DIRECCIONES DIR
LEFT JOIN LINMASTER.DD_PRV_PROVINCIA PRV ON PRV.DD_PRV_ID=DIR.DD_PRV_ID
WHERE DIR.DIR_ID IN (16803256, 16516147, 16612808);