package es.pfsgroup.plugin.gestorDocumental.model.documentos;

public class CatalogosDocumentales {
	
	/**
	 * Código de la serie documental del documento
	 */
	private String codSerieDocumental;

	/**
	 * Descripción de la serie documental del documento
	 */
	private String descSerieDocumental;

	/**
	 * Código TDN1 del documento
	 */
	private String codTdn1;

	/**
	 * Descripción TDN1
	 */
	private String descTdn1;

	/**
	 * Código TDN2
	 */
	private String codTdn2;

	/**
	 * Descripción TDN2
	 */
	private String descTdn2;

	public String getCodSerieDocumental() {
		return codSerieDocumental;
	}

	public void setCodSerieDocumental(String codSerieDocumental) {
		this.codSerieDocumental = codSerieDocumental;
	}

	public String getDescSerieDocumental() {
		return descSerieDocumental;
	}

	public void setDescSerieDocumental(String descSerieDocumental) {
		this.descSerieDocumental = descSerieDocumental;
	}

	public String getCodTdn1() {
		return codTdn1;
	}

	public void setCodTdn1(String codTdn1) {
		this.codTdn1 = codTdn1;
	}

	public String getDescTdn1() {
		return descTdn1;
	}

	public void setDescTdn1(String descTdn1) {
		this.descTdn1 = descTdn1;
	}

	public String getCodTdn2() {
		return codTdn2;
	}

	public void setCodTdn2(String codTdn2) {
		this.codTdn2 = codTdn2;
	}

	public String getDescTdn2() {
		return descTdn2;
	}

	public void setDescTdn2(String descTdn2) {
		this.descTdn2 = descTdn2;
	}
	
	

}