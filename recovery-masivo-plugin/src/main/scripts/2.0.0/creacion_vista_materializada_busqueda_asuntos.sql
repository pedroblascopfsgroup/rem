DROP TABLE V_MSV_BUSQUEDA_ASUNTOS;

CREATE MATERIALIZED VIEW V_MSV_BUSQUEDA_ASUNTOS 
BUILD IMMEDIATE
REFRESH COMPLETE NEXT SYSDATE+(24*60)
WITH PRIMARY KEY
ENABLE QUERY REWRITE
AS
(SELECT ASU.ASU_ID "ASU_ID", ASU.ASU_NOMBRE "ASU_NOMBRE", PLA.DD_PLA_DESCRIPCION "PLAZA", JUZ.DD_JUZ_DESCRIPCION "JUZGADO" , PRC.PRC_COD_PROC_EN_JUZGADO "AUTO"
FROM ASU_ASUNTOS ASU
inner join PRC_PROCEDIMIENTOS PRC ON ASU.ASU_ID=PRC.ASU_ID
inner join DD_JUZ_JUZGADOS_PLAZA JUZ ON PRC.DD_JUZ_ID=JUZ.DD_JUZ_ID 
inner join DD_PLA_PLAZAS PLA ON PLA.DD_PLA_ID = JUZ.DD_PLA_ID
WHERE ASU.BORRADO=0 AND ASU.DD_EAS_ID = '03'
AND PRC.BORRADO=0 AND PRC.DD_EPR_ID = '03'
AND JUZ.BORRADO=0
AND PLA.BORRADO=0);