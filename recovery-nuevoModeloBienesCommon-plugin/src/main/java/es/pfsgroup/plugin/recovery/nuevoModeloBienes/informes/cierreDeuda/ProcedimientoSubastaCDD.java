package es.pfsgroup.plugin.recovery.nuevoModeloBienes.informes.cierreDeuda;

public class ProcedimientoSubastaCDD {
 
	private String tipoProcedimiento;
	private String letrado;
	private String juzgado;
	private String principal;
	private String deudaJudicial;
	private String costasLetrado;
	private String costasProcurador;
	private String fechaCelebracionSubasta;
	private String subastaConPostores;

	public String getTipoProcedimiento() {
		return tipoProcedimiento;
	}

	public void setTipoProcedimiento(String tipoProcedimiento) {
		this.tipoProcedimiento = tipoProcedimiento;
	}

	public String getLetrado() {
		return letrado;
	}

	public void setLetrado(String letrado) {
		this.letrado = letrado;
	}

	public String getJuzgado() {
		return juzgado;
	}

	public void setJuzgado(String juzgado) {
		this.juzgado = juzgado;
	}

	public String getPrincipal() {
		return principal;
	}

	public void setPrincipal(String principal) {
		this.principal = principal;
	}

	public String getDeudaJudicial() {
		return deudaJudicial;
	}

	public void setDeudaJudicial(String deudaJudicial) {
		this.deudaJudicial = deudaJudicial;
	}

	public String getCostasLetrado() {
		return costasLetrado;
	}

	public void setCostasLetrado(String costasLetrado) {
		this.costasLetrado = costasLetrado;
	}

	public String getCostasProcurador() {
		return costasProcurador;
	}

	public void setCostasProcurador(String costasProcurador) {
		this.costasProcurador = costasProcurador;
	}

	public String getFechaCelebracionSubasta() {
		return fechaCelebracionSubasta;
	}

	public void setFechaCelebracionSubasta(String fechaCelebracionSubasta) {
		this.fechaCelebracionSubasta = fechaCelebracionSubasta;
	}

	public String getSubastaConPostores() {
		return subastaConPostores;
	}

	public void setSubastaConPostores(String subastaConPostores) {
		this.subastaConPostores = subastaConPostores;
	}

}