package es.pfsgroup.plugin.recovery.nuevoModeloBienes.informes.subastabankia;


public class ContratosBean {

	String operacion;
	String codigoFondo;
	String nombreFondo;
	
	
	public String getOperacion() {
		return operacion;
	}
	public void setOperacion(String operacion) {
		this.operacion = operacion;
	}
	public String getCodigoFondo() {
		return codigoFondo;
	}
	public void setCodigoFondo(String codigoFondo) {
		this.codigoFondo = codigoFondo;
	}
	public String getNombreFondo() {
		return nombreFondo;
	}
	public void setNombreFondo(String nombreFondo) {
		this.nombreFondo = nombreFondo;
	}
		
}
