package es.pfsgroup.plugin.recovery.nuevoModeloBienes.informes.subastabankia;


public class IntervinientesBean {

	String intervencion;
	String nombre;
	String demandado;
	String nif;
	
	public String getIntervencion() {
		return intervencion;
	}
	public void setIntervencion(String intervencion) {
		this.intervencion = intervencion;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDemandado() {
		return demandado;
	}
	public void setDemandado(String demandado) {
		this.demandado = demandado;
	}
	public String getNif() {
		return nif;
	}
	public void setNif(String nif) {
		this.nif = nif;
	}
	
}
