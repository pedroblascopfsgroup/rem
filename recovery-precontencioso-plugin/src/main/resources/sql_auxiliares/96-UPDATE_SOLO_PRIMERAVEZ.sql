-- EJECUTAR SÓLO LA PRIMERA VEZ SOBRE LAS TABLAS 
CREATE TABLE CNV_AUX_ALTA_PRC_BCK AS SELECT * FROM CNV_AUX_ALTA_PRC;
CREATE TABLE CNV_AUX_ALTA_PRC_CNT_BCK AS SELECT * FROM CNV_AUX_ALTA_PRC_CNT;
CREATE TABLE CNV_AUX_ALTA_PRC_PER_BCK AS SELECT * FROM CNV_AUX_ALTA_PRC_PER;

UPDATE CNV_AUX_ALTA_PRC_BCK SET CODIGO_PROCEDIMIENTO_NUSE=CODIGO_PROCEDIMIENTO_NUSE*100, CODIGO_EXPEDIENTE_NUSE=CODIGO_EXPEDIENTE_NUSE*100;
UPDATE CNV_AUX_ALTA_PRC_CNT_BCK SET CODIGO_PROCEDIMIENTO=CODIGO_PROCEDIMIENTO*100;
UPDATE CNV_AUX_ALTA_PRC_PER_BCK SET CODIGO_PROCEDIMIENTO=CODIGO_PROCEDIMIENTO*100;
UPDATE USD_USUARIOS_DESPACHOS SET USD_GESTOR_DEFECTO=1 
	WHERE USU_ID IN (SELECT USU_ID FROM HAYAMASTER.USU_USUARIOS WHERE USU_USERNAME='A171573') AND DES_ID=1219;
COMMIT;

-- COMPROBACIÓN DE DATOS DE PRUEBAS
SELECT 'CNV_AUX_ALTA_PRC_BCK ' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PRC_BCK 
UNION 
SELECT 'CNV_AUX_ALTA_PRC' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PRC
UNION
SELECT 'CNV_AUX_ALTA_PRC_CNT_BCK' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PRC_CNT_BCK 
UNION 
SELECT 'CNV_AUX_ALTA_PRC_CNT' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PRC_CNT
UNION
SELECT 'CNV_AUX_ALTA_PRC_PER_BCK' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PRC_PER_BCK 
UNION 
SELECT 'CNV_AUX_ALTA_PRC_PER' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PRC_PER
UNION 
SELECT 'CNV_AUX_ALTA_PCO' TABLA, COUNT(*) FROM CNV_AUX_ALTA_PCO
ORDER BY TABLA;