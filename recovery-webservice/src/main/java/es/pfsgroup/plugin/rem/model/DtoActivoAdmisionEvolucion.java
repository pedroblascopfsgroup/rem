package es.pfsgroup.plugin.rem.model;

public class DtoActivoAdmisionEvolucion{

	//Datos Registrales Inscripcion
	private String estadoEvolucion;
	private String subestadoEvolucion;
	private String fechaEvolucion;
	private String observacionesEvolucion;
	private String gestorEvolucion;
	
	public String getEstadoEvolucion() {
		return estadoEvolucion;
	}
	public void setEstadoEvolucion(String estadoEvolucion) {
		this.estadoEvolucion = estadoEvolucion;
	}
	public String getSubestadoEvolucion() {
		return subestadoEvolucion;
	}
	public void setSubestadoEvolucion(String subestadoEvolucion) {
		this.subestadoEvolucion = subestadoEvolucion;
	}
	public String getFechaEvolucion() {
		return fechaEvolucion;
	}
	public void setFechaEvolucion(String fechaEvolucion) {
		this.fechaEvolucion = fechaEvolucion;
	}
	public String getGestorEvolucion() {
		return gestorEvolucion;
	}
	public void setGestorEvolucion(String gestorEvolucion) {
		this.gestorEvolucion = gestorEvolucion;
	}
	public String getObservacionesEvolucion() {
		return observacionesEvolucion;
	}
	public void setObservacionesEvolucion(String observacionesEvolucion) {
		this.observacionesEvolucion = observacionesEvolucion;
	}

}
