package es.pfsgroup.plugin.rem.model;

public class DtoHonorarios {

	private String nombreHonorarios;
    private String conceptoHonorarios;
    private String porcentajeHonorarios;
    private String importeHonorarios;
    
	public String getNombreHonorarios() {
		return nombreHonorarios;
	}
	public void setNombreHonorarios(String nombreHonorarios) {
		this.nombreHonorarios = nombreHonorarios;
	}
	public String getConceptoHonorarios() {
		return conceptoHonorarios;
	}
	public void setConceptoHonorarios(String conceptoHonorarios) {
		this.conceptoHonorarios = conceptoHonorarios;
	}
	public String getPorcentajeHonorarios() {
		return porcentajeHonorarios;
	}
	public void setPorcentajeHonorarios(String porcentajeHonorarios) {
		this.porcentajeHonorarios = porcentajeHonorarios;
	}
	public String getImporteHonorarios() {
		return importeHonorarios;
	}
	public void setImporteHonorarios(String importeHonorarios) {
		this.importeHonorarios = importeHonorarios;
	}
	
}
