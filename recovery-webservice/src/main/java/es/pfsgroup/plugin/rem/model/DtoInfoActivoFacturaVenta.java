package es.pfsgroup.plugin.rem.model;

public class DtoInfoActivoFacturaVenta {
	
	private String descPorcActivo;
	private String numRefActivo;
	private String direccionActivo;
	private String infoRegistralActivo;
	
	public String getDescPorcActivo() {
		return descPorcActivo;
	}
	public void setDescPorcActivo(String descPorcActivo) {
		this.descPorcActivo = descPorcActivo;
	}
	public String getNumRefActivo() {
		return numRefActivo;
	}
	public void setNumRefActivo(String numRefActivo) {
		this.numRefActivo = numRefActivo;
	}
	public String getDireccionActivo() {
		return direccionActivo;
	}
	public void setDireccionActivo(String direccionActivo) {
		this.direccionActivo = direccionActivo;
	}
	public String getInfoRegistralActivo() {
		return infoRegistralActivo;
	}
	public void setInfoRegistralActivo(String infoRegistralActivo) {
		this.infoRegistralActivo = infoRegistralActivo;
	}
}
