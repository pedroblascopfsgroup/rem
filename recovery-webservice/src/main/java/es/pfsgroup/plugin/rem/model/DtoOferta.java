package es.pfsgroup.plugin.rem.model;

public class DtoOferta {

	private String numOferta;
	private String numAgrup;
	private String numActivo;
	private String titularOferta;
	private String importeOferta;
	private String fechaOferta;
	private String situacionOferta;
	
	public String getNumOferta() {
		return numOferta;
	}
	public void setNumOferta(String numOferta) {
		this.numOferta = numOferta;
	}
	
	public String getNumAgrup() {
		return numAgrup;
	}
	public void setNumAgrup(String numAgrup) {
		this.numAgrup = numAgrup;
	}
	public String getNumActivo() {
		return numActivo;
	}
	public void setNumActivo(String numActivo) {
		this.numActivo = numActivo;
	}
	public String getTitularOferta() {
		return titularOferta;
	}
	public void setTitularOferta(String titularOferta) {
		this.titularOferta = titularOferta;
	}
	public String getImporteOferta() {
		return importeOferta;
	}
	public void setImporteOferta(String importeOferta) {
		this.importeOferta = importeOferta;
	}
	public String getFechaOferta() {
		return fechaOferta;
	}
	public void setFechaOferta(String fechaOferta) {
		this.fechaOferta = fechaOferta;
	}
	public String getSituacionOferta() {
		return situacionOferta;
	}
	public void setSituacionOferta(String situacionOferta) {
		this.situacionOferta = situacionOferta;
	}
	
}
