package es.pfsgroup.plugin.rem.model;

public class DtoTasacionInforme {

	private String numTasacion;
	private String tipoTasacion;
	private String importeTasacion;
	private String fechaTasacion;
	private String firmaTasacion;
	
	public String getNumTasacion() {
		return numTasacion;
	}
	public void setNumTasacion(String numTasacion) {
		this.numTasacion = numTasacion;
	}
	public String getTipoTasacion() {
		return tipoTasacion;
	}
	public void setTipoTasacion(String tipoTasacion) {
		this.tipoTasacion = tipoTasacion;
	}
	public String getImporteTasacion() {
		return importeTasacion;
	}
	public void setImporteTasacion(String importeTasacion) {
		this.importeTasacion = importeTasacion;
	}
	public String getFechaTasacion() {
		return fechaTasacion;
	}
	public void setFechaTasacion(String fechaTasacion) {
		this.fechaTasacion = fechaTasacion;
	}
	public String getFirmaTasacion() {
		return firmaTasacion;
	}
	public void setFirmaTasacion(String firmaTasacion) {
		this.firmaTasacion = firmaTasacion;
	}
	
}
