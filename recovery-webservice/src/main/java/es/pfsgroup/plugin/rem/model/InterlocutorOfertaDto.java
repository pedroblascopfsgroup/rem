package es.pfsgroup.plugin.rem.model;

public class InterlocutorOfertaDto  {
    private String idPersonaHaya;
    private String funcion;
    private String estoToBC;
    
	public String getIdPersonaHaya() {
		return idPersonaHaya;
	}
	public void setIdPersonaHaya(String idPersonaHaya) {
		this.idPersonaHaya = idPersonaHaya;
	}
	public String getFuncion() {
		return funcion;
	}
	public void setFuncion(String funcion) {
		this.funcion = funcion;
	}
	public String getEstoToBC() {
		return estoToBC;
	}
	public void setEstoToBC(String estoToBC) {
		this.estoToBC = estoToBC;
	}
    
}