	public void setInteger(Integer integer) {
		this.integer = integer;
	}

	public Integer getAnotherInteger() {
		return anotherInteger;
	}

	public void setAnotherInteger(Integer anotherInteger) {
		this.anotherInteger = anotherInteger;
	}

