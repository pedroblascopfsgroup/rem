											,   tabl.table_name AS DICCIONARIO 
											,   comm.comments   AS DESCRIPCION
							FROM
								sys.all_tables tabl
							left join SYS.all_tab_comments comm on comm.table_name = tabl.table_name     
							WHERE
								tabl.table_name in ('PEF_PERFILES','ACT_CMP_COMAUTONOMA_PROVINCIA','ACT_PVE_PROVEEDOR') OR tabl.table_name IN (
						(select table_name from SYS.all_tab_columns where table_name like 'DD_%' and column_name in 
							(select column_name
							from SYS.all_tab_columns 
							where column_name like 'DD_%' 
							and column_name like '%_ID' 
							group by column_name having count(1)>1)
							)) group by tabl.owner , tabl.table_name , comm.comments    
													ORDER BY tabl.table_name ASC;	

