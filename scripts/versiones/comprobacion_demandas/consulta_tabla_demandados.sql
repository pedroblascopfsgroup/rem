SELECT CONCAT(CONCAT(CONCAT(CONCAT(PER.PER_APELLIDO1, ' '), PER.PER_APELLIDO2), ','), PER.PER_NOMBRE) "APELLIDOSNOMBRE",
PER.PER_DOC_ID "DOC_ID",
DIR.DIR_DOMICILIO "DOMICILIO"
FROM PER_PERSONAS PER 
INNER JOIN PRC_PER PPE ON PPE.PER_ID=PER.PER_ID
LEFT JOIN DIR_PER DPE ON DPE.PER_ID=PER.PER_ID
LEFT JOIN DIR_DIRECCIONES DIR ON DIR.DIR_ID=DPE.DIR_ID
WHERE 
PER.BORRADO=0 AND DPE.BORRADO=0 AND DIR.BORRADO=0 AND
PPE.PRC_ID=126010; 
