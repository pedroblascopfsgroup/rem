WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;



DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#';
 V_ESQUEMA_MINIREC VARCHAR2(25 CHAR):= '#ESQUEMA_MINIREC#';-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		      -- Configuracion Esquema Master
 TABLA_1 VARCHAR(30) :='H_VENTA_CARTERA_COMENTARIOS';
 TABLA_2 VARCHAR (30) := 'H_FIN_AUX_PRC_CERRAR_INICIAL';
 err_num NUMBER;
 err_msg VARCHAR2(2048 CHAR); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);
 



BEGIN 

--Validamos si la tabla existe antes de crearla

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA_1;
  

  
  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA_MINIREC||'.'||TABLA_1||' 
   (	
  FECHA_HISTORICO DATE,
	ENTIDAD NUMBER(5, 0) 
, NUMERO_EXP VARCHAR2(20 CHAR) 
, ID_EXPEDIENTE NUMBER(8, 0) 
, ID_ANOTACION NUMBER(16, 0) NOT NULL 
, FECHA_ANOTACION DATE 
, TITULO_ANOTACION VARCHAR2(100 CHAR) 
, CODIGO_ANOTACION VARCHAR2(100 CHAR) 
, EMPLEADO VARCHAR2(10 CHAR) 
, GESTOR VARCHAR2(50 CHAR) 
, ID_PROCEDI NUMBER(12, 0) 
, TEXTO_LIBRE VARCHAR2(2500 CHAR) 
, PRC_ID NUMBER(16, 0) 
, TIPO_TAR_DPR VARCHAR2(10 CHAR) NOT NULL 
   )';
 
 
  IF V_EXISTE = 0 THEN     
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.'||TABLA_1||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA_MINIREC||'.'||TABLA_1||' CASCADE CONSTRAINTS ');
     DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.'||TABLA_1||' BORRADA');  
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.'||TABLA_1||' CREADA');  
    
  END IF;   
  
  
--Validamos si la tabla existe antes de crearla (HISTORICO)

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA_2;
  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA_MINIREC||'.'||TABLA_2||' 
   (	
  FECHA_HISTORICO DATE,
	ASU_ID 			NUMBER(16,0),		   
	PRC_ID 			NUMBER(16,0),		   
	CNT_ID 			NUMBER(16,0)
	
   )';
   
  IF V_EXISTE = 0 THEN     
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.'||TABLA_2||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA_MINIREC||'.'||TABLA_2||' CASCADE CONSTRAINTS ');
     DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.'||TABLA_2||' BORRADA');  
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.'||TABLA_2||' CREADA');  
    
  END IF;   

--Fin crear tabla

--Excepciones
          

EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT