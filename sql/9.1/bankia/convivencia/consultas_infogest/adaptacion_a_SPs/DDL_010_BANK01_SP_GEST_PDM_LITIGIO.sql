SELECT DISTINCT
   CASE WHEN ULT_MOVIMIENTO_CNT.CD_EXPEDIENTE_NUSE IS NOT NULL THEN substr(ULT_MOVIMIENTO_CNT.CD_EXPEDIENTE_NUSE,0,8) ELSE substr(EXPEDIENTE_ASUNTO.CD_EXPEDIENTE_NUSE,0,8) END ID_EXPEDIENTE
