  ,CASE DD_PAS.DD_PAS_CODIGO
    WHEN 'BANKIA' THEN '00000'
    WHEN 'SAREB' THEN '05074'
    ELSE NULL
  END ENTIDAD
  ,TO_NUMBER(ASU.ASU_ID_EXTERNO) ID_PROCEDI
  ,CASE
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%APELACION%' THEN 'APELACION'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%CAMBIARIO%' THEN 'CAMBIARIO'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%CONCURSO%' THEN 'CONCURSO'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%EJECUTIVO%' OR UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%NO JUDICIAL%' THEN 'EJECUTIVO'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%HIPOTECARIO%' THEN 'HIPOTECARIO'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%MONITORIO%' THEN 'MONITORIO'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%ORDINARIO%' THEN 'ORDINARIO'
      WHEN UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%PENAL%' OR UPPER(ULT_TIPO_PROC.DD_TPO_DESCRIPCION) LIKE '%ABREVIADO%' THEN 'PENAL'
