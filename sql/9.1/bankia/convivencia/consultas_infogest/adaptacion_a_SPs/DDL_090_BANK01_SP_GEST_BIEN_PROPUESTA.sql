WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;

CREATE OR REPLACE PROCEDURE GEST_BIEN_PROPUESTA AUTHID CURRENT_USER AS

BEGIN
/* v0.1 */

	INSERT INTO #ESQUEMA_MINIREC#.RCV_GEST_LOG VALUES (#ESQUEMA_MINIREC#.S_RCV_GEST_LOG.NEXTVAL, 'RCV_GEST_BIEN_PROPUESTA', 'INSERT EN MINIREC.RCV_GEST_BIEN_PROPUESTA', 'INICIO', TO_CHAR(SYSTIMESTAMP, 'DD/MM/RR HH24:MI:SS'));

	DBMS_OUTPUT.PUT_LINE('[INFO] La tabla MINIREC.RCV_GEST_BIEN_PROPUESTA va a ser truncada.');

	#ESQUEMA_MINIREC#.OPERACION_DDL.DDL_TABLE('TRUNCATE','RCV_GEST_BIEN_PROPUESTA');

COMMIT;

	DBMS_OUTPUT.PUT_LINE('[INFO] Tabla MINIREC.RCV_GEST_BIEN_PROPUESTA truncada.');

	DBMS_OUTPUT.PUT_LINE('[INFO] Se van a insertar registros en MINIREC.RCV_GEST_BIEN_PROPUESTA.');

INSERT INTO #ESQUEMA_MINIREC#.RCV_GEST_BIEN_PROPUESTA
	SELECT DISTINCT
	  A.ID_PROPUESTA_RCV  
	  ,LOB.BIE_ID          							ID_BIEN_RCV
	FROM #ESQUEMA_MINIREC#.RCV_GEST_PROPUESTA 		A
		,#ESQUEMA#.SUB_SUBASTA 						SUB
		,#ESQUEMA#.BIE_ADJ_ADJUDICACION      		BAD
		,#ESQUEMA#.LOB_LOTE_BIEN             		LOB
		,#ESQUEMA#.LOS_LOTE_SUBASTA          		LOS
	WHERE A.ID_PROPUESTA_RCV = SUB.PRC_ID
		AND SUB.SUB_ID = LOS.SUB_ID
		AND LOS.LOS_ID = LOB.LOS_ID
		AND LOB.BIE_ID = BAD.BIE_ID;

COMMIT;


	DBMS_OUTPUT.PUT_LINE('[INFO] Registros insertados en MINIREC.RCV_GEST_BIEN_PROPUESTA.');

	DBMS_OUTPUT.PUT_LINE('[FIN]: Script ejecutado correctamente');
	
	INSERT INTO #ESQUEMA_MINIREC#.RCV_GEST_LOG VALUES (#ESQUEMA_MINIREC#.S_RCV_GEST_LOG.NEXTVAL, 'RCV_GEST_BIEN_PROPUESTA', 'INSERT EN MINIREC.RCV_GEST_BIEN_PROPUESTA', 'FIN', TO_CHAR(SYSTIMESTAMP, 'DD/MM/RR HH24:MI:SS'));


EXCEPTION
	
	WHEN OTHERS THEN
     
		DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion: '||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
		DBMS_OUTPUT.put_line(SQLERRM);

	ROLLBACK;
	RAISE;          

END GEST_BIEN_PROPUESTA;
/

EXIT;
