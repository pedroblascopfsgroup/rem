WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;

DECLARE

 V_ESQUEMA VARCHAR(25) := 'CMMASTER';
 TABLA VARCHAR(30) := 'DD_LOC_LOCALIDAD';
 ITABLE_SPACE VARCHAR(25) :='IRECOVERYONL8M';
 TABLE_SPACE VARCHAR(25)  :='DRECOVERYONL8M';
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER(1);

BEGIN 

--Validamos si la tabla existe antes de crearla
  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA||'';



  V_MSQL := '  
      CREATE TABLE "'||V_ESQUEMA||'"."'||TABLA||'"   
   (	"DD_PRV_ID" NUMBER(16,0) NOT NULL ENABLE, 
        "DD_LOC_ID" NUMBER(16,0) NOT NULL ENABLE, 
	"DD_LOC_CODIGO" VARCHAR2(20 CHAR) NOT NULL ENABLE, 
	"DD_LOC_DESCRIPCION" VARCHAR2(50 CHAR), 
	"DD_LOC_DESCRIPCION_LARGA" VARCHAR2(250 CHAR), 
	"VERSION" NUMBER(*,0) DEFAULT 0 NOT NULL ENABLE, 
	"USUARIOCREAR" VARCHAR2(10 CHAR) NOT NULL ENABLE, 
	"FECHACREAR" TIMESTAMP (6) NOT NULL ENABLE, 
	"USUARIOMODIFICAR" VARCHAR2(10 CHAR), 
	"FECHAMODIFICAR" TIMESTAMP (6), 
	"USUARIOBORRAR" VARCHAR2(10 CHAR), 
	"FECHABORRAR" TIMESTAMP (6), 
	"BORRADO" NUMBER(1,0) DEFAULT 0 NOT NULL ENABLE, 
	 CONSTRAINT "PK_DD_LOC_LOCALIDAD" PRIMARY KEY ("DD_LOC_ID")
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 NOCOMPRESS LOGGING
  TABLESPACE "'||ITABLE_SPACE||'"  ENABLE
   ) SEGMENT CREATION DEFERRED 
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING
  TABLESPACE "'||ITABLE_SPACE||'" ' ;
     
IF V_EXISTE = 0 THEN   
   EXECUTE IMMEDIATE V_MSQL;
   DBMS_OUTPUT.PUT_LINE(''||TABLA||' CREADA');
else 
   EXECUTE IMMEDIATE('ALTER TABLE "'||V_ESQUEMA||'"."'||TABLA||'"  drop CONSTRAINT UK_DD_LOC_LOCALIDAD ');
END IF;   
     


EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT;  
