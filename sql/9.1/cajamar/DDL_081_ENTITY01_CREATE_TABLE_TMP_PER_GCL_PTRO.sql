WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):=   '#ESQUEMA#'; 			-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		-- Configuracion Esquema Master 
 TABLA1 VARCHAR(30) :='TMP_PER_GCL_PTRO'; 
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL1 VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1):=null;


BEGIN 

--Validamos si la tabla existe antes de crearla
  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA1||'';

 
  IF V_EXISTE = 1 THEN   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA||'.'||TABLA1 );
     DBMS_OUTPUT.PUT_LINE(''||TABLA1||' BORRADA');
  END IF;   
          


  V_MSQL1 := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA1||'  
     (	"TMP_PER_GCL_ID" NUMBER(16,0) NOT NULL ENABLE, 
	"TMP_PER_GCL_FECHA_EXTRACCION" DATE NOT NULL ENABLE, 
	"TMP_PER_GCL_FECHA_DATO" DATE NOT NULL ENABLE, 
	"TMP_PER_GCL_COD_ENTIDAD" NUMBER(4,0) NOT NULL ENABLE, 
	"TMP_PER_GCL_COD_PERSONA" NUMBER(16,0) NOT NULL ENABLE, 
	"TMP_PER_GCL_CODIGO" NUMBER(20,0) NOT NULL ENABLE, 
	"TMP_PER_GCL_TIPO_RELACION" VARCHAR2(4 CHAR) NOT NULL ENABLE, 
	"TMP_PER_GCL_CHAR_EXTRA1" VARCHAR2(50 CHAR), 
	"TMP_PER_GCL_CHAR_EXTRA2" VARCHAR2(50 CHAR), 
	"TMP_PER_GCL_FLAG_EXTRA1" VARCHAR2(1 CHAR), 
	"TMP_PER_GCL_FLAG_EXTRA2" VARCHAR2(1 CHAR), 
	"TMP_PER_GCL_DATE_EXTRA1" DATE, 
	"TMP_PER_GCL_DATE_EXTRA2" DATE, 
	"TMP_PER_GCL_DATE_EXTRA3" DATE, 
	"TMP_PER_GCL_NUM_EXTRA1" NUMBER(14,2), 
	"TMP_PER_GCL_NUM_EXTRA2" NUMBER(14,2), 
	"TMP_PER_GCL_NUM_EXTRA3" NUMBER(14,2), 
	"VERSION" NUMBER(*,0) NOT NULL ENABLE, 
	"USUARIOCREAR" VARCHAR2(10 CHAR) NOT NULL ENABLE, 
	"FECHACREAR" TIMESTAMP (6) NOT NULL ENABLE, 
	"USUARIOMODIFICAR" VARCHAR2(10 CHAR), 
	"FECHAMODIFICAR" TIMESTAMP (6), 
	"USUARIOBORRAR" VARCHAR2(10 CHAR), 
	"FECHABORRAR" TIMESTAMP (6), 
	"BORRADO" NUMBER(1,0) NOT NULL ENABLE
   ) SEGMENT CREATION IMMEDIATE 
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 
 NOCOMPRESS LOGGING
  STORAGE(INITIAL 65536 NEXT 1048576 MINEXTENTS 1 MAXEXTENTS 2147483645
  PCTINCREASE 0 FREELISTS 1 FREELIST GROUPS 1
  BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
';
          

     EXECUTE IMMEDIATE V_MSQL1;
     DBMS_OUTPUT.PUT_LINE(''||TABLA1||' CREADA');

  



EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
