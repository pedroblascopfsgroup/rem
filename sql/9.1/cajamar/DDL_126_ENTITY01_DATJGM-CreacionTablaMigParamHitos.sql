WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; 			-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		-- Configuracion Esquema Master
 TABLA VARCHAR(30) :='MIG_PARAM_HITOS';
 TABLA_HITOS_VAL VARCHAR (30) :='MIG_PARAM_HITOS_VALORES';
 FK_HITOS_VAL VARCHAR (30) := 'FK_A_MIG_PARAM_HITOS';
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048 CHAR); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA;

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
   (	
	MIG_PARAM_HITO_ID NUMBER 		   NOT NULL ENABLE, 
	COD_TIPO_PROCEDIMIENTO VARCHAR2(2 BYTE)    NOT NULL ENABLE, 
	TIPO_PROCEDIMIENTO VARCHAR2(50 BYTE) 	   NOT NULL ENABLE, 
	DD_TPO_CODIGO VARCHAR2(10 BYTE) 	   NOT NULL ENABLE, 
	DD_TPO_DESC VARCHAR2(100 BYTE) 		   NOT NULL ENABLE, 
	COD_HITO_ACTUAL VARCHAR2(2 BYTE) 	   NOT NULL ENABLE, 
	TAP_CODIGO VARCHAR2(100 BYTE), 
	ORDEN NUMBER NOT NULL ENABLE, 
	TAR_TAREA_PENDIENTE VARCHAR2(100 BYTE), 
	FLAG_POR_CADA_BIEN NUMBER(1,0) DEFAULT 0   NOT NULL ENABLE,
	TAR_FINALIZADA     NUMBER(1)               NOT NULL ENABLE, 
	CONSTRAINT "PK_MIG_PARAM_HITOS" PRIMARY KEY ("MIG_PARAM_HITO_ID")
         USING INDEX
         TABLESPACE '||ITABLE_SPACE||' ENABLE
   )';
 

 
 
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA||'.'||TABLA||' CASCADE CONSTRAINTS ');
     DBMS_OUTPUT.PUT_LINE(TABLA||' BORRADA');
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');  
     EXECUTE IMMEDIATE ('ALTER TABLE '||TABLA_HITOS_VAL||' ADD CONSTRAINT '||FK_HITOS_VAL||' FOREIGN KEY ("MIG_PARAM_HITO_ID") 
                         REFERENCES '||V_ESQUEMA||'.'||TABLA||' ("MIG_PARAM_HITO_ID") ENABLE');
     DBMS_OUTPUT.PUT_LINE('CONSTRAINT '||FK_HITOS_VAL||' CREADA');  
    
  END IF;   

--Fin crear tabla

--Excepciones
          

EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT
