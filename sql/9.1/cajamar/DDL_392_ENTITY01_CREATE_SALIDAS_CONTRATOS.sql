WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; 			-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		-- Configuracion Esquema Master
 TABLA VARCHAR(30) :='SALIDAS_CONTRATOS';
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048 CHAR); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla

  SELECT COUNT(*) INTO V_EXISTE
    FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA;

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
               (
         FECHA_DATOS DATE,
         CNT_ID  NUMBER(16,0), 
	 CNT_CONTRATO VARCHAR2(50 CHAR), 
	 SITUACION  VARCHAR2(6 CHAR), 
	 EMPLEADO VARCHAR2(50 CHAR),
         EXP_ID NUMBER(16,0),
	 EXP_DESCRIPCION VARCHAR2(310 CHAR),
	 FECHA_ALTA DATE,
	 FECHA_FIN DATE,
	 COD_LETRADO VARCHAR2(50 CHAR),
         COD_LETRADO_MAIL VARCHAR2(50 CHAR),
	 COD_SOC_EXT VARCHAR2(50 CHAR),
	 COD_SIT_CIRBE VARCHAR2(2 CHAR),
	 COD_SIT_KPIS VARCHAR2(4 CHAR),
	 NUMEROS_EXPEDIENTES NUMBER(16,0),
	 IMPORTE NUMBER(15,2),
	 FECHA_SUBASTA DATE,
	 FECHA_ULT_IMPAGO DATE,
	 FECHA_PRI_IMPAGO DATE,
	 BLOQUEO_JUDICIAL  VARCHAR2(50 CHAR)
	 
               )';
              
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');
  ELSE   
     DBMS_OUTPUT.PUT_LINE(TABLA||' YA EXISTE, NO CREADA');
  END IF;   

--Fin crear tabla

--Excepciones
          
EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT
