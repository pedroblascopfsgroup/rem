    SELECT count(*) INTO V_ENTIDAD_ID
    FROM all_sequences
    WHERE sequence_name = 'S_DD_LOC_LOCALIDAD' and sequence_owner=V_ESQUEMA_M;
    if V_ENTIDAD_ID is not null and V_ENTIDAD_ID = 1 then    
