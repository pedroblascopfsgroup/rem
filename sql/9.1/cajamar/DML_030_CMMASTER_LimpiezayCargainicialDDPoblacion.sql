   END LOOP; --LOOP POBLACION
    COMMIT;
   V_TMP_TIPO_POBLACION:= NULL;
   V_TIPO_POBLACION:= NULL;
  


   DBMS_OUTPUT.PUT_LINE('Script ejecutado correctamente'); 

