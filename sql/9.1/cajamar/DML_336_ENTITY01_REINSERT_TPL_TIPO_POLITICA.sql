		IF V_NUM_TABLAS = 0 THEN		
			V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TPL_TIPO_POLITICA  (TPL_ID, TPL_CODIGO, TEN_ID, TPL_DESCRIPCION, TPL_DESCRIPCION_LARGA, DD_POL_ID, USUARIOCREAR, FECHACREAR) 
