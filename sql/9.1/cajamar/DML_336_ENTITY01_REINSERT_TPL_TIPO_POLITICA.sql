	V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.DD_POL_POLITICAS WHERE DD_POL_CODIGO = ''3'' ';
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM_TABLAS;
	IF V_NUM_TABLAS > 0 THEN	
		V_MSQL := 'SELECT * FROM '||V_ESQUEMA||'.DD_POL_POLITICAS WHERE DD_POL_CODIGO = ''3'' ';
		EXECUTE IMMEDIATE V_MSQL INTO V_POLITICA;
	
