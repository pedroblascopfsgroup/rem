                          ,TAR_TAREA
                          ,TAP_CODIGO
                          ,TEV_ORDEN
                          ,TEV_NOMBRE
                          ,TEV_VALOR)
                   VALUES
                         (v_TEV_ID
                         ,v_TAR_ID
                         ,k.TAR_TAREA_PENDIENTE
                         ,l.TAP_CODIGO
                         ,l.ORDEN
                         ,l.TEV_NOMBRE
