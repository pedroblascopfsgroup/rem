                          TAR_FECHA,
                          TAR_TAREA_FINALIZADA,
                          CD_BIEN
                          ,COD_HITO_ACTUAL
                          ,ULTIMO_HITO)
                 VALUES
                        (v_CONCURSOS.CD_CONCURSO
                        ,v_PRC_ID
                        ,v_PRC_PRC_ID
                        ,v_ORDEN
                        ,k.DD_TPO_CODIGO
                        ,k.DD_TPO_DESC
                        ,k.TAP_CODIGO
                        ,v_TAR_ID
                        ,k.TAR_TAREA_PENDIENTE
                        ,NULL
                        ,v_TAR_FINALIZADA
                        ,NULL
                        ,k.COD_HITO_ACTUAL
