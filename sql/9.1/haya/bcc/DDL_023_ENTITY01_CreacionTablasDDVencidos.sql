WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_SCHEMA_MASTER VARCHAR(25) := '#ESQUEMA_MASTER#';
 V_SCHEMA VARCHAR(25) := '#ESQUEMA#'; 
 TABLA1 VARCHAR(30) :='DD_MAD_MOTIVO_ALTA_DUDOSO'; 
 TABLA2 VARCHAR(30) :='DD_MBD_MOTIVO_BAJA_DUDOSO';  
 TABLA3 VARCHAR(30) :='DD_TVE_TIPO_VENCIDO'; 
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL1 VARCHAR2(8500 CHAR);
 V_MSQL2 VARCHAR2(8500 CHAR);
 V_MSQL3 VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1):=null;


BEGIN 

--Validamos si la tabla existe antes de crearla
  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA1||'';

 
  IF V_EXISTE = 1 THEN   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_SCHEMA||'.'||TABLA1 );
     DBMS_OUTPUT.PUT_LINE(''||TABLA1||' BORRADA');
  END IF;   
          


  V_MSQL1 := 'CREATE TABLE '||V_SCHEMA||'.'||TABLA1||'  
  (	
        DD_MAD_ID NUMBER(16,0) NOT NULL ENABLE, 
        DD_MAD_CODIGO VARCHAR2(20 CHAR) NOT NULL ENABLE, 
        DD_MAD_DESCRIPCION VARCHAR2(50 CHAR), 
        DD_MAD_DESCRIPCION_LARGA VARCHAR2(250 CHAR), 
        VERSION NUMBER(*,0) DEFAULT 0 NOT NULL ENABLE, 
        USUARIOCREAR VARCHAR2(10 CHAR) NOT NULL ENABLE, 
        FECHACREAR TIMESTAMP (6) NOT NULL ENABLE, 
        USUARIOMODIFICAR VARCHAR2(10 CHAR), 
        FECHAMODIFICAR TIMESTAMP (6), 
        USUARIOBORRAR VARCHAR2(10 CHAR), 
        FECHABORRAR TIMESTAMP (6), 
        BORRADO NUMBER(1,0) DEFAULT 0 NOT NULL ENABLE, 
         CONSTRAINT PK_DD_MAD_MOTIVO_ALTA_DUDOSO PRIMARY KEY (DD_MAD_ID)
          USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 NOCOMPRESS LOGGING
          TABLESPACE '||ITABLE_SPACE||'  ENABLE
           ) SEGMENT CREATION DEFERRED 
          PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING  
  ';
          

     EXECUTE IMMEDIATE V_MSQL1;
     DBMS_OUTPUT.PUT_LINE(''||TABLA1||' CREADA');

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA2||'';
     
  IF V_EXISTE = 1 THEN   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_SCHEMA||'.'||TABLA2 );
     DBMS_OUTPUT.PUT_LINE(''||TABLA2||' BORRADA');
  END IF;   
          
     
  
  V_MSQL2 := 'CREATE TABLE '||V_SCHEMA||'.'||TABLA2||'  
  (	
        DD_MBD_ID NUMBER(16,0) NOT NULL ENABLE, 
	DD_MBD_CODIGO VARCHAR2(20 CHAR) NOT NULL ENABLE, 
	DD_MBD_DESCRIPCION VARCHAR2(50 CHAR), 
	DD_MBD_DESCRIPCION_LARGA VARCHAR2(250 CHAR), 
	VERSION NUMBER(*,0) DEFAULT 0 NOT NULL ENABLE, 
	USUARIOCREAR VARCHAR2(10 CHAR) NOT NULL ENABLE, 
	FECHACREAR TIMESTAMP (6) NOT NULL ENABLE, 
	USUARIOMODIFICAR VARCHAR2(10 CHAR), 
	FECHAMODIFICAR TIMESTAMP (6), 
	USUARIOBORRAR VARCHAR2(10 CHAR), 
	FECHABORRAR TIMESTAMP (6), 
	BORRADO NUMBER(1,0) DEFAULT 0 NOT NULL ENABLE, 
	 CONSTRAINT PK_DD_MBD_MOTIVO_BAJA_DUDOSO PRIMARY KEY (DD_MBD_ID)
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 NOCOMPRESS LOGGING
  TABLESPACE '||ITABLE_SPACE||'   ENABLE
   ) SEGMENT CREATION DEFERRED 
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING
       ';
 
 
     EXECUTE IMMEDIATE V_MSQL2;
     DBMS_OUTPUT.PUT_LINE(''||TABLA2||' CREADA');

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA3||'';     
     
  IF V_EXISTE = 1 THEN   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_SCHEMA||'.'||TABLA3 );
     DBMS_OUTPUT.PUT_LINE(''||TABLA3||' BORRADA');
  END IF;   
          
  
    
  V_MSQL3 := 'CREATE TABLE '||V_SCHEMA||'.'||TABLA3||'  
  ( 
        DD_TVE_ID NUMBER(16,0) NOT NULL ENABLE, 
	DD_TVE_CODIGO VARCHAR2(20 CHAR) NOT NULL ENABLE, 
	DD_TVE_DESCRIPCION VARCHAR2(50 CHAR), 
	DD_TVE_DESCRIPCION_LARGA VARCHAR2(250 CHAR), 
