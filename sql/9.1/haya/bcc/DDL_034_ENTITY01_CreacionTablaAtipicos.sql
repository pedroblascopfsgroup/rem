WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR(25) := '#ESQUEMA#';
 TABLA VARCHAR(30) :='TMP_APR_AUX_ATIPICOS';
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla
  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA||'';

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
   (	
        "FECHA_EXTRACCION" DATE NOT NULL ENABLE, 
	"FECHA_DATO" DATE NOT NULL ENABLE, 
	"TIPO_PRODUCTO" VARCHAR2 (5 CHAR) NOT NULL ENABLE , 
	"NUMERO_CONTRATO" NUMBER (17,0) NOT NULL ENABLE,
	"NUMERO_ESPEC" NUMBER (15,0) NOT NULL ENABLE,
	"CODIGO_ATIPICO" NUMBER (20,0) NOT NULL ENABLE,
	"IMPORTE_ATIPICO" NUMBER(14,2) NOT NULL ENABLE,
	"TIPO_ATIPICO" VARCHAR2 (4 CHAR) NOT NULL ENABLE,
	"MOTIVO_ATIPICO" VARCHAR2 (9 CHAR),
	"FECHA_VALOR" DATE NOT NULL,
	"FECHA_MOVIMIENTO" DATE NOT NULL,
	"CHAR_EXTRA_1" VARCHAR2(50 CHAR), 
	"CHAR_EXTRA_2" VARCHAR2(50 CHAR), 
	"FLAG_EXTRA_1" VARCHAR2(1 CHAR), 
	"FLAG_EXTRA_2" VARCHAR2(1 CHAR), 
	"DATE_EXTRA_1" DATE, 
	"DATE_EXTRA_2" DATE, 
	"DATE_EXTRA_3" DATE, 
	"NUM_EXTRA_1" NUMBER(14,2), 
	"NUM_EXTRA_2" NUMBER(14,2),
	"NUM_EXTRA_3" NUMBER(14,2),
	CONSTRAINT '||ITABLE_SPACE||' PRIMARY KEY ("CODIGO_ATIPICO")
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 COMPUTE STATISTICS NOCOMPRESS LOGGING
  STORAGE( INITIAL 65536 NEXT 1048576 MINEXTENTS 1 MAXEXTENTS 2147483645
  PCTINCREASE 0 BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
  TABLESPACE '||ITABLE_SPACE||'  ENABLE
   ) SEGMENT CREATION DEFERRED 
  PCTFREE 10 PCTUSED 0 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING
  STORAGE( INITIAL 65536 NEXT 1048576 MINEXTENTS 1 MAXEXTENTS 2147483645
  PCTINCREASE 0 BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
  ';
 
 
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(''||TABLA||' CREADA');
  ELSE 
     DBMS_OUTPUT.PUT_LINE(''||TABLA||' YA EXISTE Y NO SE CREARA');  
  END IF;   
          


EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT;   
