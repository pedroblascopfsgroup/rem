WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR(25) := '#ESQUEMA#';
 TABLA VARCHAR(30) :='APR_AUX_DSP_DISPOSICIONES';
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla
  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA||'';

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
   (	"DSP_FECHA_EXTRACCION" DATE NOT NULL, 
	"DSP_FECHA_DATO" DATE NOT NULL, 
	"DSP_CODIGO_ENTIDAD" NUMBER(4,0) NOT NULL, 
	"DSP_CODIGO_PROPIETARIO" NUMBER(5,0) NOT NULL, 
	"DD_TIPO_PRODUCTO" VARCHAR2(5 CHAR) NOT NULL, 
	"DSP_NUMERO_CONTRATO" NUMBER(17,0) NOT NULL,
	"DSP_NUMERO_SPEC" NUMBER(15,0) NOT NULL, 
	"DSP_CODIGO_DISPOSICION" NUMBER(20,0) NOT NULL, 
	"DD_TIPO_DISPOSICION" VARCHAR2(4 CHAR) NOT NULL, 
	"DD_SUBTIPO_DISPOSICION" VARCHAR2(4 CHAR), 
	"DD_SITUACION_DISPOSICION" VARCHAR2(4 CHAR) NOT NULL, 
	"DD_MONEDA" NUMBER(4,0) NOT NULL, 
	"DSP_IMPORTE_DISPOSICION" NUMBER(14,2) NOT NULL, 
	"DSP_CAPITAL" NUMBER(14,2) NOT NULL, 
	"DSP_INT_ORDIN_DEVEN" NUMBER(14,2) NOT NULL, 
	"DSP_INT_ORDIN_MORAT" NUMBER(14,2) NOT NULL, 
	"DSP_COMISIONES" NUMBER(14,2) NOT NULL, 
	"DSP_GASTOS_NO_COBRADOS" NUMBER(14,2) NOT NULL, 
	"DSP_IMPUESTOS" NUMBER(14,2), 
	"DSP_FECHA_VENCIMIENTO" DATE NOT NULL, 
	"DSP_CHAR_EXTRA1" VARCHAR2(50 CHAR), 
	"DSP_CHAR_EXTRA2" VARCHAR2(50 CHAR), 
	"DSP_FLAG_EXTRA1" VARCHAR2(1 CHAR), 
	"DSP_FLAG_EXTRA2" VARCHAR2(1 CHAR), 
	"DSP_DATE_EXTRA1" DATE, 
	"DSP_DATE_EXTRA2" DATE, 
	"DSP_DATE_EXTRA3" DATE, 
	"DSP_NUM_EXTRA1" NUMBER(14,2), 
	"DSP_NUM_EXTRA2" NUMBER(14,2), 
	"DSP_NUM_EXTRA3" NUMBER(14,2), 
	 PRIMARY KEY ("DSP_CODIGO_DISPOSICION")
  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 COMPUTE STATISTICS NOCOMPRESS LOGGING
  STORAGE( INITIAL 65536 NEXT 1048576 MINEXTENTS 1
  FREELISTS 1 FREELIST GROUPS 1 BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
  TABLESPACE "DRECOVERYONL8M"  ENABLE
   ) SEGMENT CREATION DEFERRED 
  PCTFREE 10 PCTUSED 0 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING
  STORAGE( INITIAL 131072 NEXT 1048576 MINEXTENTS 1
  FREELISTS 1 FREELIST GROUPS 1 BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
  ';
 
 
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(''||TABLA||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA||'.'||TABLA );
     DBMS_OUTPUT.PUT_LINE(''||TABLA||' BORRADA');
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(''||TABLA||' CREADA');     
  END IF;   
          


EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT;   



