WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; 			-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		-- Configuracion Esquema Master
 TABLA VARCHAR(30) :='MIG_EXPEDIENTES_NOTIFICACIONES';
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048 CHAR); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA;

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
   (
        CD_EXPEDIENTE NUMBER(16,0) NOT NULL ENABLE, 
	CD_PROCEDIMIENTO NUMBER(16,0) NOT NULL ENABLE, 
	CODIGO_ENTIDAD NUMBER(4,0) NOT NULL ENABLE, 
	CODIGO_PROPIETARIO NUMBER(5,0) NOT NULL ENABLE, 
	CODIGO_PERSONA NUMBER(16,0) NOT NULL ENABLE, 
	TIPO_VIA VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	NOMBRE_VIA VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	NUM_DOMICILIO VARCHAR2(10 BYTE), 
	PORTAL VARCHAR2(10 BYTE), 
	PISO VARCHAR2(10 BYTE), 
	ESCALERA VARCHAR2(10 BYTE), 
	PUERTA VARCHAR2(10 BYTE), 
	CODIGO_POSTAL VARCHAR2(10 BYTE) NOT NULL ENABLE, 
	PROVINCIA VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	POBLACION VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	MUNICIPIO VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	PAIS VARCHAR2(100 BYTE) NOT NULL ENABLE, 
	FECHA_ENVIO DATE NOT NULL ENABLE, 
	FECHA_ACUSE_RECIBO DATE, 
	ACUSE_RECIBO VARCHAR2(100 BYTE)
    ) SEGMENT CREATION IMMEDIATE 
    PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 
   NOCOMPRESS LOGGING';
 
 
 
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA||'.'||TABLA||' CASCADE CONSTRAINTS ');
     DBMS_OUTPUT.PUT_LINE(TABLA||' BORRADA');
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');  
    
  END IF;   

--Fin crear tabla

--Excepciones
          

EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT
