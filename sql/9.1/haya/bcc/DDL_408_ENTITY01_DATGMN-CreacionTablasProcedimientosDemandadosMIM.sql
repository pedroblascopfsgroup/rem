WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):=   '#ESQUEMA#'; 			-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		-- Configuracion Esquema Master
 TABLA VARCHAR(30) :='MIG_PROCEDIMS_DEMANDADOS_MIM';
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA||'';

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
   (	
	  CD_PROCEDIMIENTO_DEMANDADO    NUMBER(16)       NOT NULL,
	  CD_PROCEDIMIENTO        	NUMBER(16)       NOT NULL,
	  CODIGO_ENTIDAD          	NUMBER(4)        NOT NULL,
	  CODIGO_PROPIETARIO      	NUMBER(5),
	  CODIGO_PERSONA          	NUMBER(16),
	  REQUERIDO               	VARCHAR2(1 CHAR),
	  FECHA_REQUERIMIENTO     	DATE,
	  OPOSICION               	VARCHAR2(1 CHAR),
	  FECHA_OPOSICION         	DATE,
	  FECHA_VISTA             	DATE,
	  RESULTADO_VISTA         	VARCHAR2(1),
	  FECHA_COMPARECENCIA     	DATE,
	  FECHA_RESOLUCION        	DATE,
	  RESULTADO_RESOLUCION    	VARCHAR2(1),
	  FECHA_CONTESTACION      	DATE,
	  FECHA_AUDIENCIA_PREVIA  	DATE,
	  PROPOSICION             	VARCHAR2(1),
	  FECHA_JUICIO            	DATE,
	  FECHA_PRUEBA            	DATE,
	  ULTIMO_HITO_PERSONA_PRC 	VARCHAR2(100 CHAR), 
	  ID_PERSONA_NUSE         	NUMBER(8),
	  NOMBRE_COMPLETO  	        VARCHAR2(60),
	  CONSTRAINT "PK_ID_PROC_DEMAND_MIM" PRIMARY KEY ("CD_PROCEDIMIENTO_DEMANDADO") 
	  USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 NOCOMPRESS LOGGING
	  TABLESPACE '||ITABLE_SPACE||'  ENABLE
   ) SEGMENT CREATION DEFERRED 
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING';
 
 
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE( TABLA||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA||'.'||TABLA );
     DBMS_OUTPUT.PUT_LINE( TABLA||' BORRADA');
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE( TABLA||' CREADA');     
  END IF;  

--Fin crear tabla

--Excepciones

EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT;   



