WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.  
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

    V_TEXT1 VARCHAR2(2400 CHAR); -- Vble. auxiliar
    
    V_ESQUEMA_MIN VARCHAR2(25 CHAR):= '#ESQUEMA_MINI#'; -- Configuracion Esquema minirec
    V_ESQUEMA_DWH VARCHAR2(25 CHAR):= '#ESQUEMA_DWH#'; -- Configuracion Esquema recovery_bankia_dwh
    V_ESQUEMA_STG VARCHAR2(25 CHAR):= '#ESQUEMA_STG#'; -- Configuracion Esquema recovery_bankia_datastage
    
    V_NOMBRE_VISTA VARCHAR2(30 CHAR):= 'V_LIS_PREPROYECT_EXP';    
    
BEGIN
	
    -- Comprobamos si existe la vista   
    V_SQL := 'SELECT COUNT(1) FROM ALL_VIEWS WHERE VIEW_NAME = '''||V_NOMBRE_VISTA||''' and owner = '''||V_ESQUEMA||'''';
    EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
         
    if V_NUM_TABLAS > 0 
     
     then          
          DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_NOMBRE_VISTA||' Ya Existe');
          V_MSQL := 'DROP VIEW '||V_ESQUEMA||'.'||V_NOMBRE_VISTA;
          EXECUTE IMMEDIATE V_MSQL;
          DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_NOMBRE_VISTA||'... Vista borrada');
    END IF;
        
    EXECUTE IMMEDIATE '
		CREATE OR REPLACE FORCE VIEW '||V_ESQUEMA||'.'||V_NOMBRE_VISTA|| ' AS 
			WITH SUM_CONTRATOS AS (
			  SELECT /*+ MATERIALIZE */ EXP_ID, COUNT(1) NUMERO
			  FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			    INNER JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0
			  WHERE CEX.BORRADO = 0
			  GROUP BY EXP_ID)
			  
			,CNT_MAS_VENCIDO AS (
			  SELECT /*+ MATERIALIZE */ EXP_ID
			    ,CASE WHEN TRUNC(SYSDATE-CNT_FECHA_VENC)  < 0 THEN 0 ELSE TRUNC(SYSDATE-CNT_FECHA_VENC) END DIAS_VENCIDOS
			    ,ROW_NUMBER() OVER (PARTITION BY EXP_ID ORDER BY (SYSDATE-CNT_FECHA_VENC) DESC) NUMERO
			  FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			    INNER JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0
			  WHERE CEX.BORRADO = 0
			)
			
			,NUM_TIPOS_PROPUESTAS_EXP AS (
			  SELECT /*+ MATERIALIZE */ EXP_ID, COUNT(1) TIPOS
			  FROM (
			      SELECT /*+ MATERIALIZE */ DISTINCT CEX.EXP_ID, TEA.DD_TPA_ID
			      FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			      INNER JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0
			        INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			          INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0
			      WHERE CEX.BORRADO = 0
			    )
			  GROUP BY EXP_ID
			)
			
			,TIPOS_PROPUESTAS_EXP AS (
			  SELECT /*+ MATERIALIZE */ DISTINCT CEX.EXP_ID, NUM.TIPOS,
			  CASE WHEN NUM.TIPOS > 1 THEN ''Varios'' ELSE TPA.DD_TPA_DESCRIPCION END PROPUESTA
			  FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			    INNER JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0
			      INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			        INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0
			          INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACUERDO TPA ON TPA.DD_TPA_ID = TEA.DD_TPA_ID AND TPA.BORRADO = 0
			    INNER JOIN NUM_TIPOS_PROPUESTAS_EXP NUM ON NUM.EXP_ID = CEX.EXP_ID
			  WHERE CEX.BORRADO = 0
			)
			
			,REGULACION_EXP AS (
			  SELECT /*+ MATERIALIZE */ CEX.EXP_ID, MAX(ACU.ACU_FECHA_LIMITE) FECHA_REGU
			  FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			    INNER JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0
			      INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			        INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0
			          INNER JOIN '||V_ESQUEMA||'.ACU_ACUERDO_PROCEDIMIENTOS ACU ON ACU.ACU_ID = TEA.ACU_ID AND ACU.BORRADO = 0
			  WHERE CEX.BORRADO = 0
			  GROUP BY CEX.EXP_ID
			)
			
			,NUM_TERMINOS_CNT AS (
			  SELECT /*+ MATERIALIZE */ CNT.CNT_ID, COUNT(1) TERMINOS
			  FROM '||V_ESQUEMA||'.CNT_CONTRATOS CNT
			    INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			      INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0  
			  WHERE CNT.BORRADO = 0
			  GROUP BY CNT.CNT_ID
			)
			
			,ESTADOS_TERMINOS_CNT AS (
			  SELECT /*+ MATERIALIZE */ CNT_ID, COUNT(1) ESTADOS_TERMINOS
			  FROM (
			    SELECT /*+ MATERIALIZE */ DISTINCT CNT.CNT_ID, TEA.DD_EGT_ID
			    FROM '||V_ESQUEMA||'.CNT_CONTRATOS CNT
			      INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			        INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0  
			    WHERE CNT.BORRADO = 0
			  )
			  GROUP BY CNT_ID
			)
			
			,RIESGO_EXP AS (
			  SELECT /*+ MATERIALIZE */ CEX.EXP_ID, NVL(SUM(MOV.MOV_RIESGO),0) RIESGO
			  FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			    INNER JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0
			      INNER JOIN '||V_ESQUEMA||'.MOV_MOVIMIENTOS MOV ON MOV.CNT_ID = CNT.CNT_ID AND MOV.MOV_FECHA_EXTRACCION = CNT.CNT_FECHA_EXTRACCION
			  WHERE CEX.BORRADO = 0
			  GROUP BY CEX.EXP_ID
			)
			
			,IMPORTES_EXP AS (
			  SELECT /*+ MATERIALIZE */ CEX.EXP_ID
			    , NVL(SUM(VEN.VEN_IMPORTE_INICIAL),0) IMPORTE_INICIAL
			    , NVL(SUM(VEN.VEN_IMPORTE_PTE_DIFER),0) IMPORTE_PTE_DIFER
			    , NVL(SUM(VEN.VEN_CAPITAL_VIVO),0) CAPITAL_VIVO
			  FROM '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX
			    INNER JOIN '||V_ESQUEMA||'.VEN_VENCIDOS VEN ON VEN.CNT_ID = CEX.CNT_ID
			  WHERE CEX.BORRADO = 0
			  GROUP BY CEX.EXP_ID
			)
			  
			,TIPOS_TERMINOS_CNT AS (
			  SELECT /*+ MATERIALIZE */ DISTINCT CNT.CNT_ID
			  ,CASE WHEN NTC.TERMINOS > 1 THEN ''Varios'' ELSE TPA.DD_TPA_DESCRIPCION END TERMINO
			  ,CASE WHEN ETC.ESTADOS_TERMINOS > 1 THEN ''Varios'' ELSE DDEGT.DD_EGT_DESCRIPCION END ESTADO_TERMINOS
			   FROM '||V_ESQUEMA||'.CNT_CONTRATOS CNT
			    INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			      INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0 
			        INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACUERDO TPA ON TPA.DD_TPA_ID = TEA.DD_TPA_ID AND TPA.BORRADO = 0
			        LEFT JOIN '||V_ESQUEMA_M||'.DD_EGT_EST_GEST_TERMINO  DDEGT ON DDEGT.DD_EGT_ID = TEA.DD_EGT_ID
			    INNER JOIN NUM_TERMINOS_CNT NTC ON NTC.CNT_ID = CNT.CNT_ID
			    INNER JOIN ESTADOS_TERMINOS_CNT ETC ON ETC.CNT_ID = CNT.CNT_ID
			  WHERE CNT.BORRADO = 0  
			)
			
			,REGULACION_CNT AS (
			  SELECT /*+ MATERIALIZE */ CNT.CNT_ID, MAX(ACU.ACU_FECHA_LIMITE) FECHA_REGU
			    FROM '||V_ESQUEMA||'.CNT_CONTRATOS CNT
			      INNER JOIN '||V_ESQUEMA||'.TEA_CNT ON TEA_CNT.CNT_ID = CNT.CNT_ID AND TEA_CNT.BORRADO = 0
			        INNER JOIN '||V_ESQUEMA||'.TEA_TERMINOS_ACUERDO TEA ON TEA.TEA_ID = TEA_CNT.TEA_ID AND TEA.BORRADO = 0
			          INNER JOIN '||V_ESQUEMA||'.ACU_ACUERDO_PROCEDIMIENTOS ACU ON ACU.ACU_ID = TEA.ACU_ID AND ACU.BORRADO = 0
			  WHERE CNT.BORRADO = 0
			  GROUP BY CNT.CNT_ID
			)
			
			
			SELECT DISTINCT EXP.EXP_ID
			  ,PER.PER_NOM50 TITULAR /* REVISAR: SE USA COMO TITULAR DEL EXPEDIENTE LA PERSONA DE PASE */
			  ,PER.PER_DOC_ID NIF_TITULAR
			  ,PER.PER_TELEFONO_1 TELF_1
			  ,DDEST.DD_EST_DESCRIPCION FASE
			  ,TAR.TAR_FECHA_VENC FECHA_VTO_TAREA
			  ,CASE WHEN SCNT.NUMERO IS NULL THEN 0 ELSE SCNT.NUMERO END NUM_CONTRATOS
			  ,NVL(RIESGO_EXP.RIESGO,0) VOL_RIESGO_EXP
			  ,IMPEXP.IMPORTE_INICIAL IMPORTE_INICIAL_EXP
			  ,(IMPEXP.IMPORTE_INICIAL - IMPEXP.CAPITAL_VIVO) REGULARIZADO_EXP  
			  ,IMPEXP.CAPITAL_VIVO IMPORTE_ACTUAL
			  ,IMPEXP.IMPORTE_PTE_DIFER IMPORTE_PTE_DIFER
			  ,DDTDV.DD_TDV_DESCRIPCION TRAMO_EXP
			  ,VENC_CNT.DIAS_VENCIDOS DIAS_VENCIDOS_EXP
			  ,((SYSDATE + 90) - VENC_CNT.DIAS_VENCIDOS) FECHA_PASE_A_MORA_EXP
			  ,TPE.PROPUESTA
			  ,REGU.FECHA_REGU FECHA_PREV_REGU_EXP
			  ,CNT.CNT_ID
			  ,CNT.CNT_CONTRATO NRO_CONTRATO
			  ,MOV.MOV_RIESGO RIESGO_TOTAL_CNT
			  ,MOV.MOV_DEUDA_IRREGULAR DEUDA_IRREGULAR_CNT
			  ,DDTDVCNT.DD_TDV_DESCRIPCION TRAMO_CNT
			  ,TRUNC(SYSDATE-CNT.CNT_FECHA_VENC) DIAS_VENCIDOS_CNT
			  ,CASE WHEN TRUNC(SYSDATE-CNT.CNT_FECHA_VENC) < 0
			    THEN (SYSDATE+90)
			    ELSE (SYSDATE+90) - TRUNC(SYSDATE-CNT.CNT_FECHA_VENC)
			  END FECHA_PASE_A_MORA_CNT
			  ,TERMINOSCNT.TERMINO PROPUESTA_CNT
			  ,TERMINOSCNT.ESTADO_TERMINOS ESTADO_GESTION_CNT
			  ,REGUCNT.FECHA_REGU FECHA_PREV_REGU_CNT
			FROM '||V_ESQUEMA||'.EXP_EXPEDIENTES EXP
			  LEFT JOIN '||V_ESQUEMA||'.CEX_CONTRATOS_EXPEDIENTE CEX ON CEX.EXP_ID = EXP.EXP_ID AND CEX.BORRADO = 0
			    LEFT JOIN '||V_ESQUEMA||'.CNT_CONTRATOS CNT ON CNT.CNT_ID = CEX.CNT_ID AND CNT.BORRADO = 0 
			      LEFT JOIN '||V_ESQUEMA||'.DD_TDV_TRAMOS_DIAS_VENCIDOS DDTDVCNT ON TRUNC(SYSDATE-CNT.CNT_FECHA_VENC) BETWEEN DDTDVCNT.DD_TDV_DIA_INICIO AND DDTDVCNT.DD_TDV_DIA_FIN
			      LEFT JOIN TIPOS_TERMINOS_CNT TERMINOSCNT ON TERMINOSCNT.CNT_ID = CNT.CNT_ID
			      LEFT JOIN REGULACION_CNT REGUCNT ON REGUCNT.CNT_ID = CNT.CNT_ID
			      LEFT JOIN '||V_ESQUEMA||'.MOV_MOVIMIENTOS MOV ON MOV.CNT_ID = CNT.CNT_ID AND MOV.MOV_FECHA_EXTRACCION = CNT.CNT_FECHA_EXTRACCION
			  LEFT JOIN '||V_ESQUEMA||'.PEX_PERSONAS_EXPEDIENTE PEX ON PEX.EXP_ID = EXP.EXP_ID AND PEX.PEX_PASE = 1 AND PEX.BORRADO = 0
			    LEFT JOIN '||V_ESQUEMA||'.PER_PERSONAS PER ON PER.PER_ID = PEX.PER_ID AND PER.BORRADO = 0    
			  LEFT JOIN '||V_ESQUEMA_M||'.DD_EST_ESTADOS_ITINERARIOS DDEST ON DDEST.DD_EST_ID = EXP.DD_EST_ID AND DDEST.BORRADO = 0    
			  LEFT JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.EXP_ID = EXP.EXP_ID AND TAR.TAR_TAREA_FINALIZADA IS NULL AND TAR.BORRADO = 0
			  LEFT JOIN SUM_CONTRATOS SCNT ON SCNT.EXP_ID = EXP.EXP_ID
			  LEFT JOIN CNT_MAS_VENCIDO VENC_CNT ON VENC_CNT.EXP_ID = EXP.EXP_ID AND VENC_CNT.NUMERO = 1
			    LEFT JOIN '||V_ESQUEMA||'.DD_TDV_TRAMOS_DIAS_VENCIDOS DDTDV ON VENC_CNT.DIAS_VENCIDOS BETWEEN DDTDV.DD_TDV_DIA_INICIO AND DDTDV.DD_TDV_DIA_FIN
			  LEFT JOIN TIPOS_PROPUESTAS_EXP TPE ON TPE.EXP_ID = EXP.EXP_ID
			  LEFT JOIN REGULACION_EXP REGU ON REGU.EXP_ID = EXP.EXP_ID
			  LEFT JOIN RIESGO_EXP ON RIESGO_EXP.EXP_ID = EXP.EXP_ID
			  LEFT JOIN IMPORTES_EXP IMPEXP ON IMPEXP.EXP_ID = EXP.EXP_ID
			WHERE EXP.BORRADO = 0';
			
	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_NOMBRE_VISTA||' Creada');			    


EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecuci贸n:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE;   
END;
/
EXIT;			