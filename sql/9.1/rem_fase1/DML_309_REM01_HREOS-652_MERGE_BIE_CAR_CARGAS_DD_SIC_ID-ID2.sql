--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE
WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM NUMBER(16); 
    V_COUNTER NUMBER(16);
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

    V_TEXT1 VARCHAR2(2400 CHAR); -- Vble. auxiliar 
    V_TEXT_TABLA VARCHAR2(2400 CHAR) := 'BIE_CAR_CARGAS'; -- Vble. auxiliar para almacenar el nombre de la tabla de ref.

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INFO] COMPROBANDO REGISTROS INCORRECTOS DE CARGAS REGISTRALES...');
	
	--COMPROBAMOS CUANTOS REGISTROS EXISTEN QUE NO SE CORRESPONDAN CON LOS QUE DEBE HABER
	V_MSQL := '
	SELECT COUNT(1) FROM (
	SELECT BIE_CAR_ID, DECODE(BIE_CAR_REGISTRAL,0,NULL,BIE_CAR_REGISTRAL) AS BIE_CAR_REGISTRAL, DD_SIC_ID, DECODE(BIE_CAR_ECONOMICA,0,NULL,BIE_CAR_ECONOMICA) AS BIE_CAR_ECONOMICA, DD_SIC_ID2 
	FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA||'
	WHERE (BIE_CAR_REGISTRAL = 1 AND BIE_CAR_ECONOMICA = 0)
	AND (DD_SIC_ID IS NULL AND DD_SIC_ID2 IS NOT NULL)
	)
	'
	;
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM;
	
	IF V_NUM > 0 THEN
	
		DBMS_OUTPUT.PUT_LINE('[INFO] EXISTEN '||V_NUM||' CÓDIGOS DE DICCIONARIO DD_SIC_ID INFORMADOS EN DD_SIC_ID2, VAMOS A CORREGIRLO.');
		
		V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA||' CAR USING
		(
		SELECT BIE_CAR_ID, DECODE(BIE_CAR_REGISTRAL,0,NULL,BIE_CAR_REGISTRAL) AS BIE_CAR_REGISTRAL, DD_SIC_ID, DECODE(BIE_CAR_ECONOMICA,0,NULL,BIE_CAR_ECONOMICA) AS BIE_CAR_ECONOMICA, DD_SIC_ID2 
		FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA||'
		WHERE (BIE_CAR_REGISTRAL = 1 AND BIE_CAR_ECONOMICA = 0)
		AND (DD_SIC_ID IS NULL AND DD_SIC_ID2 IS NOT NULL)
		) TMP
		ON (CAR.BIE_CAR_ID = TMP.BIE_CAR_ID)
		WHEN MATCHED THEN UPDATE SET
		CAR.DD_SIC_ID = TMP.DD_SIC_ID2,
		CAR.DD_SIC_ID2 = NULL,
		CAR.USUARIOMODIFICAR = ''HREOS-652'',
		CAR.FECHAMODIFICAR = SYSDATE
		'
		;
		EXECUTE IMMEDIATE V_MSQL; 
		
		DBMS_OUTPUT.PUT_LINE('[INFO] REGISTROS ACTUALIZADOS.');
		
	ELSE
	
		DBMS_OUTPUT.PUT_LINE('[INFO] NO EXISTEN CÓDIGOS DE DICCIONARIO DD_SIC_ID INFORMADOS EN DD_SIC_ID2.');
		
	END IF;
	
	
	DBMS_OUTPUT.PUT_LINE('[INFO] COMPROBANDO REGISTROS INCORRECTOS DE CARGAS REGISTRALES Y ECONÓMICAS...');
	
	--COMPROBAMOS CUANTOS REGISTROS EXISTEN QUE NO SE CORRESPONDAN CON LOS QUE DEBE HABER
	V_MSQL := '
	SELECT COUNT(1) FROM (
	SELECT BIE_CAR_ID, DECODE(BIE_CAR_REGISTRAL,0,NULL,BIE_CAR_REGISTRAL) AS BIE_CAR_REGISTRAL, DD_SIC_ID, DECODE(BIE_CAR_ECONOMICA,0,NULL,BIE_CAR_ECONOMICA) AS BIE_CAR_ECONOMICA, DD_SIC_ID2 
	FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA||'
	WHERE (BIE_CAR_REGISTRAL = 1 AND BIE_CAR_ECONOMICA = 1)
	AND (DD_SIC_ID IS NULL AND DD_SIC_ID2 IS NOT NULL)
	)
	'
	;
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM;
	
	IF V_NUM > 0 THEN
	
		DBMS_OUTPUT.PUT_LINE('[INFO] EXISTEN '||V_NUM||' CÓDIGOS DE DICCIONARIO DD_SIC_ID INFORMADOS SOLO EN DD_SIC_ID2, VAMOS A CORREGIRLO.');
		
		V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA||' CAR USING
		(
		SELECT BIE_CAR_ID, DECODE(BIE_CAR_REGISTRAL,0,NULL,BIE_CAR_REGISTRAL) AS BIE_CAR_REGISTRAL, DD_SIC_ID, DECODE(BIE_CAR_ECONOMICA,0,NULL,BIE_CAR_ECONOMICA) AS BIE_CAR_ECONOMICA, DD_SIC_ID2 
		FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA||'
		WHERE (BIE_CAR_REGISTRAL = 1 AND BIE_CAR_ECONOMICA = 1)
		AND (DD_SIC_ID IS NULL AND DD_SIC_ID2 IS NOT NULL)
		) TMP
		ON (CAR.BIE_CAR_ID = TMP.BIE_CAR_ID)
		WHEN MATCHED THEN UPDATE SET
		CAR.DD_SIC_ID = TMP.DD_SIC_ID2,
		CAR.USUARIOMODIFICAR = ''HREOS-652'',
		CAR.FECHAMODIFICAR = SYSDATE
		'
		;
		EXECUTE IMMEDIATE V_MSQL; 
		
		DBMS_OUTPUT.PUT_LINE('[INFO] REGISTROS ACTUALIZADOS.');
		
	ELSE
	
		DBMS_OUTPUT.PUT_LINE('[INFO] NO EXISTEN CÓDIGOS DE DICCIONARIO DD_SIC_ID INFORMADOS EN DD_SIC_ID2.');
		
	END IF;
	
		DBMS_OUTPUT.PUT_LINE('[INFO] NO HACEMOS LA COMPROBACIÓN PARA LAS CARGAS ECONÓMICAS (DD_SIC_ID2) PORQUE ESTE CAMPO ESTÁ YA INFORMADO.');
	
	COMMIT;
	
	
EXCEPTION

     WHEN OTHERS THEN
          err_num := SQLCODE;
          err_msg := SQLERRM;

          DBMS_OUTPUT.PUT_LINE('KO no modificada');
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(err_msg);

          ROLLBACK;
          RAISE;          

END;

/

EXIT
