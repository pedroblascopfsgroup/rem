--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

	TABLE_COUNT NUMBER(10,0) := 0;
	V_ESQUEMA VARCHAR2(10 CHAR) := '#ESQUEMA#';
	V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := '#ESQUEMA_MASTER#';
	V_TABLA VARCHAR2(30 CHAR) := 'ACT_AJD_ADJJUDICIAL';
	V_NUM NUMBER(10,0) := 0;

BEGIN
  
  DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZANDO DD_EDJ_ID SOBRE LOS ACTIVOS AFECTADOS.');
	
	EXECUTE IMMEDIATE '
	UPDATE '||V_ESQUEMA||'.'||V_TABLA||' ADJ
	SET DD_EDJ_ID = 1,
	USUARIOMODIFICAR = ''HREOS-302'',
	FECHAMODIFICAR = SYSDATE
	WHERE DD_EDJ_ID = 2
	AND USUARIOMODIFICAR IS NULL
  AND EXISTS (
    SELECT 1 FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT WHERE ACT.ACT_ID = ADJ.ACT_ID AND ACT.DD_CRA_ID = (SELECT DD_CRA_ID FROM '||V_ESQUEMA||'.DD_CRA_CARTERA WHERE DD_CRA_CODIGO = ''02'')
  )
	'
	;
  
  DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' updateada. '||SQL%ROWCOUNT||' Filas. DD_EDJ_ID = 2 POR EL 1');
  
	COMMIT;
  
	EXECUTE IMMEDIATE('ANALYZE TABLE '||V_ESQUEMA||'.'||V_TABLA||' COMPUTE STATISTICS');
  
	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' ANALIZADA.');
	
	EXECUTE IMMEDIATE '
	UPDATE '||V_ESQUEMA||'.'||V_TABLA||' ADJ
	SET DD_EDJ_ID = 2,
	USUARIOMODIFICAR = ''HREOS-302'',
	FECHAMODIFICAR = SYSDATE
	WHERE DD_EDJ_ID = 1
	AND USUARIOMODIFICAR IS NULL
  AND EXISTS (
    SELECT 1 FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT WHERE ACT.ACT_ID = ADJ.ACT_ID AND ACT.DD_CRA_ID = (SELECT DD_CRA_ID FROM '||V_ESQUEMA||'.DD_CRA_CARTERA WHERE DD_CRA_CODIGO = ''02'')
  )
	'
	;
  
  DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' updateada. '||SQL%ROWCOUNT||' Filas. DD_EDJ_ID = 1 POR EL 2');
  
	COMMIT;
  
	EXECUTE IMMEDIATE('ANALYZE TABLE '||V_ESQUEMA||'.'||V_TABLA||' COMPUTE STATISTICS');
  
	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' ANALIZADA.');
  
EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
