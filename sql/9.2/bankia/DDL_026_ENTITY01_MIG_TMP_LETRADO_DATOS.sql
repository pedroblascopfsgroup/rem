WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    
    V_NUM NUMBER; 
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
BEGIN
	V_SQL := 'SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = ''MIG_TMP_LETRADO_DATOS'' AND OWNER='''||V_ESQUEMA||'''';
	EXECUTE IMMEDIATE V_SQL INTO V_NUM;
	
	IF V_NUM=0 THEN
  		DBMS_OUTPUT.PUT_LINE('CREAMOS TABLA '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS');

EXECUTE IMMEDIATE'
CREATE TABLE '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS
(
ANAGRAMA VARCHAR2 (200 CHAR),
DES_PERSONA_CONTACTO VARCHAR2 (200 CHAR),
CONTRATO_VIGOR VARCHAR2 (20 CHAR),
SERV_INTEGRAL VARCHAR2 (2 CHAR),
FECHA_SERV_INTEGRAL DATE,
AMBITO VARCHAR2 (40 CHAR),
CLASIF_CONCURSOS VARCHAR2 (2 CHAR),
CLASIF_PERFIL VARCHAR2 (1 CHAR),
REL_BANKIA VARCHAR2 (40 CHAR)
)';
		DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS CREADA');

ELSE
		DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS BORRADA');
EXECUTE IMMEDIATE'
DROP TABLE '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS';

		DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS CREADA');
EXECUTE IMMEDIATE'
CREATE TABLE '||V_ESQUEMA||'.MIG_TMP_LETRADO_DATOS
(
ANAGRAMA VARCHAR2 (200 CHAR),
DES_PERSONA_CONTACTO VARCHAR2 (200 CHAR),
CONTRATO_VIGOR VARCHAR2 (20 CHAR),
SERV_INTEGRAL VARCHAR2 (2 CHAR),
FECHA_SERV_INTEGRAL DATE,
AMBITO VARCHAR2 (40 CHAR),
CLASIF_CONCURSOS VARCHAR2 (2 CHAR),
CLASIF_PERFIL VARCHAR2 (1 CHAR),
REL_BANKIA VARCHAR2 (40 CHAR)
)';

	END IF;
	
    COMMIT;
	
EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE;   
    
END;
/

EXIT;




