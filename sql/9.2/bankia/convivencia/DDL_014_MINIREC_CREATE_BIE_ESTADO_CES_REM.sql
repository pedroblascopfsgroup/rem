WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_ESQUEMA_MINIREC VARCHAR2(25 CHAR):= '#ESQUEMA_MINIREC#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    
    V_NUM NUMBER; 
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
BEGIN
	V_SQL := 'SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = ''BIEN_ESTADO_CESION_REMATE'' AND OWNER='''||V_ESQUEMA_MINIREC||'''';
	EXECUTE IMMEDIATE V_SQL INTO V_NUM;
	
	IF V_NUM=0 THEN
  		DBMS_OUTPUT.PUT_LINE('CREAMOS TABLA '||V_ESQUEMA_MINIREC||'.BIEN_ESTADO_CESION_REMATE');

EXECUTE IMMEDIATE'
CREATE TABLE '||V_ESQUEMA_MINIREC||'.BIEN_ESTADO_CESION_REMATE
(
CODIGO_PROPIETARIO NUMBER(5),
TIPO_PRODUCTO VARCHAR2(5 CHAR),
NUMERO_CONTRATO NUMBER(17),
NUMERO_ESPEC NUMBER(15),
FECHA_FLUJO NUMBER(8),
COD_TITUL_SIRVA VARCHAR(2 CHAR),
FECHA_RESUL_CES_REM NUMBER(8),
NUMERO_ACTIVO_ADJUDICADO  VARCHAR2(50),
TIPO_PROCEDIMIENTO VARCHAR2 (16 CHAR),
NUM_FINCA_REGISTRAL VARCHAR2 (9 CHAR),
RESUL_CES_REM VARCHAR2(1 CHAR),
FECHA_BAJA_CES_REM NUMBER(8),
CONSTRAINT "PK_BIEN_CESION_REMATE" PRIMARY KEY (CODIGO_PROPIETARIO,
    TIPO_PRODUCTO,
    NUMERO_CONTRATO,
    NUMERO_ESPEC)
)';
		DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.BIEN_ESTADO_CESION_REMATE CREADA');

ELSE
		DBMS_OUTPUT.PUT_LINE('TABLA '||V_ESQUEMA_MINIREC||'.BIEN_ESTADO_CESION_REMATE YA EXISTE');
	END IF;
	
    COMMIT;
	
EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE;   
    
END;
/

EXIT;




