	-------------------------------
	-------DAR AL PERFIL ADMINISTRADOR PERMISO PARA FUNCION MANTENIMIENTO CATEGORIAS
	-------[FIN]
	----------------------------------------------------------------
	


	-------------------------------
	-------UPDATEAR PEN_PARAM_ENTIDAD PARA PEN_PARAM = codigoTipoDespachoLetradoValido
	-------[INICIO]
	----------------------------------------------------------------
	V_SQL := 'SELECT COUNT (1) FROM '||V_ESQUEMA||'.PEN_PARAM_ENTIDAD WHERE PEN_PARAM = ''codigoTipoDespachoLetradoValido''';
	EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
	
	IF V_NUM_TABLAS > 0 THEN
    	V_MSQL := 'UPDATE '||V_ESQUEMA||'.PEN_PARAM_ENTIDAD SET PEN_VALOR=''D-CJ-LETR'' where PEN_PARAM = ''codigoTipoDespachoLetradoValido''';
    	DBMS_OUTPUT.PUT_LINE('EJECUTAMOS UPDATE DE PEN_PARAM: '||V_MSQL);		
    	EXECUTE IMMEDIATE V_MSQL;
    END IF;
    -------------------------------
	-------UPDATEAR PEN_PARAM_ENTIDAD PARA PEN_PARAM = codigoTipoDespachoLetradoValido
	-------[FIN]
	----------------------------------------------------------------

	-------------------------------
	-------UPDATEAR CTG_CATEGORIZACIONES PARA CTG_CODIGO = CDD
	-------[INICIO]
	----------------------------------------------------------------
	V_SQL := 'SELECT COUNT (1) FROM '||V_ESQUEMA||'.CTG_CATEGORIZACIONES WHERE CTG_CODIGO = ''CDD''';
	EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;

	IF V_NUM_TABLAS > 0 THEN

		V_SQL := 'SELECT COUNT (1) FROM '||V_ESQUEMA||'.DES_DESPACHO_EXTERNO WHERE DES_CODIGO = ''PROYFORMS''';
		EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS2;

		IF V_NUM_TABLAS2 > 0 THEN
			V_SQL := 'SELECT DES_ID FROM '||V_ESQUEMA||'.DES_DESPACHO_EXTERNO WHERE DES_CODIGO = ''PROYFORMS''';
