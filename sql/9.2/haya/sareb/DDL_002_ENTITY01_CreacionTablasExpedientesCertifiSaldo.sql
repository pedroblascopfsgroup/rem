WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;


DECLARE

 V_ESQUEMA VARCHAR2(25 CHAR):=   '#ESQUEMA#'; 			-- Configuracion Esquema
 V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; 		-- Configuracion Esquema Master
 ITABLE_SPACE VARCHAR(25) :='#TABLESPACE_INDEX#';
 TABLA VARCHAR(30) :='MIG_EXPEDIENTES_CERTIFI_SALDO';
 err_num NUMBER;
 err_msg VARCHAR2(2048); 
 V_MSQL VARCHAR2(8500 CHAR);
 V_EXISTE NUMBER (1);


BEGIN 

--Validamos si la tabla existe antes de crearla

  SELECT COUNT(*) INTO V_EXISTE
  FROM ALL_TABLES
  WHERE TABLE_NAME = ''||TABLA;

  
  V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||TABLA||' 
   (	
	 MIG_ID_EXP_CERTIF_SALDO NUMBER(16,0) , 
	 CD_EXPEDIENTE  NUMBER(16,0)          , 
	 CODIGO_ENTIDAD NUMBER(4,0)           ,
	 CODIGO_PROPIETARIO NUMBER(5,0)       ,
	 TIPO_PRODUCTO VARCHAR2(5 CHAR)       ,
	 NUMERO_CONTRATO NUMBER(17,0)         ,
	 NUMERO_ESPEC NUMBER(15,0)            ,
	 CD_NOTARIO VARCHAR2(20)              ,
	 FECHA_ENVIO_NOTARIO DATE             ,
	 FECHA_RECEPCION_RESPUESTA DATE,
	 RESPUESTA VARCHAR2(20),
	 DISPONIBLE_GESDOC NUMBER(1,0),
	 IMPORTE_LIQUIDACION NUMBER(14,2)     ,
	 FECHA_LIQUIDACION DATE               ,
	 CONSTRAINT "PK_MIG_EXP_CERTIF_SALDO" PRIMARY KEY ("MIG_ID_EXP_CERTIF_SALDO")
	 USING INDEX PCTFREE 10 INITRANS 2 MAXTRANS 255 NOCOMPRESS LOGGING
	 TABLESPACE '||ITABLE_SPACE||'  ENABLE
   ) SEGMENT CREATION DEFERRED 
  PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 NOCOMPRESS LOGGING';
 
 
  IF V_EXISTE = 0 THEN   
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');
  ELSE   
     EXECUTE IMMEDIATE ('DROP TABLE '||V_ESQUEMA||'.'||TABLA );
     DBMS_OUTPUT.PUT_LINE(TABLA||' BORRADA');
     EXECUTE IMMEDIATE V_MSQL;
     DBMS_OUTPUT.PUT_LINE(TABLA||' CREADA');     
  END IF;   
          
--Fin crear tabla

--Excepciones

EXCEPTION
WHEN OTHERS THEN  
  err_num := SQLCODE;
  err_msg := SQLERRM;

  DBMS_OUTPUT.put_line('Error:'||TO_CHAR(err_num));
  DBMS_OUTPUT.put_line(err_msg);
  
  ROLLBACK;
  RAISE;
END;
/
EXIT;
