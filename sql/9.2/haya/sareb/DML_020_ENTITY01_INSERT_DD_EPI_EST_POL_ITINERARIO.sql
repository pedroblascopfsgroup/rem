	  			V_MSQL := 'SELECT '|| V_ESQUEMA_M ||'.S_DD_EPI_EST_POL_ITINERARIO.NEXTVAL FROM DUAL';
	          	EXECUTE IMMEDIATE V_MSQL INTO V_ENTIDAD_ID;
	  				DBMS_OUTPUT.PUT_LINE('INSERT INTO '|| V_ESQUEMA_M ||'.DD_EPI_EST_POL_ITINERARIO (DD_EPI_ID, DD_EST_ID, DD_EPI_CODIGO, DD_EPI_DESCRIPCION, VERSION, USUARIOCREAR, FECHACREAR, BORRADO ) VALUES ('||V_ENTIDAD_ID||','||V_DD_EST_ID||' ,''ENSAN'',0,''PRODUCTO-1123'',SYSDATE,0)');
