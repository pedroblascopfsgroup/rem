MERGE INTO HAYA01.DPR_DECISIONES_PROCEDIMIENTOS DPR
USING(
SELECT DPR.DPR_ID FROM HAYA01.ASU_ASUNTOS ASU
JOIN HAYA01.PRC_PROCEDIMIENTOS PRC ON PRC.ASU_ID = ASU.ASU_ID
JOIN HAYA01.DPR_DECISIONES_PROCEDIMIENTOS DPR ON PRC.PRC_ID = DPR.PRC_ID 
WHERE  PRC.PRC_ID=1000000000014514
) TMP ON (TMP.DPR_ID=DPR.DPR_ID)
WHEN MATCHED THEN UPDATE SET DPR.DD_EDE_ID=(SELECT DD_EDE_ID FROM HAYAMASTER.DD_EDE_ESTADOS_DECISION WHERE DD_EDE_CODIGO = '01');