--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA VARCHAR2(20 CHAR) := '#ESQUEMA#';
V_TABLA VARCHAR2(40 CHAR) := '';

BEGIN

	/***** APR_AUX_CNS_CENTROS_SUPERIOR *****/

	V_TABLA := 'APR_AUX_CNS_CENTROS_SUPERIOR';
	
	SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA||'';

	IF TABLE_COUNT > 0 THEN
		DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
		EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA||'.'||V_TABLA||'';
	END IF;

	EXECUTE IMMEDIATE 'CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||'(
		 COD_ENTIDAD_GRUPO	   						NUMBER(5,0)                    
		,CLAVE_CENTRO								NUMBER(5,0)                
		,SUBSECCION_CENTRO							VARCHAR2(2 CHAR)
		,NOMBRE_LARGO_CENTRO						VARCHAR2(45 CHAR)
		,DOMICILIO_COMPACTADO_1						VARCHAR2(60 CHAR)
		,CODIGO_POSTAL								NUMBER(5,0)
		,CODIGO_PROVINCIA							NUMBER(2,0)
		,CODIGO_MUNICIPIO							NUMBER(9,0)
		,CLAVE_CENTRO_SUPERIOR						NUMBER(5,0)
	)';

	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' CREADA');  
	
	/***** APR_AUX_CNS_REJECTS *****/

	V_TABLA := 'APR_AUX_CNS_REJECTS';
	
	SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA||'';

	IF TABLE_COUNT > 0 THEN
		DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
		EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA||'.'||V_TABLA||'';
	END IF;

	EXECUTE IMMEDIATE 'CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||'(
		ERRORCODE	VARCHAR2(255 CHAR),
		ERRORMESSAGE	VARCHAR2(512 CHAR),
		ROWREJECTED	VARCHAR2(2048 CHAR)
	)';

	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' CREADA');  
	
	/***** ACT_CNS_CENTROS_SUPERIOR *****/

	V_TABLA := 'ACT_CNS_CENTROS_SUPERIOR';
	
	SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA||'';

	IF TABLE_COUNT = 0 THEN
		EXECUTE IMMEDIATE 'CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||'(
			 COD_ENTIDAD_GRUPO	   						NUMBER(5,0)                    
			,CLAVE_CENTRO								NUMBER(5,0)                
			,SUBSECCION_CENTRO							VARCHAR2(2 CHAR)
			,NOMBRE_LARGO_CENTRO						VARCHAR2(45 CHAR)
			,DOMICILIO_COMPACTADO_1						VARCHAR2(60 CHAR)
			,CODIGO_POSTAL								NUMBER(5,0)
			,CODIGO_PROVINCIA							NUMBER(2,0)
			,CODIGO_MUNICIPIO							NUMBER(9,0)
			,CLAVE_CENTRO_SUPERIOR						NUMBER(5,0)
			,VERSION 									NUMBER(38,0) DEFAULT 0
			,USUARIOCREAR								VARCHAR2(50 CHAR)
			,FECHACREAR									TIMESTAMP(6)
			,USUARIOMODIFICAR							VARCHAR2(50 CHAR) DEFAULT NULL
			,FECHAMODIFICAR								TIMESTAMP(6) DEFAULT NULL
			,USUARIOBORRAR								VARCHAR2(50 CHAR) DEFAULT NULL
			,FECHABORRAR								TIMESTAMP(6) DEFAULT NULL
			,BORRADO									NUMBER(1,0) DEFAULT 0
		)';
		DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' CREADA');  
	ELSE 
		DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA||'.'||V_TABLA||' YA EXISTENTE. REVISAR SI LA DEFINICIÓN ES LA CORRECTA.');
	END IF;
	
EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;