WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE

	V_MSQL VARCHAR2(32000 CHAR); 
	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; 
	V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#';
	V_SQL VARCHAR2(4000 CHAR);
	V_NUM NUMBER(16);
	ERR_NUM NUMBER(25);  
	ERR_MSG VARCHAR2(1024 CHAR); 

	


BEGIN	

	DBMS_OUTPUT.PUT_LINE('[INFO] INICIO DEL PROCESO.');
	
	V_MSQL := '
	SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = ''APR_AUX_BIE_ADN'' AND OWNER = '''||V_ESQUEMA||'''
	'
	;
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM;
	
	IF V_NUM != 0 THEN
	
		DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA APR_AUX_BIE_ADN YA EXISTE.');
		
		
		
	ELSE
	
		DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA APR_AUX_BIE_ADN NO EXISTE. SE CREARÁ.');
		


	EXECUTE IMMEDIATE '
	CREATE TABLE APR_AUX_BIE_ADN (
		
	BIE_ID NUMBER(16,0), 
	BIE_ENTIDAD_ID VARCHAR2(20 CHAR),
	FECHA_TITULO DATE, 
	FECHA_FIRMA_TITULO DATE, 
	VALOR_ADQUISICION NUMBER(16,2), 
	TRAMITADOR_TITULO VARCHAR2(250 CHAR), 
	NUM_REFERENCIA VARCHAR2(250 CHAR), 
	VERSION NUMBER(38,0) DEFAULT 0 NOT NULL ENABLE, 
	USUARIOCREAR VARCHAR2(50 CHAR) NOT NULL ENABLE, 
	FECHACREAR TIMESTAMP (6) NOT NULL ENABLE, 
	USUARIOMODIFICAR VARCHAR2(50 CHAR), 
	FECHAMODIFICAR TIMESTAMP (6), 
	USUARIOBORRAR VARCHAR2(50 CHAR), 
	FECHABORRAR TIMESTAMP (6), 
	BORRADO NUMBER(1,0) DEFAULT 0 NOT NULL ENABLE

		)
	'
	;

	DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA APR_AUX_BIE_ADN CREADA.');


	
  
  COMMIT;
  	END IF;
  
  DBMS_OUTPUT.PUT_LINE('[INFO] PROCESO FINALIZADO.');

         
EXCEPTION

   WHEN OTHERS THEN
   
        err_num := SQLCODE;
        err_msg := SQLERRM;

        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
        DBMS_OUTPUT.put_line(err_msg);

        ROLLBACK;
        RAISE;          

END;
