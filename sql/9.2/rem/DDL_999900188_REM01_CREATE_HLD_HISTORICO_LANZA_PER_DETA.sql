		HLD_TABLA_MODIFICAR_CLAVE		VARCHAR2(30 CHAR)	NOT NULL,
		HLD_TABLA_MODIFICAR_CLAVE_ID	NUMBER(16)			NOT NULL,
		HLD_CAMPO_MODIFICAR				VARCHAR2(30 CHAR)	NOT NULL,
		HLD_VALOR_ORIGINAL				VARCHAR2(30 CHAR)	NOT NULL,
		HLD_VALOR_ACTUALIZADO			VARCHAR2(30 CHAR)	NOT NULL
    )
	LOGGING 
	NOCOMPRESS 
	NOCACHE
	NOPARALLEL
	NOMONITORING
	';
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] ' ||V_ESQUEMA||'.'||V_TEXT_TABLA||'... Tabla creada.');
	

	-- Creamos indice	
