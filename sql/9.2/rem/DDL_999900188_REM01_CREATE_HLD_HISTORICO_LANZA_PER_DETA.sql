	V_MSQL := 'CREATE TABLE ' ||V_ESQUEMA||'.'||V_TEXT_TABLA||'
	(
		HLD_SP_CARGA					VARCHAR2(30 CHAR)	NOT NULL,
		HLD_FECHA_EJEC					TIMESTAMP			NOT NULL,
		HLD_CODIGO_REG					NUMBER(16)			NOT NULL,
