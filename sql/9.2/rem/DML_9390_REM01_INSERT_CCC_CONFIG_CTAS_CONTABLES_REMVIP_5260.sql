		  AND EJE.EJE_ANYO = ''2019''
		  AND STG.DD_STG_CODIGO = ''75''
		  AND TGA.DD_TGA_CODIGO = ''15''
		  AND CCC.CCC_ARRENDAMIENTO = 1';
		
