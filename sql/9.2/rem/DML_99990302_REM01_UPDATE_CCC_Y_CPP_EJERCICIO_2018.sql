WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	
    V_TEXT_TABLA1 VARCHAR2(100 CHAR):= 'CCC_CONFIG_CTAS_CONTABLES';
    V_TEXT_TABLA2 VARCHAR2(100 CHAR):= 'CPP_CONFIG_PTDAS_PREP';
    V_USUARIO VARCHAR2(100 CHAR):= 'HREOS-3645';
    V_ENTIDAD_ID NUMBER(16);
    V_ID NUMBER(16);

    
BEGIN	
	
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');

	V_SQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA1||' WHERE DD_CRA_ID = (SELECT DD_CRA_ID FROM '||V_ESQUEMA||'.DD_CRA_CARTERA WHERE DD_CRA_CODIGO = ''02'') AND EJE_ID = (SELECT EJE_ID FROM '||V_ESQUEMA||'.ACT_EJE_EJERCICIO WHERE EJE_ANYO = ''2018'') AND BORRADO = 0';
	EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
	IF V_NUM_TABLAS < 1 THEN
		V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA1||' (CCC_ID, DD_STG_ID, DD_CRA_ID, PRO_ID, CCC_CUENTA_ACTIVABLE, VERSION, USUARIOCREAR, FECHACREAR, BORRADO, CCC_CUENTA_CONTABLE, EJE_ID, CCC_ARRENDAMIENTO)
					SELECT '||V_ESQUEMA||'.S_'||V_TEXT_TABLA1||'.NEXTVAL CCC_ID, DD_STG_ID, DD_CRA_ID, PRO_ID, CCC_CUENTA_ACTIVABLE, 0 VERSION, '''||V_USUARIO||''' USUARIOCREAR, SYSDATE FECHACREAR, 0 BORRADO, CCC_CUENTA_CONTABLE, (SELECT EJE_ID FROM '||V_ESQUEMA||'.ACT_EJE_EJERCICIO WHERE EJE_ANYO = ''2018'') EJE_ID, CCC_ARRENDAMIENTO
					FROM (SELECT * FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA1||' WHERE DD_CRA_ID = (SELECT DD_CRA_ID FROM '||V_ESQUEMA||'.DD_CRA_CARTERA WHERE DD_CRA_CODIGO = ''02'') AND EJE_ID = (SELECT EJE_ID FROM '||V_ESQUEMA||'.ACT_EJE_EJERCICIO WHERE EJE_ANYO = ''2017'') AND BORRADO = 0)';
		EXECUTE IMMEDIATE V_MSQL;
	END IF;
	
	
	V_SQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA2||' WHERE DD_CRA_ID = (SELECT DD_CRA_ID FROM '||V_ESQUEMA||'.DD_CRA_CARTERA WHERE DD_CRA_CODIGO = ''02'') AND EJE_ID = (SELECT EJE_ID FROM '||V_ESQUEMA||'.ACT_EJE_EJERCICIO WHERE EJE_ANYO = ''2018'') AND BORRADO = 0';
	EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
	IF V_NUM_TABLAS < 1 THEN
		V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA2||' (CPP_ID, EJE_ID, DD_STG_ID, DD_CRA_ID, DD_SCR_ID, PRO_ID, PVE_ID, CPP_PARTIDA_PRESUPUESTARIA, VERSION, USUARIOCREAR, FECHACREAR, BORRADO, CPP_ARRENDAMIENTO, CPP_CANTIDAD_MAX_PRO)
					SELECT '||V_ESQUEMA||'.S_'||V_TEXT_TABLA2||'.NEXTVAL CPP_ID, (SELECT EJE_ID FROM '||V_ESQUEMA||'.ACT_EJE_EJERCICIO WHERE EJE_ANYO = ''2018'') EJE_ID, DD_STG_ID, DD_CRA_ID, DD_SCR_ID, PRO_ID, PVE_ID, CPP_PARTIDA_PRESUPUESTARIA, 0 VERSION, '''||V_USUARIO||''' USUARIOCREAR, SYSDATE FECHACREAR, 0 BORRADO, CPP_ARRENDAMIENTO, CPP_CANTIDAD_MAX_PRO
					FROM (SELECT * FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA2||' WHERE DD_CRA_ID = (SELECT DD_CRA_ID FROM '||V_ESQUEMA||'.DD_CRA_CARTERA WHERE DD_CRA_CODIGO = ''02'') AND EJE_ID = (SELECT EJE_ID FROM '||V_ESQUEMA||'.ACT_EJE_EJERCICIO WHERE EJE_ANYO = ''2017'') AND BORRADO = 0)';
		EXECUTE IMMEDIATE V_MSQL;
	END IF;
	COMMIT;
EXCEPTION
     WHEN OTHERS THEN
          err_num := SQLCODE;
          err_msg := SQLERRM;

          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(err_msg);

          ROLLBACK;
          RAISE;          

END;

