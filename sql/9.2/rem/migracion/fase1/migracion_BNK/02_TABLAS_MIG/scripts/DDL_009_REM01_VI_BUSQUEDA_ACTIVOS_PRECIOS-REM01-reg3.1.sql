WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01'; -- Configuracion Esquemas
    V_ESQUEMA_MASTER VARCHAR2(25 CHAR):= 'REMMASTER'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_BUSQUEDA_ACTIVOS_PRECIOS' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_ACTIVOS_PRECIOS...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.V_BUSQUEDA_ACTIVOS_PRECIOS';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_ACTIVOS_PRECIOS... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_BUSQUEDA_ACTIVOS_PRECIOS' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_ACTIVOS_PRECIOS...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.V_BUSQUEDA_ACTIVOS_PRECIOS';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_ACTIVOS_PRECIOS... borrada OK');
  END IF;

  
  
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_ACTIVOS_PRECIOS...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.V_BUSQUEDA_ACTIVOS_PRECIOS 
	AS
		SELECT
		  	ACT.ACT_ID,
			ACT.ACT_NUM_ACTIVO,
	        TPA.DD_TPA_CODIGO AS TIPO_ACTIVO_CODIGO,
	        TPA.DD_TPA_DESCRIPCION AS TIPO_ACTIVO_DESCRIPCION,
	        SAC.DD_SAC_CODIGO AS SUBTIPO_ACTIVO_CODIGO,
	        SAC.DD_SAC_DESCRIPCION AS SUBTIPO_ACTIVO_DESCRIPCION,
			CRA.DD_CRA_CODIGO AS ENTIDAD_CODIGO,
			CRA.DD_CRA_DESCRIPCION AS ENTIDAD_DESCRIPCION,
			TTA.DD_TTA_CODIGO AS TIPO_TITULO_CODIGO,
	        TTA.DD_TTA_DESCRIPCION AS TIPO_TITULO_DESCRIPCION,
			STA.DD_STA_CODIGO AS SUBTIPO_TITULO_CODIGO,
	        STA.DD_STA_DESCRIPCION AS SUBTIPO_TITULO_DESCRIPCION,
		    NVL2(TIT.TIT_FECHA_INSC_REG, ''1'',''0'') AS INSCRITO,
			NVL2(ACT_SIT.SPS_FECHA_TOMA_POSESION, ''1'',''0'') AS CON_POSESION,
			NVL2(ACT.ACT_FECHA_REV_CARGAS, ''1'',''0'') AS CON_FECHA_REVISION_CARGAS,
			-- AS TIPO_COMERCIALIZACION,
			-- AS CON_OFERTA_APROBADA,
			-- AS CON_RESERVA,
			NVL2(MEDIADOR.PVE_ID, ''1'',''0'') AS TIENE_MEDIADOR,
			-- AS TIENE_LLAVES_MEDIADOR,
			NVL2(ICO.ICO_FECHA_EMISION_INFORME, '''', NVL2(ICO.ICO_FECHA_ACEPTACION, ''01'', ''02'')) AS ESTADO_INF_COMERCIAL, 
			DECODE(TASACIONES.CONTADOR, 0,''1'',''0'') AS CON_TASACION,
	        PRV.DD_PRV_CODIGO AS PROVINCIA_CODIGO,
	        PRV.DD_PRV_DESCRIPCION AS PROVINCIA_DESCRIPCION,
	        LOC.DD_LOC_CODIGO AS LOCALIDAD_CODIGO,
	        LOC.DD_LOC_DESCRIPCION AS LOCALIDAD_DESCRIPCION,
			BIE_LOC.BIE_LOC_COD_POST AS CODIGO_POSTAL,
			TPVIA.DD_TVI_DESCRIPCION || '' '' || BIE_LOC.BIE_LOC_NOMBRE_VIA || '' ''|| BIE_LOC.BIE_LOC_NUMERO_DOMICILIO || '' ''||  BIE_LOC.BIE_LOC_PUERTA AS DIRECCION,
			PRECIO_APROBADO_VENTA.VAL_IMPORTE AS PRECIO_APROBADO_VENTA,
			PRECIO_APROBADO_RENTA.VAL_IMPORTE AS PRECIO_APROBADO_RENTA,
			PRECIO_MINIMO_AUTORIZADO.VAL_IMPORTE AS PRECIO_MINIMO_AUTORIZADO,
			PRECIO_DESCUENTO_APROBADO.VAL_IMPORTE AS PRECIO_DESCUENTO_APROBADO,
			PRECIO_DESCUENTO_PUBLICADO.VAL_IMPORTE AS PRECIO_DESCUENTO_PUBLICADO,
			FSV_VENTA.VAL_IMPORTE AS FSV_VENTA,
			FSV_RENTA.VAL_IMPORTE AS FSV_RENTA,
			NVL2(ACT.ACT_BLOQUEO_PRECIO_FECHA_INI, ''1'',''0'') AS CON_BLOQUEO,
			ACT.ACT_FECHA_IND_PRECIAR AS FECHA_PRECIAR,
			ACT.ACT_FECHA_IND_REPRECIAR AS FECHA_REPRECIAR,
			ACT.ACT_FECHA_IND_DESCUENTO AS FECHA_DESCUENTO
		FROM ' || V_ESQUEMA || '.ACT_ACTIVO ACT
		LEFT JOIN ' || V_ESQUEMA || '.DD_SAC_SUBTIPO_ACTIVO SAC ON SAC.DD_SAC_ID = ACT.DD_SAC_ID
	    LEFT JOIN ' || V_ESQUEMA || '.DD_TPA_TIPO_ACTIVO TPA ON TPA.DD_TPA_ID = ACT.DD_TPA_ID
		LEFT JOIN ' || V_ESQUEMA || '.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
		LEFT JOIN ' || V_ESQUEMA || '.DD_TTA_TIPO_TITULO_ACTIVO TTA ON TTA.DD_TTA_ID =  ACT.DD_TTA_ID
 		LEFT JOIN ' || V_ESQUEMA || '.DD_STA_SUBTIPO_TITULO_ACTIVO STA ON STA.DD_STA_ID =  ACT.DD_STA_ID
		LEFT JOIN ' || V_ESQUEMA || '.ACT_SPS_SIT_POSESORIA ACT_SIT ON ACT.ACT_ID = ACT_SIT.ACT_ID
		LEFT JOIN ' || V_ESQUEMA || '.ACT_ICO_INFO_COMERCIAL ICO ON ICO.ACT_ID = ACT.ACT_ID
		LEFT JOIN ' || V_ESQUEMA || '.ACT_TIT_TITULO TIT ON TIT.ACT_ID = ACT.ACT_ID
		LEFT JOIN ' || V_ESQUEMA || '.ACT_PVE_PROVEEDOR MEDIADOR ON ICO.ICO_MEDIADOR_ID = MEDIADOR.PVE_ID
		LEFT JOIN ' || V_ESQUEMA || '.BIE_LOCALIZACION BIE_LOC ON ACT.BIE_ID = BIE_LOC.BIE_ID
		LEFT JOIN ' || V_ESQUEMA_MASTER || '.DD_TVI_TIPO_VIA TPVIA ON TPVIA.DD_TVI_ID = BIE_LOC.DD_TVI_ID
		LEFT JOIN ' || V_ESQUEMA_MASTER || '.DD_PRV_PROVINCIA PRV ON PRV.DD_PRV_ID = BIE_LOC.DD_PRV_ID
		LEFT JOIN ' || V_ESQUEMA_MASTER || '.DD_LOC_LOCALIDAD LOC ON LOC.DD_LOC_ID = BIE_LOC.DD_LOC_ID
	    LEFT JOIN  (SELECT ACT1.ACT_ID ACTIVO_TAS, COUNT(TAS_ID) CONTADOR FROM ' || V_ESQUEMA || '.ACT_ACTIVO ACT1
	                LEFT JOIN ' || V_ESQUEMA || '.ACT_TAS_TASACION TAS ON TAS.ACT_ID = ACT1.ACT_ID
	                GROUP BY ACT1.ACT_ID) TASACIONES ON ACTIVO_TAS = ACT.ACT_ID
	    LEFT JOIN (SELECT VAL1.ACT_ID, VAL1.VAL_IMPORTE 
                	FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL1
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC1 ON VAL1.DD_TPC_ID = TPC1.DD_TPC_ID AND TPC1.DD_TPC_CODIGO = ''02'' 
						AND VAL1.BORRADO=0) PRECIO_APROBADO_VENTA ON ACT.ACT_ID = PRECIO_APROBADO_VENTA.ACT_ID
	    LEFT JOIN (SELECT VAL2.ACT_ID, VAL2.VAL_IMPORTE 
	                FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL2 
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC2 ON VAL2.DD_TPC_ID = TPC2.DD_TPC_ID AND TPC2.DD_TPC_CODIGO = ''03'' 
						AND VAL2.BORRADO=0) PRECIO_APROBADO_RENTA ON ACT.ACT_ID = PRECIO_APROBADO_RENTA.ACT_ID
	    LEFT JOIN (SELECT VAL3.ACT_ID, VAL3.VAL_IMPORTE 
	                FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL3 
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC3 ON VAL3.DD_TPC_ID = TPC3.DD_TPC_ID AND TPC3.DD_TPC_CODIGO = ''04'' 
						AND VAL3.BORRADO=0) PRECIO_MINIMO_AUTORIZADO ON ACT.ACT_ID = PRECIO_MINIMO_AUTORIZADO.ACT_ID
	    LEFT JOIN (SELECT VAL4.ACT_ID, VAL4.VAL_IMPORTE 
	                FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL4
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC4 ON VAL4.DD_TPC_ID = TPC4.DD_TPC_ID AND TPC4.DD_TPC_CODIGO = ''07'' 
						AND VAL4.BORRADO=0) PRECIO_DESCUENTO_APROBADO ON ACT.ACT_ID = PRECIO_DESCUENTO_APROBADO.ACT_ID
	    LEFT JOIN (SELECT VAL5.ACT_ID, VAL5.VAL_IMPORTE
                  	FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL5
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC5 ON VAL5.DD_TPC_ID = TPC5.DD_TPC_ID AND TPC5.DD_TPC_CODIGO = ''13'' 
						AND VAL5.BORRADO=0) PRECIO_DESCUENTO_PUBLICADO ON ACT.ACT_ID = PRECIO_DESCUENTO_PUBLICADO.ACT_ID
	    LEFT JOIN (SELECT VAL6.ACT_ID, VAL6.VAL_IMPORTE 
	                FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL6
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC6 ON VAL6.DD_TPC_ID = TPC6.DD_TPC_ID AND TPC6.DD_TPC_CODIGO = ''19'' 
						AND VAL6.BORRADO=0) FSV_VENTA ON ACT.ACT_ID = FSV_VENTA.ACT_ID
	    LEFT JOIN (SELECT VAL7.ACT_ID, VAL7.VAL_IMPORTE 
	                FROM ' || V_ESQUEMA || '.ACT_VAL_VALORACIONES VAL7 
	                INNER JOIN ' || V_ESQUEMA || '.DD_TPC_TIPO_PRECIO TPC7 ON VAL7.DD_TPC_ID = TPC7.DD_TPC_ID AND TPC7.DD_TPC_CODIGO = ''20'' 
						AND VAL7.BORRADO=0) FSV_RENTA ON ACT.ACT_ID = FSV_RENTA.ACT_ID';

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_ACTIVOS_PRECIOS...Creada OK');
  
END;
