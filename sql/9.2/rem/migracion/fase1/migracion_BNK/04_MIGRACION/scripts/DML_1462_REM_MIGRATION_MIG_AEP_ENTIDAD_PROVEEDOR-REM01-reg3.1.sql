--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(10,0) := 0;
V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01';
V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER';
V_TABLA VARCHAR2(40 CHAR) := 'ACT_ETP_ENTIDAD_PROVEEDOR';
V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG_AEP_ENTIDAD_PROVEEDOR_BNK';
V_SENTENCIA VARCHAR2(1600 CHAR);
V_REG_MIG NUMBER(10,0) := 0;
V_REG_INSERTADOS NUMBER(10,0) := 0;
V_REJECTS NUMBER(10,0) := 0;
V_COD NUMBER(10,0) := 0;
V_OBSERVACIONES VARCHAR2(3000 CHAR) := '';

BEGIN

  DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
  
  DBMS_OUTPUT.PUT_LINE('[INFO] ['||V_TABLA||'] COMPROBANDO PROVEEDORES...');
  
  V_SENTENCIA := '
  SELECT COUNT(PVE_DOCIDENTIF) 
  FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG 
  WHERE NOT EXISTS (
    SELECT 1 FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVE WHERE MIG.PVE_DOCIDENTIF = PVE.PVE_DOCIDENTIF
  )
  '
  ;
  EXECUTE IMMEDIATE V_SENTENCIA INTO TABLE_COUNT;
  
  IF TABLE_COUNT = 0 THEN
  
    DBMS_OUTPUT.PUT_LINE('[INFO] TODOS LOS PROVEEDORES EXISTEN EN ACT_PVE_PROVEEDOR');
    
  ELSE
  
    DBMS_OUTPUT.PUT_LINE('[INFO] SE HAN INFORMADO '||TABLE_COUNT||' PROVEEDORES INEXISTENTES EN ACT_PVE_PROVEEDOR. SE DERIVARÁN A LA TABLA '||V_ESQUEMA||'.PVE_NOT_EXISTS.');
    
    --BORRAMOS LOS REGISTROS QUE HAYA EN NOT_EXISTS REFERENTES A ESTA INTERFAZ
    
    EXECUTE IMMEDIATE '
    DELETE FROM '||V_ESQUEMA||'.PVE_NOT_EXISTS
    WHERE TABLA_MIG = '''||V_TABLA_MIG||'''
    '
    ;
    
    COMMIT;
  
    EXECUTE IMMEDIATE '
    INSERT INTO '||V_ESQUEMA||'.PVE_NOT_EXISTS (
    PVE_DOCIDENTIF,
    TABLA_MIG,
    FECHA_COMPROBACION
    )
    WITH PVE_DOCIDENTIF AS (
    SELECT DISTINCT
    MIG.PVE_DOCIDENTIF 
    FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG 
    WHERE NOT EXISTS (
      SELECT 1 FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVE WHERE MIG.PVE_DOCIDENTIF = PVE.PVE_DOCIDENTIF
    )
    )
    SELECT
    MIG.PVE_DOCIDENTIF                              							 PVE_DOCIDENTIF,
    '''||V_TABLA_MIG||'''                                                        TABLA_MIG,
    SYSDATE                                                                      FECHA_COMPROBACION
    FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG  
    INNER JOIN PVE_DOCIDENTIF
    ON PVE_DOCIDENTIF.PVE_DOCIDENTIF = MIG.PVE_DOCIDENTIF
    '
    ;
    
    COMMIT;
    
    /*DBMS_OUTPUT.PUT_LINE('[INFO] SE BORRARÁN DE '||V_ESQUEMA||'.'||V_TABLA_MIG||' LOS PROVEEDORES INEXISTENTES. MIRAR '||V_ESQUEMA||'.PVE_NOT_EXISTS.');
    
    EXECUTE IMMEDIATE '
    DELETE 
    FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG
    WHERE EXISTS (
      SELECT 1 FROM PVE_NOT_EXISTS WHERE MIG.PVE_DOCIDENTIF = PVE_DOCIDENTIF
    )
    '
    ;*/
    
    COMMIT;

  END IF;
  
	EXECUTE IMMEDIATE ('
	INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||' (
	ETP_ID,
  DD_CRA_ID,
  PVE_ID,
  DD_TCL_ID,
  ETP_FECHA_CONTRATO,
  ETP_FECHA_INICIO,
  ETP_FECHA_FIN,
  ETP_ESTADO
	)
	WITH PVE_DOCIDENTIF AS (
      SELECT MIGW.PVE_DOCIDENTIF
    FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIGW
    INNER JOIN  '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVEW
      ON PVEW.PVE_DOCIDENTIF = MIGW.PVE_DOCIDENTIF
    AND NOT EXISTS (
      SELECT 1 
      FROM '||V_ESQUEMA||'.'||V_TABLA||' ETPW
      WHERE ETPW.PVE_ID = PVEW.PVE_ID  
    ) 
	)
	SELECT
	 '||V_ESQUEMA||'.S_ACT_PVC_PROVEEDOR_CONTACTO.NEXTVAL	  ETP_ID,
  (SELECT CRA.DD_CRA_ID
  FROM  '||V_ESQUEMA||'.DD_CRA_CARTERA CRA 
  WHERE CRA.DD_CRA_CODIGO =  MIG.ENTIDAD_PROPIETARIA)       DD_CRA_ID,
  (SELECT PVE_ID.PVE_ID
  FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVE_ID
  WHERE PVE_ID.PVE_DOCIDENTIF = MIG.PVE_DOCIDENTIF)             PVE_ID,
  (SELECT TCL.DD_TCL_ID
  FROM  '||V_ESQUEMA||'.DD_TCL_TIPO_COLABORACION TCL
  WHERE TCL.DD_TCL_CODIGO = MIG.TIPO_COLABORACION )         DD_TCL_ID,
  MIG.ETP_FECHA_CONTRATO                                                       ETP_FECHA_CONTRATO,
  MIG.ETP_FECHA_INI                                                                    ETP_FECHA_INICIO,
  MIG.ETP_FECHA_FIN                                                                   ETP_FECHA_FIN,
  MIG.ETP_ESTADO                                                                       ETP_ESTADO  
	FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG
  INNER JOIN PVE_DOCIDENTIF PVE
    ON PVE.PVE_DOCIDENTIF = MIG.PVE_DOCIDENTIF
    LEFT JOIN PVE_NOT_EXISTS PNE
    ON PNE.PVE_DOCIDENTIF = MIG.PVE_DOCIDENTIF
    WHERE PNE.PVE_DOCIDENTIF IS NULL
	')
	;

	
  
  DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');
  
  COMMIT;
  
  EXECUTE IMMEDIATE('ANALYZE TABLE '||V_ESQUEMA||'.'||V_TABLA||' COMPUTE STATISTICS');
  
  DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' ANALIZADA.');
  
  -- INFORMAMOS A LA TABLA INFO
  
  -- Registros MIG
  V_SENTENCIA := '
	SELECT COUNT(PVE_DOCIDENTIF) FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||'
  '
  ;
  
  EXECUTE IMMEDIATE V_SENTENCIA INTO V_REG_MIG;
  
  -- Registros insertados en REM
  V_SENTENCIA := '
	SELECT COUNT(ETP_ID) FROM '||V_ESQUEMA||'.'||V_TABLA||'
	WHERE PVE_ID NOT IN (
		SELECT PVE_ID FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR WHERE USUARIOCREAR != ''MIGRAREM01BNK''
	) 
  '
  ;
  
  EXECUTE IMMEDIATE V_SENTENCIA INTO V_REG_INSERTADOS;
  
  -- Total registros rechazados
  V_REJECTS := V_REG_MIG - V_REG_INSERTADOS;	
  
  -- Diccionarios rechazados
  V_SENTENCIA := '
  SELECT COUNT(1) FROM '||V_ESQUEMA||'.DD_COD_NOT_EXISTS 
	WHERE DICCIONARIO IN (''DD_CRA_CARTERA'',''DD_TCL_TIPO_COLABORACION'')
	AND FICHERO_ORIGEN = ''ENTIDAD_PROVEEDOR.dat''
	AND CAMPO_ORIGEN IN (''ENTIDAD_PROPIETARIA'',''TIPO_COLABORACION'')
  '
  ;
  
  EXECUTE IMMEDIATE V_SENTENCIA INTO V_COD;
  
  -- Observaciones
  IF V_REJECTS != 0 THEN
	V_OBSERVACIONES := 'Del total de registros rechazados, '||TABLE_COUNT||' han sido por PROVEEDORES inexistentes en ACT_PVE_PROVEEDOR.';
  END IF;
  
  EXECUTE IMMEDIATE ('
	INSERT INTO '||V_ESQUEMA||'.MIG_INFO_TABLE (
	TABLA_MIG,
	TABLA_REM,
	REGISTROS_TABLA_MIG,
	REGISTROS_INSERTADOS,
	REGISTROS_RECHAZADOS,
	DD_COD_INEXISTENTES,
	FECHA,
	OBSERVACIONES
	)
	SELECT
	'''||V_TABLA_MIG||''',
	'''||V_TABLA||''',
	'||V_REG_MIG||',
	'||V_REG_INSERTADOS||',
	'||V_REJECTS||',
	'||V_COD||',
	SYSDATE,
	'''||V_OBSERVACIONES||'''
	FROM DUAL
  ')
  ;
  
  COMMIT;
  
  
  EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
