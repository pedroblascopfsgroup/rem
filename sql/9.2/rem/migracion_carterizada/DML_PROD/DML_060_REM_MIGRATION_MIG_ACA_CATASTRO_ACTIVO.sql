--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(10,0) := 0;
V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01';
V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER';
V_USUARIO VARCHAR2(50 CHAR) := '#USUARIO_MIGRACION#';
V_TABLA VARCHAR2(40 CHAR) := 'ACT_CAT_CATASTRO';
V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG_ACA_CATASTRO_ACTIVO';
V_SENTENCIA VARCHAR2(2000 CHAR);
V_REG_MIG NUMBER(10,0) := 0;
V_REG_INSERTADOS NUMBER(10,0) := 0;
V_REJECTS NUMBER(10,0) := 0;
V_COD NUMBER(10,0) := 0;
V_OBSERVACIONES VARCHAR2(3000 CHAR) := '';
VAR1 NUMBER(10,0) := 0;

BEGIN
	
    DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
  
	EXECUTE IMMEDIATE ('
	INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||' (
	CAT_ID,
	ACT_ID,
	CAT_REF_CATASTRAL,
	CAT_POLIGONO,
	CAT_PARCELA,
	CAT_TITULAR_CATASTRAL,
	CAT_SUPERFICIE_CONSTRUIDA,
	CAT_SUPERFICIE_UTIL,
	CAT_SUPERFICIE_REPER_COMUN,
	CAT_SUPERFICIE_PARCELA,
	CAT_SUPERFICIE_SUELO,
	CAT_VALOR_CATASTRAL_CONST,
	CAT_VALOR_CATASTRAL_SUELO,
	CAT_FECHA_REV_VALOR_CATASTRAL,
	VERSION,
	USUARIOCREAR,
	FECHACREAR,
	BORRADO,
	CAT_IND_INTERFAZ_BANKIA
	)
	WITH ACT_NUMERO_ACTIVO AS (
		SELECT DISTINCT(ACT_NUMERO_ACTIVO || CAT_REF_CATASTRAL) UNICO,
		ACT_NUMERO_ACTIVO,
		CAT_REF_CATASTRAL,
		CAT_POLIGONO,
		CAT_PARCELA,
		CAT_TITULAR_CATASTRAL,
		CAT_SUPERFICIE_CONSTRUIDA,
		CAT_SUPERFICIE_UTIL,
		CAT_SUPERFICIE_REPER_COMUN,
		CAT_SUPERFICIE_PARCELA,
		CAT_SUPERFICIE_SUELO,
		CAT_VALOR_CATASTRAL_CONST,
		CAT_VALOR_CATASTRAL_SUELO,
		CAT_FECHA_REV_VALOR_CATASTRAL,
		CAT_IND_INTERFAZ_BANKIA 
		FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' 
		WHERE ACT_NUMERO_ACTIVO NOT IN (
		  SELECT ACT_NUM_ACTIVO 
		  FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
		  INNER JOIN '||V_ESQUEMA||'.'||V_TABLA||' CAT
		  ON CAT.ACT_ID = ACT.ACT_ID 
			)
		AND VALIDACION = 0
	)
	SELECT
	'||V_ESQUEMA||'.S_ACT_CAT_CATASTRO.NEXTVAL						CAT_ID,
	(SELECT ACT_ID
	FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT 
	WHERE ACT.ACT_NUM_ACTIVO = MIG.ACT_NUMERO_ACTIVO)     				ACT_ID,
	MIG.CAT_REF_CATASTRAL												CAT_REF_CATASTRAL,
	MIG.CAT_POLIGONO													CAT_POLIGONO,
	MIG.CAT_PARCELA														CAT_PARCELA,
	MIG.CAT_TITULAR_CATASTRAL											CAT_TITULAR_CATASTRAL,
	MIG.CAT_SUPERFICIE_CONSTRUIDA										CAT_SUPERFICIE_CONSTRUIDA,
	MIG.CAT_SUPERFICIE_UTIL												CAT_SUPERFICIE_UTIL,
	MIG.CAT_SUPERFICIE_REPER_COMUN										CAT_SUPERFICIE_REPER_COMUN,
	MIG.CAT_SUPERFICIE_PARCELA											CAT_SUPERFICIE_PARCELA,
	MIG.CAT_SUPERFICIE_SUELO											CAT_SUPERFICIE_SUELO,
	MIG.CAT_VALOR_CATASTRAL_CONST										CAT_VALOR_CATASTRAL_CONST,
	MIG.CAT_VALOR_CATASTRAL_SUELO										CAT_VALOR_CATASTRAL_SUELO,
	MIG.CAT_FECHA_REV_VALOR_CATASTRAL									CAT_FECHA_REV_VALOR_CATASTRAL,
	''0''                                                 				VERSION,
	'||V_USUARIO||'                                               	USUARIOCREAR,
	SYSDATE                                               				FECHACREAR,
	0                                                     				BORRADO,
	MIG.CAT_IND_INTERFAZ_BANKIA											CAT_IND_INTERFAZ_BANKIA
	FROM ACT_NUMERO_ACTIVO MIG
	LEFT JOIN '||V_ESQUEMA||'.ACT_NOT_EXISTS NOTT
    ON NOTT.ACT_NUM_ACTIVO = MIG.ACT_NUMERO_ACTIVO
	WHERE NOTT.ACT_NUM_ACTIVO IS NULL
	')
	;

	
  
  DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');
  
  COMMIT;
  
  EXECUTE IMMEDIATE('ANALYZE TABLE '||V_ESQUEMA||'.'||V_TABLA||' COMPUTE STATISTICS');
  
  DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' ANALIZADA.');
  
EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;