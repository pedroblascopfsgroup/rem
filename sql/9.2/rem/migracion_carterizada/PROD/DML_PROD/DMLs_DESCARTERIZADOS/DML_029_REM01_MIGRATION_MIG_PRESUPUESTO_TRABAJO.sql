--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01'; --REM01
V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER'; --REMMASTER
V_USUARIO VARCHAR2(50 CHAR) := '#USUARIO_MIGRACION#';
V_TABLA VARCHAR2(40 CHAR) := 'ACT_PRT_PRESUPUESTO_TRABAJO';
V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG_APT_PRESUPUESTO_TRABAJO';
V_SENTENCIA VARCHAR2(1600 CHAR);

BEGIN

  DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
  
  --SOLO VAMOS A CARGAR LOS PRESUPUESTOS ASOCIADOS A UN PROVEEDOR TÉCNICO. NO ES UN ERROR, PERO NO SE CARGARÁN.

  EXECUTE IMMEDIATE ('
  INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||' (
	PRT_ID,
	TBJ_ID,
	PVE_ID,
	DD_ESP_ID,
	PRT_IMPORTE,
	PRT_FECHA,
	PRT_REPARTIR_PROPORCIONAL,
	PRT_COMENTARIOS,
	VERSION,
	USUARIOCREAR,
	FECHACREAR,
	BORRADO
	)
  WITH PERIMETRO AS (
    SELECT 
    DISTINCT
    MIGAPT.PVE_DOCIDENTIF,
    PVE.PVE_ID,
    MIGAPT.TBJ_NUM_TRABAJO,
    ATT.TBJ_ID,
    MIGAPT.PRT_IMPORTE,
    MIGAPT.PRT_FECHA,
    MIGAPT.ESTADO_PRESUPUESTO,
    MIGAPT.PRT_REPARTIR_PROPORCIONAL,
    MIGAPT.PRT_COMENTARIOS
    FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIGAPT
    INNER JOIN '||V_ESQUEMA||'.ACT_TBJ_TRABAJO ATT
      ON ATT.TBJ_NUM_TRABAJO = MIGAPT.TBJ_NUM_TRABAJO	
    INNER JOIN '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVE
      ON MIGAPT.PVE_DOCIDENTIF = PVE.PVE_COD_UVEM
    INNER JOIN  '||V_ESQUEMA||'.DD_TPR_TIPO_PROVEEDOR TPR 
      ON PVE.DD_TPR_ID = TPR.DD_TPR_ID
    INNER JOIN DD_TTR_TIPO_TRABAJO TTR
      ON TTR.DD_TTR_ID = ATT.DD_TTR_ID
    WHERE (TPR.DD_TPR_CODIGO = ''05'' AND TTR.DD_TTR_CODIGO = ''03'')
    OR (TPR.DD_TPR_CODIGO = ''01'' AND TTR.DD_TTR_CODIGO = ''02'')
	AND MIGAPT.VALIDACION = 0
  )
	SELECT
	'||V_ESQUEMA||'.S_ACT_PRT_PRESUPUESTO_TRABAJO.NEXTVAL	PRT_ID,
	MIG.TBJ_ID     		TBJ_ID,	
	MIG.PVE_ID     			PVE_ID,
	(SELECT DD_ESP_ID
	FROM '||V_ESQUEMA||'.DD_ESP_ESTADO_PRESUPUESTO 
	WHERE DD_ESP_CODIGO = MIG.ESTADO_PRESUPUESTO)			DD_ESP_ID,
	MIG.PRT_IMPORTE											PRT_IMPORTE,
	MIG.PRT_FECHA											PRT_FECHA,
	MIG.PRT_REPARTIR_PROPORCIONAL							PRT_REPARTIR_PROPORCIONAL,
	MIG.PRT_COMENTARIOS										PRT_COMENTARIOS,
	''0''                                                 	VERSION,
	'''||V_USUARIO||'''                                               	USUARIOCREAR,
	SYSDATE                                               	FECHACREAR,
	0                                                     	BORRADO
	FROM PERIMETRO MIG
  ')
  ;
	
  
  DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');
  
  COMMIT;
  
  V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''ANALYZE'','''||V_TABLA||''',''10''); END;';
  EXECUTE IMMEDIATE V_SENTENCIA;
 
EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
