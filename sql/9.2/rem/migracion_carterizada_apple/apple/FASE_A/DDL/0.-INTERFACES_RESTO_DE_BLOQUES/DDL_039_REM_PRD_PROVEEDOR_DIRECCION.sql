EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_PRD_PROVEEDOR_DIRECCION.PRD_COD_PROVINCIA IS ''Código de la Provincia de la Dirección (Código según Dic. Datos).''';
