WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REM01'; --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
V_TABLESPACE_IDX VARCHAR2(30 CHAR) := '#TABLESPACE_INDEX#';
V_TABLA VARCHAR2(40 CHAR) := 'MIG_ACA_CABECERA';

BEGIN

SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

IF TABLE_COUNT > 0 THEN

    DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');

    EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'';
    
END IF;

EXECUTE IMMEDIATE '
CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'
(
	ACT_NUMERO_ACTIVO	   		NUMBER(16,0)                NOT NULL,     
	ACT_NUMERO_EXTERNO			NUMBER(16,0),		
	ENTIDAD_PROPIETARIA			VARCHAR2(20 CHAR) 			NOT NULL,
	ENTIDAD_ORIGEN				VARCHAR2(20 CHAR) 			NOT NULL,
	ENTIDAD_ORIGEN_ANTERIOR		VARCHAR2(20 CHAR) 			NOT NULL,
	ACT_DESCRIPCION				VARCHAR2(250 CHAR),
	ACT_RATING					VARCHAR2(20 CHAR), 
	ACT_DIVISION_HORIZONTAL		NUMBER(1,0),
	ACT_FECHA_REV_CARGAS		DATE,
	ACT_CON_CARGAS				NUMBER(1,0),
	TIPO_ACTIVO					VARCHAR2(20 CHAR), 
	SUBTIPO_ACTIVO				VARCHAR2(20 CHAR), 
	ESTADO_ACTIVO				VARCHAR2(20 CHAR), 
	USO_ACTIVO					VARCHAR2(20 CHAR), 
	TIPO_TITULO					VARCHAR2(20 CHAR), 
	SUBTIPO_TITULO				VARCHAR2(20 CHAR), 
	ACT_VPO						NUMBER(1,0),
	ESTADO_ADMISION				NUMBER(1,0)					NOT NULL,
	ESTADO_GESTION				NUMBER(1,0)					NOT NULL,
	GESTION_HRE					NUMBER(1,0),
	SELLO_CALIDAD				NUMBER(1,0),
	SITUACION_COMERCIAL			VARCHAR2(20 CHAR),
	ACT_COD_PROMOCION_EXTERNO	VARCHAR2(50 CHAR)
, VALIDACION NUMBER(1) DEFAULT 0 NOT NULL )'
;

DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA||' CREADA');  

EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_NUMERO_ACTIVO IS ''Código identificador único del activo.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_NUMERO_EXTERNO IS ''Código identificador único del activo en Origen.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ENTIDAD_PROPIETARIA IS ''Entidad / Cartera (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ENTIDAD_ORIGEN IS ''Entidad origen de la operación financiera (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ENTIDAD_ORIGEN_ANTERIOR IS ''Entidad origen anterior de la operación financiera (Sociedad Patrimonial) (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_DESCRIPCION IS ''Descripción del activo.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_RATING IS ''Valoración del activo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_DIVISION_HORIZONTAL IS ''Indicador de si el activo está en división horizontal.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_FECHA_REV_CARGAS IS ''Última fecha de revisión de las cargas del activo.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_CON_CARGAS IS ''Indicador de si el activo tiene cargas.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.TIPO_ACTIVO IS ''Tipo de activo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.SUBTIPO_ACTIVO IS ''Subtipo activo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ESTADO_ACTIVO IS ''Estado del activo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.USO_ACTIVO IS ''Uso (destino) del activo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.TIPO_TITULO IS ''Catalogación del tipo de título del activo. (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.SUBTIPO_TITULO IS ''Catalogación del subtipo de título del activo. (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_VPO IS ''Indicador de si el activo es de vpo o no.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ESTADO_ADMISION IS ''Estado de admisión departamento de Admisión.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ESTADO_GESTION IS ''Estado de gestión departamento de Gestión.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.GESTION_HRE IS ''Indicador control de gestión por Haya.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.SELLO_CALIDAD IS ''Indicador estado calidad (sello calidad).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.SITUACION_COMERCIAL IS ''Situación comercial del activo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_ACA_CABECERA.ACT_COD_PROMOCION_EXTERNO IS ''Código de promoción Externo''';

IF V_ESQUEMA_2 != V_ESQUEMA_1 THEN

EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';

DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_2||''); 

