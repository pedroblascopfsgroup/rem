--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01'; --REM01
V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER'; --REMMASTER
V_USUARIO VARCHAR2(50 CHAR) := '#USUARIO_MIGRACION#';
V_TABLA VARCHAR2(40 CHAR) := 'ACT_DIS_DISTRIBUCION';
V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG_AID_INFCOMERCIAL_DISTR';
V_SENTENCIA VARCHAR2(2000 CHAR);

BEGIN
  
    DBMS_OUTPUT.PUT_LINE('[INICIO] Comienza la migración de ACTIVOS COMERCIAL-DISTRIBUCION');

	-------------------------------------------------
    --INSERCION EN ACT_DIS_DISTRIBUCION--
    -------------------------------------------------
    DBMS_OUTPUT.PUT_LINE('	[INFO] INSERCION EN ACT_DIS_DISTRIBUCION');
  
  
	EXECUTE IMMEDIATE ('
	INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||' (
		DIS_ID,
		ICO_ID,
		DIS_NUM_PLANTA,
		DD_TPH_ID,
		DIS_CANTIDAD,
		DIS_SUPERFICIE,
		DIS_DESCRIPCION,
		VERSION,
		USUARIOCREAR,
		FECHACREAR,
		BORRADO
	)
	WITH ACTIVOS_DESCARTADOS AS 
	(
		  SELECT ACT_NUMERO_ACTIVO 
		  FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' 
		  WHERE ACT_NUMERO_ACTIVO IN (
			  SELECT ACT_NUMERO_ACTIVO 
			  FROM (
				  SELECT MIG.ACT_NUMERO_ACTIVO, 
						 ACT.ACT_ID 
				  FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG
				  LEFT JOIN '||V_ESQUEMA||'.ACT_ACTIVO 	ACT ON ACT.ACT_NUM_ACTIVO = MIG.ACT_NUMERO_ACTIVO
				  WHERE ACT.ACT_NUM_ACTIVO IS NULL
					AND MIG.VALIDACION = 0
			  )
		  )
		  UNION
		  SELECT ACT_NUMERO_ACTIVO 
		  FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' 
		  WHERE ACT_NUMERO_ACTIVO IN (
			  SELECT ACT_NUMERO_ACTIVO 
			  FROM (
				  SELECT DISTINCT (ACT_NUMERO_ACTIVO||DIS_NUM_PLANTA||TIPO_HABITACULO), 
								   COUNT(*), 
								   ACT_NUMERO_ACTIVO 
				  FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||'
				  WHERE VALIDACION = 0
				  GROUP BY (ACT_NUMERO_ACTIVO||DIS_NUM_PLANTA||TIPO_HABITACULO), ACT_NUMERO_ACTIVO
				  HAVING COUNT(*)> 1
			  )
		  )
	),
	ACTIVOS_ICO_ACT AS 
	(
		SELECT ICO.ICO_ID, 
			   ICO.ACT_ID  
		FROM '||V_ESQUEMA||'.ACT_ICO_INFO_COMERCIAL ICO
		WHERE NOT EXISTS (
							  SELECT 1 
							  FROM '||V_ESQUEMA||'.ACT_DIS_DISTRIBUCION DIST
							  WHERE DIST.ICO_ID = ICO.ICO_ID
						 )
	)
	SELECT	'||V_ESQUEMA||'.S_ACT_DIS_DISTRIBUCION.NEXTVAL				DIS_ID,
			ICO.ICO_ID    												ICO_ID,
			MIG.DIS_NUM_PLANTA											DIS_NUM_PLANTA,
			(SELECT DD_TPH_ID 
			FROM '||V_ESQUEMA||'.DD_TPH_TIPO_HABITACULO DD 
			WHERE DD.DD_TPH_CODIGO = MIG.TIPO_HABITACULO)				DD_TPH_ID,
			MIG.DIS_CANTIDAD											DIS_CANTIDAD,
			MIG.DIS_SUPERFICIE											DIS_SUPERFICIE,
			MIG.DIS_DESCRIPCION											DIS_DESCRIPCION,
			''0''														VERSION,
			'''||V_USUARIO||'''											USUARIOCREAR,
			SYSDATE 													FECHACREAR,
			0															BORRADO
	FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' 	MIG
	JOIN '||V_ESQUEMA||'.ACT_ACTIVO 	    ACT ON ACT.ACT_NUM_ACTIVO = MIG.ACT_NUMERO_ACTIVO
	JOIN ACTIVOS_ICO_ACT					ICO ON ICO.ACT_ID = ACT.ACT_ID
	LEFT JOIN ACTIVOS_DESCARTADOS			DES ON DES.ACT_NUMERO_ACTIVO = MIG.ACT_NUMERO_ACTIVO
	WHERE DES.ACT_NUMERO_ACTIVO IS NULL
	  AND MIG.VALIDACION = 0
  ');
  
  DBMS_OUTPUT.PUT_LINE('	[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');
  
  COMMIT;
  
  V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''STATS'','''||V_TABLA||''',''1''); END;';
  EXECUTE IMMEDIATE V_SENTENCIA;
  
  DBMS_OUTPUT.PUT_LINE('[FIN] Acaba la migración de ACTIVOS COMERCIAL-DISTRIBUCION');
  

EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/
EXIT;
