--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REM01'; --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
V_TABLESPACE_IDX VARCHAR2(30 CHAR) := '#TABLESPACE_INDEX#';
V_TABLA VARCHAR2(40 CHAR) := 'MIG2_GEX_GASTOS_EXPEDIENTE';

BEGIN

SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

IF TABLE_COUNT > 0 THEN

    DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
    EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'';
    
END IF;

EXECUTE IMMEDIATE '
CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'
(
        GEX_COD_OFERTA                          NUMBER(16,0)                                            NOT NULL,
		GEX_COD_CONCEPTO_GASTO                  VARCHAR2(20),
		GEX_COD_TIPO_CALCULO                    VARCHAR2(20),
		GEX_IMPORTE_CALCULO                     NUMBER(16,2),
		GEX_IMPORTE_FINAL                       NUMBER(16,2),
		GEX_IND_PAGADOR                         NUMBER(1,0),
		GEX_IND_APROBADO                        NUMBER(1,0),
		GEX_COD_PROVEEDOR                       VARCHAR2(50),
		GEX_ACT_NUMERO_ACTIVO                   NUMBER(16,0),
		GEX_TIPO_CALCULO_ALQUILER               VARCHAR2(20),
		GEX_IMPORTE_CALCULO_ALQUILER            NUMBER(16,2)       
, VALIDACION NUMBER(1) DEFAULT 0 NOT NULL )'
;

DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA||' CREADA');  

IF V_ESQUEMA_2 != V_ESQUEMA_1 THEN

	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_2||''); 

END IF;

EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_COD_OFERTA IS ''Código identificador único de la Oferta Aceptada''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_COD_CONCEPTO_GASTO IS ''Código de la Concepto del Gasto (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_COD_TIPO_CALCULO IS ''Código del Tipo de Cálculo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_IMPORTE_CALCULO IS ''Importe del Cálculo''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_IMPORTE_FINAL IS ''Importe Final del Cálculo''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_IND_PAGADOR IS ''Indicador de si es Pagador''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_IND_APROBADO IS ''Indicador de si el Gasto está Aprobado''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_COD_PROVEEDOR IS ''Código del Proveedor en ORIGEN[Para Honorarios](Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_ACT_NUMERO_ACTIVO IS ''Código del activo sobre el que se aplica el gasto''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_TIPO_CALCULO_ALQUILER IS ''Código del Tipo de Cálculo (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG2_GEX_GASTOS_EXPEDIENTE.GEX_IMPORTE_CALCULO_ALQUILER IS ''Importe del cálculo de la oferta de alquiler''';

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
