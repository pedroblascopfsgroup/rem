WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REM01';       --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
V_TABLA VARCHAR2(40 CHAR) := 'MIG_AAG_AGRUPACIONES';

BEGIN

        SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

        IF TABLE_COUNT > 0 THEN

                DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');

                EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'';

        END IF;

        EXECUTE IMMEDIATE '
        CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'
        (
AGR_EXTERNO NUMBER(16,0) ,
AGR_GESTOR NUMBER(16,0) ,
AGR_MEDIADOR NUMBER(16,0) ,
TIPO_AGRUPACION VARCHAR2(20 CHAR) ,
AGR_NOMBRE VARCHAR2(250 CHAR) ,
AGR_DESCRIPCION VARCHAR2(250 CHAR) ,
AGR_FECHA_ALTA DATE ,
AGR_ELIMINADO NUMBER(1,0) ,
AGR_FECHA_BAJA DATE ,
AGR_PUBLICADO NUMBER(1,0) ,
AGR_TEXTO_WEB VARCHAR2(512 CHAR) ,
AGR_ACT_PRINCIPAL NUMBER(16,0) ,
LOC_AGRUP_OBRA_NUEVA VARCHAR2(20 CHAR) ,
PROV_AGRUP_OBRA_NUEVA VARCHAR2(20 CHAR) ,
ESTADO_AGRUP_OBRA_NUEVA VARCHAR2(20 CHAR) ,
ONV_DIR_AGRUP_OBRA_NUEVA VARCHAR2(255 CHAR) ,
ONV_CP_AGRUP_OBRA_NUEVA NUMBER(8,0) ,
ONV_ACRE_PDV_AGRUP_OBRA_NUEVA VARCHAR2(255 CHAR) ,
LOC_AGRUP_LOTE_RESTRINGIDO VARCHAR2(20 CHAR) ,
PRO_AGRUP_LOTE_RESTRINGIDO VARCHAR2(20 CHAR) ,
RES_DIR_AGRUP_LOTE_RESTRINGIDO VARCHAR2(255 CHAR) ,
RES_CP_AGRUP_LOTE_RESTRINGIDO NUMBER(8,0) ,
AGR_FECHA_FIRMA DATE ,
AGR_FECHA_VENCIMIENTO DATE ,
AGR_IS_FORMALIZACION NUMBER(1,0) ,
VALIDACION NUMBER(1) DEFAULT 0 NOT NULL )'
	;

	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA||' CREADA');
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_EXTERNO IS ''Código identificador único de la agrupación en origen''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_GESTOR IS ''Código gestor de la agrupación''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_MEDIADOR IS ''Código mediador de la agrupación''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.TIPO_AGRUPACION IS ''Tipo de agrupación (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_NOMBRE IS ''Nombre descriptivo de la agrupación.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_DESCRIPCION IS ''Descripción breve de la agrupación.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_FECHA_ALTA IS ''Fecha de creación de la agrupación.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_ELIMINADO IS ''Indicador de si la agrupación ha sido dada de baja o no.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_FECHA_BAJA IS ''Fecha de baja de la agrupación.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_PUBLICADO IS ''Indicador de si la agrupación ha sido publicada en la web o no.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_TEXTO_WEB IS ''Texto descriptivo para la web.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_ACT_PRINCIPAL IS ''Código identificador único del activo principal.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.LOC_AGRUP_OBRA_NUEVA IS ''Localidad (municipio) de la agrupación de obra nueva (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.PROV_AGRUP_OBRA_NUEVA IS ''Provincia de la agrupación de obra nueva (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.ESTADO_AGRUP_OBRA_NUEVA IS ''Estado de la agrupación de obra nueva (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.ONV_DIR_AGRUP_OBRA_NUEVA IS ''Dirección de la agrupación de obra nueva.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.ONV_CP_AGRUP_OBRA_NUEVA IS ''Código postal de la agrupación de obra nueva.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.ONV_ACRE_PDV_AGRUP_OBRA_NUEVA IS ''Acreedor de la agrupación de obra nueva.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.LOC_AGRUP_LOTE_RESTRINGIDO IS ''Localidad (municipio)  de la agrupación de lote restringido (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.PRO_AGRUP_LOTE_RESTRINGIDO IS ''Provincia de la agrupación de lote restringido (Código según Dic. Datos).''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.RES_DIR_AGRUP_LOTE_RESTRINGIDO IS ''Dirección de la agrupación de lote restringido.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.RES_CP_AGRUP_LOTE_RESTRINGIDO IS ''Código postal de la agrupación de lote restringido.''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_FECHA_FIRMA IS ''Fecha firma para Asistidas''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_FECHA_VENCIMIENTO IS ''Fecha vencimiento para Asistidas''';
EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA_1 || '.MIG_AAG_AGRUPACIONES.AGR_IS_FORMALIZACION IS ''Es de tipo formalizacion''';

    IF V_ESQUEMA_2 != V_ESQUEMA_1 THEN

		EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';

		DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_2||'');

END IF;

EXCEPTION

    WHEN OTHERS THEN
	DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
	DBMS_OUTPUT.put_line('-----------------------------------------------------------');
	DBMS_OUTPUT.put_line(SQLERRM);
	ROLLBACK;
	RAISE;




END; 
/ 
EXIT;
