--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01';
V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER';
V_USUARIO VARCHAR2(50 CHAR) := '#USUARIO_MIGRACION#';
V_TABLA VARCHAR2(40 CHAR) := 'COM_COMPRADOR';
V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG2_COM_COMPRADORES';
V_SENTENCIA VARCHAR2(32000 CHAR);

BEGIN
 
      --Inicio del proceso de volcado sobre CLC_CLIENTE_COMERCIAL
      DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
      
      EXECUTE IMMEDIATE '
        INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||' 
        (
              COM_ID, DD_TPE_ID, COM_NOMBRE, COM_APELLIDOS, DD_TDI_ID
            , COM_DOCUMENTO, COM_TELEFONO1, COM_TELEFONO2, COM_EMAIL, COM_DIRECCION, COM_CODIGO_POSTAL
            , USUARIOCREAR, FECHACREAR, CLC_ID, DD_LOC_ID, DD_PRV_ID, ID_COMPRADOR_URSUS
        )
        SELECT '||V_ESQUEMA||'.S_'||V_TABLA||'.NEXTVAL, TPE.DD_TPE_ID, MIG2.COM_NOMBRE, MIG2.COM_APELLIDOS, TDI.DD_TDI_ID
             , MIG2.COM_DOCUMENTO, MIG2.COM_TELEFONO1, MIG2.COM_TELEFONO2, MIG2.COM_EMAIL, MIG2.COM_DIRECCION, MIG2.COM_CODIGO_POSTAL
             , '''||V_USUARIO||''', SYSDATE, CLC.CLC_ID, LOC.DD_LOC_ID, PRV.DD_PRV_ID, CLC.CLC_NUM_CLIENTE_UVEM
        FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||' MIG2
        JOIN '||V_ESQUEMA||'.CLC_CLIENTE_COMERCIAL CLC ON CLC.CLC_REM_ID = MIG2.CLC_REM_ID OR (MIG2.CLC_REM_ID IS NULL AND CLC.CLC_COD_CLIENTE_PRINEX = MIG2.COM_COD_COMPRADOR )
        LEFT JOIN '||V_ESQUEMA||'.DD_TPE_TIPO_PERSONA TPE ON TPE.DD_TPE_CODIGO = MIG2.COM_COD_TIPO_PERSONA AND TPE.BORRADO = 0
        LEFT JOIN '||V_ESQUEMA||'.DD_TDI_TIPO_DOCUMENTO_ID TDI ON TDI.DD_TDI_CODIGO = MIG2.COM_COD_TIPO_DOCUMENTO AND TDI.BORRADO = 0
        LEFT JOIN '||V_ESQUEMA_MASTER||'.DD_LOC_LOCALIDAD LOC ON LOC.DD_LOC_CODIGO = MIG2.COM_COD_LOCALIDAD AND LOC.BORRADO = 0
        LEFT JOIN '||V_ESQUEMA_MASTER||'.DD_PRV_PROVINCIA PRV ON PRV.DD_PRV_CODIGO = MIG2.COM_COD_PROVINCIA AND PRV.BORRADO = 0
        LEFT JOIN '||V_ESQUEMA||'.'||V_TABLA||' COM ON COM.COM_DOCUMENTO = MIG2.COM_DOCUMENTO
        WHERE MIG2.VALIDACION = 0 AND COM.COM_ID IS NULL';
      
      DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');
      
      COMMIT;
      
      V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''STATS'','''||V_TABLA||''',''1''); END;';
      EXECUTE IMMEDIATE V_SENTENCIA;
      DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' ANALIZADA.');
 
EXCEPTION
      WHEN OTHERS THEN
            DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
            DBMS_OUTPUT.put_line('-----------------------------------------------------------');
            DBMS_OUTPUT.put_line(SQLERRM);
            ROLLBACK;
            RAISE;
END;

/

EXIT;
