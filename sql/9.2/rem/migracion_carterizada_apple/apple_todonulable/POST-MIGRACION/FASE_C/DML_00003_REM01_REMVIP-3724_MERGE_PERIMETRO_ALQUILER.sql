--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master

BEGIN			
			
	DBMS_OUTPUT.PUT_LINE('[INICIO]'); 

	EXECUTE IMMEDIATE  'MERGE INTO '||V_ESQUEMA||'.ACT_ACTIVO  T1
						USING (
							SELECT ACT.ACT_ID
							FROM '||V_ESQUEMA||'.AUX_MMC_REMVIP_3147              AUX
							JOIN '||V_ESQUEMA||'.ACT_ACTIVO                       ACT ON ACT.ACT_NUM_ACTIVO = AUX.ACTIVO
							JOIN '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA            SPS ON SPS.ACT_ID = ACT.ACT_ID
							JOIN '||V_ESQUEMA||'.BIE_BIEN                         BIE ON ACT.BIE_ID = BIE.BIE_ID
							JOIN '||V_ESQUEMA||'.BIE_ADJ_ADJUDICACION             ADJ ON ADJ.BIE_ID = BIE.BIE_ID
							LEFT JOIN '||V_ESQUEMA||'.ACT_PTA_PATRIMONIO_ACTIVO   PTA ON PTA.ACT_ID = ACT.ACT_ID
						) T2
						ON (T1.ACT_ID = T2.ACT_ID)
						WHEN MATCHED THEN UPDATE SET
							T1.DD_TCO_ID = (SELECT DD_TCO_ID FROM '||V_ESQUEMA||'.DD_TCO_TIPO_COMERCIALIZACION WHERE DD_TCO_CODIGO = ''03''),
							T1.DD_TAL_ID = (SELECT DD_TAL_ID FROM '||V_ESQUEMA||'.DD_TAL_TIPO_ALQUILER WHERE DD_TAL_CODIGO = ''01''), 
							T1.USUARIOMODIFICAR = ''REMVIP-3724'',
							T1.FECHAMODIFICAR = SYSDATE
    ';
	DBMS_OUTPUT.PUT_LINE('	[INFO] '||SQL%ROWCOUNT||' activos actualizados.');  

	EXECUTE IMMEDIATE  'MERGE INTO '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA T1
						USING (
							SELECT SPS.SPS_ID
							FROM '||V_ESQUEMA||'.AUX_MMC_REMVIP_3147              AUX
							JOIN '||V_ESQUEMA||'.ACT_ACTIVO                       ACT ON ACT.ACT_NUM_ACTIVO = AUX.ACTIVO
							JOIN '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA            SPS ON SPS.ACT_ID = ACT.ACT_ID
							JOIN '||V_ESQUEMA||'.BIE_BIEN                         BIE ON ACT.BIE_ID = BIE.BIE_ID
							JOIN '||V_ESQUEMA||'.BIE_ADJ_ADJUDICACION             ADJ ON ADJ.BIE_ID = BIE.BIE_ID
							LEFT JOIN '||V_ESQUEMA||'.ACT_PTA_PATRIMONIO_ACTIVO   PTA ON PTA.ACT_ID = ACT.ACT_ID
						) T2
						ON (T1.SPS_ID = T2.SPS_ID)
						WHEN MATCHED THEN UPDATE SET
						  T1.SPS_OCUPADO = 1,
						  T1.SPS_CON_TITULO = 1,
						  T1.DD_TPA_ID = (SELECT DD_TPA_ID FROM '||V_ESQUEMA||'.DD_TPA_TIPO_TITULO_ACT WHERE DD_TPA_CODIGO = ''01''),
						  T1.USUARIOMODIFICAR = ''REMVIP-3724'',
						  T1.FECHAMODIFICAR = SYSDATE
    ';
	DBMS_OUTPUT.PUT_LINE('	[INFO] '||SQL%ROWCOUNT||' activos actualizados.');  
	
	EXECUTE IMMEDIATE  'MERGE INTO '||V_ESQUEMA||'.ACT_PTA_PATRIMONIO_ACTIVO  T1
						USING (
							SELECT PTA.ACT_PTA_ID, 
								   ACT.ACT_ID
							FROM '||V_ESQUEMA||'.AUX_MMC_REMVIP_3147              AUX
							JOIN '||V_ESQUEMA||'.ACT_ACTIVO                       ACT ON ACT.ACT_NUM_ACTIVO = AUX.ACTIVO
							JOIN '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA            SPS ON SPS.ACT_ID = ACT.ACT_ID
							JOIN '||V_ESQUEMA||'.BIE_BIEN                         BIE ON ACT.BIE_ID = BIE.BIE_ID
							JOIN '||V_ESQUEMA||'.BIE_ADJ_ADJUDICACION             ADJ ON ADJ.BIE_ID = BIE.BIE_ID
							LEFT JOIN '||V_ESQUEMA||'.ACT_PTA_PATRIMONIO_ACTIVO   PTA ON PTA.ACT_ID = ACT.ACT_ID
						) T2
						ON (T1.ACT_PTA_ID = T2.ACT_PTA_ID AND T1.ACT_ID = T2.ACT_ID)
						WHEN MATCHED THEN UPDATE SET
							T1.CHECK_HPM = 1,
							T1.DD_EAL_ID = (SELECT DD_EAL_ID FROM '||V_ESQUEMA||'.DD_EAL_ESTADO_ALQUILER WHERE DD_EAL_CODIGO = ''02''),
							T1.USUARIOMODIFICAR = ''REMVIP-3724'',
							T1.FECHAMODIFICAR = SYSDATE
						WHEN NOT MATCHED THEN INSERT (T1.ACT_PTA_ID, T1.ACT_ID, T1.CHECK_HPM, T1.DD_EAL_ID, T1.USUARIOCREAR, T1.FECHACREAR, T1.BORRADO)
						VALUES (
							'||V_ESQUEMA||'.S_ACT_PTA_PATRIMONIO_ACTIVO.NEXTVAL,
							T2.ACT_ID,
							1,
							(SELECT DD_EAL_ID FROM '||V_ESQUEMA||'.DD_EAL_ESTADO_ALQUILER WHERE DD_EAL_CODIGO = ''02''),
							''REMVIP-3724'',
							SYSDATE,
							0
						)
    ';
	DBMS_OUTPUT.PUT_LINE('	[INFO] '||SQL%ROWCOUNT||' activos actualizados.');  


	COMMIT;
	DBMS_OUTPUT.PUT_LINE('[FIN]');

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;

/

EXIT
