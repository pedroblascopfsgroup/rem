--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE
WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

	V_TABLA VARCHAR2(30 CHAR) := 'AUX_CARGA_CARGAS_REMVIP_1564_2'; -- Variable para tabla de salida para el borrado
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error
	V_SQL VARCHAR2(4000 CHAR);
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'HREOS-4591';

BEGIN
	
	DBMS_OUTPUT.PUT_LINE('[INICIO]'||CHR(10));
	
	INSERT INTO REM01.ACT_PRY_PROYECTO (
	AGR_ID
	,DD_PRV_ID
	,DD_LOC_ID
	,PRY_DIRECCION
	,PRY_ACREEDOR_PDV
	,PRY_CP
	,DD_TPA_ID
	,DD_SAC_ID
	,DD_EAC_ID
	,PRY_GESTOR_ACTIVO
	,PRY_DOBLE_GESTOR_ACTIVO
	,PRY_GESTOR_COMERCIAL
	,DD_CRA_ID)
	WITH INFO_AFICIONAL AS (
		SELECT DISTINCT ID_PROYECTO, DD_CRA_ID, DD_PRV_ID
		FROM (
				SELECT AUX1.ID_PROYECTO, AUX1.ACT_NUM_ACTIVO, CRA.DD_CRA_ID, BIELOC.DD_PRV_ID,
				ROW_NUMBER()
					OVER (PARTITION BY AUX1.ID_PROYECTO, CRA.DD_CRA_ID, BIELOC.DD_PRV_ID ORDER BY AUX1.ID_PROYECTO ASC) AS RN2
		FROM (		
				SELECT ID_PROYECTO,ACT_NUM_ACTIVO,
				ROW_NUMBER()
							OVER (PARTITION BY ACT_NUM_ACTIVO ORDER BY ID_PROYECTO ASC) AS RN
				FROM REM01.AUX_AGRUP_PROYECTO_HREOS_4591
				WHERE EXISTE_ACTIVO = 1
				AND CARGAR_AGRUPACION = 1
			) AUX        
		INNER JOIN REM01.AUX_AGRUP_PROYECTO_HREOS_4591 AUX1 ON AUX1.ACT_NUM_ACTIVO = AUX.ACT_NUM_ACTIVO
		INNER JOIN REM01.ACT_ACTIVO ACT ON ACT.ACT_NUM_ACTIVO = AUX.ACT_NUM_ACTIVO
		INNER JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
		INNER JOIN REM01.BIE_LOCALIZACION BIELOC ON BIELOC.BIE_ID = ACT.BIE_ID
		)
	)
	SELECT
	AGR.AGR_ID              AS AGR_ID
	,INF.DD_PRV_ID          AS DD_PRV_ID
	,NULL                   AS DD_LOC_ID
	,NULL                   AS PRY_DIRECCION
	,NULL                   AS PRY_ACREEDOR_PDV
	,NULL                   AS PRY_CP
	,NULL                   AS DD_TPA_ID
	,NULL                   AS DD_SAC_ID
	,NULL                   AS DD_EAC_ID
	,NULL                   AS PRY_GESTOR_ACTIVO
	,NULL                   AS PRY_DOBLE_GESTOR_ACTIVO
	,NULL                   AS PRY_GESTOR_COMERCIAL
	,INF.DD_CRA_ID          AS DD_CRA_ID
	FROM REM01.ACT_AGR_AGRUPACION AGR
	INNER JOIN INFO_AFICIONAL INF ON INF.ID_PROYECTO = AGR.AGR_DESCRIPCION
	LEFT JOIN REM01.ACT_PRY_PROYECTO PRY ON PRY.AGR_ID = AGR.AGR_ID
	WHERE AGR.USUARIOCREAR = 'HREOS-4591'
	AND PRY.AGR_ID IS NULL;

	DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||' '||V_ESQUEMA||'.ACT_AGA_AGRUPACION_ACTIVO cargada. '||SQL%ROWCOUNT||' Filas.');
	
	COMMIT;

	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
