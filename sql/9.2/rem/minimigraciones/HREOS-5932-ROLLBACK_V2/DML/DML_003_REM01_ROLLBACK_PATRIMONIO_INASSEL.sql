		-- BORRAMOS LOS GESTORES SITUACION POSESORIA
		
		V_MSQL := 'DELETE FROM '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA';
							
		EXECUTE IMMEDIATE V_MSQL;
  		
  		DBMS_OUTPUT.PUT_LINE('	[INFO] Se han borado '||SQL%ROWCOUNT||' registros de la ACT_SPS_SIT_POSESORIA.'); 
  		
  		-- ACTUALIZAMOS CON EL BACKUP
		
		V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA (SELECT * FROM  '||V_ESQUEMA_BACKUP||'.'||V_TABLA_BACKUP||')';
				
  		EXECUTE IMMEDIATE V_MSQL;
  		
  		DBMS_OUTPUT.PUT_LINE('	[INFO] Se ha hecho rollback de  '||SQL%ROWCOUNT||' ACT_SPS_SIT_POSESORIA.'); 
		
		DBMS_OUTPUT.put_line('	[INFO] Se realiza rollback sobre tabla ACT_SPS_SIT_POSESORIA ');
  		
/** 	ROLLBACK PATRIMONIO		**/

