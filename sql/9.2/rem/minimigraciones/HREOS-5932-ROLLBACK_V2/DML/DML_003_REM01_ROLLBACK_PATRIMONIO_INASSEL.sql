/** 	ROLLBACK HISTORICO PATRIMONIO		**/

		DBMS_OUTPUT.put_line('	[INFO] Se realiza rollback sobre tabla HIST_PTA_PATRIMONIO_ACTIVO ');
		
		-- OBTENEMOS LA TABLA BACKUP
		V_MSQL := 'SELECT TABLA_BACKUP FROM '||V_ESQUEMA||'.AUX_MIG_ALQUILERES_TAB_AFEC  WHERE TABLA = ''HIST_PTA_PATRIMONIO_ACTIVO'' ';
