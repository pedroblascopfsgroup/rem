--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE
WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

	V_TABLA VARCHAR2(30 CHAR) := 'AUX_CARGA_LLAVES'; -- Variable para tabla de salida para el borrado
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error
	V_MSQL VARCHAR2(4000 CHAR);
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-1428';

BEGIN
	
	PL_OUTPUT := '[INICIO]'||CHR(10);

	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.ACT_LLV_LLAVE (LLV_ID, ACT_ID, LLV_ARCHIVO1, LLV_ARCHIVO2, USUARIOCREAR, FECHACREAR)
		WITH NUM_LLAVES AS (
		    SELECT LEVEL NUM_LLAVES
		    FROM DUAL 
		    CONNECT BY LEVEL <= (SELECT MAX(NUM_LLAVES) FROM '||V_ESQUEMA||'.AUX_CARGA_LLAVES)
		    )
		SELECT '||V_ESQUEMA||'.S_ACT_LLV_LLAVE.NEXTVAL LLV_ID, ACT.ACT_ID, AUX.ARMARIO, AUX.POSICION, '''||V_USUARIO||''' USUARIOCREAR, SYSDATE FECHACREAR 
		FROM '||V_ESQUEMA||'.AUX_CARGA_LLAVES AUX
		JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_NUM_ACTIVO_UVEM = AUX.ACT_NUM_UVEM AND ACT.BORRADO = 0
		JOIN NUM_LLAVES ON NUM_LLAVES.NUM_LLAVES <= AUX.NUM_LLAVES';
	EXECUTE IMMEDIATE V_MSQL;
	PL_OUTPUT := PL_OUTPUT || '[INFO] Se han insertado '||SQL%ROWCOUNT||' filas en la tabla llaves.' || CHR(10);

	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.ACT_MLV_MOVIMIENTO_LLAVE (MLV_ID, LLV_ID, DD_TTE_ID, MLV_COD_TENEDOR, MLV_NOM_TENEDOR
		    , MLV_FECHA_ENTREGA, USUARIOCREAR, FECHACREAR)
		SELECT '||V_ESQUEMA||'.S_ACT_MLV_MOVIMIENTO_LLAVE.NEXTVAL MLV_ID, LLV.LLV_ID, TTE.DD_TTE_ID, AUX.COD_MEDIADOR, AUX.DESC_MEDIADOR
		    , TO_DATE(AUX.FECHA_ENVIO, ''DD/MM/YYYY'') MVL_FECHA_ENTREGA, '''||V_USUARIO||''' USUARIOCREAR, SYSDATE FECHACREAR
		FROM '||V_ESQUEMA||'.ACT_LLV_LLAVE LLV
		JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = LLV.ACT_ID AND ACT.BORRADO = 0
		JOIN '||V_ESQUEMA||'.AUX_CARGA_LLAVES AUX ON AUX.ACT_NUM_UVEM = ACT.ACT_NUM_ACTIVO_UVEM
		JOIN '||V_ESQUEMA||'.DD_TTE_TIPO_TENEDOR TTE ON TTE.DD_TTE_CODIGO = ''01''
		WHERE LLV.BORRADO = 0 AND AUX.FECHA_ENVIO IS NOT NULL';
	EXECUTE IMMEDIATE V_MSQL;
	PL_OUTPUT := PL_OUTPUT || '[INFO] Se han insertado '||SQL%ROWCOUNT||' filas en la tabla de movimiento de llaves.' || CHR(10);

	COMMIT;

	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_MSQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
