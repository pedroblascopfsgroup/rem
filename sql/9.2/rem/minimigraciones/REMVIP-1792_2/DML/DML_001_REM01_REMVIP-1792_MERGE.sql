WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

V_SQL VARCHAR2(4000 CHAR);
TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01'; -- Configuracion Esquema
V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
V_TABLESPACE_IDX VARCHAR2(30 CHAR) := '#TABLESPACE_INDEX#';
V_TABLA_TMP VARCHAR2(40 CHAR) := 'TMP_UPDATE_FECHAS';
V_TABLA_TIT VARCHAR2(40 CHAR) := 'ACT_TIT_TITULO';
V_TABLA_AJD VARCHAR2(40 CHAR) := 'ACT_AJD_ADJJUDICIAL';
V_TABLA_ADJ VARCHAR2(40 CHAR) := 'BIE_ADJ_ADJUDICACION';
V_TABLA_ACT VARCHAR2(40 CHAR) := 'ACT_ACTIVO';
V_TABLA_ETI VARCHAR2(40 CHAR) := 'DD_ETI_ESTADO_TITULO';

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INFO] Comienza el mergeo a las tablas finales');

	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA_TIT||' T1
			USING(
				SELECT
				ACT.ACT_ID
				, TO_DATE(TMP.TIT_FECHA_ENTREGA_GESTORIA,''DD/MM/YYYY'') TIT_FECHA_ENTREGA_GESTORIA
				, TO_DATE(TMP.TIT_FECHA_PRESENT_HACIENDA,''DD/MM/YYYY'') TIT_FECHA_PRESENT_HACIENDA
				, TO_DATE(TMP.TIT_FECHA_PRESENT1_REG,''DD/MM/YYYY'') TIT_FECHA_PRESENT1_REG
				, TO_DATE(TMP.TIT_FECHA_PRESENT2_REG,''DD/MM/YYYY'') TIT_FECHA_PRESENT2_REG
				, ETI.DD_ETI_ID
				, TO_DATE(TMP.TIT_FECHA_INSC_REG ,''DD/MM/YYYY'') TIT_FECHA_INSC_REG
				FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' TMP
				JOIN '||V_ESQUEMA||'.'||V_TABLA_ACT||' ACT ON ACT.ACT_NUM_ACTIVO = TMP.ACT_NUM_ACTIVO
				JOIN '||V_ESQUEMA||'.'||V_TABLA_ETI||' ETI ON ETI.DD_ETI_CODIGO = TMP.DD_ETI_CODIGO) T2
			ON (T1.ACT_ID = T2.ACT_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.TIT_FECHA_ENTREGA_GESTORIA = T2.TIT_FECHA_ENTREGA_GESTORIA,
			T1.TIT_FECHA_PRESENT_HACIENDA = T2.TIT_FECHA_PRESENT_HACIENDA,
			T1.TIT_FECHA_PRESENT1_REG = T2.TIT_FECHA_PRESENT1_REG,
			T1.TIT_FECHA_PRESENT2_REG = T2.TIT_FECHA_PRESENT2_REG,
			T1.DD_ETI_ID = T2.DD_ETI_ID,
			T1.TIT_FECHA_INSC_REG = T2.TIT_FECHA_INSC_REG,
			T1.USUARIOMODIFICAR = ''REMVIP-1792'',
			T1.FECHAMODIFICAR = SYSDATE';
	
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('MERGEADOS '||SQL%ROWCOUNT||' REGISTROS EN LA TABLA '||V_TABLA_TIT);
	DBMS_OUTPUT.PUT_LINE('');
	
	
	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA_AJD||' T1
			USING(
				SELECT
				ACT.ACT_ID
				, TO_DATE(TMP.AJD_FECHA_ADJUDICACION,''DD/MM/YYYY'') AJD_FECHA_ADJUDICACION
				FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' TMP
				JOIN '||V_ESQUEMA||'.'||V_TABLA_ACT||' ACT ON ACT.ACT_NUM_ACTIVO = TMP.ACT_NUM_ACTIVO) T2
			ON (T1.ACT_ID = T2.ACT_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.AJD_FECHA_ADJUDICACION = T2.AJD_FECHA_ADJUDICACION,
			T1.USUARIOMODIFICAR = ''REMVIP-1792'',
			T1.FECHAMODIFICAR = SYSDATE';
			
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('MERGEADOS '||SQL%ROWCOUNT||' REGISTROS EN LA TABLA '||V_TABLA_AJD);
	DBMS_OUTPUT.PUT_LINE('');
	
	
	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA_ADJ||' T1
			USING(
				SELECT
				ACT.BIE_ID
				, TO_DATE(TMP.BIE_ADJ_F_DECRETO_FIRME,''DD/MM/YYYY'') BIE_ADJ_F_DECRETO_FIRME
				, TO_DATE(TMP.BIE_ADJ_F_ENVIO_ADICION,''DD/MM/YYYY'') BIE_ADJ_F_ENVIO_ADICION
				FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' TMP
				JOIN '||V_ESQUEMA||'.'||V_TABLA_ACT||' ACT ON ACT.ACT_NUM_ACTIVO = TMP.ACT_NUM_ACTIVO) T2
			ON (T1.BIE_ID = T2.BIE_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.BIE_ADJ_F_DECRETO_FIRME = T2.BIE_ADJ_F_DECRETO_FIRME,
			T1.BIE_ADJ_F_ENVIO_ADICION = T2.BIE_ADJ_F_ENVIO_ADICION,
			T1.USUARIOMODIFICAR = ''REMVIP-1792'',
			T1.FECHAMODIFICAR = SYSDATE';
			
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('MERGEADOS '||SQL%ROWCOUNT||' REGISTROS EN LA TABLA '||V_TABLA_ADJ);
	
	
	COMMIT;	  
 


EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
