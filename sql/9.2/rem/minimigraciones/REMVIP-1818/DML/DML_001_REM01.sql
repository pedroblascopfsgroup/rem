--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE
WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

	V_TABLA VARCHAR2(30 CHAR) := 'AUX_MMC_ECO_EXPEDIENTES'; -- Variable para tabla de salida para el borrado
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error
	V_SQL VARCHAR2(4000 CHAR);
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-1818';

BEGIN
	
	PL_OUTPUT := '[INICIO]'||CHR(10);

	V_SQL := '  MERGE INTO '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL T1
				USING (
					SELECT  AUX.ECO_NUM_EXPEDIENTE,
							AUX.ECO_FECHA_CONT_PROPIETARIO,
							AUX.ECO_PETICIONARIO_ANULACION,
							AUX.ECO_IMP_DEV_ENTREGAS,
							AUX.ECO_FECHA_DEV_ENTREGAS,
							AUX.ECO_FECHA_SANCION_COMITE 
					FROM '||V_ESQUEMA||'.'||V_TABLA||' AUX
				) T2
				ON (T1.ECO_NUM_EXPEDIENTE = T2.ECO_NUM_EXPEDIENTE)
				WHEN MATCHED THEN UPDATE 
				SET         
				  T1.ECO_FECHA_CONT_PROPIETARIO = NVL2(T1.ECO_FECHA_CONT_PROPIETARIO, T1.ECO_FECHA_CONT_PROPIETARIO, TRUNC(TO_DATE(SUBSTR(T2.ECO_FECHA_CONT_PROPIETARIO,1,10),''YYYY-MM-DD'')) )
				, T1.ECO_PETICIONARIO_ANULACION = NVL2(T1.ECO_PETICIONARIO_ANULACION, T1.ECO_PETICIONARIO_ANULACION, T2.ECO_PETICIONARIO_ANULACION )
				, T1.ECO_IMP_DEV_ENTREGAS =       NVL2(T1.ECO_IMP_DEV_ENTREGAS, T1.ECO_IMP_DEV_ENTREGAS, TO_NUMBER(T2.ECO_IMP_DEV_ENTREGAS))
				, T1.ECO_FECHA_DEV_ENTREGAS =     NVL2(T1.ECO_FECHA_DEV_ENTREGAS, T1.ECO_FECHA_DEV_ENTREGAS, TRUNC(TO_DATE(SUBSTR(T2.ECO_FECHA_DEV_ENTREGAS,1,10),''YYYY-MM-DD'')) )
				, T1.ECO_FECHA_SANCION_COMITE =   NVL2(T1.ECO_FECHA_SANCION_COMITE, T1.ECO_FECHA_SANCION_COMITE, TRUNC(TO_DATE(SUBSTR(T2.ECO_FECHA_SANCION_COMITE,1,10),''YYYY-MM-DD'')) )
				, T1.USUARIOMODIFICAR = NVL2(T1.ECO_FECHA_CONT_PROPIETARIO||T1.ECO_PETICIONARIO_ANULACION||T1.ECO_IMP_DEV_ENTREGAS||T1.ECO_FECHA_DEV_ENTREGAS||T1.ECO_FECHA_SANCION_COMITE, 
											 T1.USUARIOMODIFICAR, '''||V_USUARIO||''')
				, T1.FECHAMODIFICAR = NVL2(T1.ECO_FECHA_CONT_PROPIETARIO||T1.ECO_PETICIONARIO_ANULACION||T1.ECO_IMP_DEV_ENTREGAS||T1.ECO_FECHA_DEV_ENTREGAS||T1.ECO_FECHA_SANCION_COMITE, 
											 T1.FECHAMODIFICAR, SYSDATE)
	';
	EXECUTE IMMEDIATE V_SQL;
	COMMIT;

	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
