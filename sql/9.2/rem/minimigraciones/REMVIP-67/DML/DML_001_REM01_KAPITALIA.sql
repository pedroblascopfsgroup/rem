--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE
WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01'; 			--REM01
    V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER'; 	--REMMASTER
    V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-67';
    V_TABLA VARCHAR2(40 CHAR) := 'ACT_ICO_INFO_COMERCIAL';
    V_TABLA_MIG VARCHAR2(40 CHAR) := 'AUX_KAPITALIA';
    V_SENTENCIA VARCHAR2(2000 CHAR);

BEGIN

    DBMS_OUTPUT.PUT_LINE('[INICIO]');

    EXECUTE IMMEDIATE 'MERGE INTO REM01.ACT_ICO_INFO_COMERCIAL T1
		USING (SELECT ICO.ICO_ID, AUX.PVE_COD_REM ICO_MEDIADOR_ID
			FROM REM01.AUX_KAPITALIA AUX
			JOIN REM01.ACT_ACTIVO ACT ON ACT.ACT_NUM_ACTIVO = AUX.ACT_NUM_ACTIVO AND ACT.BORRADO = 0
			JOIN REM01.ACT_ICO_INFO_COMERCIAL ICO ON ICO.ACT_ID = ACT.ACT_ID AND ICO.BORRADO = 0
			WHERE AUX.PVE_COD_REM <> NVL(ICO.ICO_MEDIADOR_ID,0)) T2
		ON (T1.ICO_ID = T2.ICO_ID)
		WHEN MATCHED THEN UPDATE SET
			T1.ICO_MEDIADOR_ID = T2.ICO_MEDIADOR_ID, T1.USUARIOMODIFICAR = ''REMVIP-67'', T1.FECHAMODIFICAR = SYSDATE';
    DBMS_OUTPUT.PUT_LINE('	[INFO] - '||to_char(sysdate,'HH24:MI:SS')||' '||SQL%ROWCOUNT||' mediadores actualizados.');

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('[FIN]');

EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/
EXIT;
