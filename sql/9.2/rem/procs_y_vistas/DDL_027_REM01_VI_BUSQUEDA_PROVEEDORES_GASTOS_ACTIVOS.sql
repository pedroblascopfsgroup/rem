--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_BUSQUEDA_PROVEEDORES_ACTIVO' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_BUSQUEDA_PROVEEDORES_ACTIVO' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO 
	AS
		SELECT DISTINCT
				ACTPVE.PVE_ID,
				ACTPVE.PVE_COD_UVEM,
				ACTPVE.PVE_COD_REM,
				ACT.ACT_ID,
				ACT.ACT_NUM_ACTIVO,
				TPR.DD_TPR_CODIGO,
				TPR.DD_TPR_DESCRIPCION,
				ACTPVE.PVE_DOCIDENTIF,
				ACTPVE.PVE_NOMBRE,
				EPR.DD_EPR_CODIGO,
				EPR.DD_EPR_DESCRIPCION,
				AIN.AIN_FECHA_EXCLUSION
							
		FROM ' || V_ESQUEMA || '.ACT_PVE_PROVEEDOR ACTPVE
		LEFT JOIN ' || V_ESQUEMA || '.GPV_GASTOS_PROVEEDOR GPV on ACTPVE.PVE_ID = GPV.PVE_ID_EMISOR
		LEFT JOIN ' || V_ESQUEMA || '.GPV_ACT GPVACT on GPV.GPV_ID = GPVACT.GPV_ID
		LEFT JOIN ' || V_ESQUEMA || '.ACT_ACTIVO ACT on ACT.ACT_ID = GPVACT.ACT_ID
		LEFT JOIN ' || V_ESQUEMA || '.DD_TPR_TIPO_PROVEEDOR TPR on ACTPVE.DD_TPR_ID = TPR.DD_TPR_ID 
		LEFT JOIN ' || V_ESQUEMA || '.DD_EPR_ESTADO_PROVEEDOR EPR on ACTPVE.DD_EPR_ID = EPR.DD_EPR_ID
		LEFT JOIN ' || V_ESQUEMA || '.AIN_ACTIVO_INTEGRADO AIN on ACT.ACT_ID = AIN.ACT_ID AND ACTPVE.PVE_ID = AIN.PVE_ID';

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO...Creada OK');
  
  EXECUTE IMMEDIATE 'COMMENT ON TABLE ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO IS ''VISTA PARA RECOGER LOS PROVEEDORES DE LOS ACTIVOS''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.PVE_ID IS ''Id del proveedor''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.ACT_ID IS ''Id del activo''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.ACT_NUM_ACTIVO IS ''Número del activo''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.DD_TPR_CODIGO IS ''Tipo de proveedor código''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.DD_TPR_DESCRIPCION IS ''Tipo de proveedor descripción''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.PVE_DOCIDENTIF IS ''Documento proveedor''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.PVE_NOMBRE IS ''Nombre proveedor''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.DD_EPR_CODIGO IS ''Estado proveedor código''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.DD_EPR_DESCRIPCION IS ''Nombre proveedor descripción''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.AIN_FECHA_EXCLUSION IS ''Fecha de exclusión del proveedor del activo''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.DD_EPR_CODIGO IS ''Estado del proveedor código''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_BUSQUEDA_PROVEEDORES_ACTIVO.DD_EPR_DESCRIPCION IS ''Estado del proveedor descripción''';

  
  DBMS_OUTPUT.PUT_LINE('Creados los comentarios en CREATE VIEW '|| V_ESQUEMA ||'.VI_BUSQUEDA_PROVEEDORES_ACTIVO...Creada OK');
  
END;
/

EXIT;