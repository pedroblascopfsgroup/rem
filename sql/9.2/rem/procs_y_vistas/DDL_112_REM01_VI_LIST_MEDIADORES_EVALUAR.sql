--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_MASTER VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_LIST_MEDIADORES_EVALUAR' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_EVALUAR...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.V_LIST_MEDIADORES_EVALUAR';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_EVALUAR... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_LIST_MEDIADORES_EVALUAR' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_EVALUAR...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.V_LIST_MEDIADORES_EVALUAR';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_EVALUAR... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_EVALUAR...');
  EXECUTE IMMEDIATE 'CREATE OR REPLACE VIEW ' || V_ESQUEMA || '.V_LIST_MEDIADORES_EVALUAR 
		
	AS	
    SELECT PVE.PVE_ID                   AS ID_MEDIADOR,
      PVE.PVE_COD_REM                   AS CODIGO_REM ,
      PVE.PVE_NOMBRE                    AS NOMBRE_MEDIADOR ,
      PRV.DD_PRV_CODIGO                 AS COD_PROVINCIA ,
      PRV.DD_PRV_DESCRIPCION            AS DES_PROVINCIA ,
      LOC.DD_LOC_CODIGO                 AS COD_LOCALIDAD ,
      LOC.DD_LOC_DESCRIPCION            AS DES_LOCALIDAD ,
      PVE.PVE_FECHA_ALTA                AS FECHA_ALTA ,
      PVE.PVE_CUSTODIO                  AS ES_CUSTODIO ,
      EPR.DD_EPR_CODIGO                 AS COD_ESTADO_PROVEEDOR ,
      EPR.DD_EPR_DESCRIPCION            AS DES_ESTADO_PROVEEDOR ,
      CRA.DD_CRA_CODIGO                 AS COD_CARTERA ,
      CRA.DD_CRA_DESCRIPCION            AS DES_CARTERA ,
      CPRV.DD_CPR_CODIGO                AS COD_CALIFICACION_VIGENTE ,
      CPRV.DD_CPR_DESCRIPCION           AS DES_CALIFICACION_VIGENTE ,
      DECODE(PVE.PVE_TOP,1,1,NULL)      AS ES_TOP_150_VIGENTE ,
      CPRP.DD_CPR_CODIGO                AS COD_CALIFICACION_PROPUESTA ,
      CPRP.DD_CPR_DESCRIPCION           AS DES_CALIFICACION_PROPUESTA ,
      DECODE(PVE.PVE_TOP_PROP,1,1,NULL) AS ES_TOP_150_PROPUESTO,
      PVE.PVE_HOMOLOGADO                AS ES_HOMOLOGADO
    FROM '|| V_ESQUEMA ||'.ACT_PVE_PROVEEDOR PVE
    INNER JOIN '|| V_ESQUEMA ||'.DD_TPR_TIPO_PROVEEDOR TPR ON PVE.DD_TPR_ID = TPR.DD_TPR_ID AND TPR.DD_TPR_CODIGO = '04'
    LEFT OUTER JOIN '|| V_ESQUEMA ||'.ACT_ETP_ENTIDAD_PROVEEDOR ETP ON PVE.PVE_ID = ETP.PVE_ID
    LEFT OUTER JOIN '|| V_ESQUEMA ||'.DD_CRA_CARTERA CRA ON ETP.DD_CRA_ID = CRA.DD_CRA_ID
    LEFT OUTER JOIN '|| V_ESQUEMA_MASTER ||'.DD_PRV_PROVINCIA PRV ON PVE.DD_PRV_ID = PRV.DD_PRV_ID
    LEFT OUTER JOIN '|| V_ESQUEMA_MASTER ||'.DD_LOC_LOCALIDAD LOC ON PVE.DD_LOC_ID = LOC.DD_LOC_ID
    LEFT OUTER JOIN '|| V_ESQUEMA ||'.DD_EPR_ESTADO_PROVEEDOR EPR ON PVE.DD_EPR_ID = EPR.DD_EPR_ID
    LEFT OUTER JOIN '|| V_ESQUEMA ||'.DD_CPR_CALIFICACION_PROVEEDOR CPRV ON PVE.DD_CPR_ID = CPRV.DD_CPR_ID
    LEFT OUTER JOIN '|| V_ESQUEMA ||'.DD_CPR_CALIFICACION_PROVEEDOR CPRP ON PVE.DD_CPR_ID_PROP = CPRP.DD_CPR_ID
';

      	
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_EVALUAR...Creada OK');
  
END;
