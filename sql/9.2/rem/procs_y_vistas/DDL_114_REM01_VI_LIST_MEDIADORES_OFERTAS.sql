--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_MASTER VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_LIST_MEDIADORES_OFERTAS' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_OFERTAS...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.V_LIST_MEDIADORES_OFERTAS';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_OFERTAS... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_LIST_MEDIADORES_OFERTAS' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_OFERTAS...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.V_LIST_MEDIADORES_OFERTAS';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_OFERTAS... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_OFERTAS...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.V_LIST_MEDIADORES_OFERTAS 
		
	AS	
    SELECT ICO.ICO_MEDIADOR_ID AS ID_MEDIADOR,       -- MEDIADOR RESPONSABLE DE OFERTA
      OFR.OFR_ID               AS ID_OFERTA,        --ID OFERTA
      OFR.OFR_NUM_OFERTA       AS NUM_OFERTA,        --NUM OFERTA
      OFR.AGR_ID               AS ID_AGRUPACION,     -- AGRUPACION
      AOF.ACT_ID               AS ID_ACTIVO,         --ACTIVO
      OFR.DD_EOF_ID            AS EOF_ESTADO_OFERTA, -- ESTADOS DE OFERTAS
      OFR.DD_TOF_ID            AS TOF_TIPO_OFERTA,     --TIPOS DE OFERTAS
      ECO.ECO_ID               AS ID_EXPEDIENTE,  --ID EXPEDIENTE
      ECO.ECO_NUM_EXPEDIENTE   AS NUM_EXPEDIENTE, --NUM EXPEDIENTE
      ECO.DD_EEC_ID            AS EEC_ESTADO_EXPEDIENTE, --ESTADOS EXPEDIENTES COMERCIALES
      ACT.DD_SAC_ID            AS SAC_SUBTIPO_ACTIVO, --SUBTIPOS DE ACTIVOS
      OFR.OFR_IMPORTE_APROBADO AS IMPORTE_OFERTA, -- IMPORTE OFERTA
      OFR.CLC_ID               AS ID_OFERTANTE,   -- OFERTANTE
      CLC.CLC_NOMBRE ||' ' || CLC.CLC_APELLIDOS AS NOMBRE_OFERTANTE
    FROM '|| V_ESQUEMA ||'.OFR_OFERTAS OFR
    INNER JOIN '|| V_ESQUEMA ||'.ACT_OFR AOF ON OFR.OFR_ID = AOF.OFR_ID
    INNER JOIN '|| V_ESQUEMA ||'.ACT_ACTIVO ACT ON AOF.ACT_ID = ACT.ACT_ID
    INNER JOIN '|| V_ESQUEMA ||'.ACT_ICO_INFO_COMERCIAL ICO ON ACT.ACT_ID = ICO.ACT_ID
    INNER JOIN '|| V_ESQUEMA ||'.CLC_CLIENTE_COMERCIAL CLC ON OFR.CLC_ID = CLC.CLC_ID
    LEFT OUTER JOIN '|| V_ESQUEMA ||'.ECO_EXPEDIENTE_COMERCIAL ECO ON OFR.OFR_ID = ECO.OFR_ID
';

      	
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_LIST_MEDIADORES_OFERTAS...Creada OK');
  
END;
/

EXIT;