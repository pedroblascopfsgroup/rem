--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
V_SQL VARCHAR2(10000 CHAR);
V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
V_TABLA VARCHAR2(40 CHAR) := 'ACT_AHP_HIST_PUBLICACION';
ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

BEGIN			
			
	DBMS_OUTPUT.PUT_LINE('[INICIO]');

	DBMS_OUTPUT.PUT_LINE('[BORRADO ACTIVOS DE VENTA DUPLICADOS]');

	--borrado logico de ACTIVOS DE VENTA DUPLICADOS
	
		V_SQL := '
		UPDATE '||V_ESQUEMA||'.ACT_AHP_HIST_PUBLICACION 
		SET BORRADO = 1,
		   USUARIOBORRAR = ''BORRADO_DUPLICADOS_VENTA'',
		   FECHABORRAR = SYSDATE
		WHERE AHP_ID IN (
                   SELECT DISTINCT AHP_ID FROM (
                       SELECT AHP.AHP_ID,ACT.ACT_NUM_ACTIVO, TRUNC(AHP_FECHA_INI_VENTA), TRUNC(AHP_FECHA_FIN_VENTA), AHP_CHECK_OCULTAR_V, AHP.DD_TPU_V_ID, AHP.DD_MTO_V_ID, AHP.DD_EPV_ID, 
                              ROW_NUMBER() OVER (PARTITION BY ACT.ACT_NUM_ACTIVO, TRUNC(AHP_FECHA_INI_VENTA), TRUNC(AHP_FECHA_FIN_VENTA), AHP_CHECK_OCULTAR_V, AHP.DD_TPU_V_ID, AHP.DD_MTO_V_ID, AHP.DD_EPV_ID ORDER BY 1 ASC) AS RN
                       FROM '||V_ESQUEMA||'.ACT_AHP_HIST_PUBLICACION AHP
                       JOIN '||V_ESQUEMA||'.ACT_ACTIVO               ACT  ON ACT.ACT_ID = AHP.ACT_ID 
                       JOIN '||V_ESQUEMA||'.ACT_APU_ACTIVO_PUBLICACION APU ON ACT.ACT_ID = APU.ACT_ID
                       WHERE AHP.BORRADO = 0 AND ACT.BORRADO = 0 AND APU.DD_TCO_ID IN (1)
                       ORDER BY 1 ASC
                   )
                   WHERE RN > 1)';

	EXECUTE IMMEDIATE V_SQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han BORRADO '||SQL%ROWCOUNT||' registros VENTA');

	DBMS_OUTPUT.PUT_LINE('[BORRADO ACTIVOS DE ALQUILER DUPLICADOS]');

	--borrado logico de ACTIVOS DE ALQUILER DUPLICADOS
	
		V_SQL := '
		UPDATE '||V_ESQUEMA||'.ACT_AHP_HIST_PUBLICACION 
		SET BORRADO = 1,
		   USUARIOBORRAR = ''BORRADO_DUPLICADOS_ALQUILER'',
		   FECHABORRAR = SYSDATE
		WHERE AHP_ID IN (
                   SELECT DISTINCT AHP_ID FROM (
                       SELECT AHP.AHP_ID,ACT.ACT_NUM_ACTIVO, TRUNC(AHP_FECHA_INI_ALQUILER), TRUNC(AHP_FECHA_FIN_ALQUILER), AHP_CHECK_OCULTAR_A, AHP.DD_TPU_A_ID, AHP.DD_MTO_A_ID, AHP.DD_EPA_ID,  
                              ROW_NUMBER() OVER (PARTITION BY ACT.ACT_NUM_ACTIVO, TRUNC(AHP_FECHA_INI_ALQUILER), TRUNC(AHP_FECHA_FIN_ALQUILER), AHP_CHECK_OCULTAR_A, AHP.DD_TPU_A_ID, AHP.DD_MTO_A_ID, AHP.DD_EPA_ID ORDER BY 1 ASC) AS RN
                       FROM '||V_ESQUEMA||'.ACT_AHP_HIST_PUBLICACION AHP
                       JOIN '||V_ESQUEMA||'.ACT_ACTIVO               ACT  ON ACT.ACT_ID = AHP.ACT_ID 
                       JOIN '||V_ESQUEMA||'.ACT_APU_ACTIVO_PUBLICACION APU ON ACT.ACT_ID = APU.ACT_ID
                       WHERE AHP.BORRADO = 0 AND ACT.BORRADO = 0 AND APU.DD_TCO_ID IN (3,4)
                       ORDER BY 1 ASC
                   )
                   WHERE RN > 1)';

	EXECUTE IMMEDIATE V_SQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han BORRADO '||SQL%ROWCOUNT||' registros ALQUILER');

	DBMS_OUTPUT.PUT_LINE('[BORRADO ACTIVOS DE VENTA Y ALQUILER DUPLICADOS]');

	--borrado logico de ACTIVOS DE VENTA Y ALQUILER DUPLICADOS
	
		V_SQL := '
		UPDATE '||V_ESQUEMA||'.ACT_AHP_HIST_PUBLICACION 
		SET BORRADO = 1,
		   USUARIOBORRAR = ''BORRADO_DUPLICADOS_V_A'',
		   FECHABORRAR = SYSDATE
		WHERE AHP_ID IN (
                   SELECT DISTINCT AHP_ID FROM (
                       SELECT AHP.AHP_ID,ACT.ACT_NUM_ACTIVO, TRUNC(AHP_FECHA_INI_ALQUILER), TRUNC(AHP_FECHA_FIN_ALQUILER), AHP_CHECK_OCULTAR_A, AHP.DD_TPU_A_ID, AHP.DD_MTO_A_ID, AHP.DD_EPA_ID, 
                       TRUNC(AHP_FECHA_INI_VENTA), TRUNC(AHP_FECHA_FIN_VENTA), AHP_CHECK_OCULTAR_V, AHP.DD_TPU_V_ID, AHP.DD_MTO_V_ID, AHP.DD_EPV_ID,
                              ROW_NUMBER() OVER (PARTITION BY ACT.ACT_NUM_ACTIVO, TRUNC(AHP_FECHA_INI_ALQUILER), TRUNC(AHP_FECHA_FIN_ALQUILER), AHP_CHECK_OCULTAR_A, AHP.DD_TPU_A_ID, AHP.DD_MTO_A_ID, AHP.DD_EPA_ID,
                              TRUNC(AHP_FECHA_INI_VENTA), TRUNC(AHP_FECHA_FIN_VENTA), AHP_CHECK_OCULTAR_V, AHP.DD_TPU_V_ID, AHP.DD_MTO_V_ID, AHP.DD_EPV_ID ORDER BY 1 ASC) AS RN
                       FROM '||V_ESQUEMA||'.ACT_AHP_HIST_PUBLICACION AHP
                       JOIN '||V_ESQUEMA||'.ACT_ACTIVO               ACT  ON ACT.ACT_ID = AHP.ACT_ID 
                       JOIN '||V_ESQUEMA||'.ACT_APU_ACTIVO_PUBLICACION APU ON ACT.ACT_ID = APU.ACT_ID
                       WHERE AHP.BORRADO = 0 AND ACT.BORRADO = 0 AND APU.DD_TCO_ID IN (2)
                       ORDER BY 1 ASC
                   )
                   WHERE RN > 1)';

	EXECUTE IMMEDIATE V_SQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han BORRADO '||SQL%ROWCOUNT||' registros VENTA Y ALQUILER');

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN]');

EXCEPTION
    WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE));
      DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------');
      DBMS_OUTPUT.PUT_LINE(SQLERRM);
      ROLLBACK;
      RAISE;
END;

