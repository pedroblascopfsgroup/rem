WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
    V_SQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar         
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_COUNT NUMBER(16); -- Vble. para contar.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	V_USUARIO VARCHAR2(32 CHAR) := 'HREOS-3763';
    
 BEGIN
 
 V_SQL := 'MERGE INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD T1 USING (
			SELECT USU2.USU_ID, GAC.GEE_ID, TGE.DD_TGE_DESCRIPCION, USU.USU_USERNAME FROM '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC
			JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GAC.GEE_ID
			JOIN REMMASTER.USU_USUARIOS USU ON USU.USU_ID = GEE.USU_ID
			JOIN REMMASTER.DD_TGE_TIPO_GESTOR TGE ON TGE.DD_TGE_ID = TGE.DD_TGE_ID AND TGE.DD_TGE_CODIGO = ''GADM''
			JOIN '||V_ESQUEMA||'.ACT_GES_DIST_GESTORES ACT_GES ON ACT_GES.TIPO_GESTOR = ''GADM''
			JOIN REMMASTER.USU_USUARIOS USU2 ON USU2.USU_USERNAME = ACT_GES.USERNAME
			WHERE USU.USU_USERNAME = ''GESTADM''
			) T2 ON (T1.GEE_ID = T2.GEE_ID)
			WHEN MATCHED THEN UPDATE SET
				T1.USU_ID = T2.USU_ID,
				T1.USUARIOMODIFICAR = '''||V_USUARIO||''',
				T1.FECHAMODIFICAR = SYSDATE
		  ';
		  

  EXECUTE IMMEDIATE V_SQL;
  
    DBMS_OUTPUT.PUT_LINE('[INFO] Se han cambiado '||SQL%ROWCOUNT||' registros en la tabla GEE_GESTOR_ENTIDAD');
  
 V_SQL := 'MERGE INTO GEH_GESTOR_ENTIDAD_HIST T1 USING(
			SELECT USU2.USU_ID, GAH.GEH_ID, TGE.DD_TGE_DESCRIPCION, USU.USU_USERNAME FROM GAH_GESTOR_ACTIVO_HISTORICO GAH
			JOIN GEH_GESTOR_ENTIDAD_HIST GEH ON GEH.GEH_ID = GAH.GEH_ID
			JOIN REMMASTER.USU_USUARIOS USU ON USU.USU_ID = GEH.USU_ID
			JOIN REMMASTER.DD_TGE_TIPO_GESTOR TGE ON TGE.DD_TGE_ID = GEH.DD_TGE_ID AND TGE.DD_TGE_CODIGO = ''GADM''
			JOIN '||V_ESQUEMA||'.ACT_GES_DIST_GESTORES ACT_GES ON ACT_GES.TIPO_GESTOR = ''GADM''
			JOIN REMMASTER.USU_USUARIOS USU2 ON USU2.USU_USERNAME = ACT_GES.USERNAME
			WHERE USU.USU_USERNAME = ''GESTADM''
			) T2 ON (T1.GEH_ID = T2.GEH_ID)
			WHEN MATCHED THEN UPDATE SET
				T1.USU_ID = T2.USU_ID,
				T1.USUARIOMODIFICAR = '''||V_USUARIO||''',
				T1.FECHAMODIFICAR = SYSDATE
		    ';
  
  EXECUTE IMMEDIATE V_SQL;
 
  DBMS_OUTPUT.PUT_LINE('[INFO] Se han cambiado '||SQL%ROWCOUNT||' registros en la tabla GEH_GESTOR_ENTIDAD_HIST');

 COMMIT;
 
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;
/
EXIT;
