--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE
WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_ESQUEMA VARCHAR2(25 CHAR) := '#ESQUEMA#';-- '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR) := '#ESQUEMA_MASTER#';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-1414';
    V_SQL VARCHAR2(4000 CHAR); 
    ERR_NUM NUMBER;-- Numero de errores
    ERR_MSG VARCHAR2(2048);-- Mensaje de error
    PL_OUTPUT VARCHAR2(32000 CHAR);
    V_NUM_EXPEDIENTE NUMBER(16,0):= 80891;
    V_COD_RESERVA VARCHAR2(20 CHAR):= '02';  --RESERVADO
    V_COD_OFERTA VARCHAR2(20 CHAR):= '01'; --TRAMITADA
    ACT_NUM_ACTIVO NUMBER(16):= 5961610;

BEGIN

    --PONGO EL ACTIVO EN DISPONIBLE PARA LA VENTACON CON OFERTA Y QUITO LA FECHA DE VENTA DEL EXPEDIENTE
	V_SQL := 'UPDATE '||V_ESQUEMA||'.ACT_ACTIVO SET
				  DD_SCM_ID = (SELECT DD_SCM_ID FROM '||V_ESQUEMA||'.DD_SCM_SITUACION_COMERCIAL WHERE DD_SCM_CODIGO = ''03'')
				, USUARIOMODIFICAR = '''||V_USUARIO||'''
				, FECHAMODIFICAR   = SYSDATE
				WHERE ACT_NUM_ACTIVO = '||ACT_NUM_ACTIVO||'
				';
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('  [INFO] '||SQL%ROWCOUNT||' estado activo actualizado');

	
	
	V_SQL := 'UPDATE '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL SET 
				 ECO_FECHA_VENTA   = NULL
				, USUARIOMODIFICAR  = '''||V_USUARIO||'''
				, FECHAMODIFICAR    = SYSDATE 
				WHERE OFR_ID = (SELECT OFR_ID FROM '||V_ESQUEMA||'.OFR_OFERTAS
				WHERE OFR_NUM_OFERTA = ''70936431''
				)
				';

	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('  [INFO] '||SQL%ROWCOUNT||' fecha venta actualizada');


    --ACTUALIZO ESTADO OFERTA
      V_SQL := 'UPDATE '||V_ESQUEMA||'.OFR_OFERTAS
		SET DD_EOF_ID = (SELECT DD_EOF_ID FROM '||V_ESQUEMA||'.DD_EOF_ESTADOS_OFERTA WHERE DD_EOF_CODIGO = '''||V_COD_OFERTA||''') 
		, USUARIOMODIFICAR = '''||V_USUARIO||''' 
		, FECHAMODIFICAR = SYSDATE 
		WHERE OFR_NUM_OFERTA = ''70936431''';

    EXECUTE IMMEDIATE V_SQL;
    DBMS_OUTPUT.PUT_LINE('  [INFO] '||SQL%ROWCOUNT||' estado oferta actualizado');

   
    --REPOSICIONO TRAMITE Y ESTADO EXPEDIENTE
    #ESQUEMA#.REPOSICIONAMIENTO_TRAMITE('REMVIP-1414','80891','T013_ResultadoPBC','80891','06',PL_OUTPUT);
    DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

   COMMIT;

EXCEPTION
    WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------');
		DBMS_OUTPUT.PUT_LINE(SQLERRM);
		ROLLBACK;
		RAISE;

END;
/
EXIT;
