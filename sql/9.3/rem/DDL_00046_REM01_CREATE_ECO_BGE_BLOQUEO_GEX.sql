--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.  
    V_TABLA VARCHAR2(150 CHAR):= 'ECO_BGE_BLOQUEO_GEX'; -- Vble. con el nombre de la tabla.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

BEGIN

    -- ******** ECO_BGE_BLOQUEO_GEX *******
    DBMS_OUTPUT.PUT_LINE('******** '||V_TABLA||' ********'); 
    DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||'... Comprobaciones previas'); 
    
    -- Creacion Tabla ECO_BGE_BLOQUEO_GEX
    
    -- Comprobamos si existe la tabla   
    V_MSQL := 'SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = '''||V_TABLA||''' and owner = '''||V_ESQUEMA||'''';
    EXECUTE IMMEDIATE V_MSQL INTO V_NUM_TABLAS;
    
    -- Si no existe la tabla se crea
    IF V_NUM_TABLAS = 0 THEN        
        --Creamos la tabla
        DBMS_OUTPUT.PUT_LINE('[CREAMOS '||V_TABLA||']');
            V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||' (
                     BGE_ID                      NUMBER(16,0)                GENERATED BY DEFAULT AS IDENTITY
                    ,ECO_ID						 NUMBER(16,0)				 NOT NULL
                    ,BGE_FECHA_DESBLOQUEO        TIMESTAMP(6)		         
                    ,USU_ID         			 NUMBER(16,0)				 NOT NULL
                    ,BGE_MOTIVO_DESBLOQUEO       VARCHAR2(250 CHAR)
                    ,VERSION                     NUMBER(1,0)                 DEFAULT 0
                    ,USUARIOCREAR                VARCHAR2(50 CHAR) 
                    ,FECHACREAR                  TIMESTAMP(6)                DEFAULT SYSTIMESTAMP
                    ,USUARIOMODIFICAR            VARCHAR2(50 CHAR) 
                    ,FECHAMODIFICAR              TIMESTAMP(6)
                    ,USUARIOBORRAR               VARCHAR2(50 CHAR)
                    ,FECHABORRAR                 TIMESTAMP(6)
                    ,BORRADO                     NUMBER(1,0)                 DEFAULT 0
                    ,CONSTRAINT PK_BGE_ID        PRIMARY KEY (BGE_ID)
                    ,CONSTRAINT FK_BGE_ECO_ID 	 FOREIGN KEY (ECO_ID)     	 REFERENCES ECO_EXPEDIENTE_COMERCIAL (ECO_ID)
                    ,CONSTRAINT FK_BGE_USU_ID 	 FOREIGN KEY (USU_ID)     	 REFERENCES '||V_ESQUEMA_M||'.USU_USUARIOS (USU_ID)
                    )';        	

            EXECUTE IMMEDIATE V_MSQL;
            DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.'||V_TABLA||'... Tabla creada');
                    
        EXECUTE IMMEDIATE 'COMMENT ON TABLE  ' || V_ESQUEMA || '.'||V_TABLA||' IS ''Tabla de históricos.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.BGE_ID IS ''ID único del registro de la tabla.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.ECO_ID IS ''ID único que hace referencia a ECO_EXPEDIENTE_COMERCIAL.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.USU_ID IS ''ID único que hace referencia a USU_USUARIOS.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.BGE_FECHA_DESBLOQUEO IS ''Fecha de desbloqueo.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.BGE_MOTIVO_DESBLOQUEO IS ''Motivo por el cual se desbloquea''';
        
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.VERSION IS ''Indica la versión del registro''';  
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.USUARIOCREAR IS ''Indica el usuario que creó el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.FECHACREAR IS ''Indica la fecha en la que se creó el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.USUARIOMODIFICAR IS ''Indica el usuario que modificó el registro.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.FECHAMODIFICAR IS ''Indica la fecha en la que se modificó el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.USUARIOBORRAR IS ''Indica el usuario que borró el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.FECHABORRAR IS ''Indica la fecha en la que se borró el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.'||V_TABLA||'.BORRADO IS ''Indicador de borrado''';
    
        DBMS_OUTPUT.PUT_LINE('Creados los comentarios en TABLA '|| V_ESQUEMA ||'.'||V_TABLA||'... OK');
    
        
    ELSE
        --Si existe la tabla no se hace nada
        DBMS_OUTPUT.PUT_LINE('[INFO] La Tabla' || V_ESQUEMA || '.'||V_TABLA||'... ya existe');
    END IF;
COMMIT;
DBMS_OUTPUT.PUT_LINE('[INFO] COMMIT');
    
    
EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.PUT_LINE(V_MSQL);
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE;   
END;
/
EXIT;