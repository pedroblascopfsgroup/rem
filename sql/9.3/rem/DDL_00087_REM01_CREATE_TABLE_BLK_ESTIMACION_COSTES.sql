--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.  
    V_TABLA VARCHAR2(2400 CHAR):= 'BLK_ESTIMACION_COSTES'; -- Vble. con el nombre de la tabla.
    V_REF_TABLA_BLK VARCHAR2(2400 CHAR):='BLK_TIPO_ACTIVO'; --Vble. auxiliar para almacenar el nombre de la tabla de ref. 
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

BEGIN

    -- ******** BLK_ESTIMACION_COSTES *******
    DBMS_OUTPUT.PUT_LINE('******** '||V_TABLA||' ********'); 
    DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||'... Comprobaciones previas'); 
    
    -- Creacion Tabla BLK_ESTIMACION_COSTES
    
    -- Comprobamos si existe la tabla   
    V_SQL := 'SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = '''||V_TABLA||''' and owner = '''||V_ESQUEMA||'''';
    EXECUTE IMMEDIATE V_SQL INTO V_NUM_TABLAS;
    
    -- Si existe la tabla no se crea y si no la creamos
    IF V_NUM_TABLAS = 1 THEN 
            DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||'... Tabla YA EXISTE.');    	
	       
    ELSE
        --Creamos la tabla
        DBMS_OUTPUT.PUT_LINE('[CREAMOS '||V_TABLA||']');
        V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||' 
            (
                  BLK_COSTES_ID                 NUMBER(16,0)        GENERATED BY DEFAULT AS IDENTITY
				, BLK_TIPO_ID   				NUMBER(16,0) 	    NOT NULL                
                , BLK_PORCENTAJE_OFERTA         NUMBER(16,2)                        
                , VERSION                       NUMBER(1,0)         DEFAULT 0   NOT NULL    ENABLE
                , USUARIOCREAR                  VARCHAR2(50 CHAR)   NOT NULL    ENABLE
                , FECHACREAR                    TIMESTAMP(6)        DEFAULT SYSTIMESTAMP    NOT NULL    ENABLE
                , USUARIOMODIFICAR              VARCHAR2(50 CHAR) 
                , FECHAMODIFICAR                TIMESTAMP(6)
                , USUARIOBORRAR                 VARCHAR2(50 CHAR)
                , FECHABORRAR                   TIMESTAMP(6)
                , BORRADO                       NUMBER(1,0)         DEFAULT 0   NOT NULL    ENABLE       
            )';        	

        EXECUTE IMMEDIATE V_MSQL;
        DBMS_OUTPUT.PUT_LINE('[INFO] ' || V_ESQUEMA || '.'||V_TABLA||'... Tabla creada');

        -- Creamos primary key
        V_MSQL := 'ALTER TABLE '||V_ESQUEMA||'.'||V_TABLA||' ADD (CONSTRAINT '||V_TABLA||'_PK PRIMARY KEY (BLK_COSTES_ID))';
        EXECUTE IMMEDIATE V_MSQL;
        DBMS_OUTPUT.PUT_LINE('[INFO] ' ||V_ESQUEMA||'.'||V_TABLA||'_PK... PK creada.');

        -- Creamos foreign key
        V_MSQL := 'ALTER TABLE '||V_ESQUEMA||'.'||V_TABLA||' ADD (CONSTRAINT BLK_TIPO_ID_FK FOREIGN KEY (BLK_TIPO_ID)
	    REFERENCES '||V_ESQUEMA||'.'||V_REF_TABLA_BLK||' (BLK_TIPO_ID))';
	    EXECUTE IMMEDIATE V_MSQL;
	    DBMS_OUTPUT.PUT_LINE('[INFO] BLK_TIPO_ID_FK ... FK creada.');

            
        EXECUTE IMMEDIATE 'COMMENT ON TABLE  ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES IS ''ESTIMACIÓN DE COSTES POR TIPOLOGÍA ACTIVO''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.BLK_COSTES_ID IS ''ID único del coste de activo''';
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.BLK_TIPO_ID IS ''ID único del tipo de activo''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.BLK_PORCENTAJE_OFERTA IS ''PORCENTAJE sobre el valor oferta''';          
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.VERSION IS ''Indica la versión del registro''';  
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.USUARIOCREAR IS ''Indica el usuario que creó el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.FECHACREAR IS ''Indica la fecha en la que se creó el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.USUARIOMODIFICAR IS ''Indica el usuario que modificó el registro.''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.FECHAMODIFICAR IS ''Indica la fecha en la que se modificó el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.USUARIOBORRAR IS ''Indica el usuario que borró el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.FECHABORRAR IS ''Indica la fecha en la que se borró el registro''';
        EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.BLK_ESTIMACION_COSTES.BORRADO IS ''Indicador de borrado''';
    
        DBMS_OUTPUT.PUT_LINE('Creados los comentarios en TABLA '|| V_ESQUEMA ||'.BLK_ESTIMACION_COSTES... OK');
        COMMIT;
        DBMS_OUTPUT.PUT_LINE('[INFO] COMMIT');
    END IF; 
    
EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE;   
END;
/
EXIT;
