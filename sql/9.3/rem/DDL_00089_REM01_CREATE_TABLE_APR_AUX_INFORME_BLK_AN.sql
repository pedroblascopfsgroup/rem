--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); 
	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; 
	V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#';
	V_SQL VARCHAR2(4000 CHAR);
	V_NUM NUMBER(16);
	ERR_NUM NUMBER(25);  
	ERR_MSG VARCHAR2(1024 CHAR); 

	V_TABLA VARCHAR2(30 CHAR):= 'APR_AUX_INFORME_BLK_AN';

BEGIN	

	DBMS_OUTPUT.PUT_LINE('[INFO] INICIO DEL PROCESO.');
	
	V_MSQL := '
	SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = '''||V_TABLA||''' AND OWNER = '''||V_ESQUEMA||'''
	'
	;
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM;
	
	IF V_NUM != 0 THEN
	
		DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA '''||V_TABLA||''' YA EXISTE, SE BORRA Y SE VUELVE A CREAR');
		EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA||'.'||V_TABLA||' CASCADE CONSTRAINTS';
		
	ELSE
	
		DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA '||V_TABLA||' NO EXISTE. SE CREARÁ.');	

	END IF;	
	

	EXECUTE IMMEDIATE '
	CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||' (
	NUM_OFERTA_ID NUMBER(16,0),  
	NR VARCHAR2(50 CHAR),
	ACT_NUM_ACTIVO NUMBER(16,0),
	AGR_NUM_AGRUP_REM NUMBER(16,0),	
	DD_TDC_DESCRIPCION VARCHAR2(100 CHAR),
	DD_LOC_DESCRIPCION VARCHAR2(50 CHAR),
	DD_PRV_DESCRIPCION VARCHAR2(50 CHAR),
	BLK_TIPO_DESCRIPCION VARCHAR2(100 CHAR),
	VALOR_TASACION NUMBER(16,2),	
	FECHA_TASACION DATE,
	FECHA_EMISION_OFERTA DATE,
	PRECIO_INICIAL NUMBER(16,2),
	PRECIO_OFERTA NUMBER(16,2),
	NOMBRE_LICITADOR VARCHAR2(100 CHAR),
	COMENTARIOS VARCHAR2(2048 CHAR),
	TARIFA_API NUMBER(16,2),
	SERVICIO_VENTA_ADM VARCHAR2(10 CHAR),
	COSTE_TOTAL NUMBER(16,2),
	FECHA_VENTA_ESTIMADA DATE,
	PORTFOLIO VARCHAR2(50 CHAR),
	WEB_LINK VARCHAR2(256 CHAR),
	GESTOR_COMERCIAL VARCHAR2(256 CHAR),
	PVE_COD_API VARCHAR2(50 CHAR),
	HRE_OFFER_CODE VARCHAR2(50 CHAR),
	UNIDAD_VENTA_ID NUMBER(16,0),
	DIRECCION VARCHAR2(256 CHAR),
	NUM_BEDROOMS NUMBER(16,0),
	VAL_FECHA_INICIO DATE,
	NUM_LEADS NUMBER(16,0),
	DD_TBK_CODIGO VARCHAR2(20 CHAR)
	)
	'
	;

	DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA '''||V_TABLA||''' HA SIDO CREADA CON EXITO.');
  COMMIT;
    
  DBMS_OUTPUT.PUT_LINE('[INFO] PROCESO FINALIZADO.');

         
EXCEPTION

   WHEN OTHERS THEN
   
        ERR_NUM := SQLCODE;
        ERR_MSG := SQLERRM;

        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
        DBMS_OUTPUT.put_line(ERR_MSG);

        ROLLBACK;
        RAISE;          

END;
/
EXIT
