--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); 
	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; 
	V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#';
	V_SQL VARCHAR2(4000 CHAR);
	V_NUM NUMBER(16);
	ERR_NUM NUMBER(25);  
	ERR_MSG VARCHAR2(1024 CHAR); 

	V_TABLA VARCHAR2(30 CHAR):= 'AUX_APR_MAIN_TASACIONES_BBVA';

BEGIN	

	DBMS_OUTPUT.PUT_LINE('[INFO] INICIO DEL PROCESO.');
	
	V_MSQL := '
	SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = '''||V_TABLA||''' AND OWNER = '''||V_ESQUEMA||'''
	'
	;
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM;
	
	IF V_NUM != 0 THEN
	
		DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA '''||V_TABLA||''' YA EXISTE, SE BORRA Y SE VUELVE A CREAR');
		EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA||'.'||V_TABLA||' CASCADE CONSTRAINTS';
		
	ELSE
	
		DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA '||V_TABLA||' NO EXISTE. SE CREARÁ.');	

	END IF;	
	

	EXECUTE IMMEDIATE '
	CREATE TABLE '||V_ESQUEMA||'.'||V_TABLA||' (
	ID_ACTIVO_HAYA VARCHAR2(16 CHAR), 
	IMPORTE_TAS_TOTAL VARCHAR2(17 CHAR), 
	VALOR_MERCADO VARCHAR2(17 CHAR), 
	VALOR_SUELO VARCHAR2(17 CHAR), 
	VALOR_SEGURO VARCHAR2(17 CHAR), 
	VALOR_OBRA_TERMINADA VARCHAR2(17 CHAR), 
	PORCENTAJE_OBRA_EJEC VARCHAR2(6 CHAR), 
	PORCENTAJE_AJUSTE VARCHAR2(6 CHAR), 
	CIF_TASADORA VARCHAR2(15 CHAR), 
	NOMBRE_TASADORA VARCHAR2(100 CHAR), 
	FECHA_TASACION VARCHAR2(10 CHAR), 
	TECNICO_TASADOR VARCHAR2(50 CHAR), 
	TIPO_TASACION VARCHAR2(20 CHAR), 
	FECHA_CADUCIDAD VARCHAR2(10 CHAR), 
	TASACION_ACTIVA VARCHAR2(1 CHAR), 
	CONDICIONANTE VARCHAR2(1 CHAR), 
	ORDEN_ECO VARCHAR2(1 CHAR), 
	ADVERTENCIAS VARCHAR2(1 CHAR), 
	PORCENTAJE_PARTICIPA VARCHAR2(6 CHAR), 
	OBSERVACIONES VARCHAR2(512 CHAR), 
	IMPORTE_VAL_LEGAL_FINCA VARCHAR2(17 CHAR), 
	COSTE_UNI_REPO_NETO VARCHAR2(17 CHAR), 
	INDICE_TOTAL_DEPRE VARCHAR2(17 CHAR), 
	COSTE_REPO_NETO_FINALIZADO VARCHAR2(17 CHAR), 
	COSTE_REPO_NETO_ACTUAL VARCHAR2(17 CHAR), 
	CODIGO_FIRMA VARCHAR2(16 CHAR), 
	FECHA_RECEPCION_TASACION VARCHAR2(10 CHAR), 
	ID_EXTERNO VARCHAR2(16 CHAR), 
	ILOCALIZABLE VARCHAR2(1 CHAR)
	)
	'
	;

	DBMS_OUTPUT.PUT_LINE('[INFO] LA TABLA '''||V_TABLA||''' HA SIDO CREADA CON EXITO.');
  COMMIT;
    
  DBMS_OUTPUT.PUT_LINE('[INFO] PROCESO FINALIZADO.');

         
EXCEPTION

   WHEN OTHERS THEN
   
        ERR_NUM := SQLCODE;
        ERR_MSG := SQLERRM;

        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
        DBMS_OUTPUT.put_line(ERR_MSG);

        ROLLBACK;
        RAISE;          

END;
/
EXIT
