--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

	TABLE_COUNT NUMBER(1,0) := 0;
	V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
	V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REMMASTER'; --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
	V_TABLA VARCHAR2(40 CHAR) := 'ACT_COT_COMPLEMENTO_TITULO';

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INICIO]');

	DBMS_OUTPUT.PUT_LINE('[INFO] BUSCAMOS TABLA '||V_TABLA||'');

	SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

	IF TABLE_COUNT = 1 THEN

		DBMS_OUTPUT.PUT_LINE('[INFO] BUSCAMOS CAMPO COT_FECHA_ALTA EN TABLA '||V_TABLA||'');

		SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TAB_COLUMNS WHERE TABLE_NAME = ''||V_TABLA||'' AND COLUMN_NAME = 'COT_FECHA_ALTA' AND OWNER= ''||V_ESQUEMA_1||'' AND DATA_TYPE != 'DATE';

		IF TABLE_COUNT = 1 THEN

			DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICAMOS CAMPO COT_FECHA_ALTA EN TABLA '||V_TABLA||' A TIPO DATE');

			EXECUTE IMMEDIATE 'ALTER TABLE '||V_ESQUEMA_1||'.'||V_TABLA||' MODIFY COT_FECHA_ALTA DATE';
			
			DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||', CAMPO COT_FECHA_ALTA (DATE) MODIFICADO CORRECTAMENTE.');

		ELSE 

			DBMS_OUTPUT.PUT_LINE('[INFO] CAMPO COT_FECHA_ALTA EN TABLA '||V_TABLA||' NO EXISTE O YA ES TIPO DATE');

		END IF;

	ELSE 

		DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_TABLA||' NO EXISTE');
			
	END IF;

	DBMS_OUTPUT.PUT_LINE('[FIN]');

	COMMIT;

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;