WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF; 
DECLARE

	V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejECVtar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_NUM_FILAS NUMBER(16);
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	V_USU VARCHAR2(30 CHAR) := 'REMVIP-8272'; -- USUARIOCREAR/USUARIOMODIFICAR.
    V_FUN_CODIGO VARCHAR2(50) := 'VALORES_PERIMETRO_APPLE';
	V_PERFIL VARCHAR2(30 CHAR) := 'USUMSVPAPPLE';
	V_PERFIL_DESC VARCHAR2(100 CHAR) := 'Usuario masivo perimetro Apple';

	TYPE T_USUARIO IS TABLE OF VARCHAR2(150);
    TYPE T_ARRAY_USUARIO IS TABLE OF T_USUARIO;
    V_USUARIO T_ARRAY_USUARIO := T_ARRAY_USUARIO(
      T_USUARIO('rvazquez'),
      T_USUARIO('ajimenezj'),
	  T_USUARIO('adiazleante'),
      T_USUARIO('jhernandezma')
    ); 
    V_TMP_USUARIO T_USUARIO; 

BEGIN	


	DBMS_OUTPUT.PUT_LINE('[INICIO] TABLA PEF_PERFILES');
	
	V_MSQL := 'SELECT COUNT(*) FROM '||V_ESQUEMA||'.PEF_PERFILES WHERE PEF_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0';
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM_FILAS;
	
	IF V_NUM_FILAS = 0 THEN
	
		V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.PEF_PERFILES (PEF_ID, PEF_CODIGO, PEF_DESCRIPCION, PEF_DESCRIPCION_LARGA,
					USUARIOCREAR, FECHACREAR)
					SELECT '||V_ESQUEMA||'.S_PEF_PERFILES.NEXTVAL,'''||V_PERFIL||''','''||V_PERFIL_DESC||''','''||V_PERFIL_DESC||''',
					'''||V_USU||''',SYSDATE FROM DUAL';
	
		EXECUTE IMMEDIATE V_MSQL;
    
		DBMS_OUTPUT.PUT_LINE('[FIN] PERFIL INSERTADO');
		
	ELSE
		
		DBMS_OUTPUT.PUT_LINE('[FIN] PERFIL YA EXISTE');
		
	END IF;

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[INICIO] TABLA ZON_PEF_USU');

	V_MSQL := 'SELECT COUNT(*) FROM '||V_ESQUEMA||'.PEF_PERFILES WHERE PEF_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0';
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM_FILAS;

	IF V_NUM_FILAS = 0 THEN

		DBMS_OUTPUT.PUT_LINE('[FIN] PERFIL NO EXISTE');

	ELSE

		FOR I IN V_USUARIO.FIRST .. V_USUARIO.LAST 
    	LOOP
        	V_TMP_USUARIO := V_USUARIO(I);

			V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.ZON_PEF_USU 
						WHERE PEF_ID = (SELECT PEF_ID FROM '||V_ESQUEMA||'.PEF_PERFILES WHERE PEF_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0) 
						AND USU_ID = (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||TRIM(V_TMP_USUARIO(1))||''' AND BORRADO = 0)
						AND BORRADO = 0';
			EXECUTE IMMEDIATE V_MSQL INTO V_NUM_FILAS;

			IF V_NUM_FILAS > 0 THEN	  
				
				DBMS_OUTPUT.PUT_LINE('[INFO] Ya existen los datos en la tabla '||V_ESQUEMA||'.ZON_PEF_USU...no se modifica nada.');

			ELSE

				DBMS_OUTPUT.PUT_LINE('[INFO] INSERTAMOS USUARIO '||TRIM(V_TMP_USUARIO(1))||' EN ZON_PEF_USU ASOCIADO AL PERFIL '||V_PERFIL||'.');
			
				V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.ZON_PEF_USU (ZPU_ID, ZON_ID, PEF_ID, USU_ID, USUARIOCREAR, FECHACREAR) 
							SELECT '||V_ESQUEMA||'.S_ZON_PEF_USU.NEXTVAL,
							(SELECT ZON_ID FROM '||V_ESQUEMA||'.ZON_ZONIFICACION WHERE ZON_DESCRIPCION = ''REM'' AND BORRADO = 0),
							(SELECT PEF_ID FROM '||V_ESQUEMA||'.PEF_PERFILES WHERE PEF_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0),
							(SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||TRIM(V_TMP_USUARIO(1))||''' AND BORRADO = 0),
							'''||V_USU||''', SYSDATE FROM DUAL';
				EXECUTE IMMEDIATE V_MSQL;
				
		    END IF;	

		END LOOP;

		DBMS_OUTPUT.PUT_LINE('[INFO] Datos de la tabla '||V_ESQUEMA||'.ZON_PEF_USU insertados correctamente.');

	END IF;

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[INICIO] TABLA FUN_PEF');
	
	V_MSQL := 'SELECT COUNT(*) FROM '||V_ESQUEMA||'.FUN_PEF WHERE PEF_ID = (SELECT PEF_ID FROM '||V_ESQUEMA||'.PEF_PERFILES
					WHERE PEF_CODIGO = '''||V_PERFIL||''') AND BORRADO = 0';
	EXECUTE IMMEDIATE V_MSQL INTO V_NUM_FILAS;
	
	IF V_NUM_FILAS = 0 THEN
	
		V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.FUN_PEF (FP_ID, FUN_ID, PEF_ID, USUARIOCREAR, FECHACREAR)
					SELECT '||V_ESQUEMA||'.S_FUN_PEF.NEXTVAL,
					(SELECT FUN_ID FROM '||V_ESQUEMA_M||'.FUN_FUNCIONES WHERE FUN_DESCRIPCION='''||V_FUN_CODIGO||''' AND BORRADO = 0),
					(SELECT PEF_ID FROM '||V_ESQUEMA||'.PEF_PERFILES WHERE PEF_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0),
					'''||V_USU||''',SYSDATE FROM DUAL';
	
		EXECUTE IMMEDIATE V_MSQL;
    
		DBMS_OUTPUT.PUT_LINE('[FIN] PERFIL ASIGNADO A FUNCION');
		
	ELSE
		
		DBMS_OUTPUT.PUT_LINE('[FIN] PERFIL YA ASIGNADO A FUNCION');
		
	END IF;

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN] USUARIOS ASIGNADO A PERFIL CREADO '||V_PERFIL||' Y PERFIL ASIGNADO A FUNCION '||V_FUN_CODIGO||'');
 
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;
/
EXIT;
