WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;

DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	
    V_USUARIO VARCHAR2(25 CHAR):= 'HREOS-12948';
    V_NOMBRE_TABLA_AUX VARCHAR2(50 CHAR):= 'AUX_CALC_ADECUA_GENCAT';
    V_EXISTS NUMBER(1);
    PL_OUTPUT VARCHAR2(32000 CHAR);

BEGIN	

	
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');
	
	V_MSQL := 'SELECT COUNT(1) FROM ALL_TABLES WHERE TABLE_NAME = '''||V_NOMBRE_TABLA_AUX||''' AND OWNER = '''||V_ESQUEMA||'''';

	EXECUTE IMMEDIATE V_MSQL INTO V_EXISTS;
    IF V_EXISTS = 1 THEN 
        V_MSQL := 'DROP TABLE '||V_ESQUEMA||'.'||V_NOMBRE_TABLA_AUX||' PURGE';

        EXECUTE IMMEDIATE V_MSQL;
        DBMS_OUTPUT.PUT_LINE('Borrada tabla '''||V_NOMBRE_TABLA_AUX||'''');
    ELSE
        DBMS_OUTPUT.PUT_LINE('No existe la tabla '''||V_NOMBRE_TABLA_AUX||'''');
    END IF;

    V_MSQL := 'CREATE TABLE '||V_ESQUEMA||'.AUX_CALC_ADECUA_GENCAT AS
		WITH GESTOR AS (
		    SELECT  ACT_ID, USU_ID, ROW_NUMBER() OVER (PARTITION BY ACT_ID ORDER BY GEE.FECHACREAR) RN
		    FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
		    JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
		    JOIN '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR TGE ON TGE.DD_TGE_ID = GEE.DD_TGE_ID
		        AND TGE.DD_TGE_CODIGO = ''GACT''
		    WHERE GEE.BORRADO = 0
		    )
		, SUPERVISOR AS (
		    SELECT ACT_ID, USU_ID, ROW_NUMBER() OVER (PARTITION BY ACT_ID ORDER BY GEE.FECHACREAR) RN
		    FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
		    JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
		    JOIN '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR TGE ON TGE.DD_TGE_ID = GEE.DD_TGE_ID
		        AND TGE.DD_TGE_CODIGO = ''SUPACT''
		    WHERE GEE.BORRADO = 0
		    )
		SELECT TRA.TRA_ID,
		    '||V_ESQUEMA||'.S_TAR_TAREAS_NOTIFICACIONES.NEXTVAL TAR_ID,
		    '||V_ESQUEMA||'.S_TEX_TAREA_EXTERNA.NEXTVAL TEX_ID,
		    ACT.ACT_ID,
		    TAPCOM.TAP_ID, 
		    TAPCOM.TAP_DESCRIPCION, 
		    GES.USU_ID, 
		    SUP.USU_ID SUP_ID,
		    STA.DD_STA_ID
		FROM '||V_ESQUEMA||'.TEX_TAREA_EXTERNA TEX
		JOIN '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC ON TAC.TAR_ID = TEX.TAR_ID
		JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = TAC.ACT_ID
		JOIN '||V_ESQUEMA||'.TAP_TAREA_PROCEDIMIENTO TAP ON TAP.TAP_ID = TEX.TAP_ID AND TAP.TAP_CODIGO = ''T016_ProcesoAdecuacion''
		JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.TAR_ID = TEX.TAR_ID AND TAR_TAREA_FINALIZADA = 0
		JOIN '||V_ESQUEMA||'.ACT_TRA_TRAMITE TRA ON TRA.TRA_ID = TAC.TRA_ID
		JOIN '||V_ESQUEMA||'.TAP_TAREA_PROCEDIMIENTO TAPCOM ON TAPCOM.TAP_CODIGO = ''T016_ComunicarGENCAT''
		JOIN GESTOR GES ON GES.ACT_ID = ACT.ACT_ID AND GES.RN = 1
		JOIN SUPERVISOR SUP ON SUP.ACT_ID = ACT.ACT_ID AND SUP.RN = 1
		LEFT JOIN '||V_ESQUEMA_M||'.DD_STA_SUBTIPO_TAREA_BASE STA ON STA.DD_STA_ID = TAPCOM.DD_STA_ID';

    EXECUTE IMMEDIATE V_MSQL;
    
    DBMS_OUTPUT.PUT_LINE('Creada tabla '''||V_NOMBRE_TABLA_AUX||'''');
	
      V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_ADG_ADECUACION_GENCAT T1
		USING (
		    WITH ACTIVOS_CON_CEDULA AS (
		        SELECT ADO.ACT_ID
		        FROM '||V_ESQUEMA||'.ACT_ADO_ADMISION_DOCUMENTO ADO
		        JOIN '||V_ESQUEMA||'.ACT_CFD_CONFIG_DOCUMENTO CFD ON CFD.CFD_ID = ADO.CFD_ID AND CFD.BORRADO = 0
		        JOIN '||V_ESQUEMA||'.DD_TPD_TIPO_DOCUMENTO TPD ON TPD.DD_TPD_ID = CFD.DD_TPD_ID AND TPD.BORRADO = 0
		            AND TPD.DD_TPD_CODIGO IN (''13'',''90'')
		        JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACTIVO TPA ON TPA.DD_TPA_ID = CFD.DD_TPA_ID AND TPA.BORRADO = 0
		            AND TPA.DD_TPA_CODIGO = ''02''
		        WHERE ADO.BORRADO = 0 
		            AND NVL(ADO.ADO_FECHA_OBTENCION,TO_DATE(''31/12/2099'',''DD/MM/YYYY'')) < SYSDATE
		            AND NVL(ADO.ADO_FECHA_CADUCIDAD,TO_DATE(''31/12/2099'',''DD/MM/YYYY'')) > SYSDATE
		    )
		    SELECT DISTINCT ADG.ADG_ID, DTR.BIE_DREG_SUPERFICIE_CONSTRUIDA, CASE WHEN CED.ACT_ID IS NOT NULL THEN ''OBT'' ELSE ''NO'' END AS CEDULA
		    FROM '||V_ESQUEMA||'.ACT_ADG_ADECUACION_GENCAT ADG
		    JOIN '||V_ESQUEMA||'.ACT_CMG_COMUNICACION_GENCAT CMG ON CMG.CMG_ID = ADG.CMG_ID AND CMG.BORRADO = 0
		    JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = CMG.ACT_ID AND ACT.BORRADO = 0
			JOIN '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC ON ACT.ACT_ID = TAC.ACT_ID AND TAC.BORRADO = 0
			JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.TAR_ID = TAC.TAR_ID AND TAR.BORRADO = 0 AND TAR.TAR_TAREA_FINALIZADA = 0
			JOIN '||V_ESQUEMA||'.TEX_TAREA_EXTERNA TEX ON TAR.TAR_ID = TEX.TAR_ID AND TEX.BORRADO = 0
			JOIN '||V_ESQUEMA||'.TAP_TAREA_PROCEDIMIENTO TAP ON TAP.TAP_ID = TEX.TAP_ID AND TAP.TAP_CODIGO = ''T016_ProcesoAdecuacion''
		    LEFT JOIN '||V_ESQUEMA||'.BIE_DATOS_REGISTRALES DTR ON DTR.BIE_ID = ACT.BIE_ID AND DTR.BORRADO = 0
		    LEFT JOIN ACTIVOS_CON_CEDULA CED ON CED.ACT_ID = ACT.ACT_ID
		    WHERE ADG.BORRADO = 0) T2
		ON (T1.ADG_ID = T2.ADG_ID)
		WHEN MATCHED THEN UPDATE SET
		    T1.ADG_REFORMA = (CASE
		                        WHEN T2.CEDULA != ''OBT'' THEN 0
		                        ELSE 1
		                    END),
		    T1.ADG_IMPORTE = (CASE
		                        WHEN T2.CEDULA = ''OBT'' AND T2.BIE_DREG_SUPERFICIE_CONSTRUIDA IS NOT NULL THEN ROUND(T2.BIE_DREG_SUPERFICIE_CONSTRUIDA * 52.3, 2)
		                        ELSE NULL
		                    END),
		    T1.FECHAMODIFICAR = SYSDATE,
		    T1.USUARIOMODIFICAR = '''|| V_USUARIO || '''';
      EXECUTE IMMEDIATE V_MSQL;
      DBMS_OUTPUT.PUT_LINE('Actualizadas '||SQL%ROWCOUNT||' tareas de adecuación del trámite de GENCAT.');

      
      V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES (TAR_ID, DD_EIN_ID
		        , DD_STA_ID, TAR_CODIGO, TAR_TAREA, TAR_DESCRIPCION, TAR_FECHA_INI
		        , USUARIOCREAR, FECHACREAR, TAR_FECHA_VENC, DTYPE)
			    SELECT 
			        AUX.TAR_ID,
			        (SELECT EIN.DD_EIN_ID FROM '||V_ESQUEMA_M||'.DD_EIN_ENTIDAD_INFORMACION EIN WHERE EIN.DD_EIN_CODIGO = ''61''),
			        AUX.DD_STA_ID,
			        1,
			        AUX.TAP_DESCRIPCION,
			        AUX.TAP_DESCRIPCION,
			        SYSDATE,
			        '''|| V_USUARIO || ''',
			        SYSDATE,
			        SYSDATE + 15 AS TAR_FECHA_VENC,
			        ''EXTTareaNotificacion''
			    FROM '||V_ESQUEMA||'.'||V_NOMBRE_TABLA_AUX||' AUX';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE('Creadas '||SQL%ROWCOUNT||' tareas de comunicación del trámite de GENCAT.');


    V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.ETN_EXTAREAS_NOTIFICACIONES (TAR_ID, TAR_FECHA_VENC_REAL)
    SELECT AUX.TAR_ID,
        SYSDATE + 15 AS TAR_FECHA_VENC_REAL
    FROM '||V_ESQUEMA||'.'||V_NOMBRE_TABLA_AUX||' AUX';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE('Insertadas '||SQL%ROWCOUNT||' fechas de vencimiento de las tareas de comunicacion de trámite de GENCAT.');

    V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TEX_TAREA_EXTERNA (TEX_ID, TAR_ID, TAP_ID, USUARIOCREAR
        , FECHACREAR, DTYPE, USUARIOBORRAR, FECHABORRAR, BORRADO)
    SELECT 
        AUX.TEX_ID,
        AUX.TAR_ID,
        AUX.TAP_ID,
        '''|| V_USUARIO || ''',
        SYSDATE,
        ''EXTTareaExterna'',
        AUX.USU_ID,
        SYSDATE,
        1
    FROM '||V_ESQUEMA||'.'||V_NOMBRE_TABLA_AUX||' AUX';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE('Insertados '||SQL%ROWCOUNT||' registros en la TEX de trámite de GENCAT para las tareas de comunicacion.');

    V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS (TAR_ID, TRA_ID, ACT_ID, USU_ID, SUP_ID
        , USUARIOCREAR, FECHACREAR)
    SELECT 
        AUX.TAR_ID,
        AUX.TRA_ID,
        AUX.ACT_ID,
        AUX.USU_ID,
        AUX.SUP_ID,
        '''|| V_USUARIO || ''',
        SYSDATE
    FROM '||V_ESQUEMA||'.'||V_NOMBRE_TABLA_AUX||' AUX';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE(SQL%ROWCOUNT||' activos relacionados con las tareas de comunicacion del trámite de GENCAT.');

    
    --Cambiar para que cierre los de adecuacion
    V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TEV_TAREA_EXTERNA_VALOR (TEV_ID, TEX_ID, TEV_NOMBRE, TEV_VALOR
			    , USUARIOCREAR, FECHACREAR, DTYPE)
			SELECT '||V_ESQUEMA||'.S_TEV_TAREA_EXTERNA_VALOR.NEXTVAL, TEX.TEX_ID, TFI.TFI_NOMBRE
			    , CASE TFI.TFI_NOMBRE
			        WHEN ''titulo'' THEN NULL
			        WHEN ''observaciones'' THEN NULL
			        WHEN ''fechaRevision'' THEN TO_CHAR(SYSDATE, ''YYYY-MM-DD'')
			        WHEN ''necesitaReforma'' THEN DECODE(ADG.ADG_REFORMA, 0, ''02'', 1, ''01'')
			        WHEN ''importeReforma'' THEN TO_CHAR(ADG.ADG_IMPORTE)
			        END TEV_VALOR
			    , '''|| V_USUARIO || ''', SYSDATE, ''TareaExternaValor''
			FROM '||V_ESQUEMA||'.TEX_TAREA_EXTERNA TEX
			JOIN '||V_ESQUEMA||'.TAP_TAREA_PROCEDIMIENTO TAP ON TAP.TAP_ID = TEX.TAP_ID AND TAP.TAP_CODIGO = ''T016_ProcesoAdecuacion''
			JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.TAR_ID = TEX.TAR_ID AND TAR.TAR_TAREA_FINALIZADA = 0
			JOIN '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC ON TAC.TAR_ID = TAR.TAR_ID
			JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = TAC.ACT_ID
			JOIN '||V_ESQUEMA||'.TFI_TAREAS_FORM_ITEMS TFI ON TFI.TAP_ID = TAP.TAP_ID
			JOIN '||V_ESQUEMA||'.ACT_CMG_COMUNICACION_GENCAT CMG ON CMG.ACT_ID = ACT.ACT_ID
			JOIN '||V_ESQUEMA||'.ACT_ADG_ADECUACION_GENCAT ADG ON ADG.CMG_ID = CMG.CMG_ID';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE('Insertados '||SQL%ROWCOUNT||' de datos de la tarea de comunicacion del trámite de GENCAT.');
    
    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES T1
    USING (
        SELECT DISTINCT TAR.TAR_ID
        FROM '||V_ESQUEMA||'.TEX_TAREA_EXTERNA TEX
		JOIN '||V_ESQUEMA||'.TAP_TAREA_PROCEDIMIENTO TAP ON TAP.TAP_ID = TEX.TAP_ID AND TAP.TAP_CODIGO = ''T016_ProcesoAdecuacion''
		JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.TAR_ID = TEX.TAR_ID AND TAR.TAR_TAREA_FINALIZADA = 0) T2
    ON (T1.TAR_ID = T2.TAR_ID)
    WHEN MATCHED THEN UPDATE SET
        T1.TAR_TAREA_FINALIZADA = 1, T1.TAR_FECHA_FIN = SYSDATE, T1.BORRADO = 1
        , T1.USUARIOBORRAR = '''|| V_USUARIO || ''', T1.FECHABORRAR = SYSDATE';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE(SQL%ROWCOUNT||' tareas de comunicacion del trámite GENCAT completadas.');

    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_TRA_TRAMITE T1
    USING (
        SELECT DISTINCT AUX.TRA_ID
        FROM '||V_ESQUEMA||'.'||V_NOMBRE_TABLA_AUX||' AUX) T2
    ON (T1.TRA_ID = T2.TRA_ID)
    WHEN MATCHED THEN UPDATE SET
        T1.TRA_PROCESS_BPM = NULL, T1.USUARIOMODIFICAR = '''|| V_USUARIO || ''', T1.FECHAMODIFICAR = SYSDATE';
    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE(SQL%ROWCOUNT||' borrado campo del tramite para que se vea afectado por BPM.');
    
    ALTA_BPM_INSTANCES('SP_BPM',PL_OUTPUT);
    
    DBMS_OUTPUT.PUT_LINE('Finalizados '||SQL%ROWCOUNT||' registros de tipo de trámite comercial de venta para la tarea de resolución de tanteo para los activos a los que se les ha creado comunicación GENCAT.');


  COMMIT;
   
  	DBMS_OUTPUT.PUT_LINE('	[INFO] REGISTROS MODIFICADOS CORRECTAMENTE');

EXCEPTION
     WHEN OTHERS THEN
          err_num := SQLCODE;
          err_msg := SQLERRM;

          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(err_msg);

          ROLLBACK;
          RAISE;          

END;

/

EXIT
