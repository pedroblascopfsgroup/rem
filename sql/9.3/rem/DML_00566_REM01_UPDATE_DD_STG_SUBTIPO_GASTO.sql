--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
	V_MSQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
 	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
 	V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-8871'; -- USUARIO CREAR/MODIFICAR
	V_COUNT NUMBER(16); -- Vble. para comprobar
	err_num NUMBER; -- Numero de errores
	err_msg VARCHAR2(2048); -- Mensaje de error
	
BEGIN
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');

	V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO WHERE DD_STG_ID = 95 AND BORRADO = 0 AND DD_STG_DESCRIPCION != ''CFK''';
	EXECUTE IMMEDIATE V_MSQL INTO V_COUNT;

	IF V_COUNT = 1 THEN

		DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICAMOS DESCRIPCION SUBTIPO GASTO CON ID 95');

		V_MSQL := 'UPDATE '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO SET
					DD_STG_DESCRIPCION = ''CFK'',
					DD_STG_DESCRIPCION_LARGA = ''CFK'',
					USUARIOMODIFICAR = '''||V_USUARIO||''',
					FECHAMODIFICAR = SYSDATE
					WHERE DD_STG_ID = 95 AND BORRADO = 0';
		EXECUTE IMMEDIATE V_MSQL;

		DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN DD_STG_SUBTIPOS_GASTO');

	ELSE

		DBMS_OUTPUT.PUT_LINE('[INFO] YA MODIFICADO DESCRIPCION SUBTIPO GASTO CON ID 95 O NO EXISTE');

	END IF;

	V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO WHERE DD_STG_CODIGO = ''114'' AND BORRADO = 0';
	EXECUTE IMMEDIATE V_MSQL INTO V_COUNT;

	IF V_COUNT = 0 THEN

		DBMS_OUTPUT.PUT_LINE('[INFO] INSERTAMOS NUEVO SUBTIPO GASTO');

		V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO (DD_STG_ID,DD_TGA_ID,DD_STG_CODIGO,DD_STG_DESCRIPCION,
						DD_STG_DESCRIPCION_LARGA,USUARIOCREAR,FECHACREAR) VALUES 
						( '||V_ESQUEMA||'.S_DD_STG_SUBTIPOS_GASTO.NEXTVAL, 14, ''114'', ''Informes Ocupacionales'', ''Informes Ocupacionales'',
						'''||V_USUARIO||''',SYSDATE)';
		EXECUTE IMMEDIATE V_MSQL;

		DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN DD_STG_SUBTIPOS_GASTO');

	ELSE

		DBMS_OUTPUT.PUT_LINE('[INFO] YA EXISTE SUBTIPO GASTO CON DESCRIPCION ''Informes Ocupacionales'' O CON CODIGO 114');

	END IF;

	DBMS_OUTPUT.PUT_LINE('[FIN] ');
	
	EXCEPTION
	
	    WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------');
		DBMS_OUTPUT.PUT_LINE(SQLERRM);
		DBMS_OUTPUT.PUT_LINE(V_MSQL);
		ROLLBACK;
		RAISE;
END;
/
EXIT;