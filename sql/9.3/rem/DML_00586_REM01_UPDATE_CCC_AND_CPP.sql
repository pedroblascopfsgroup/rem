WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar.
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema.
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_TEXT_TABLA_CCC VARCHAR2(27 CHAR) := 'ACT_CONFIG_CTAS_CONTABLES';
	V_TEXT_TABLA_CPP VARCHAR2(27 CHAR) := 'ACT_CONFIG_PTDAS_PREP';
    V_USU VARCHAR2(30 CHAR) := 'REMVIP-9507'; -- Vble. auxiliar para almacenar el nombre de usuario que modifica los registros.
    
	TYPE T_TIPO_DATA IS TABLE OF VARCHAR2(150);
    TYPE T_ARRAY_DATA IS TABLE OF T_TIPO_DATA;
    V_TIPO_DATA T_ARRAY_DATA := T_ARRAY_DATA(
				--	TGA	 STG	CPP		CCC
		T_TIPO_DATA('02','10','PP075','6312008'),
		T_TIPO_DATA('02','09','PP076','6312009'),
		T_TIPO_DATA('02','08','PP077','6312010'),
		T_TIPO_DATA('02','12','PP078','6312011'),
		T_TIPO_DATA('02','14','PP079','6312012'),
		T_TIPO_DATA('02','16','PP071','6312013'),
		T_TIPO_DATA('02','15','PP080','6312014'),
		T_TIPO_DATA('02','18','PP081','6312015'),
		T_TIPO_DATA('02','17','PP082','6312016'),
		T_TIPO_DATA('02','13','PP083','6312017'),
		T_TIPO_DATA('02','11','PP084','6312018'));
	V_TMP_TIPO_DATA T_TIPO_DATA;

BEGIN		

	DBMS_OUTPUT.PUT_LINE('[INICIO]');
	FOR I IN V_TIPO_DATA.FIRST .. V_TIPO_DATA.LAST
    LOOP
        V_TMP_TIPO_DATA := V_TIPO_DATA(I);

		V_MSQL:= 'MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA_CCC||' T1 USING (
				SELECT DISTINCT CTAS.CCC_CTAS_ID FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA_CCC||' CTAS
				JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = CTAS.DD_SCR_ID AND SCR.BORRADO = 0
				JOIN '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO STG ON STG.DD_STG_ID = CTAS.DD_STG_ID AND STG.BORRADO = 0
				WHERE SCR.DD_SCR_CODIGO IN (''151'',''152'') AND STG.DD_STG_CODIGO = '''||V_TMP_TIPO_DATA(2)||'''
				AND CTAS.CCC_CUENTA_CONTABLE = '''||V_TMP_TIPO_DATA(4)||''' AND CTAS.DD_TIM_ID = 1) T2
			ON (T1.CCC_CTAS_ID = T2.CCC_CTAS_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.BORRADO = 1,
			T1.USUARIOBORRAR = '''||V_USU||''',
			T1.FECHABORRAR = SYSDATE';
		EXECUTE IMMEDIATE V_MSQL;

		DBMS_OUTPUT.PUT_LINE('[INFO] BORRADO DUPLICADO '|| SQL%ROWCOUNT ||' REGISTROS DE CUENTAS CONTABLES PARA SUBTIPO GASTO CON CODIGO '''||V_TMP_TIPO_DATA(2)||'''');

		V_MSQL:= 'MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA_CPP||' T1 USING (
				SELECT DISTINCT PTDAS.CPP_PTDAS_ID FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA_CPP||' PTDAS
				JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = PTDAS.DD_SCR_ID AND SCR.BORRADO = 0
				JOIN '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO STG ON STG.DD_STG_ID = PTDAS.DD_STG_ID AND STG.BORRADO = 0
				WHERE SCR.DD_SCR_CODIGO IN (''151'',''152'') AND STG.DD_STG_CODIGO = '''||V_TMP_TIPO_DATA(2)||'''
				AND PTDAS.CPP_PARTIDA_PRESUPUESTARIA = '''||V_TMP_TIPO_DATA(3)||''' AND PTDAS.DD_TIM_ID = 1) T2
			ON (T1.CPP_PTDAS_ID = T2.CPP_PTDAS_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.BORRADO = 1,
			T1.USUARIOBORRAR = '''||V_USU||''',
			T1.FECHABORRAR = SYSDATE';
		EXECUTE IMMEDIATE V_MSQL;

		DBMS_OUTPUT.PUT_LINE('[INFO] BORRADO DUPLICADO '|| SQL%ROWCOUNT ||' REGISTROS DE PARTIDAS PRESUPUESTARIAS PARA SUBTIPO GASTO CON CODIGO '''||V_TMP_TIPO_DATA(2)||'''');

		V_MSQL:= 'MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA_CCC||' T1 USING (
				SELECT DISTINCT CTAS.CCC_CTAS_ID FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA_CCC||' CTAS
				JOIN '||V_ESQUEMA||'.ACT_EJE_EJERCICIO EJE ON EJE.EJE_ID = CTAS.EJE_ID AND EJE.BORRADO = 0
				JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = CTAS.DD_SCR_ID AND SCR.BORRADO = 0
				JOIN '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO STG ON STG.DD_STG_ID = CTAS.DD_STG_ID AND STG.BORRADO = 0
				WHERE CTAS.BORRADO = 0 AND SCR.DD_SCR_CODIGO IN (''151'',''152'') AND STG.DD_STG_CODIGO = '''||V_TMP_TIPO_DATA(2)||'''
				AND CTAS.DD_TIM_ID = 1) T2
			ON (T1.CCC_CTAS_ID = T2.CCC_CTAS_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.CCC_CUENTA_CONTABLE = '''||V_TMP_TIPO_DATA(4)||''',
			T1.USUARIOMODIFICAR = '''||V_USU||''',
			T1.FECHAMODIFICAR = SYSDATE';
		EXECUTE IMMEDIATE V_MSQL;

		DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS '|| SQL%ROWCOUNT ||' REGISTROS DE CUENTAS CONTABLES PARA SUBTIPO GASTO CON CODIGO '''||V_TMP_TIPO_DATA(2)||'''');

		V_MSQL:= 'MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA_CPP||' T1 USING (
				SELECT DISTINCT PTDAS.CPP_PTDAS_ID FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA_CPP||' PTDAS
				JOIN '||V_ESQUEMA||'.ACT_EJE_EJERCICIO EJE ON EJE.EJE_ID = PTDAS.EJE_ID AND EJE.BORRADO = 0
				JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = PTDAS.DD_SCR_ID AND SCR.BORRADO = 0
				JOIN '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO STG ON STG.DD_STG_ID = PTDAS.DD_STG_ID AND STG.BORRADO = 0
				WHERE PTDAS.BORRADO = 0 AND SCR.DD_SCR_CODIGO IN (''151'',''152'') AND STG.DD_STG_CODIGO = '''||V_TMP_TIPO_DATA(2)||'''
				AND PTDAS.DD_TIM_ID = 1) T2
			ON (T1.CPP_PTDAS_ID = T2.CPP_PTDAS_ID)
			WHEN MATCHED THEN UPDATE SET
			T1.CPP_PARTIDA_PRESUPUESTARIA = '''||V_TMP_TIPO_DATA(3)||''',
			T1.USUARIOMODIFICAR = '''||V_USU||''',
			T1.FECHAMODIFICAR = SYSDATE';
		EXECUTE IMMEDIATE V_MSQL;

		DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS '|| SQL%ROWCOUNT ||' REGISTROS DE PARTIDAS PRESUPUESTARIAS PARA SUBTIPO GASTO CON CODIGO '''||V_TMP_TIPO_DATA(2)||'''');
        
    END LOOP;

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN]');


EXCEPTION
		WHEN OTHERS THEN
			err_num := SQLCODE;
			err_msg := SQLERRM;

			DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
			DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
			DBMS_OUTPUT.put_line(err_msg);

			ROLLBACK;
			RAISE;          

END;

/

EXIT