  ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
  ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
  V_USUARIO VARCHAR2(32 CHAR):= 'HREOS-13988';
  ACT_NUM_ACTIVO_UVEM NUMBER(32);
  DD_SCR_DESCRIPCION VARCHAR2(72 CHAR);
  ACT_NUM_ACTIVO_OLD NUMBER(32);
  ACT_NUM_ACTIVO_NEW NUMBER(32);


BEGIN

  V_SQL := 'UPDATE '||V_ESQUEMA||'.'||V_TABLA||' SET 
          USUARIOMODIFICAR = '''||V_USUARIO||'''
        , FECHAMODIFICAR = SYSDATE
        , AC3_TRANSFORMACION = ''    LEFT JOIN '||V_ESQUEMA||'.DD_MPT_MTV_IN_EX_PERIMETRO MPT ON MPT.DD_MPT_CODIGO = TMP.VALOR_NUEVO AND MPT.BORRADO = 0
    JOIN (SELECT
    CASE WHEN (CHCK_0.ACT_NUM_ACTIVO IS NOT NULL AND CHCK_1.ACT_NUM_ACTIVO IS NULL) OR (NO_CHCK.ACT_NUM_ACTIVO IS NULL AND PAC.PAC_CHECK_COMERCIALIZAR = 0) THEN AUX_ACT.ACT_NUM_ACTIVO END ACT_NUM_ACTIVO
    FROM '||V_ESQUEMA||'.ACT_ACTIVO AUX_ACT
    LEFT JOIN '||V_ESQUEMA||'.ESPARTA_EXCEL1 CHCK_0 ON AUX_ACT.ACT_NUM_ACTIVO = CHCK_0.ACT_NUM_ACTIVO AND CHCK_0.CAMPO = ''''183'''' AND CHCK_0.VALOR_NUEVO = 0
    LEFT JOIN '||V_ESQUEMA||'.ESPARTA_EXCEL1 CHCK_1 ON AUX_ACT.ACT_NUM_ACTIVO = CHCK_1.ACT_NUM_ACTIVO AND CHCK_1.CAMPO = ''''183'''' AND CHCK_1.VALOR_NUEVO = 1
    LEFT JOIN '||V_ESQUEMA||'.ESPARTA_EXCEL1 NO_CHCK ON AUX_ACT.ACT_NUM_ACTIVO = NO_CHCK.ACT_NUM_ACTIVO AND NO_CHCK.CAMPO = ''''183''''
    LEFT JOIN '||V_ESQUEMA||'.ACT_PAC_PERIMETRO_ACTIVO PAC ON AUX_ACT.ACT_ID = PAC.ACT_ID AND PAC.BORRADO = 0
    WHERE AUX_ACT.BORRADO = 0) AUX ON AUX.ACT_NUM_ACTIVO = TMP.ACT_NUM_ACTIVO''
        WHERE DD_CCS_ID = (SELECT CCS.DD_CCS_ID FROM '||V_ESQUEMA||'.DD_CCS_CAMPOS_CONV_SAREB CCS 
        JOIN '||V_ESQUEMA||'.DD_COS_CAMPOS_ORIGEN_CONV_SAREB COS ON COS.DD_COS_ID = CCS.DD_COS_ID AND COS.BORRADO = 0
        WHERE DD_CCS_CAMPO = ''PAC_MOT_EXCL_COMERCIALIZAR'' AND CCS.BORRADO = 0 AND COS.DD_COS_CODIGO = ''184'')
        ';
