    V_SQL := 'UPDATE '||V_ESQUEMA||'.'||V_TABLA||' SET 
          USUARIOMODIFICAR = '''||V_USUARIO||'''
        , FECHAMODIFICAR = SYSDATE
        , AC3_TRANSFORMACION = ''    LEFT JOIN '||V_ESQUEMA||'.DD_MPT_MTV_IN_EX_PERIMETRO MPT ON MPT.DD_MPT_CODIGO = TMP.VALOR_NUEVO AND MPT.BORRADO = 0
