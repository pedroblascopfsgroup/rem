WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

    V_TABLA VARCHAR2(30 CHAR) := 'ACT_AGR_AGRUPACION';  -- Tabla a modificar
    V_USR VARCHAR2(30 CHAR) := 'HREOS-15625'; -- USUARIOCREAR/USUARIOMODIFICAR

BEGIN

    DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZANDO TIPOS INFORME.');

    V_MSQL :=   'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA||' T1
				USING (
					SELECT DISTINCT AGR.AGR_ID
						, CASE 
							WHEN AGR.AGR_FECHA_ALTA IS NULL AND AGR.AGR_INI_VIGENCIA IS NULL THEN TO_DATE(''19000101'', ''YYYYMMDD'')
							WHEN AGR.AGR_FECHA_ALTA IS NULL AND AGR.AGR_INI_VIGENCIA <= SYSDATE THEN AGR.AGR_INI_VIGENCIA
							WHEN AGR.AGR_FECHA_ALTA >= SYSDATE THEN SYSDATE
							WHEN AGR.AGR_FECHA_ALTA < SYSDATE THEN AGR.AGR_FECHA_ALTA
							END AGR_FECHA_ALTA_NEW
						, CASE 
							WHEN AGR.AGR_FECHA_BAJA IS NULL AND AGR.AGR_FIN_VIGENCIA IS NULL THEN SYSDATE
							WHEN AGR.AGR_FECHA_BAJA IS NULL AND AGR.AGR_FIN_VIGENCIA < SYSDATE THEN AGR.AGR_FIN_VIGENCIA
							WHEN AGR.AGR_FECHA_BAJA IS NULL AND AGR.AGR_FIN_VIGENCIA >= SYSDATE THEN SYSDATE
							WHEN AGR.AGR_FECHA_BAJA >= SYSDATE THEN SYSDATE
							WHEN AGR.AGR_FECHA_BAJA < SYSDATE THEN AGR.AGR_FECHA_BAJA
							END AGR_FECHA_BAJA_NEW
						, CASE 
							WHEN AGR.AGR_INI_VIGENCIA IS NULL AND AGR.AGR_FECHA_ALTA IS NULL THEN TO_DATE(''19000101'', ''YYYYMMDD'')
							WHEN AGR.AGR_INI_VIGENCIA IS NULL AND AGR.AGR_FECHA_ALTA <= SYSDATE THEN AGR.AGR_FECHA_ALTA
							WHEN AGR.AGR_INI_VIGENCIA >= SYSDATE THEN SYSDATE
							WHEN AGR.AGR_INI_VIGENCIA < SYSDATE THEN AGR.AGR_INI_VIGENCIA
							END AGR_INI_VIGENCIA_NEW
						, CASE 
							WHEN AGR.AGR_FIN_VIGENCIA IS NULL AND AGR.AGR_FECHA_BAJA IS NULL THEN SYSDATE
							WHEN AGR.AGR_FIN_VIGENCIA IS NULL AND AGR.AGR_FECHA_BAJA < SYSDATE THEN AGR.AGR_FECHA_BAJA
							WHEN AGR.AGR_FIN_VIGENCIA IS NULL AND AGR.AGR_FECHA_BAJA >= SYSDATE THEN SYSDATE
							WHEN AGR.AGR_FIN_VIGENCIA >= SYSDATE THEN SYSDATE
							WHEN AGR.AGR_FIN_VIGENCIA < SYSDATE THEN AGR.AGR_FIN_VIGENCIA
							END AGR_FIN_VIGENCIA_NEW
					FROM '||V_ESQUEMA||'.ACT_AGR_AGRUPACION AGR
					JOIN '||V_ESQUEMA||'.ACT_AGA_AGRUPACION_ACTIVO AGA ON AGA.AGR_ID = AGR.AGR_ID
						AND AGA.BORRADO = 0
					JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = AGA.ACT_ID
						AND ACT.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_TAG_TIPO_AGRUPACION TAG ON TAG.DD_TAG_ID = AGR.DD_TAG_ID
						AND TAG.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
						AND CRA.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = ACT.DD_SCR_ID
						AND SCR.BORRADO = 0
					WHERE AGR.BORRADO = 0
						AND TAG.DD_TAG_CODIGO = ''02''
						AND CRA.DD_CRA_CODIGO = ''03''
						AND SCR.DD_SCR_CODIGO <> ''07''
						AND AGR.AGR_NUM_AGRUP_BC IS NULL) T2 
				ON (T1.AGR_ID = T2.AGR_ID)
				WHEN MATCHED THEN
					UPDATE SET
						T1.AGR_FECHA_ALTA = T2.AGR_FECHA_ALTA_NEW
						, T1.AGR_FECHA_BAJA = T2.AGR_FECHA_BAJA_NEW
						, T1.AGR_INI_VIGENCIA = T2.AGR_INI_VIGENCIA_NEW
						, T1.AGR_FIN_VIGENCIA = T2.AGR_FIN_VIGENCIA_NEW
						, T1.USUARIOMODIFICAR = ''BAJA_AGR_HYDRA''
						, T1.FECHAMODIFICAR = SYSDATE
					';

    EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] INFORME ACTUALIZADO CORRECTAMENTE EN '|| SQL%ROWCOUNT ||'  REGISTROS.');
	
       

    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
    err_num := SQLCODE;
    err_msg := SQLERRM;

    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------');
    DBMS_OUTPUT.put_line(err_msg);
    DBMS_OUTPUT.put_line(V_MSQL);
    ROLLBACK;
    RAISE;

END;

/

EXIT