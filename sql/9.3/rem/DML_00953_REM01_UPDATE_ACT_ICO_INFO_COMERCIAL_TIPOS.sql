WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

    V_TABLA VARCHAR2(30 CHAR) := 'ACT_ICO_INFO_COMERCIAL';  -- Tabla a modificar
    V_USR VARCHAR2(30 CHAR) := 'REMVIP-9721'; -- USUARIOCREAR/USUARIOMODIFICAR

BEGIN

    DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZANDO TIPOS INFORME.');

    V_MSQL :=   'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA||' ICO
				USING(
				    SELECT DISTINCT ICO.ICO_ID, TIC.DD_TIC_ID,
				    CASE TPA.DD_TPA_CODIGO WHEN ''02'' THEN ''01'' WHEN ''03'' THEN ''02'' WHEN ''07'' THEN CASE SAC.DD_SAC_CODIGO WHEN ''24'' THEN ''03'' ELSE NULL END ELSE NULL END AS TIPO_INFORME_CODIGO
				    FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
				    INNER JOIN '||V_ESQUEMA||'.ACT_ICO_INFO_COMERCIAL ICO ON ICO.ACT_ID = ACT.ACT_ID
				    INNER JOIN '||V_ESQUEMA||'.DD_TIC_TIPO_INFO_COMERCIAL TIC ON TIC.DD_TIC_ID = ICO.DD_TIC_ID
				    INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACTIVO TPA ON TPA.DD_TPA_ID = ICO.DD_TPA_ID
				    INNER JOIN '||V_ESQUEMA||'.DD_SAC_SUBTIPO_ACTIVO SAC ON SAC.DD_SAC_ID = ICO.DD_SAC_ID
				    INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACTIVO TPA2 ON TPA2.DD_TPA_ID = ACT.DD_TPA_ID
				    INNER JOIN '||V_ESQUEMA||'.DD_SAC_SUBTIPO_ACTIVO SAC2 ON SAC2.DD_SAC_ID = ACT.DD_SAC_ID
				    LEFT JOIN '||V_ESQUEMA||'.ACT_VIV_VIVIENDA VIV ON VIV.ICO_ID = ICO.ICO_ID
				    LEFT JOIN '||V_ESQUEMA||'.ACT_LCO_LOCAL_COMERCIAL LCO ON LCO.ICO_ID = ICO.ICO_ID
				    LEFT JOIN '||V_ESQUEMA||'.ACT_APR_PLAZA_APARCAMIENTO APR ON APR.ICO_ID = ICO.ICO_ID
				    WHERE ACT.BORRADO = 0 AND ICO.BORRADO = 0 AND (
				    (TPA.DD_TPA_CODIGO = ''02'' AND (TIC.DD_TIC_CODIGO != ''01'' OR ICO.DD_TIC_ID IS NULL OR VIV.ICO_ID IS NULL)) OR --VIVIENDA
				    (TPA.DD_TPA_CODIGO = ''03'' AND (TIC.DD_TIC_CODIGO != ''02'' OR ICO.DD_TIC_ID IS NULL OR LCO.ICO_ID IS NULL)) OR --LOCAL COMERCIAL
				    (TPA.DD_TPA_CODIGO = ''07'' AND SAC.DD_SAC_CODIGO = ''24'' AND (TIC.DD_TIC_CODIGO != ''03'' OR ICO.DD_TIC_ID IS NULL OR APR.ICO_ID IS NULL)) --GARAJE
				    )
				) AUX ON (AUX.ICO_ID = ICO.ICO_ID)
				WHEN MATCHED THEN UPDATE SET ICO.DD_TIC_ID_ANTERIOR = AUX.DD_TIC_ID, 
				ICO.DD_TIC_ID = (SELECT DD_TIC_ID FROM '||V_ESQUEMA||'.DD_TIC_TIPO_INFO_COMERCIAL WHERE DD_TIC_CODIGO = AUX.TIPO_INFORME_CODIGO), 
				USUARIOMODIFICAR = '''||V_USR||''', FECHAMODIFICAR = SYSDATE';

    EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] INFORME ACTUALIZADO CORRECTAMENTE EN '|| SQL%ROWCOUNT ||'  REGISTROS.');
	
    EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.ACT_VIV_VIVIENDA(ICO_ID) 
						SELECT DISTINCT ICO.ICO_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
						INNER JOIN '||V_ESQUEMA||'.ACT_ICO_INFO_COMERCIAL ICO ON ICO.ACT_ID = ACT.ACT_ID
						INNER JOIN '||V_ESQUEMA||'.DD_TIC_TIPO_INFO_COMERCIAL TIC ON TIC.DD_TIC_ID = ICO.DD_TIC_ID
						INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACTIVO TPA ON TPA.DD_TPA_ID = ICO.DD_TPA_ID
						LEFT JOIN '||V_ESQUEMA||'.ACT_VIV_VIVIENDA VIV ON VIV.ICO_ID = ICO.ICO_ID
						WHERE ACT.BORRADO = 0 AND ICO.BORRADO = 0 AND 
						TPA.DD_TPA_CODIGO = ''02'' AND VIV.ICO_ID IS NULL';
						
	DBMS_OUTPUT.PUT_LINE('[INFO] '|| SQL%ROWCOUNT ||' VIVIENDAS INSERTADAS CORRECTAMENTE.');
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.ACT_LCO_LOCAL_COMERCIAL(ICO_ID) 
						SELECT DISTINCT ICO.ICO_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
						INNER JOIN '||V_ESQUEMA||'.ACT_ICO_INFO_COMERCIAL ICO ON ICO.ACT_ID = ACT.ACT_ID
						INNER JOIN '||V_ESQUEMA||'.DD_TIC_TIPO_INFO_COMERCIAL TIC ON TIC.DD_TIC_ID = ICO.DD_TIC_ID
						INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACTIVO TPA ON TPA.DD_TPA_ID = ICO.DD_TPA_ID
						LEFT JOIN '||V_ESQUEMA||'.ACT_LCO_LOCAL_COMERCIAL LCO ON LCO.ICO_ID = ICO.ICO_ID
						WHERE ACT.BORRADO = 0 AND ICO.BORRADO = 0 AND 
						TPA.DD_TPA_CODIGO = ''03'' AND LCO.ICO_ID IS NULL';
						
    DBMS_OUTPUT.PUT_LINE('[INFO] '|| SQL%ROWCOUNT ||' LOCALES COMERCIALES INSERTADOS CORRECTAMENTE.');
    
    EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.ACT_APR_PLAZA_APARCAMIENTO(ICO_ID) 
						SELECT DISTINCT ICO.ICO_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
						INNER JOIN '||V_ESQUEMA||'.ACT_ICO_INFO_COMERCIAL ICO ON ICO.ACT_ID = ACT.ACT_ID
						INNER JOIN '||V_ESQUEMA||'.DD_TIC_TIPO_INFO_COMERCIAL TIC ON TIC.DD_TIC_ID = ICO.DD_TIC_ID
						INNER JOIN '||V_ESQUEMA||'.DD_TPA_TIPO_ACTIVO TPA ON TPA.DD_TPA_ID = ICO.DD_TPA_ID
						INNER JOIN '||V_ESQUEMA||'.DD_SAC_SUBTIPO_ACTIVO SAC ON SAC.DD_SAC_ID = ICO.DD_SAC_ID
						LEFT JOIN '||V_ESQUEMA||'.ACT_APR_PLAZA_APARCAMIENTO APR ON APR.ICO_ID = ICO.ICO_ID
						WHERE ACT.BORRADO = 0 AND ICO.BORRADO = 0 AND 
						TPA.DD_TPA_CODIGO = ''07'' AND SAC.DD_SAC_CODIGO = ''24'' AND APR.ICO_ID IS NULL';
						
    DBMS_OUTPUT.PUT_LINE('[INFO] '|| SQL%ROWCOUNT ||' PLAZAS APARCAMIENTO INSERTADAS CORRECTAMENTE.');
    
    

    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
    err_num := SQLCODE;
    err_msg := SQLERRM;

    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------');
    DBMS_OUTPUT.put_line(err_msg);
    DBMS_OUTPUT.put_line(V_MSQL);
    ROLLBACK;
    RAISE;

END;

