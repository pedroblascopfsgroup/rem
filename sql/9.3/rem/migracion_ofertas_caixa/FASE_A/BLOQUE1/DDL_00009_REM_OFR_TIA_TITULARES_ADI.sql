--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REM01'; --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
V_TABLA VARCHAR2(40 CHAR) := 'MIG2_OFR_TIA_TITULARES_ADI_CAIXA';

BEGIN

SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

IF TABLE_COUNT > 0 THEN

	DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
	EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'';
    
END IF;

EXECUTE IMMEDIATE '
CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'
(
	OFR_NUM_OFERTA	NUMBER(16,0),
	TIA_NOMBRE	VARCHAR2(256 CHAR),
	DD_TDI_ID	NUMBER(16,0),
	TIA_DOCUMENTO	VARCHAR2(50 CHAR),
	TIA_APELLIDOS	VARCHAR2(120 CHAR),
	TIA_DIRECCION	VARCHAR2(120 CHAR),
	DD_LOC_ID	NUMBER(16,0),
	DD_PRV_ID	NUMBER(16,0),
	TIA_CODPOSTAL	VARCHAR2(20 CHAR),
	DD_ECV_ID	NUMBER(16,0),
	DD_REM_ID	NUMBER(16,0),
	TIA_RECHAZAR_PUBLI	NUMBER(1,0),
	TIA_RECHAZAR_PROPI	NUMBER(1,0),
	TIA_RECHAZAR_PROVE	NUMBER(1,0),
	TIA_RAZON_SOCIAL	VARCHAR2(50 CHAR),
	TIA_TELEFONO1	VARCHAR2(14 CHAR),
	TIA_TELEFONO2	VARCHAR2(14 CHAR),
	TIA_EMAIL	VARCHAR2(50 CHAR),
	DD_TVI_ID	NUMBER(16,0),
	TIA_OBSERVACIONES	VARCHAR2(100 CHAR),
	DD_TDI_ID_CONYUGE	NUMBER(16,0),
	TIA_DOCUMENTO_CONYUGE	VARCHAR2(50 CHAR),
	DD_TPE_ID	NUMBER(16,0),
	DD_PAI_ID	NUMBER(16,0),
	DD_TDI_ID_RTE	NUMBER(16,0),
	TIA_DOCUMENTO_RTE	VARCHAR2(50 CHAR),
	TIA_DIRECCION_RTE	VARCHAR2(120 CHAR),
	DD_PRV_ID_RTE	NUMBER(16,0),
	DD_LOC_ID_RTE	NUMBER(16,0),
	DD_PAI_ID_RTE	NUMBER(16,0),
	TIA_CODPOSTAL_RTE	NUMBER(16,0),
	ADCOM_GDPR	NUMBER(16,0),
	ADCOM_DOC_IDENT	NUMBER(16,0),
	FECHA_ACEP_GDPR	DATE,
	TIA_C4C_ID	NUMBER(10,0),
	TIA_ID_PERSONA_HAYA	VARCHAR2(50 CHAR),
	IAP_ID	NUMBER(16,0),
	TIA_FECHA_NACIMIENTO	DATE,
	DD_LOC_NAC_ID	NUMBER(16,0),
	DD_PAI_NAC_ID	NUMBER(16,0) 
)'
;

DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA||' CREADA');  

IF V_ESQUEMA_2 != V_ESQUEMA_1 THEN

	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_2||''); 

END IF;

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
