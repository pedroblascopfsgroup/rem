		DD_FIO_REPR_ID	NUMBER(16,0)	,
		DD_EIC_ID	NUMBER(16,0)	,
		DD_EIC_REPR_ID	NUMBER(16,0)	,
		CEX_ID_PERSONA_HAYA_REPR	NUMBER(16,0)	,
		CEX_USUFRUCTUARIO	NUMBER(1,0)	
	)';

	DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA||' CREADA');  
	
