BEGIN

	SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

	IF TABLE_COUNT > 0 THEN
	
		DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
		EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'';
	
	END IF;

	EXECUTE IMMEDIATE 'CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'(COM_ID NUMBER(16,0)	,
		ECO_ID	NUMBER(16,0)	,
		DD_ECV_ID	NUMBER(16,0)	,
		DD_REM_ID	NUMBER(16,0)	,
		CEX_DOCUMENTO_CONYUGE	VARCHAR2(50 CHAR)	,
		CEX_ANTIGUO_DEUDOR	NUMBER(1,0)	,
		CEX_RELACION_ANT_DEUDOR	NUMBER(1,0)	,
		DD_UAC_ID	NUMBER(16,0)	,
		CEX_IMPTE_PROPORCIONAL_OFERTA	NUMBER(16,2)	,
		CEX_IMPTE_FINANCIADO	NUMBER(16,2)	,
		CEX_RESPONSABLE_TRAMITACION	VARCHAR2(256 CHAR)	,
		CEX_PORCION_COMPRA	NUMBER(16,2)	,
		CEX_TITULAR_RESERVA	NUMBER(1,0)	,
		CEX_TITULAR_CONTRATACION	NUMBER(1,0)	,
		CEX_NOMBRE_RTE	VARCHAR2(256 CHAR)	,
		CEX_APELLIDOS_RTE	VARCHAR2(256 CHAR)	,
		DD_TDI_ID_RTE	NUMBER(16,0)	,
		CEX_DOCUMENTO_RTE	VARCHAR2(50 CHAR)	,
		CEX_TELEFONO1_RTE	VARCHAR2(50 CHAR)	,
		CEX_TELEFONO2_RTE	VARCHAR2(50 CHAR)	,
		CEX_EMAIL_RTE	VARCHAR2(50 CHAR)	,
		CEX_DIRECCION_RTE	VARCHAR2(256 CHAR)	,
		CEX_CODIGO_POSTAL_RTE	VARCHAR2(5 CHAR)	,
		CEX_RELACION_HRE	VARCHAR2(256 CHAR)	,
		DD_EPB_ID	NUMBER(16,0)	,
		CEX_FECHA_PETICION	DATE	,
		CEX_FECHA_RESOLUCION	DATE	,
		DD_LOC_ID_RTE	NUMBER(16,0)	,
		DD_PRV_ID_RTE	NUMBER(16,0)	,
		CEX_NUM_FACTURA	VARCHAR2(50 CHAR)	,
		CEX_FECHA_FACTURA	DATE	,
		DD_TGP_ID	NUMBER(16,0)	,
		CEX_FECHA_BAJA	DATE	,
		DD_PAI_ID	NUMBER(3,0)	,
		DD_PAI_ID_RTE	NUMBER(3,0)	,
		CEX_ID_PERSONA_HAYA	VARCHAR2(50 CHAR)	,
		DD_TPI_ID	NUMBER(16,0)	,
		ADCOM_ID	NUMBER(16,0)	,
		DD_TDI_ID_CONYUGE	NUMBER(16,0)	,
		CEX_CLI_URSUS_CONYUGE_REM	VARCHAR2(256 CHAR)	,
		CEX_NUM_URSUS_CONYUGE_REM	NUMBER(16,0)	,
		CEX_NUM_URSUS_CONYUGE_BH_REM	NUMBER(16,0)	,
		CEX_C4C_ID	NUMBER(10,0)	,
		DD_ECL_ID	NUMBER(16,0)	,
		ECO_ECL_FECHA	TIMESTAMP(6)	,
		IAP_REPR_ID	NUMBER(16,0)	,
		CEX_FECHA_NACIMIENTO_REPR	DATE	,
