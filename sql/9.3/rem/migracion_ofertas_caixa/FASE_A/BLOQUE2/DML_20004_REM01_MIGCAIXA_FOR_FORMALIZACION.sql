--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01';
V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER';
V_USUARIO VARCHAR2(50 CHAR) := 'MIG_CAIXA';
V_TABLA VARCHAR2(40 CHAR) := 'FOR_FORMALIZACION';
V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG2_FOR_FORMALIZACION_CAIXA';
V_SENTENCIA VARCHAR2(2000 CHAR);
V_NUM_TABLAS NUMBER(16);
V_MSQL VARCHAR2(32000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.

BEGIN 
 
	--Inicio del proceso de volcado sobre COE_CONDICIONANTES_EXPEDIENTE

V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||'';

EXECUTE IMMEDIATE V_MSQL INTO V_NUM_TABLAS;

IF V_NUM_TABLAS = 0 THEN
DBMS_OUTPUT.PUT_LINE('La tabla/s de migración implicada está vacía. No se realiza ninguna acción');

ELSE

	DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
 
	EXECUTE IMMEDIATE '
		INSERT INTO '||V_ESQUEMA||'MIG2_FOR_FORMALIZACION_CAIXA(ECO_ID	,
		FOR_NOMBRE_NOTARIO	,
		FOR_DIRECCION_NOTARIO	,
		FOR_CONTACTO_NOTARIO	,
		FOR_CONTACTO_CARGO	,
		FOR_CONTACTO_EMAIL	,
		FOR_CONTACTO_TELEFONO	,
		FOR_PETICIONARIO	,
		FOR_FECHA_PETICION	,
		FOR_FECHA_RESOLUCION	,
		FOR_FECHA_ESCRITURA	,
		FOR_FECHA_CONTABILIZACION	,
		FOR_FECHA_PAGO	,
		FOR_IMPORTE	,
		FOR_FORMA_PAGO	,
		FOR_GASTOS_CARGO	,
		FOR_MOTIVO_RESOLUCION	,
		DD_TRC_ID	,
		FOR_NUMEXPEDIENTE	,
		FOR_CAPITALCONCEDIDO	
		)
		SELECT MIG.ECO_ID, FOR_NOMBRE_NOTARIO,
		FOR_DIRECCION_NOTARIO,
		FOR_CONTACTO_NOTARIO,
		FOR_CONTACTO_CARGO,
		FOR_CONTACTO_EMAIL,
		FOR_CONTACTO_TELEFONO,
		FOR_PETICIONARIO,
		FOR_FECHA_PETICION,
		FOR_FECHA_RESOLUCION,
		FOR_FECHA_ESCRITURA,
		FOR_FECHA_CONTABILIZACION,
		FOR_FECHA_PAGO,
		FOR_IMPORTE,
		FOR_FORMA_PAGO,
		FOR_GASTOS_CARGO,
		FOR_MOTIVO_RESOLUCION,
		DD_TRC_ID,
		FOR_NUMEXPEDIENTE,
		FOR_CAPITALCONCEDIDO 
		FROM '||V_ESQUEMA||'.FOR_FORMALIZACION INF
		JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.ECO_ID = INF.ECO_ID
		JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_ID = ECO.OFR_ID
		JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG ON MIG.COD_OFERTA_CAIXA = OFR.OFR_NUM_OFERTA
	';
  
  DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||' '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');
  
  COMMIT;
  
  V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''ANALYZE'','''||V_TABLA||''',''10''); END;';
  EXECUTE IMMEDIATE (V_SENTENCIA);
  DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA||'.'||V_TABLA||' ANALIZADA.');
END IF;                          
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/
EXIT;
