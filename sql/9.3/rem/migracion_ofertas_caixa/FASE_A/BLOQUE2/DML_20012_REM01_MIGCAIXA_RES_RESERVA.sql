DECLARE
	TABLE_COUNT NUMBER(10,0) := 0;
	TABLE_COUNT_2 NUMBER(10,0) := 0;
	V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01'; --REM01
	V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER'; --REMMASTER
	V_USUARIO VARCHAR2(50 CHAR) := '#USUARIO_MIGRACION#';
	V_TABLA VARCHAR2(40 CHAR) := 'RES_RESERVAS';
	V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG2_RES_RESERVAS_CAIXA';
	V_SENTENCIA VARCHAR2(32000 CHAR);
	V_NUM_TABLAS NUMBER(16);
	V_MSQL VARCHAR2(32000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.

BEGIN
	--Inicio del proceso de volcado sobre MIG2_RES_RESERVAS_CAIXA


	DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.MIG2_RES_RESERVAS_CAIXA RES (
		OFR_NUM_OFERTA,
		RES_NUM_RESERVA,
		RES_FECHA_ENVIO,
		RES_FECHA_FIRMA,
		RES_FECHA_VENCIMIENTO,
		RES_FECHA_ANULACION,
		RES_MOTIVO_ANULACION,
		DD_TAR_ID,
		DD_ERE_ID,
		RES_FECHA_SOLICITUD,
		RES_FECHA_RESOLUCION,
		RES_IND_IMP_ANULACION,
		RES_IMPORTE_DEVUELTO,
		DD_EDE_ID,
		DD_MAN_ID,
		DD_MAR_ID,
		DD_DER_ID,
		RES_FECHA_CONTABILIZACION,
		RES_FECHA_VIGENCIA_ARRAS,
		RES_FECHA_AMPLIACION_ARRAS,
		RES_SOLICITUD_AMPLIACION_ARRAS,
		DD_MAA_ID,
		RES_FECHA_CONT_ARRAS
	)
	SELECT
		MIG.COD_OFERTA_CAIXA,
		RES.RES_NUM_RESERVA,
		RES.RES_FECHA_ENVIO,
		RES.RES_FECHA_FIRMA,
		RES.RES_FECHA_VENCIMIENTO,
		RES.RES_FECHA_ANULACION,
		RES.RES_MOTIVO_ANULACION,
		RES.DD_TAR_ID,
		RES.DD_ERE_ID,
		RES.RES_FECHA_SOLICITUD,
		RES.RES_FECHA_RESOLUCION,
		RES.RES_IND_IMP_ANULACION,
		RES.RES_IMPORTE_DEVUELTO,
		RES.DD_EDE_ID,
		RES.DD_MAN_ID,
		RES.DD_MAR_ID,
		RES.DD_DER_ID,
		RES.RES_FECHA_CONTABILIZACION,
		RES.RES_FECHA_VIGENCIA_ARRAS,
		RES.RES_FECHA_AMPLIACION_ARRAS,
		RES.RES_SOLICITUD_AMPLIACION_ARRAS,
		RES.DD_MAA_ID,
		RES.RES_FECHA_CONT_ARRAS											
		FROM '||V_ESQUEMA||'.RES_RESERVAS RES
		JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.ECO_ID = RES.ECO_ID
		JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_ID = ECO.OFR_ID
		JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG ON MIG.COD_OFERTA_CAIXA = OFR.OFR_NUM_OFERTA
	';

	DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');

	COMMIT;

	V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''ANALYZE'','''||V_TABLA||''',''10''); END;';
	EXECUTE IMMEDIATE V_SENTENCIA;

EXCEPTION
	WHEN OTHERS THEN
		DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.put_line('-----------------------------------------------------------');
		DBMS_OUTPUT.put_line(SQLERRM);
		ROLLBACK;
		RAISE;
END;
