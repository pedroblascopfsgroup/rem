--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	TABLE_COUNT NUMBER(10,0) := 0;
	TABLE_COUNT_2 NUMBER(10,0) := 0;
	V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01'; --REM01
	V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER'; --REMMASTER
	V_USUARIO VARCHAR2(50 CHAR) := 'MIG_CAIXA';
	V_TABLA VARCHAR2(40 CHAR) := 'GCO_GESTOR_ADD_ECO';
	V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG2_GEO_GESTORES_OFERTAS_CAIXA';
	V_SENTENCIA VARCHAR2(32000 CHAR);
	V_NUM_TABLAS NUMBER(16);
	V_MSQL VARCHAR2(32000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.

BEGIN
	--Inicio del proceso de volcado sobre TXO_TEXTOS_OFERTA
V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||'';

EXECUTE IMMEDIATE V_MSQL INTO V_NUM_TABLAS;

IF V_NUM_TABLAS = 0 THEN
DBMS_OUTPUT.PUT_LINE('La tabla/s de migración implicada está vacía. No se realiza ninguna acción');

ELSE


	DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD OEX (
		GEE_ID,
		USU_ID,
		DD_TGE_ID,
		USUARIOCREAR,
		FECHACREAR,
		BORRADO,
		VERSION
	)
	SELECT
		'||V_ESQUEMA||'.S_GEE_GESTOR_ENTIDAD.NEXTVAL,
		MIG2.USU_ID,
		MIG2.DD_TGE_ID,
		''MIG_CAIXA'',
		SYSDATE,
		0,
		ECO.ECO_ID
		FROM '||V_ESQUEMA||'.MIG2_GEO_GESTORES_OFERTAS_CAIXA MIG2
        	INNER JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG ON MIG2.OFR_COD_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_NUM_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = OFR.OFR_ID
	';
	
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.GCO_GESTOR_ADD_ECO OEX (
		GEE_ID,
		ECO_ID
	)
	SELECT DISTINCT
		GEE.GEE_ID,
		ECO.ECO_ID
		FROM '||V_ESQUEMA||'.MIG2_GEO_GESTORES_OFERTAS_CAIXA MIG2
        	INNER JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG ON MIG2.OFR_COD_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_NUM_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = OFR.OFR_ID
		INNER JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.VERSION = ECO.ECO_ID
	';
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST OEX (
		GEH_ID,
		USU_ID,
		DD_TGE_ID,
		GEH_FECHA_DESDE,
		USUARIOCREAR,
		FECHACREAR,
		BORRADO,
		VERSION
	)
	SELECT
		'||V_ESQUEMA||'.S_GEH_GESTOR_ENTIDAD_HIST.NEXTVAL,
		MIG2.USU_ID,
		MIG2.DD_TGE_ID,
		SYSDATE,
		''MIG_CAIXA'',
		SYSDATE,
		0,
		ECO.ECO_ID
		FROM '||V_ESQUEMA||'.MIG2_GEO_GESTORES_OFERTAS_CAIXA MIG2
        	INNER JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG ON MIG2.OFR_COD_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_NUM_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = OFR.OFR_ID
	';
	
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.GCH_GESTOR_ECO_HISTORICO OEX (
		GEH_ID,
		ECO_ID
	)
	SELECT DISTINCT
		GEH.GEH_ID,
		ECO.ECO_ID
		FROM '||V_ESQUEMA||'.MIG2_GEO_GESTORES_OFERTAS_CAIXA MIG2
        	INNER JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG ON MIG2.OFR_COD_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_NUM_OFERTA = MIG.COD_OFERTA_CAIXA
		INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = OFR.OFR_ID
		INNER JOIN '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH ON GEH.VERSION = ECO.ECO_ID
	';

	DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');

	COMMIT;

	V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''ANALYZE'','''||V_TABLA||''',''10''); END;';
	EXECUTE IMMEDIATE V_SENTENCIA;
END IF;
EXCEPTION
	WHEN OTHERS THEN
		DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.put_line('-----------------------------------------------------------');
		DBMS_OUTPUT.put_line(SQLERRM);
		ROLLBACK;
		RAISE;
END;
/
EXIT;
