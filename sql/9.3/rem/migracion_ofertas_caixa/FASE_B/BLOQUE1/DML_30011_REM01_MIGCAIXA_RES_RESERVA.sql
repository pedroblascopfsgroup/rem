--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	TABLE_COUNT NUMBER(10,0) := 0;
	TABLE_COUNT_2 NUMBER(10,0) := 0;
	V_ESQUEMA VARCHAR2(10 CHAR) := 'REM01'; --REM01
	V_ESQUEMA_MASTER VARCHAR2(15 CHAR) := 'REMMASTER'; --REMMASTER
	V_USUARIO VARCHAR2(50 CHAR) := 'MIG_CAIXA';
	V_TABLA VARCHAR2(40 CHAR) := 'RES_RESERVAS';
	V_TABLA_MIG VARCHAR2(40 CHAR) := 'MIG2_RES_RESERVAS_CAIXA';
	V_SENTENCIA VARCHAR2(32000 CHAR);
	V_NUM_TABLAS NUMBER(16);
	V_MSQL VARCHAR2(32000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.

BEGIN
	--Inicio del proceso de volcado sobre TXO_TEXTOS_OFERTA
V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.'||V_TABLA_MIG||'';

EXECUTE IMMEDIATE V_MSQL INTO V_NUM_TABLAS;

IF V_NUM_TABLAS = 0 THEN
DBMS_OUTPUT.PUT_LINE('La tabla/s de migración implicada está vacía. No se realiza ninguna acción');

ELSE


	DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE MIGRACION SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'.');
	
	EXECUTE IMMEDIATE 'INSERT INTO '||V_ESQUEMA||'.RES_RESERVAS RES (
		RES_ID,
		ECO_ID,
		RES_NUM_RESERVA,
		RES_FECHA_ENVIO,
		RES_FECHA_FIRMA,
		RES_FECHA_VENCIMIENTO,
		RES_FECHA_ANULACION,
		RES_MOTIVO_ANULACION,
		DD_TAR_ID,
		DD_ERE_ID,
		VERSION,
		USUARIOCREAR,
		FECHACREAR,
		USUARIOMODIFICAR,
		FECHAMODIFICAR,
		USUARIOBORRAR,
		FECHABORRAR,
		BORRADO,
		RES_FECHA_SOLICITUD,
		RES_FECHA_RESOLUCION,
		RES_IND_IMP_ANULACION,
		RES_IMPORTE_DEVUELTO,
		DD_EDE_ID,
		DD_MAN_ID,
		DD_MAR_ID,
		DD_DER_ID,
		RES_FECHA_CONTABILIZACION,
		RES_FECHA_VIGENCIA_ARRAS,
		RES_FECHA_AMPLIACION_ARRAS,
		RES_SOLICITUD_AMPLIACION_ARRAS,
		DD_MAA_ID,
		RES_FECHA_CONT_ARRAS
	)
	SELECT
		'||V_ESQUEMA||'.S_RES_RESERVAS.NEXTVAL,
		MIG2.ECO_ID,
		RES_NUM_RESERVA,
		RES_FECHA_ENVIO,
		RES_FECHA_FIRMA,
		RES_FECHA_VENCIMIENTO,
		RES_FECHA_ANULACION,
		RES_MOTIVO_ANULACION,
		DD_TAR_ID,
		DD_ERE_ID,
		1,
		''MIG_CAIXA'',
		SYSDATE,
		NULL,
		NULL,
		NULL,
		NULL,
		0,
		RES_FECHA_SOLICITUD,
		RES_FECHA_RESOLUCION,
		RES_IND_IMP_ANULACION,
		RES_IMPORTE_DEVUELTO,
		DD_EDE_ID,
		DD_MAN_ID,
		DD_MAR_ID,
		DD_DER_ID,
		RES_FECHA_CONTABILIZACION,
		RES_FECHA_VIGENCIA_ARRAS,
		RES_FECHA_AMPLIACION_ARRAS,
		RES_SOLICITUD_AMPLIACION_ARRAS,
		DD_MAA_ID,
		RES_FECHA_CONT_ARRAS
		FROM '||V_ESQUEMA||'.MIG2_RES_RESERVAS_CAIXA MIG
		JOIN '||V_ESQUEMA||'.MIG2_OFR_MAPEO_NUM_OFERTAS MIG2 ON MIG2.COD_OFERTA_CAIXA = MIG.OFR_NUM_OFERTA
	';

	DBMS_OUTPUT.PUT_LINE('[INFO] - '||to_char(sysdate,'HH24:MI:SS')||'  '||V_ESQUEMA||'.'||V_TABLA||' cargada. '||SQL%ROWCOUNT||' Filas.');

	COMMIT;

	V_SENTENCIA := 'BEGIN '||V_ESQUEMA||'.OPERACION_DDL.DDL_TABLE(''ANALYZE'','''||V_TABLA||''',''10''); END;';
	EXECUTE IMMEDIATE V_SENTENCIA;
END IF;
EXCEPTION
	WHEN OTHERS THEN
		DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.put_line('-----------------------------------------------------------');
		DBMS_OUTPUT.put_line(SQLERRM);
		ROLLBACK;
		RAISE;
END;
