WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	
	V_TABLA_TMP VARCHAR2(30 CHAR) := 'TMP_HREOS_11531'; -- Variable para tabla de salida para el borrado	
	V_TABLA VARCHAR2(30 CHAR) := 'ACT_ETD_ENT_TDI'; -- Variable para tabla de salida para el borrado	
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error	
	V_SQL VARCHAR2(4000 CHAR);--Sentencia a ejecutar	
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'HREOS-11531';
	

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INICIO]'||CHR(10));	
	DBMS_OUTPUT.PUT_LINE('[INFO] LIMPIANDO LOS DATOS DE LA TABLA '||V_ESQUEMA||'.'||V_TABLA||'');

	V_SQL := 'TRUNCATE TABLE '||V_ESQUEMA||'.'||V_TABLA;
	EXECUTE IMMEDIATE V_SQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] INSERTANDO LOS DATOS DE LA TABLA '||V_ESQUEMA||'.'||V_TABLA_TMP||' EN '||V_TABLA||'');

	V_SQL := 'INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||'
			(ETD_ID, ENT_ID, TDI_ID, DD_ENT_ID, USUARIOCREAR, FECHACREAR)
		SELECT '||V_ESQUEMA||'.S_'||V_TABLA||'.NEXTVAL, ACT.ACT_ID, TDI.TDI_ID, ENT.DD_ENT_ID
			, '''||V_USUARIO||''', SYSDATE
		FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' TMP
		JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = TMP.ENT_ID
		    AND ACT.BORRADO = 0
		JOIN '||V_ESQUEMA||'.DD_ENT_ENTIDAD_GASTO ENT ON ENT.DD_ENT_CODIGO = TMP.TIPO_ENTIDAD
		    AND ENT.BORRADO = 0
		JOIN '||V_ESQUEMA||'.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
		    AND CRA.BORRADO = 0
		JOIN '||V_ESQUEMA||'.DD_TBE_TIPO_ACTIVO_BDE TBE ON TBE.DD_TBE_CODIGO = TMP.TIPO_ACTIVO_BDE
		    AND TBE.BORRADO = 0
		JOIN '||V_ESQUEMA||'.ACT_TDI_TIPO_DIARIO TDI ON TDI.TIPO_INMUEBLE = TMP.TIPO_INMUEBLE
		    AND TDI.DD_TBE_ID = TBE.DD_TBE_ID
		    AND TDI.BORRADO = 0
		WHERE ENT.DD_ENT_CODIGO = ''ACT''
		    AND CRA.DD_CRA_CODIGO = ''08''';
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han insertado en total '||SQL%ROWCOUNT||' registros en la tabla '||V_TABLA);
	COMMIT;
	
	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
