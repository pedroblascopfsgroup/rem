WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	
	V_TABLA_TMP VARCHAR2(30 CHAR) := 'ACT_ACTIVO'; -- Variable para tabla de salida para el borrado	
	V_TABLA VARCHAR2(30 CHAR) := 'ACT_ACTIVO_CAIXA'; -- Variable para tabla de salida para el borrado	
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error	
	V_SQL VARCHAR2(4000 CHAR);--Sentencia a ejecutar	
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'HREOS-14471';
	
BEGIN

	DBMS_OUTPUT.PUT_LINE('[INICIO]'||CHR(10));
	DBMS_OUTPUT.PUT_LINE('[INFO] INICIALIZAMOS El ESTADO TÉCNICO DE LOS ACTIVOS EN '||V_ESQUEMA||'.'||V_TABLA||'');

	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA||' CBX
				USING (
					SELECT 
					CX.CBX_ID
					, (SELECT DD_EAT_ID FROM '||V_ESQUEMA||'.DD_EAT_EST_TECNICO WHERE DD_EAT_CODIGO = ''E02'') DD_EAT_ID
					FROM '||V_ESQUEMA||'.'||V_TABLA||' CX
					JOIN '||V_ESQUEMA||'.'||V_TABLA_TMP||' ACT ON CX.ACT_ID = ACT.ACT_ID AND ACT.BORRADO = 0) AUX
				ON (CBX.CBX_ID = AUX.CBX_ID)
				WHEN MATCHED THEN
					UPDATE SET CBX.DD_EAT_ID = AUX.DD_EAT_ID';
	DBMS_OUTPUT.PUT_LINE(V_SQL);
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han inicilizado a E02 un total de '||SQL%ROWCOUNT||' filas de la tabla '||V_TABLA);

	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA||' CBX
				USING (
					SELECT 
					CX.CBX_ID
					, (SELECT DD_EAT_ID FROM '||V_ESQUEMA||'.DD_EAT_EST_TECNICO WHERE DD_EAT_CODIGO = ''E04'') DD_EAT_ID
					FROM '||V_ESQUEMA||'.'||V_TABLA||' CX
					JOIN '||V_ESQUEMA||'.'||V_TABLA_TMP||' ACT ON CX.ACT_ID = ACT.ACT_ID AND ACT.BORRADO = 0
					JOIN '||V_ESQUEMA||'.ACT_APU_ACTIVO_PUBLICACION APU ON APU.ACT_ID = ACT.ACT_ID AND APU.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_SCM_SITUACION_COMERCIAL SCM ON ACT.DD_SCM_ID = SCM.DD_SCM_ID AND SCM.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_TCO_TIPO_COMERCIALIZACION TCO ON TCO.DD_TCO_ID = APU.DD_TCO_ID AND TCO.BORRADO = 0
					WHERE TCO.DD_TCO_CODIGO IN (''01'', ''02'')
					AND (ACT.OK_TECNICO = 1 
					OR EXISTS (SELECT 1 FROM ACT_OFR ACO
					JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON ACO.OFR_ID = OFR.OFR_ID AND OFR.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_EOF_ESTADOS_OFERTA EOF ON OFR.DD_EOF_ID = EOF.DD_EOF_ID AND EOF.DD_EOF_CODIGO = ''01''
					WHERE ACO.ACT_ID = ACT.ACT_ID)
					OR SCM.DD_SCM_CODIGO = ''05'')) AUX
				ON (CBX.CBX_ID = AUX.CBX_ID)
				WHEN MATCHED THEN
					UPDATE SET CBX.DD_EAT_ID = AUX.DD_EAT_ID
					, CBX.USUARIOMODIFICAR = '''||V_USUARIO||'''
					, CBX.FECHAMODIFICAR = SYSDATE';
	DBMS_OUTPUT.PUT_LINE(V_SQL);
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado a E04 un total de '||SQL%ROWCOUNT||' filas de la tabla '||V_TABLA);

	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA||' CBX
				USING (
					SELECT 
					CX.CBX_ID
					, (SELECT DD_EAT_ID FROM '||V_ESQUEMA||'.DD_EAT_EST_TECNICO WHERE DD_EAT_CODIGO = ''E07'') DD_EAT_ID
					FROM '||V_ESQUEMA||'.'||V_TABLA||' CX
					JOIN '||V_ESQUEMA||'.'||V_TABLA_TMP||' ACT ON CX.ACT_ID = ACT.ACT_ID AND ACT.BORRADO = 0
					JOIN '||V_ESQUEMA||'.ACT_APU_ACTIVO_PUBLICACION APU ON APU.ACT_ID = ACT.ACT_ID AND APU.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_SCM_SITUACION_COMERCIAL SCM ON ACT.DD_SCM_ID = SCM.DD_SCM_ID AND SCM.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_TCO_TIPO_COMERCIALIZACION TCO ON TCO.DD_TCO_ID = APU.DD_TCO_ID AND TCO.BORRADO = 0
					WHERE TCO.DD_TCO_CODIGO = ''03''
					AND (ACT.OK_TECNICO = 1 
					OR EXISTS (SELECT 1 FROM ACT_OFR ACO
					JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON ACO.OFR_ID = OFR.OFR_ID AND OFR.BORRADO = 0
					JOIN '||V_ESQUEMA||'.DD_EOF_ESTADOS_OFERTA EOF ON OFR.DD_EOF_ID = EOF.DD_EOF_ID AND EOF.DD_EOF_CODIGO = ''01''
					WHERE ACO.ACT_ID = ACT.ACT_ID)
					OR SCM.DD_SCM_CODIGO = ''05'')) AUX
				ON (CBX.CBX_ID = AUX.CBX_ID)
				WHEN MATCHED THEN
					UPDATE SET CBX.DD_EAT_ID = AUX.DD_EAT_ID
					, CBX.USUARIOMODIFICAR = '''||V_USUARIO||'''
					, CBX.FECHAMODIFICAR = SYSDATE';
	DBMS_OUTPUT.PUT_LINE(V_SQL);
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado a E07 un total de '||SQL%ROWCOUNT||' filas de la tabla '||V_TABLA);

	COMMIT;
	
	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
