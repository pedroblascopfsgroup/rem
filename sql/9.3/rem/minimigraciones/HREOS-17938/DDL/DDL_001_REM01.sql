--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE

TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REMMASTER'; --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
V_ESQUEMA_3 VARCHAR2(20 CHAR) := 'REM_QUERY';
V_ESQUEMA_4 VARCHAR2(20 CHAR) := 'PFSREM';
V_TABLESPACE_IDX VARCHAR2(30 CHAR) := 'REM_IDX';
V_TABLA VARCHAR2(40 CHAR) := 'GASTOS_INCIDENCIA_CXB_20220519';

BEGIN

SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA||'' AND OWNER= ''||V_ESQUEMA_1||'';

IF TABLE_COUNT > 0 THEN

    DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
    EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'';
    
END IF;

EXECUTE IMMEDIATE '
		CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA||'
		(
			  ID_FACTURA_FAC				VARCHAR2(250 CHAR)
			 ,NUM_FACTURA_PROV				VARCHAR2(250 CHAR)
			 ,NIF_PROV						VARCHAR2(250 CHAR)
			 ,NOMBRE_PROV					VARCHAR2(250 CHAR)
			 ,FECHA_FACTURA					VARCHAR2(250 CHAR)
			 ,CLAVE_FACTURA					VARCHAR2(250 CHAR)
			 ,DESC_SITUACION				VARCHAR2(250 CHAR)
			 ,TOTAL_FACTURA					VARCHAR2(250 CHAR)
			 ,PCT_RETENCION					VARCHAR2(250 CHAR)
			 ,REFERENCIA					VARCHAR2(250 CHAR)
			 ,FECHA_ENTRADA					VARCHAR2(250 CHAR)
			 ,SERVICIO						VARCHAR2(250 CHAR)
			 ,SUBSERVICIO					VARCHAR2(250 CHAR)
			 ,UNIDAD_REGISTRAL				VARCHAR2(250 CHAR)
			 ,IMPORTE_ACTIVACION_BASE		VARCHAR2(250 CHAR)
			 ,IMPORTE_ACTIVACION_IMPUESTO	VARCHAR2(250 CHAR)
			 ,CONEXION						VARCHAR2(250 CHAR)
			 ,ERROR							VARCHAR2(250 CHAR)
			 ,DEFINICION					VARCHAR2(250 CHAR)
			 ,ID_LOTE						VARCHAR2(250 CHAR)
			 ,CONEXION_ORIGINAL				VARCHAR2(250 CHAR)
			 ,FECHA_REG_LOTE				VARCHAR2(250 CHAR)
			 ,FECHA_ENVIO					VARCHAR2(250 CHAR)
			 ,ENVIADO						VARCHAR2(250 CHAR)
			 ,FECHA_RESPUESTA				VARCHAR2(250 CHAR)
		)'
;

DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA||' CREADA');  

IF V_ESQUEMA_2 != V_ESQUEMA_1 THEN

	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_2||''); 

END IF;

IF V_ESQUEMA_3 != V_ESQUEMA_1 THEN

	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_3||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_3||''); 

END IF;

IF V_ESQUEMA_4 != V_ESQUEMA_1 THEN
	
	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA||'" TO "'||V_ESQUEMA_4||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA||' OTORGADOS A '||V_ESQUEMA_4||''); 

END IF;

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;

/

EXIT;

