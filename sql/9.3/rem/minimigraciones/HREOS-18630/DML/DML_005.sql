--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
	V_MSQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_USUARIO VARCHAR2(50 CHAR) := 'HREOS-18630'; -- USUARIO CREAR/MODIFICAR
	V_COUNT NUMBER(16); -- Vble. para comprobar
	err_num NUMBER; -- Numero de errores
	err_msg VARCHAR2(2048); -- Mensaje de error
	
BEGIN
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');

	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR T1 USING (
				SELECT PVE.PVE_ID
				, AUX.NOMBRE
				, AUX.CIF
				, AUX.CALLE
				, AUX.COD_POSTAL
				, AUX.LOCALIDAD
				, AUX.TELEFONO
				, AUX.FAX
				, AUX.WEB
				, AUX.EMAIL
				, AUX.PROVINCIA_COD
				, AUX.CCAA
				, AUX.SUBTIPO_PROVEEDOR
				, AUX.NOMBRE_VIA
				, AUX.TIPO_CALLE
				, AUX.NUMERO_VIA
				, ROW_NUMBER() OVER (PARTITION BY PVE.PVE_ID ORDER BY PVE.FECHACREAR DESC) RN
				FROM '||V_ESQUEMA||'.APR_AUX_HREOS_18630 AUX
				JOIN '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVE ON PVE.PVE_DOCIDENTIF = AUX.CIF
				WHERE PVE.BORRADO = 0
				AND PVE.USUARIOCREAR <> ''HREOS-18630''
		   ) T2 ON (T1.PVE_ID = T2.PVE_ID AND T2.RN = 1)
		   WHEN MATCHED THEN UPDATE SET 
		   T1.PVE_NOMBRE = T2.NOMBRE,
		   T1.PVE_NOMBRE_COMERCIAL = T2.NOMBRE,							      
		   T1.PVE_CP = T2.COD_POSTAL,
		   T1.PVE_DIRECCION = T2.CALLE,
		   T1.PVE_TELF1 = T2.TELEFONO,
		   T1.PVE_FAX = T2.FAX,
		   T1.PVE_EMAIL = SUBSTR(T2.EMAIL,1,50),
		   T1.PVE_PAGINA_WEB = SUBSTR(T2.WEB, 1,50),
		   T1.DD_PRV_ID = (SELECT PRV.DD_PRV_ID FROM '||V_ESQUEMA_M||'.DD_PRV_PROVINCIA PRV WHERE PRV.DD_PRV_CODIGO = T2.PROVINCIA_COD),
		   T1.DD_LOC_ID = (SELECT LOC.DD_LOC_ID FROM '||V_ESQUEMA_M||'.DD_LOC_LOCALIDAD LOC WHERE LOC.DD_LOC_CODIGO = T2.LOCALIDAD),
		   T1.USUARIOMODIFICAR = ''HREOS-18630'', 
		   T1.FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] CREADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN ACT_PVE_PROVEEDOR');

	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.ACT_PRD_PROVEEDOR_DIRECCION 
				(PRD_ID,
				PVE_ID,
				DD_TDP_ID,
				DD_TVI_ID,
				PRD_NOMBRE,
				PRD_NUM,
				DD_PRV_ID,
				PRD_CP,
				PRD_EMAIL,
				PRD_TELEFONO,
				DD_LOC_ID,
				USUARIOCREAR,
				FECHACREAR) 
				SELECT '||V_ESQUEMA||'.S_ACT_PRD_PROVEEDOR_DIRECCION.NEXTVAL
				, PVE.PVE_ID
				, (SELECT DD_TDP_ID FROM '||V_ESQUEMA||'.DD_TDP_TIPO_DIR_PROVEEDOR WHERE DD_TDP_CODIGO = ''01'')
				, (SELECT DD_TVI_ID FROM '||V_ESQUEMA_M||'.DD_TVI_TIPO_VIA WHERE DD_TVI_CODIGO = AUX.TIPO_CALLE)
				, AUX.NOMBRE_VIA
				, AUX.NUMERO_VIA
				, (SELECT PRV.DD_PRV_ID FROM '||V_ESQUEMA_M||'.DD_PRV_PROVINCIA PRV WHERE PRV.DD_PRV_CODIGO = AUX.PROVINCIA_COD)
				, AUX.COD_POSTAL
				, SUBSTR(AUX.EMAIL,1,50)
				, AUX.TELEFONO
				, (SELECT LOC.DD_LOC_ID FROM '||V_ESQUEMA_M||'.DD_LOC_LOCALIDAD LOC WHERE LOC.DD_LOC_CODIGO = AUX.LOCALIDAD)
				, ''HREOS-18630''
				, SYSDATE
				FROM '||V_ESQUEMA||'.APR_AUX_HREOS_18630 AUX
				JOIN '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR PVE ON PVE.PVE_DOCIDENTIF = AUX.CIF
				WHERE PVE.BORRADO = 0
				AND PVE.USUARIOCREAR <> ''HREOS-18630''
				AND NOT EXISTS (SELECT 1 FROM '||V_ESQUEMA||'.ACT_PRD_PROVEEDOR_DIRECCION PRD WHERE PRD.PVE_ID = PVE.PVE_ID AND PRD.USUARIOCREAR = ''HREOS-18630'')';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] CREADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN ACT_PRD_PROVEEDOR_DIRECCION');

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN] ');
	
	EXCEPTION
	
	    WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------');
		DBMS_OUTPUT.PUT_LINE(SQLERRM);
		DBMS_OUTPUT.PUT_LINE(V_MSQL);
		ROLLBACK;
		RAISE;
END;
/
EXIT;
