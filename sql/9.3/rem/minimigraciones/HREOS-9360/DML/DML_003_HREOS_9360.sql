WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

	V_TABLA VARCHAR2(100 CHAR) := 'ACT_LLV_LLAVE'; -- Variable para tabla de salida para el borrado
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error
	V_SQL VARCHAR2(4000 CHAR);
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'HREOS-9360';
	

BEGIN
	
	DBMS_OUTPUT.PUT_LINE('[INICIO]'||CHR(10));
	V_SQL := 'INSERT INTO '||V_ESQUEMA||'.'||V_TABLA||' 
			(
			LLV_ID
			, ACT_ID
			, LLV_COMPLETO
			, VERSION
			, USUARIOCREAR
			, FECHACREAR
			, BORRADO
			, LLV_NUM_LLAVE
			, DD_TTE_ID_POSEEDOR
			, LLV_COD_TENEDOR_POSEEDOR
			, LLV_COD_TENEDOR_NO_PVE
			, LLV_FECHA_ANILLADO
			, LLV_CODE
			, LLV_OBSERVACIONES
			, LLV_FECHA_RECEPCION
			)
			SELECT 
			REM01.S_ACT_LLV_LLAVE.NEXTVAL
			, ACT.ACT_ID
			, AUX.LLV_COMPLETO
			, 0
			, '''||V_USUARIO||'''
			, SYSDATE
			, 0
			, AUX.LLV_NUM_LLAVE
			, TTE.DD_TTE_ID
			, PVE.PVE_ID LLV_COD_TENEDOR_POSEEDOR
			, CASE WHEN PVE.PVE_ID IS NULL AND AUX.LLV_COD_TENEDOR_NO_PVE IS NULL THEN ''Sin tenedor'' 
				WHEN PVE.PVE_ID IS NOT NULL THEN NULL
				ELSE AUX.LLV_COD_TENEDOR_NO_PVE
				END LLV_COD_TENEDOR_NO_PVE
			, TO_TIMESTAMP(AUX.LLV_FECHA_ANILLADO,''DD/MM/RRRR HH24:MI:SS.FF'') LLV_FECHA_ANILLADO
			, AUX.LLV_CODE
			, SUBSTR(AUX.LLV_OBSERVACIONES, 1, 100) AS LLV_OBSERVACIONES
			, TO_TIMESTAMP(AUX.LLV_FECHA_RECEPCION,''DD/MM/RRRR HH24:MI:SS.FF'') LLV_FECHA_RECEPCION
			FROM REM01.AUX_inserta_LLAVES_ACTIVO aux
			JOIN REM01.ACT_ACTIVO ACT ON ACT.ACT_NUM_ACTIVO = AUX.ACT_NUM_ACTIVO AND ACT.BORRADO = 0
			LEFT JOIN REM01.DD_TTE_TIPO_TENEDOR TTE ON TTE.DD_TTE_CODIGO = AUX.DD_TTE_CODIGO_TENEDOR AND TTE.BORRADO = 0
			LEFT JOIN REM01.ACT_PVE_PROVEEDOR PVE ON PVE.PVE_COD_REM = AUX.LLV_COD_TENEDOR AND PVE.BORRADO = 0';
				

		EXECUTE IMMEDIATE V_SQL;
		DBMS_OUTPUT.PUT_LINE('[INFO] Se han mergeado en total '||SQL%ROWCOUNT||' registros en la tabla '||V_ESQUEMA||'.'||V_TABLA||'');

	COMMIT;

	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
