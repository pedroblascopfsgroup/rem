WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	
	V_TABLA_TMP VARCHAR2(40 CHAR) := 'TMP_ASIG_GESTOR_ACTIVO'; -- Variable para tabla de salida para el borrado	
	V_TABLA_GEE VARCHAR2(40 CHAR):='GEE_GESTOR_ENTIDAD';--Variable para la tabla de volcado
	V_TABLA_GEH VARCHAR2(40 CHAR):='GEH_GESTOR_ENTIDAD_HIST';--Variable para la tabla de volcado
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error
	V_MSQL VARCHAR2(4000 CHAR);--Variable para consultar la existencia de registros en la tabla
	V_SQL VARCHAR2(4000 CHAR);--Sentencia a ejecutar
	V_NUM NUMBER(16); --Variable para comprobar si existen registros
	V_ID NUMBER(16);
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'HREOS-9387';
	

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INICIO]'||CHR(10));

	
	V_MSQL := 'SELECT COUNT(*) FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' WHERE BORRADO = 0';
  	EXECUTE IMMEDIATE V_MSQL INTO V_NUM;

	DBMS_OUTPUT.PUT_LINE('[INFO] COMIENZA EL PROCESO DE VOLCADO SOBRE LA TABLA '||V_ESQUEMA||'.'||V_TABLA_GEE||'.');

	-- LOOP para convertir CONTADOR de la tabla TMP_ASIG_GESTOR_ACTIVO en GEE_ID
    IF V_NUM > 0 THEN


	V_SQL := 'INSERT INTO '||V_ESQUEMA||'.'||V_TABLA_GEE||' (GEE_ID, USU_ID, DD_TGE_ID, VERSION, USUARIOCREAR, FECHACREAR, BORRADO, USUARIOMODIFICAR)
	SELECT '||V_ESQUEMA||'.S_'||V_TABLA_GEE||'.NEXTVAL
	,(SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME=TMP.USUARIO)
	,(SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO=TMP.GESTOR)
	, 0, '''||V_USUARIO||''', SYSDATE, 0, ACT.ACT_ID
	FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' TMP
	JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_NUM_ACTIVO = TMP.ID_HAYA
	WHERE TMP.BORRADO = 0';	

	EXECUTE IMMEDIATE V_SQL; 	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han insertado en total '||SQL%ROWCOUNT||' registros en la tabla '||V_TABLA_GEE);

	V_SQL := '
	INSERT INTO /*+ APPEND */ '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO (GEE_ID, ACT_ID)
	SELECT GEE.GEE_ID, TO_NUMBER(GEE.USUARIOMODIFICAR)
	FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
	LEFT JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID AND TO_CHAR(GAC.ACT_ID) = GEE.USUARIOMODIFICAR
	WHERE GEE.USUARIOCREAR = '''||V_USUARIO||''' AND GEE.USUARIOMODIFICAR IS NOT NULL AND GAC.GEE_ID IS NULL
	'
	;
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han insertado en total '||SQL%ROWCOUNT||' registros en la tabla GAC_GESTOR_ADD_ACTIVO');

	V_SQL := 'INSERT INTO '||V_ESQUEMA||'.'||V_TABLA_GEH||' (GEH_ID, USU_ID, DD_TGE_ID, GEH_FECHA_DESDE, VERSION, USUARIOCREAR, FECHACREAR, BORRADO, USUARIOMODIFICAR)
	SELECT '||V_ESQUEMA||'.S_'||V_TABLA_GEH||'.NEXTVAL
	,(SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME=TMP.USUARIO)
	,(SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO=TMP.GESTOR)
	, SYSDATE, 0, '''||V_USUARIO||''', SYSDATE, 0, ACT.ACT_ID
	FROM '||V_ESQUEMA||'.'||V_TABLA_TMP||' TMP
	JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_NUM_ACTIVO = TMP.ID_HAYA
	WHERE TMP.BORRADO = 0';

	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han insertado en total '||SQL%ROWCOUNT||' registros en la tabla '||V_TABLA_GEH);

	V_SQL := '
	INSERT INTO '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO (GEH_ID, ACT_ID)
	SELECT GEH.GEH_ID, TO_NUMBER(GEH.USUARIOMODIFICAR)
	FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH
	LEFT JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH ON GAH.GEH_ID = GEH.GEH_ID AND TO_CHAR(GAH.ACT_ID) = GEH.USUARIOMODIFICAR
	WHERE GEH.USUARIOCREAR = '''||V_USUARIO||''' AND GEH.USUARIOMODIFICAR IS NOT NULL AND GAH.GEH_ID IS NULL
	'
	;
	EXECUTE IMMEDIATE V_SQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han insertado en total '||SQL%ROWCOUNT||' registros en la tabla GAH_GESTOR_ACTIVO_HISTORICO');

   END IF;
	
	COMMIT;
	
	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
