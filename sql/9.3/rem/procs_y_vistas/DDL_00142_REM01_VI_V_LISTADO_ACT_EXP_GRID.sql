WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#';
    V_MSQL VARCHAR2(4000 CHAR);

    CUENTA NUMBER;

BEGIN

-- Modificaci贸n de la vista V_LISTADO_ACT_EXP de Fase1.

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_LISTADO_ACT_EXP_GRID' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_LISTADO_ACT_EXP_GRID...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.V_LISTADO_ACT_EXP_GRID';
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_LISTADO_ACT_EXP_GRID... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_LISTADO_ACT_EXP_GRID' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_LISTADO_ACT_EXP_GRID...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.V_LISTADO_ACT_EXP_GRID';
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_LISTADO_ACT_EXP_GRID... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_LISTADO_ACT_EXP_GRID...');
  EXECUTE IMMEDIATE 'CREATE VIEW V_LISTADO_ACT_EXP_GRID AS 
		WITH AUX_CALCULOS_SPS AS (SELECT ECO.ECO_ID, OFR.ACT_ID, SPS.SPS_ID,
       CASE WHEN SPS.SPS_OCUPADO IS NULL OR SPS.SPS_OCUPADO = 0 THEN ''01''
            WHEN SPS.SPS_OCUPADO IS NOT NULL AND SPS.SPS_OCUPADO = 1 AND SPS.SPS_CON_TITULO IS NOT NULL AND SPS.SPS_CON_TITULO = 1 THEN ''02''
            WHEN SPS.SPS_OCUPADO IS NOT NULL AND SPS.SPS_OCUPADO = 1 AND SPS.SPS_CON_TITULO IS NOT NULL AND (SPS.SPS_CON_TITULO = 0 OR SPS.SPS_CON_TITULO IS NULL) THEN ''03''
            ELSE NULL
            END AS SITUACION_POS_COD_I,
       CASE WHEN FPA.FECHA_POSESION IS NOT NULL THEN 1
            ELSE 0
            END AS POS_INICIAL_I
       FROM REM01.ACT_SPS_SIT_POSESORIA SPS
       JOIN REM01.ACT_OFR OFR ON SPS.ACT_ID = OFR.ACT_ID
       JOIN REM01.V_FECHA_POSESION_ACTIVO FPA ON FPA.ACT_ID=SPS.ACT_ID
       JOIN REM01.ECO_EXPEDIENTE_COMERCIAL ECO ON OFR.OFR_ID = ECO.OFR_ID
       ),
       MINI AS (
           SELECT DISTINCT OFR.ACT_ID, ECO.ECO_ID,
           CASE WHEN TPC2.DD_TPC_CODIGO IS NULL THEN NULL
           ELSE VAL2.VAL_IMPORTE
           END AS IMPORTE_TPC_MIN,
           TPC2.DD_TPC_CODIGO AS CODIGO_MIN
           FROM REM01.ECO_EXPEDIENTE_COMERCIAL ECO
           JOIN REM01.ACT_OFR OFR ON ECO.OFR_ID = OFR.OFR_ID
           INNER JOIN REM01.ACT_VAL_VALORACIONES VAL2 ON OFR.ACT_ID = VAL2.ACT_ID
           INNER JOIN REM01.DD_TPC_TIPO_PRECIO TPC2 ON VAL2.DD_TPC_ID = TPC2.DD_TPC_ID AND TPC2.DD_TPC_CODIGO = ''04''
           WHERE VAL2.BORRADO = 0
        ),
        BEX AS (
            SELECT DISTINCT BEX.ACT_BEX_ECO_ID, BEX.ACT_ID, BEX.USUARIOBORRAR
            FROM REM01.ACT_BEX_BLOQ_EXP_FORMALIZAR BEX
            JOIN REM01.ECO_EXPEDIENTE_COMERCIAL ECO ON BEX.ACT_BEX_ECO_ID = ECO.ECO_ID
            JOIN REM01.ACT_OFR OFR ON ECO.OFR_ID = OFR.OFR_ID AND BEX.ACT_ID = OFR.ACT_ID
        )
        SELECT ECO.ECO_ID||OFR.ACT_ID AS ID_VISTA, ECO.ECO_ID, OFR.ACT_ID, OFR.OFR_ACT_PORCEN_PARTICIPACION AS PORCENTAJE_PARTICIPACION, OFR.ACT_OFR_IMPORTE AS IMP_ACT_OFERTA,
        MINI.IMPORTE_TPC_MIN,
        CASE 
            WHEN JUR.ACT_ECO_FECHA_EMISION IS NULL THEN 2
            WHEN JUR.ACT_ECO_FECHA_EMISION IS NOT NULL AND BEX.USUARIOBORRAR IS NOT NULL THEN 1
            ELSE 0
        END AS BLOQUEO,
        CASE 
            WHEN (SIP.DD_SIP_CODIGO IS NOT NULL AND CAL.SITUACION_POS_COD_I IS NOT NULL AND SIP.DD_SIP_CODIGO = CAL.SITUACION_POS_COD_I)
            AND (CON.COND_POSESION_INICIAL IS NOT NULL AND CAL.POS_INICIAL_I IS NOT NULL AND CON.COND_POSESION_INICIAL = CAL.SITUACION_POS_COD_I)
            AND (ETI.DD_ETI_CODIGO IS NOT NULL AND ETI2.DD_ETI_CODIGO IS NOT NULL AND ETI.DD_ETI_CODIGO = ETI2.DD_ETI_CODIGO) THEN 1
            ELSE 0
        END AS CONDICIONES,
        ACT.ACT_NUM_ACTIVO, TPA.DD_TPA_DESCRIPCION, SAC.DD_SAC_DESCRIPCION, LOC.DD_LOC_DESCRIPCION,BIE.BIE_LOC_DIRECCION
        FROM REM01.ECO_EXPEDIENTE_COMERCIAL ECO
        JOIN REM01.ACT_OFR OFR ON ECO.OFR_ID = OFR.OFR_ID
        LEFT JOIN MINI MINI ON MINI.ECO_ID = ECO.ECO_ID AND MINI.ACT_ID = OFR.ACT_ID
        LEFT JOIN REM01.ACT_ECO_INFORME_JURIDICO JUR ON ECO.ECO_ID = JUR.ECO_ID AND OFR.ACT_ID = JUR.ACT_ID
        LEFT JOIN BEX BEX ON ECO.ECO_ID = BEX.ACT_BEX_ECO_ID AND OFR.ACT_ID = BEX.ACT_ID
        LEFT JOIN AUX_CALCULOS_SPS CAL ON ECO.ECO_ID = CAL.ECO_ID AND OFR.ACT_ID = CAL.ACT_ID
        LEFT JOIN REM01.ECO_COND_CONDICIONES_ACTIVO CON ON ECO.ECO_ID = CON.ECO_ID AND OFR.ACT_ID = CON.ACT_ID
        LEFT JOIN REM01.DD_SIP_SITUACION_POSESORIA SIP ON CON.DD_SIP_ID = SIP.DD_SIP_ID
        LEFT JOIN REM01.DD_ETI_ESTADO_TITULO ETI ON CON.DD_ETI_ID = ETI.DD_ETI_ID
        LEFT JOIN REM01.ACT_TIT_TITULO TIT ON OFR.ACT_ID = TIT.ACT_ID
        LEFT JOIN REM01.DD_ETI_ESTADO_TITULO ETI2 ON TIT.DD_ETI_ID = ETI2.DD_ETI_ID
        JOIN REM01.ACT_ACTIVO ACT ON OFR.ACT_ID = ACT.ACT_ID
        LEFT JOIN REM01.DD_SAC_SUBTIPO_ACTIVO SAC ON ACT.DD_SAC_ID = SAC.DD_SAC_ID
        LEFT JOIN REM01.DD_TPA_TIPO_ACTIVO TPA ON SAC.DD_TPA_ID = TPA.DD_TPA_ID
        JOIN REM01.BIE_LOCALIZACION BIE ON ACT.BIE_ID = BIE.BIE_ID
        LEFT JOIN REMMASTER.DD_LOC_LOCALIDAD LOC ON BIE.DD_LOC_ID = LOC.DD_LOC_ID
  ';

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_LISTADO_ACT_EXP_GRID...Creada OK');


  EXCEPTION
    WHEN OTHERS THEN
      ERR_NUM := SQLCODE;
      ERR_MSG := SQLERRM;
      DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecuci贸n:'||TO_CHAR(ERR_NUM));
      DBMS_OUTPUT.put_line('-----------------------------------------------------------');
      DBMS_OUTPUT.put_line(ERR_MSG);
      ROLLBACK;
      RAISE;
  END;
  /
  EXIT;
