--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(32000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_GRID_OFR_ACT_AGR_INC_ANULADAS' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_GRID_OFR_ACT_AGR_INC_ANULADAS' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS...');
  V_MSQL := 'CREATE VIEW ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS 
	AS
		SELECT /*+ no_merge(VAO) */
				OFR.OFR_ID AS ID,
		    OFR.OFR_NUM_OFERTA,
				OFR.OFR_FECHA_ALTA,
		    TOF.DD_TOF_DESCRIPCION,
				TOF.DD_TOF_CODIGO,
				NVL2(AGR.AGR_NUM_AGRUP_REM, AGR.AGR_NUM_AGRUP_REM, ACT.ACT_NUM_ACTIVO) AS NUM_ACTIVO_AGRUPACION,
		    NVL2(CLC.CLC_RAZON_SOCIAL,CLC.CLC_RAZON_SOCIAL, CLC.CLC_NOMBRE || NVL2(CLC.CLC_APELLIDOS, '' '' || CLC.CLC_APELLIDOS, '''')) AS OFERTANTE,
		    OFR.OFR_IMPORTE,
				OFR.OFR_IMPORTE_CONTRAOFERTA,
		    EOF.DD_EOF_DESCRIPCION,
				EOF.DD_EOF_CODIGO,
		    ECO.ECO_NUM_EXPEDIENTE,
				ECO.ECO_ID,
		    EEC.DD_EEC_DESCRIPCION,
				EEC.DD_EEC_CODIGO,
				AGR.AGR_ID,
		    OFR.OFR_ID,
				VAO.ACT_ID,
				TPR.DD_TPR_CODIGO AS CANAL,
				TPR.DD_TPR_DESCRIPCION AS CANAL_DESCRIPCION,
				OFR.OFR_OFERTA_EXPRESS AS OFERTA_EXPRESS,
				DECODE(GEN.ACT_ID,NULL,0,1)  AS GENCAT
		FROM '|| V_ESQUEMA ||'.OFR_OFERTAS OFR
		JOIN '|| V_ESQUEMA ||'.V_FIRST_ACTIVO_OFERTA VAO 				ON VAO.OFR_ID = OFR.OFR_ID
		INNER JOIN '|| V_ESQUEMA ||'.ACT_ACTIVO ACT 					ON ACT.ACT_ID = VAO.ACT_ID and act.borrado = 0
		INNER JOIN '|| V_ESQUEMA ||'.DD_TOF_TIPOS_OFERTA TOF 			ON TOF.DD_TOF_ID = OFR.DD_TOF_ID
		INNER JOIN '|| V_ESQUEMA ||'.DD_EOF_ESTADOS_OFERTA EOF 			ON EOF.DD_EOF_ID = OFR.DD_EOF_ID
		LEFT JOIN '|| V_ESQUEMA ||'.ACT_PVE_PROVEEDOR PVEPRES 			ON PVEPRES.PVE_ID = OFR.PVE_ID_PRESCRIPTOR
		LEFT JOIN '|| V_ESQUEMA ||'.DD_TPR_TIPO_PROVEEDOR TPR 			ON TPR.DD_TPR_ID = PVEPRES.DD_TPR_ID
		LEFT JOIN '|| V_ESQUEMA ||'.ECO_EXPEDIENTE_COMERCIAL ECO 		ON ECO.OFR_ID = OFR.OFR_ID
		LEFT JOIN '|| V_ESQUEMA ||'.DD_EEC_EST_EXP_COMERCIAL EEC 		ON EEC.DD_EEC_ID = ECO.DD_EEC_ID
		INNER JOIN '|| V_ESQUEMA ||'.CLC_CLIENTE_COMERCIAL CLC 			ON CLC.CLC_ID = OFR.CLC_ID
		LEFT JOIN '|| V_ESQUEMA ||'.ACT_AGR_AGRUPACION AGR 				ON AGR.AGR_ID = OFR.AGR_ID and agr.borrado = 0
		LEFT JOIN '|| V_ESQUEMA ||'.VI_ACTIVOS_AFECTOS_GENCAT GEN 		ON GEN.ACT_ID = VAO.ACT_ID
		WHERE OFR.BORRADO  = 0';

  EXECUTE IMMEDIATE	V_MSQL;
    
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS...Creada OK');
  EXECUTE IMMEDIATE 'COMMENT ON TABLE ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS IS ''VISTA PARA RECOGER LAS OFERTAS DE ACTIVOS Y AGRUPACIONES''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.OFR_NUM_OFERTA IS ''Número de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.OFR_FECHA_ALTA IS ''Fecha de creación de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.DD_TOF_DESCRIPCION IS ''Descripción del tipo de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.DD_TOF_CODIGO IS ''Código del tipo de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.NUM_ACTIVO_AGRUPACION IS ''Número de agrupación o activo''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.OFERTANTE IS ''Nombre y apellidos del Cliente comercial que realiza la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.OFR_IMPORTE IS ''Importe ofertado''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.DD_EOF_DESCRIPCION IS ''Estado de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.DD_EOF_CODIGO IS ''Código del estado de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.ECO_NUM_EXPEDIENTE IS ''Número del expediente comercial relacionado con la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.ECO_ID IS ''Código identificador único del expediente comercial relacionado con la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.DD_EEC_DESCRIPCION IS ''Estado del expediente comercial relacionado con la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.AGR_ID IS ''Código identificador único de la agrupación.''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.OFR_ID IS ''Código identificador único de la oferta.''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR_INC_ANULADAS.GENCAT IS ''Activos que pertenecen a GENCAT.''';
  
  DBMS_OUTPUT.PUT_LINE('Creados los comentarios en CREATE VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR_INC_ANULADAS...Creada OK');
  EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE; 
END;
/

EXIT;
