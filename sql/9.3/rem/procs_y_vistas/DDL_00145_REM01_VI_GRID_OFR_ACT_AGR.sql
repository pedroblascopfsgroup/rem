WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(32000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_GRID_OFR_ACT_AGR' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_GRID_OFR_ACT_AGR' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR...');
  V_MSQL := 'CREATE VIEW ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR 
	AS
		SELECT 
				OFR.OFR_ID AS ID,
		    OFR.OFR_NUM_OFERTA,
				OFR.OFR_FECHA_ALTA,
		    TOF.DD_TOF_DESCRIPCION,
				TOF.DD_TOF_CODIGO,
				NVL2(AGR.AGR_NUM_AGRUP_REM, AGR.AGR_NUM_AGRUP_REM, ACT.ACT_NUM_ACTIVO) AS NUM_ACTIVO_AGRUPACION,
		    CASE WHEN CLC.DD_TPE_ID = 2 THEN CLC.CLC_RAZON_SOCIAL
				ELSE CLC.CLC_NOMBRE || NVL2(CLC.CLC_APELLIDOS, '' '' || CLC.CLC_APELLIDOS, '''')
				END AS OFERTANTE,
        OFR.OFR_IMPORTE,
				OFR.OFR_IMPORTE_CONTRAOFERTA,
		    EOF.DD_EOF_DESCRIPCION,
				EOF.DD_EOF_CODIGO,
		    ECO.ECO_NUM_EXPEDIENTE,
				ECO.ECO_ID,
		    EEC.DD_EEC_DESCRIPCION,
				EEC.DD_EEC_CODIGO,
				AGR.AGR_ID,
		    OFR.OFR_ID,
				ACT.ACT_ID,
				TPR.DD_TPR_CODIGO AS CANAL,
				TPR.DD_TPR_DESCRIPCION AS CANAL_DESCRIPCION,
				OFR.OFR_OFERTA_EXPRESS AS OFERTA_EXPRESS,
				DECODE(GEN.ACT_ID,NULL,0,1)  AS GENCAT,
			C4C.DD_ECC_CODIGO AS EST_CODIGO_C4C,
			EDP.DD_EDP_CODIGO AS ESTADODEPOSITOCODIGO,
			EDP.DD_EDP_DESCRIPCION AS ESTADODEPOSITO
		FROM '|| V_ESQUEMA ||'.OFR_OFERTAS OFR
        	INNER JOIN '|| V_ESQUEMA ||'.ACT_OFR AOF 					ON OFR.OFR_ID = AOF.OFR_ID
		INNER JOIN '|| V_ESQUEMA ||'.ACT_ACTIVO ACT 					ON ACT.ACT_ID = AOF.ACT_ID and act.borrado = 0
		INNER JOIN '|| V_ESQUEMA ||'.DD_TOF_TIPOS_OFERTA TOF 			ON TOF.DD_TOF_ID = OFR.DD_TOF_ID
		INNER JOIN '|| V_ESQUEMA ||'.DD_EOF_ESTADOS_OFERTA EOF 			ON EOF.DD_EOF_ID = OFR.DD_EOF_ID
		LEFT JOIN '|| V_ESQUEMA ||'.ACT_PVE_PROVEEDOR PVEPRES 			ON PVEPRES.PVE_ID = OFR.PVE_ID_PRESCRIPTOR
		LEFT JOIN '|| V_ESQUEMA ||'.DD_TPR_TIPO_PROVEEDOR TPR 			ON TPR.DD_TPR_ID = PVEPRES.DD_TPR_ID
		LEFT JOIN '|| V_ESQUEMA ||'.ECO_EXPEDIENTE_COMERCIAL ECO 		ON ECO.OFR_ID = OFR.OFR_ID
		LEFT JOIN '|| V_ESQUEMA ||'.DD_EEC_EST_EXP_COMERCIAL EEC 		ON EEC.DD_EEC_ID = ECO.DD_EEC_ID
		INNER JOIN '|| V_ESQUEMA ||'.CLC_CLIENTE_COMERCIAL CLC 			ON CLC.CLC_ID = OFR.CLC_ID
		LEFT JOIN '|| V_ESQUEMA ||'.ACT_AGR_AGRUPACION AGR 				ON AGR.AGR_ID = OFR.AGR_ID and agr.borrado = 0
		LEFT JOIN '|| V_ESQUEMA ||'.VI_ACTIVOS_AFECTOS_GENCAT GEN 		ON GEN.ACT_ID = ACT.ACT_ID
		LEFT JOIN '|| V_ESQUEMA ||'.OFR_OFERTAS_CAIXA CBX				ON OFR.OFR_ID = CBX.OFR_ID 
		LEFT JOIN '|| V_ESQUEMA ||'.DD_ECC_ESTADO_COMUNICACION_C4C C4C	ON CBX.DD_ECC_ID = C4C.DD_ECC_ID
		LEFT JOIN '|| V_ESQUEMA ||'.DEP_DEPOSITO DEP ON DEP.OFR_ID = OFR.OFR_ID
		LEFT JOIN '|| V_ESQUEMA ||'.DD_EDP_EST_DEPOSITO EDP	ON EDP.DD_EDP_ID = DEP.DD_EDP_ID
		WHERE OFR.BORRADO  = 0 AND EOF.DD_EOF_CODIGO IN (''01'',''03'',''04'',''05'',''07'',''05'',''08'',''09'')';

  EXECUTE IMMEDIATE	V_MSQL;
    
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR...Creada OK');
  EXECUTE IMMEDIATE 'COMMENT ON TABLE ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR IS ''VISTA PARA RECOGER LAS OFERTAS DE ACTIVOS Y AGRUPACIONES''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.OFR_NUM_OFERTA IS ''Número de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.OFR_FECHA_ALTA IS ''Fecha de creación de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.DD_TOF_DESCRIPCION IS ''Descripción del tipo de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.DD_TOF_CODIGO IS ''Código del tipo de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.NUM_ACTIVO_AGRUPACION IS ''Número de agrupación o activo''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.OFERTANTE IS ''Nombre y apellidos del Cliente comercial que realiza la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.OFR_IMPORTE IS ''Importe ofertado''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.DD_EOF_DESCRIPCION IS ''Estado de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.DD_EOF_CODIGO IS ''Código del estado de la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.ECO_NUM_EXPEDIENTE IS ''Número del expediente comercial relacionado con la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.ECO_ID IS ''Código identificador único del expediente comercial relacionado con la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.DD_EEC_DESCRIPCION IS ''Estado del expediente comercial relacionado con la oferta''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.AGR_ID IS ''Código identificador único de la agrupación.''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.OFR_ID IS ''Código identificador único de la oferta.''';
  EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.GENCAT IS ''Activos que pertenecen a GENCAT.''';
 -- EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.ESTADODEPOSITOCODIGO IS ''Código estado depósito ''';
  --EXECUTE IMMEDIATE 'COMMENT ON COLUMN ' || V_ESQUEMA || '.VI_GRID_OFR_ACT_AGR.ESTADODEPOSITO IS ''Descripción estado depósito.''';
  
  DBMS_OUTPUT.PUT_LINE('Creados los comentarios en CREATE VIEW '|| V_ESQUEMA ||'.VI_GRID_OFR_ACT_AGR...Creada OK');
  EXCEPTION
  WHEN OTHERS THEN
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);
    ROLLBACK;
    RAISE; 
END;
/

EXIT;
