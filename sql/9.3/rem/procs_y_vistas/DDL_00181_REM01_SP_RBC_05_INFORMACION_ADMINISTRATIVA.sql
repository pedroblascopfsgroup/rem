CREATE OR REPLACE PROCEDURE SP_RBC_05_INFORMACION_ADMINISTRATIVA
        (     
	      FLAG_EN_REM IN NUMBER
        , SALIDA OUT VARCHAR2
        , COD_RETORNO OUT NUMBER
   
    )

   AS

   V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
   V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
   V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
   ERR_NUM NUMBER(25); -- Vble. auxiliar para registrar errores en el script.
   ERR_MSG VARCHAR2(10024 CHAR); -- Vble. auxiliar para registrar errores en el script.
   V_AUX NUMBER(10); -- Variable auxiliar
   V_NUM_FILAS number;

   V_FECHA_INICIO VARCHAR2(100 CHAR);
   V_FECHA_FIN VARCHAR2(100 CHAR);

BEGIN

--1º Merge tabla AUX_APR_RBC_STOCK
    SALIDA := '[INICIO]'||CHR(10);

    SALIDA := SALIDA || '[INFO] SE VA A PROCEDER A EXTRAER INFORME ADMINISTRATIVA'|| CHR(10);

    SALIDA := SALIDA || '   [INFO] 1 - EXTRACCIÓN ACT_ADM_INF_ADMINISTRATIVA'||CHR(10);

    V_MSQL := 'MERGE INTO '|| V_ESQUEMA ||'.AUX_APR_RBC_STOCK AUX
	USING (			
           SELECT
            ACT.ACT_NUM_ACTIVO_CAIXA AS NUM_IDENTIFICATIVO
            , ACT.ACT_NUM_ACTIVO AS NUM_INMUEBLE  
            , NULL /*CASE WHEN ADM.COMPRADOR_ACOJE_AYUDA = 1 THEN ''S'' ELSE ''N'' END*/ IND_COMPRADOR_ACOGE_AYUDA
            , NULL /*ADM.IMPORTE_AYUDA_FINANCIACION*100*/ IMP_AYUDA_FINANCIACION
            , NULL /*TO_CHAR(ADM.FECHA_VENCIMIENTO_AVAL_SEGURO,''YYYYMMDD'')*/ FEC_VENCIMIENTO_SEGURO
            , NULL /*TO_CHAR(ADM.FECHA_DEVOLUCION_AYUDA,''YYYYMMDD'')*/ FEC_DEVOLUCION_AYUDA
            FROM '|| V_ESQUEMA ||'.ACT_ACTIVO ACT
            JOIN '|| V_ESQUEMA ||'.ACT_ADM_INF_ADMINISTRATIVA ADM ON ACT.ACT_ID = ADM.ACT_ID AND ADM.BORRADO = 0
            JOIN '|| V_ESQUEMA ||'.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = ''03''
            JOIN '|| V_ESQUEMA ||'.ACT_PAC_PERIMETRO_ACTIVO PAC ON PAC.ACT_ID = ACT.ACT_ID AND PAC.PAC_INCLUIDO = 1
            JOIN '|| V_ESQUEMA ||'.ACT_PAC_PROPIETARIO_ACTIVO ACT_PRO ON ACT_PRO.ACT_ID = ACT.ACT_ID AND ACT_PRO.BORRADO = 0
            JOIN '|| V_ESQUEMA ||'.ACT_PRO_PROPIETARIO PRO ON PRO.PRO_ID = ACT_PRO.PRO_ID AND PRO.BORRADO = 0
            WHERE ACT.BORRADO = 0
            AND ACT.ACT_EN_TRAMITE = 0
            AND ACT.ACT_NUM_ACTIVO_CAIXA IS NOT NULL
            AND PRO.PRO_DOCIDENTIF NOT IN (''V84966126'',''V85164648'',''V85587434'',''V84322205'',''V84593961'',''V84669332'',''V85082675'',''V85623668'',''V84856319'',''V85500866'',''V85143659'',''V85594927'',''V85981231'',''V84889229'',''V84916956'',''V85160935'',''V85295087'',''V84175744'',''V84925569'',''A80352750'', ''A80514466'')
            ) US ON (US.NUM_INMUEBLE = AUX.NUM_INMUEBLE AND US.NUM_IDENTIFICATIVO = AUX.NUM_IDENTIFICATIVO)
            WHEN MATCHED THEN UPDATE SET
                 AUX.IND_COMPRADOR_ACOGE_AYUDA = US.IND_COMPRADOR_ACOGE_AYUDA
                , AUX.IMP_AYUDA_FINANCIACION = US.IMP_AYUDA_FINANCIACION
                , AUX.FEC_VENCIMIENTO_SEGURO = US.FEC_VENCIMIENTO_SEGURO
                , AUX.FEC_DEVOLUCION_AYUDA = US.FEC_DEVOLUCION_AYUDA
            WHEN NOT MATCHED THEN INSERT (
                 NUM_IDENTIFICATIVO
                , NUM_INMUEBLE
                , IND_COMPRADOR_ACOGE_AYUDA
                , IMP_AYUDA_FINANCIACION 
                , FEC_VENCIMIENTO_SEGURO 
                , FEC_DEVOLUCION_AYUDA 
                )VALUES(
                     US.NUM_IDENTIFICATIVO
                    , US.NUM_INMUEBLE
                    , US.IND_COMPRADOR_ACOGE_AYUDA
                    , US.IMP_AYUDA_FINANCIACION
                    , US.FEC_VENCIMIENTO_SEGURO
                    , US.FEC_DEVOLUCION_AYUDA
                )
   ';
   EXECUTE IMMEDIATE V_MSQL;
   
   SALIDA := SALIDA || '   [INFO] ACTUALIZADOS '|| SQL%ROWCOUNT|| CHR(10);


COMMIT;

EXCEPTION
   WHEN OTHERS THEN
      ERR_NUM := SQLCODE;
      ERR_MSG := SQLERRM;
      SALIDA := SALIDA || CHR(10) ||'    [ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM);
      SALIDA := SALIDA || CHR(10) ||'    '||ERR_MSG;
      ROLLBACK;
      RAISE;
END SP_RBC_05_INFORMACION_ADMINISTRATIVA;
/
EXIT;
