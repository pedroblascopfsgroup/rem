WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- N?mero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN
	
-- Modificaci칩n de la vista V_ACTIVOS_AGRUPACION_TRABAJO de Fase1.

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_ACTIVOS_AGRUPACION_TRABAJO' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_ACTIVOS_AGRUPACION_TRABAJO...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.V_ACTIVOS_AGRUPACION_TRABAJO';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_ACTIVOS_AGRUPACION_TRABAJO... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_ACTIVOS_AGRUPACION_TRABAJO' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_ACTIVOS_AGRUPACION_TRABAJO...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.V_ACTIVOS_AGRUPACION_TRABAJO';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_ACTIVOS_AGRUPACION_TRABAJO... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_ACTIVOS_AGRUPACION_TRABAJO...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.V_ACTIVOS_AGRUPACION_TRABAJO 
	AS

	SELECT 
		ACT.ACT_ID,
		ACT.ACT_NUM_ACTIVO,
	    ACT.ACT_NUM_ACTIVO_REM,
      PAC.PAC_CHECK_GESTIONAR AS PER_GESTION,
		TPA.DD_TPA_DESCRIPCION,
		SAC.DD_SAC_DESCRIPCION,
		CRA.DD_CRA_DESCRIPCION,
	    AGR.AGR_ID,
		AGR.AGA_ID,
		NVL2(ADN.ADN_VALOR_ADQUISICION,ADN.ADN_VALOR_ADQUISICION,NVL(ADJ.BIE_ADJ_IMPORTE_ADJUDICACION,0)) AS VAL_NETO_CONTABLE,
	    ( 	
			SELECT SUM(NVL(NVL(ADN.ADN_VALOR_ADQUISICION,ADJ.BIE_ADJ_IMPORTE_ADJUDICACION),0)) 
	      	FROM ACT_ACTIVO ACT2
        	LEFT JOIN ACT_ADN_ADJNOJUDICIAL ADN ON ADN.ACT_ID = ACT2.ACT_ID
        	LEFT JOIN ACT_AJD_ADJJUDICIAL AJD ON AJD.ACT_ID = ACT2.ACT_ID
        	LEFT JOIN BIE_ADJ_ADJUDICACION ADJ ON ADJ.BIE_ADJ_ID = AJD.BIE_ADJ_ID
        	INNER JOIN ACT_AGA_AGRUPACION_ACTIVO AGRACT ON ACT2.ACT_ID = AGRACT.ACT_ID
          where act2.borrado = 0
        	GROUP BY AGRACT.AGR_ID HAVING AGRACT.AGR_ID = AGR.AGR_ID
	    ) AS SUMA_AGRUP_NETA,
		AGR.AGA_PRINCIPAL,
		SCM.DD_SCM_DESCRIPCION AS SITUACION_COMERCIAL,
		SPS.SPS_OCUPADO,
		SPS.SPS_CON_TITULO,
		BDR.BIE_DREG_NUM_FINCA AS NUM_FINCA_REGISTRAL
	FROM ' || V_ESQUEMA || '.ACT_AGA_AGRUPACION_ACTIVO AGR,
	' || V_ESQUEMA || '.ACT_AGR_AGRUPACION AGRU,
    ' || V_ESQUEMA || '.ACT_ACTIVO ACT,
    ' || V_ESQUEMA || '.DD_TPA_TIPO_ACTIVO TPA,
	' || V_ESQUEMA || '.DD_SAC_SUBTIPO_ACTIVO SAC,
	' || V_ESQUEMA || '.DD_SCM_SITUACION_COMERCIAL SCM,
	' || V_ESQUEMA || '.DD_CRA_CARTERA CRA,
    ' || V_ESQUEMA || '.ACT_SPS_SIT_POSESORIA SPS,
	' || V_ESQUEMA || '.ACT_ADN_ADJNOJUDICIAL ADN,
  	' || V_ESQUEMA || '.ACT_AJD_ADJJUDICIAL AJD,
    ' || V_ESQUEMA || '.BIE_ADJ_ADJUDICACION ADJ,
	' || V_ESQUEMA || '.ACT_REG_INFO_REGISTRAL ARIR,
	' || V_ESQUEMA || '.BIE_DATOS_REGISTRALES BDR,
  ' ||V_ESQUEMA|| '.ACT_PAC_PERIMETRO_ACTIVO PAC
    WHERE AGRU.BORRADO = 0 AND AGR.BORRADO = 0 AND
    act.borrado = 0 and
    	AGR.AGR_ID = AGRU.AGR_ID AND
    	ACT.ACT_ID = AGR.ACT_ID AND
      ACT.ACT_ID = PAC.ACT_ID AND
    	TPA.DD_TPA_ID = ACT.DD_TPA_ID AND
		SAC.DD_SAC_ID (+) = ACT.DD_SAC_ID AND
		SCM.DD_SCM_ID (+) = ACT.DD_SCM_ID AND
  		SPS.ACT_ID = ACT.ACT_ID
		AND CRA.DD_CRA_ID = ACT.DD_CRA_ID 
  		AND	ADN.ACT_ID (+) = ACT.ACT_ID
  		AND AJD.ACT_ID (+) = ACT.ACT_ID 
  		AND ADJ.BIE_ADJ_ID (+) = AJD.BIE_ADJ_ID
		AND ACT.ACT_ID = ARIR.ACT_ID
		AND ARIR.BIE_DREG_ID = BDR.BIE_DREG_ID';  

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_ACTIVOS_AGRUPACION_TRABAJO...Creada OK');
  
EXCEPTION
   WHEN OTHERS
   THEN
      DBMS_OUTPUT.put_line ('ERROR: ' || TO_CHAR (SQLCODE));
      DBMS_OUTPUT.put_line (SQLERRM);
      ROLLBACK;
      RAISE;
END;
/
EXIT;