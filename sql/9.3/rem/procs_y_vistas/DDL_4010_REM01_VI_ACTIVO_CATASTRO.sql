--Para permitir la visualizaci칩n de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
-- 0.1
DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    ERR_NUM NUMBER; -- N?mero de errores
    ERR_MSG VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_MASTER VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_ACTIVO_CATASTRO' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_ACTIVO_CATASTRO...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.VI_ACTIVO_CATASTRO';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.VI_ACTIVO_CATASTRO... borrada OK'); 
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'VI_ACTIVO_CATASTRO' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_ACTIVO_CATASTRO...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.VI_ACTIVO_CATASTRO';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.VI_ACTIVO_CATASTRO... borrada OK');
  END IF;  
  
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_ACTIVO_CATASTRO...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.VI_ACTIVO_CATASTRO ("IDACTIVOCATASTRO", "IDACTIVO", "REFCATASTRAL", "CORRECTO", "VALORCATASTRALCONST", "VALORCATASTRALSUELO", "FECHAREVVALORCATASTRAL" , "FECHAALTACATASTRO", "CATASTROVIGENTE", "VALORCATASTRAL",
		"SUPERFICIEPARCELA", "SUPERFICIECONSTRUIDA", "ANYOCONSTRUCCION", "CODIGOPOSTAL", "TIPOVIA", "NOMBREVIA", "NUMEROVIA" , "PLANTA", "PUERTA", "ESCALERA", "PROVINCIA" , "MUNICIPIO", "LATITUD" , "LONGITUD", "ORIGENDATOSCATASTRALES", "CLASEUSOCATASTRAL", "TIPOMONEDA") AS 
	SELECT 
		    ACTCAT.CAT_ID AS IDACTIVOCATASTRO,
		    ACTCAT.ACT_ID AS IDACTIVO,
		    NVL(CAT.CAT_REF_CATASTRAL, ACTCAT.CAT_REF_CATASTRAL) AS REFCATASTRAL,
		    ACTCAT.CAT_CORRECTO AS CORRECTO,
		    ACTCAT.CAT_VALOR_CATASTRAL_CONST AS VALORCATASTRALCONST,
		    ACTCAT.CAT_VALOR_CATASTRAL_SUELO AS VALORCATASTRALSUELO,
		    ACTCAT.CAT_FECHA_REV_VALOR_CATASTRAL AS FECHAREVVALORCATASTRAL,
		    ACTCAT.FECHACREAR AS FECHAALTACATASTRO,
        ACTCAT.CAT_VIGENTE AS CATASTROVIGENTE,
        ACTCAT.CAT_VALOR_CATASTRAL AS VALORCATASTRAL,
		 
		    CAT.CAT_SUPERFICIE_PARCELA AS SUPERFICIEPARCELA,
		    CAT.CAT_SUPERFICIE_CONSTRUIDA AS SUPERFICIECONSTRUIDA,
		    CAT.CAT_ANYO_CONSTRUCCION AS ANYOCONSTRUCCION,
		    CAT.CAT_COD_POSTAL AS CODIGOPOSTAL,
		    TVI.DD_TVI_DESCRIPCION AS TIPOVIA,
		    CAT.CAT_DESCRIPCION_VIA AS NOMBREVIA,
		    CAT.CAT_NUM_VIA AS NUMEROVIA,
		    CAT.CAT_PLANTA AS  PLANTA,
		    CAT.CAT_PUERTA AS PUERTA,
		    CAT.CAT_ESCALERA AS ESCALERA,
		    PRV.DD_PRV_DESCRIPCION AS PROVINCIA,
		    LOC.DD_LOC_DESCRIPCION AS MUNICIPIO,
		    CAT.CAT_LATITUD AS LATITUD,
		    CAT.CAT_LONGITUD AS LONGITUD,
        ODC.DD_ODC_DESCRIPCION AS ORIGENDATOSCATASTRALES,
        CUC.DD_CUC_DESCRIPCION AS CLASEUSOCATASTRAL,
        MON.DD_MON_DESCRIPCION AS TIPOMONEDA
		
		FROM '|| V_ESQUEMA ||'.ACT_CAT_CATASTRO ACTCAT
		LEFT JOIN '|| V_ESQUEMA ||'.CAT_CATASTRO CAT ON ACTCAT.CAT_CATASTRO = CAT.CAT_ID AND CAT.BORRADO = 0
		LEFT JOIN '|| V_ESQUEMA_MASTER ||'.DD_PRV_PROVINCIA PRV ON CAT.DD_PRV_ID = PRV.DD_PRV_ID
		LEFT JOIN '|| V_ESQUEMA_MASTER ||'.DD_LOC_LOCALIDAD LOC ON CAT.DD_LOC_ID = LOC.DD_LOC_ID
		LEFT JOIN '|| V_ESQUEMA_MASTER ||'.DD_TVI_TIPO_VIA TVI ON CAT.DD_TVI_ID = TVI.DD_TVI_ID
    LEFT JOIN '|| V_ESQUEMA ||'.DD_ODC_ORIGEN_DATOS_CATASTRALES ODC ON ACTCAT.DD_ODC_ID = ODC.DD_ODC_ID
    LEFT JOIN '|| V_ESQUEMA ||'.DD_CUC_CLASE_USO_CATASTRAL CUC ON ACTCAT.DD_CUC_ID = CUC.DD_CUC_ID
    LEFT JOIN '|| V_ESQUEMA ||'.DD_MON_TIPO_MONEDA MON ON ACTCAT.DD_MON_ID = MON.DD_MON_ID
		WHERE ACTCAT.BORRADO = 0';

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.VI_PARTICIPACION_ELEMENTOS_LINEA_DETALLE...Creada OK');
  
  EXCEPTION
 
 
 
    -- SIEMPRE DEBE HABER UN OTHERS
    WHEN OTHERS THEN
        ERR_NUM := SQLCODE;
        ERR_MSG := SQLERRM;
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecuci칩n:'||TO_CHAR(ERR_NUM));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(ERR_MSG);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
