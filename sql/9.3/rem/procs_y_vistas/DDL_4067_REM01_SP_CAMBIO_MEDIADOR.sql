WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

CREATE OR REPLACE PROCEDURE #ESQUEMA#.CAMBIO_MEDIADOR
   (  ACT_NUM_ACTIVO IN NUMBER
	, PVE_COD_REM IN VARCHAR2
	, V_USUARIO IN VARCHAR2
	, PL_OUTPUT OUT VARCHAR2
   )
   
   AS

   V_SQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar        
   V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
   V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
   ERR_NUM NUMBER(25); -- Vble. auxiliar para registrar errores en el script.
   ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
   DESCRIPCION VARCHAR2(64 CHAR);
   V_TABLA VARCHAR2(25 CHAR):= 'ACT_ICO_INFO_COMERCIAL';
   V_COUNT NUMBER(16); -- Vble. para contar.
   V_COUNT_UPDATE NUMBER(16):= 0; -- Vble. para contar updates

BEGIN

EXECUTE IMMEDIATE 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR WHERE PVE_COD_REM = '||PVE_COD_REM INTO V_COUNT;

IF V_COUNT = 1 THEN

EXECUTE IMMEDIATE 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.ACT_ACTIVO WHERE ACT_NUM_ACTIVO = '||ACT_NUM_ACTIVO INTO V_COUNT;

	IF V_COUNT = 1 THEN
		V_SQL := 'UPDATE '||V_ESQUEMA||'.ACT_ICM_INF_COMER_HIST_MEDI
						SET 
							  ICM_FECHA_HASTA = SYSDATE
							, USUARIOMODIFICAR = '''||V_USUARIO||'''
							, FECHAMODIFICAR = SYSDATE
							WHERE
							ACT_ID = (SELECT ACT_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO WHERE ACT_NUM_ACTIVO = '||ACT_NUM_ACTIVO||')
							AND ICM_FECHA_HASTA IS NULL
							';
						
		EXECUTE IMMEDIATE V_SQL;

		V_SQL := 'INSERT INTO '||V_ESQUEMA||'.ACT_ICM_INF_COMER_HIST_MEDI (
								  ICM_ID
								, ACT_ID
								, ICO_MEDIADOR_ID
								, ICM_FECHA_DESDE
								, USUARIOCREAR
								, FECHACREAR
								) VALUES (
								  '||V_ESQUEMA||'.S_ACT_ICM_INF_COMER_HIST_MEDI.NEXTVAL
								, (SELECT ACT_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO WHERE ACT_NUM_ACTIVO = '||ACT_NUM_ACTIVO||')
								, (SELECT PVE_ID FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR WHERE PVE_COD_REM = '||PVE_COD_REM||')
								, SYSDATE
								, '''||V_USUARIO||'''
								, SYSDATE
								)';
				
		EXECUTE IMMEDIATE V_SQL;


		V_SQL := 'UPDATE '||V_ESQUEMA||'.'||V_TABLA||' ICO SET
				  ICO_MEDIADOR_ID = (SELECT PVE_ID FROM '||V_ESQUEMA||'.ACT_PVE_PROVEEDOR WHERE PVE_COD_REM = '||PVE_COD_REM||')
				, USUARIOMODIFICAR = '''||V_USUARIO||'''
				, FECHAMODIFICAR = SYSDATE
				WHERE ICO.ACT_ID IN (SELECT ACT_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT WHERE ACT.ACT_NUM_ACTIVO = '||ACT_NUM_ACTIVO||')
					';
		   
		EXECUTE IMMEDIATE V_SQL;
			
		IF SQL%ROWCOUNT > 0 THEN
			DBMS_OUTPUT.PUT_LINE('[INFO] Se ha puesto el mediador PVE_COD_REM = '||PVE_COD_REM||' al activo '||ACT_NUM_ACTIVO);
			V_COUNT_UPDATE := V_COUNT_UPDATE + 1;
		END IF;
	ELSE
		DBMS_OUTPUT.PUT_LINE('[ERROR] El activo con ACT_NUM_ACTIVO = '||ACT_NUM_ACTIVO||' no existe');
	END IF;

ELSE
	DBMS_OUTPUT.PUT_LINE('[ERROR] El mediador con PVE_COD_REM = '||PVE_COD_REM||' no existe');
END IF;

COMMIT;

EXCEPTION
   WHEN OTHERS THEN
      ERR_NUM := SQLCODE;
      ERR_MSG := SQLERRM;
      PL_OUTPUT := PL_OUTPUT || CHR(10) ||'    [ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM);
      PL_OUTPUT := PL_OUTPUT || CHR(10) ||'    '||ERR_MSG;
      ROLLBACK;
      RAISE;
END CAMBIO_MEDIADOR;
/
EXIT;
