--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 

DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    err_num NUMBER; -- Número de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_MASTER VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquemas
    V_TABLESPACE_IDX VARCHAR2(25 CHAR):= '#TABLESPACE_INDEX#'; -- Configuracion Tablespace de Indices
    V_TEXT_VISTA VARCHAR2(2400 CHAR) := 'VI_AVISOS_EXP_GENCAT'; -- Vble. auxiliar para almacenar el nombre de la tabla de ref.
    V_MSQL VARCHAR2(4000 CHAR); 
	V_COMMENT_TABLE VARCHAR2(500 CHAR):= 'Vista consultable.'; -- Vble. para los comentarios de las tablas
    
    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = V_TEXT_VISTA AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.'|| V_TEXT_VISTA ||'...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.'|| V_TEXT_VISTA ||'';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.'|| V_TEXT_VISTA ||'... borrada OK');
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = V_TEXT_VISTA AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.'|| V_TEXT_VISTA ||'...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.'|| V_TEXT_VISTA ||'';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.'|| V_TEXT_VISTA ||'... borrada OK');
  END IF;

  DBMS_OUTPUT.PUT_LINE('CREATING VIEW '|| V_ESQUEMA ||'.'|| V_TEXT_VISTA ||'...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.'|| V_TEXT_VISTA ||'
	AS
		WITH ANULADO AS (
			SELECT CMG.CMG_ID, ''Anulado por GENCAT'' DISCLAIMER
			FROM REM01.ACT_CMG_COMUNICACION_GENCAT CMG
			JOIN REM01.DD_ECG_ESTADO_COM_GENCAT ECG ON ECG.DD_ECG_ID = CMG.DD_ECG_ID AND ECG.DD_ECG_CODIGO = ''SANCIONADO''
			JOIN REM01.DD_SAN_SANCION SAN ON SAN.DD_SAN_ID = CMG.DD_SAN_ID AND SAN.DD_SAN_CODIGO = ''EJERCE''
		), 
		BLOQUEADO AS ( 
			SELECT CMG.CMG_ID, ''Bloqueado por GENCAT'' DISCLAIMER
			FROM REM01.ACT_CMG_COMUNICACION_GENCAT CMG
			JOIN REM01.DD_ECG_ESTADO_COM_GENCAT ECG ON ECG.DD_ECG_ID = CMG.DD_ECG_ID AND ECG.DD_ECG_CODIGO = ''COMUNICADO''
		), 
		PRE_BLOQUEADO AS (
			SELECT CMG.CMG_ID, ''Pre-Bloqueado por GENCAT'' DISCLAIMER
			FROM ACT_CMG_COMUNICACION_GENCAT CMG
			JOIN DD_ECG_ESTADO_COM_GENCAT ECG ON ECG.DD_ECG_ID = CMG.DD_ECG_ID AND ECG.DD_ECG_CODIGO = ''CREADO''
		)
		SELECT ECO_NUM_EXPEDIENTE, DISCLAIMER
		FROM (
			SELECT ECO.ECO_NUM_EXPEDIENTE, COALESCE(ANU.DISCLAIMER, BLO.DISCLAIMER, PBL.DISCLAIMER) DISCLAIMER, 
			ROW_NUMBER() OVER(PARTITION BY ECO.ECO_ID ORDER BY ANU.DISCLAIMER NULLS LAST, BLO.DISCLAIMER NULLS LAST, PBL.DISCLAIMER NULLS LAST) RN
			FROM REM01.ECO_EXPEDIENTE_COMERCIAL ECO
			JOIN REM01.ACT_OFG_OFERTA_GENCAT OFG ON OFG.OFR_ID = ECO.OFR_ID AND OFG.BORRADO = 0
			JOIN REM01.ACT_CMG_COMUNICACION_GENCAT CMG ON CMG.CMG_ID = OFG.CMG_ID
			JOIN REM01.ACT_ACTIVO ACT ON ACT.ACT_ID = CMG.ACT_ID
			JOIN REM01.DD_EEC_EST_EXP_COMERCIAL EEC ON EEC.DD_EEC_ID = ECO.DD_EEC_ID 
			AND EEC.DD_EEC_CODIGO NOT IN (''02'',''03'',''08'',''12'',''16'',''17'')
			LEFT JOIN ANULADO ANU ON ANU.CMG_ID = OFG.CMG_ID
			LEFT JOIN BLOQUEADO BLO ON BLO.CMG_ID = OFG.CMG_ID
			LEFT JOIN PRE_BLOQUEADO PBL ON PBL.CMG_ID = OFG.CMG_ID
		)
		WHERE RN = 1';


  	DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.'|| V_TEXT_VISTA ||'...Creada OK');

	  
END;
/

EXIT;