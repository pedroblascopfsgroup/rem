DECLARE
    seq_count number(3); -- Vble. para validar la existencia de las Secuencias.
    table_count number(3); -- Vble. para validar la existencia de las Tablas.
    v_column_count number(3); -- Vble. para validar la existencia de las Columnas.    
    v_constraint_count number(3); -- Vble. para validar la existencia de las Constraints.
    ERR_NUM NUMBER; -- N?mero de errores
    ERR_MSG VARCHAR2(2048); -- Mensaje de error
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_MASTER VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquemas
    V_MSQL VARCHAR2(4000 CHAR); 

    CUENTA NUMBER;
    
BEGIN

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='MATERIALIZED VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION...');
    EXECUTE IMMEDIATE 'DROP MATERIALIZED VIEW ' || V_ESQUEMA || '.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION';  
    DBMS_OUTPUT.PUT_LINE('DROP MATERIALIZED VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION... borrada OK'); 
  END IF;

  SELECT COUNT(*) INTO CUENTA FROM ALL_OBJECTS WHERE OBJECT_NAME = 'V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION' AND OWNER=V_ESQUEMA AND OBJECT_TYPE='VIEW';  
  IF CUENTA>0 THEN
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION...');
    EXECUTE IMMEDIATE 'DROP VIEW ' || V_ESQUEMA || '.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION';  
    DBMS_OUTPUT.PUT_LINE('DROP VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION... borrada OK');
  END IF;  
  
  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION...');
  EXECUTE IMMEDIATE 'CREATE VIEW ' || V_ESQUEMA || '.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION 
  AS
    SELECT CONCAT(TBJ.TBJ_ID, ACT.ACT_ID) AS ID, TBJ.TBJ_ID, TBJ.TBJ_NUM_TRABAJO, TBJ.TBJ_FECHA_SOLICITUD, 
    TTR.DD_TTR_CODIGO, TTR.DD_TTR_DESCRIPCION, STR.DD_STR_DESCRIPCION, EST.DD_EST_DESCRIPCION, 
		NVL2 (SOLIC.USU_NOMBRE, INITCAP (SOLIC.USU_NOMBRE) || NVL2 (SOLIC.USU_APELLIDO1, '' '' || INITCAP (SOLIC.USU_APELLIDO1), '''') ||
		NVL2 (SOLIC.USU_APELLIDO2, '' '' || INITCAP (SOLIC.USU_APELLIDO2), ''''), INITCAP (PVE2.PVE_NOMBRE)) AS SOLICITANTE,
		PVE.PVE_NOMBRE, ACT.ACT_NUM_ACTIVO, CRA.DD_CRA_DESCRIPCION, 
		SCR.DD_SCR_DESCRIPCION, PVE.PVE_ID FROM ' || V_ESQUEMA || '.ACT_TBJ_TRABAJO TBJ
		JOIN ' || V_ESQUEMA || '.ACT_TBJ ATBJ ON TBJ.TBJ_ID = ATBJ.TBJ_ID 
		JOIN ' || V_ESQUEMA || '.ACT_ACTIVO ACT ON ATBJ.ACT_ID = ACT.ACT_ID 
		JOIN ' || V_ESQUEMA || '.DD_EST_ESTADO_TRABAJO EST ON TBJ.DD_EST_ID = EST.DD_EST_ID 
		JOIN ' || V_ESQUEMA || '.DD_CRA_CARTERA CRA ON ACT.DD_CRA_ID = CRA.DD_CRA_ID
		JOIN ' || V_ESQUEMA || '.DD_TTR_TIPO_TRABAJO TTR ON TBJ.DD_TTR_ID = TTR.DD_TTR_ID AND TTR.DD_TTR_FILTRAR IS NULL
		JOIN ' || V_ESQUEMA || '.DD_STR_SUBTIPO_TRABAJO STR ON TBJ.DD_STR_ID = STR.DD_STR_ID 	
		JOIN ' || V_ESQUEMA || '.DD_SCR_SUBCARTERA SCR ON ACT.DD_SCR_ID = SCR.DD_SCR_ID 
		LEFT JOIN ' || V_ESQUEMA || '.ACT_PVC_PROVEEDOR_CONTACTO PVC ON TBJ.PVC_ID = PVC.PVC_ID 
		LEFT JOIN ' || V_ESQUEMA || '.ACT_PVE_PROVEEDOR PVE ON PVC.PVE_ID = PVE.PVE_ID 
		LEFT JOIN ' || V_ESQUEMA || '.ACT_PVE_PROVEEDOR PVE2 ON PVE2.PVE_ID = TBJ.MEDIADOR_ID
		LEFT JOIN ' || V_ESQUEMA_MASTER || '.USU_USUARIOS SOLIC ON SOLIC.USU_ID = TBJ.USU_ID
		WHERE TBJ.BORRADO = 0';

  DBMS_OUTPUT.PUT_LINE('CREATE VIEW '|| V_ESQUEMA ||'.V_BUSQUEDA_TRABAJOS_HISTORICO_PETICION...Creada OK');
  
  EXCEPTION
     
    -- Opcional: Excepciones particulares que se quieran tratar
    -- Como esta, por ejemplo:
    -- WHEN TABLE_EXISTS_EXCEPTION THEN
        -- DBMS_OUTPUT.PUT_LINE('Ya se ha realizado la copia en la tabla TMP_MOV_'||TODAY);
 
 
    -- SIEMPRE DEBE HABER UN OTHERS
    WHEN OTHERS THEN
        ERR_NUM := SQLCODE;
        ERR_MSG := SQLERRM;
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecuci贸n:'||TO_CHAR(ERR_NUM));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(ERR_MSG);
        ROLLBACK;
        RAISE;
END;
