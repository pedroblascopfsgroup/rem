WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar  
TABLE_COUNT NUMBER(1,0) := 0;
V_ESQUEMA_1 VARCHAR2(20 CHAR) := 'REM01';
V_ESQUEMA_2 VARCHAR2(20 CHAR) := 'REMMASTER'; --SE CREA UNA SEGUNDA VARIABLE DE ESQUEMA POR SI EN ALGÚN MOMENTO QUEREMOS CREAR LA TABLA EN UN ESQUEMA DIFERENTE AL DEL USUARIO QUE LA ACCEDE O VICEVERSA
V_ESQUEMA_3 VARCHAR2(20 CHAR) := 'REM_QUERY';
V_ESQUEMA_4 VARCHAR2(20 CHAR) := 'PFSREM';
V_TABLESPACE_IDX VARCHAR2(30 CHAR) := 'REM_IDX';
V_TABLA1 VARCHAR2(40 CHAR) := 'AUX_REMVIP_8046_1';
V_TABLA2 VARCHAR2(40 CHAR) := 'AUX_REMVIP_8046_2';

BEGIN

SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA1||'' AND OWNER= ''||V_ESQUEMA_1||'';
IF TABLE_COUNT > 0 THEN
    DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA1||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
    EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA1||'';
END IF;
EXECUTE IMMEDIATE 'CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA1||' AS 
   ( SELECT gpv_id, GPV_NUM_GASTO_HAYA NUM_GASTO,
DD_CRA_DESCRIPCION CARTERA,
PRO_NOMBRE PROPIETARIO,
PRO_DOCIDENTIF NIF_PROPIETARIO,
GDE_IMPORTE_TOTAL AS IMPORTE,
DD_EGA_DESCRIPCION ESTADO_GASTO,
SUMA AS PORCENTAJE_TOTAL,
(GDE_IMPORTE_TOTAL * SUMA)/100 AS IMPORTE_PORC_ERRONEO,
USUARIOCREAR,
FECHACREAR, 
USUARIOMODIFICAR,
GGE_FECHA_ENVIO_PRPTRIO AS FECHA_ENVIO,
DD_EAH_DESCRIPCION ESTADO_HAYA
FROM (
SELECT gpv.gpv_id, GPV.GPV_NUM_GASTO_HAYA,
EGA.DD_EGA_DESCRIPCION,
SUM(GA.GPV_PARTICIPACION_GASTO) AS SUMA,
CRA.DD_CRA_DESCRIPCION,
GPV.USUARIOMODIFICAR,
GPV.USUARIOCREAR, 
GPV.FECHACREAR,
GGE.GGE_FECHA_ENVIO_PRPTRIO,
GPV.FECHAMODIFICAR,
EAH.DD_EAH_DESCRIPCION,
PRO.PRO_DOCIDENTIF,PRO.PRO_NOMBRE,
GDE.GDE_IMPORTE_TOTAL
FROM REM01.GPV_GASTOS_PROVEEDOR GPV
INNER JOIN REM01.GPV_ACT GA ON GA.GPV_ID = GPV.GPV_ID
INNER JOIN REM01.ACT_ACTIVO ACT ON ACT.ACT_ID = GA.ACT_ID
INNER JOIN REM01.GGE_GASTOS_GESTION GGE ON GPV.GPV_ID = GGE.GPV_ID
INNER JOIN REM01.DD_EAH_ESTADOS_AUTORIZ_HAYA EAH ON GGE.DD_EAH_ID = EAH.DD_EAH_ID
INNER JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
INNER JOIN REM01.DD_EGA_ESTADOS_GASTO EGA ON EGA.DD_EGA_ID = GPV.DD_EGA_ID
INNER JOIN REM01.ACT_PRO_PROPIETARIO PRO ON GPV.PRO_ID = PRO.PRO_ID
INNER JOIN REM01.GDE_GASTOS_DETALLE_ECONOMICO GDE ON GDE.GPV_ID = GPV.GPV_ID
WHERE GPV.BORRADO = 0
AND ACT.BORRADO = 0
GROUP BY gpv.gpv_id, 
GPV.GPV_NUM_GASTO_HAYA,
EGA.DD_EGA_DESCRIPCION,
CRA.DD_CRA_DESCRIPCION,
GPV.USUARIOMODIFICAR,
GPV.USUARIOCREAR, 
GPV.FECHACREAR,
GGE.GGE_FECHA_ENVIO_PRPTRIO,
GPV.FECHAMODIFICAR,
EAH.DD_EAH_DESCRIPCION,
PRO.PRO_DOCIDENTIF,PRO.PRO_NOMBRE,
GDE.GDE_IMPORTE_TOTAL
)
WHERE SUMA > 100
  ) ';

DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA1||' CREADA');  

SELECT COUNT(1) INTO TABLE_COUNT FROM ALL_TABLES WHERE TABLE_NAME = ''||V_TABLA2||'' AND OWNER= ''||V_ESQUEMA_1||'';
IF TABLE_COUNT > 0 THEN
    DBMS_OUTPUT.PUT_LINE('[INFO] TABLA '||V_ESQUEMA_1||'.'||V_TABLA2||' YA EXISTENTE. SE PROCEDE A BORRAR Y CREAR DE NUEVO.');
    EXECUTE IMMEDIATE 'DROP TABLE '||V_ESQUEMA_1||'.'||V_TABLA2||'';
END IF;
EXECUTE IMMEDIATE 'CREATE TABLE '||V_ESQUEMA_1||'.'||V_TABLA2||' AS 
  ( SELECT gpv_id, GPV_NUM_GASTO_HAYA NUM_GASTO,
DD_CRA_DESCRIPCION CARTERA,
PRO_NOMBRE PROPIETARIO,
PRO_DOCIDENTIF NIF_PROPIETARIO,
GDE_IMPORTE_TOTAL AS IMPORTE,
DD_EGA_DESCRIPCION ESTADO_GASTO,
SUMA AS PORCENTAJE_TOTAL,
(GDE_IMPORTE_TOTAL * SUMA)/100 AS IMPORTE_PORC_ERRONEO,
USUARIOCREAR,
FECHACREAR, 
USUARIOMODIFICAR,
GGE_FECHA_ENVIO_PRPTRIO AS FECHA_ENVIO,
DD_EAH_DESCRIPCION ESTADO_HAYA
FROM (
SELECT gpv.gpv_id, 
GPV.GPV_NUM_GASTO_HAYA,
EGA.DD_EGA_DESCRIPCION,
SUM(GA.GPV_PARTICIPACION_GASTO) AS SUMA,
CRA.DD_CRA_DESCRIPCION,
GPV.USUARIOMODIFICAR,
GPV.USUARIOCREAR, 
GPV.FECHACREAR,
GGE.GGE_FECHA_ENVIO_PRPTRIO,
GPV.FECHAMODIFICAR,
EAH.DD_EAH_DESCRIPCION,
PRO.PRO_DOCIDENTIF,PRO.PRO_NOMBRE,
GDE.GDE_IMPORTE_TOTAL
FROM REM01.GPV_GASTOS_PROVEEDOR GPV
INNER JOIN REM01.GPV_ACT GA ON GA.GPV_ID = GPV.GPV_ID
INNER JOIN REM01.ACT_ACTIVO ACT ON ACT.ACT_ID = GA.ACT_ID
INNER JOIN REM01.GGE_GASTOS_GESTION GGE ON GPV.GPV_ID = GGE.GPV_ID
INNER JOIN REM01.DD_EAH_ESTADOS_AUTORIZ_HAYA EAH ON GGE.DD_EAH_ID = EAH.DD_EAH_ID
INNER JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
INNER JOIN REM01.DD_EGA_ESTADOS_GASTO EGA ON EGA.DD_EGA_ID = GPV.DD_EGA_ID
INNER JOIN REM01.ACT_PRO_PROPIETARIO PRO ON GPV.PRO_ID = PRO.PRO_ID
INNER JOIN REM01.GDE_GASTOS_DETALLE_ECONOMICO GDE ON GDE.GPV_ID = GPV.GPV_ID
WHERE GPV.BORRADO = 0
AND ACT.BORRADO = 0 
and cra.dd_cra_codigo not in (''02'',''08'')
GROUP BY gpv.gpv_id,
GPV.GPV_NUM_GASTO_HAYA,
EGA.DD_EGA_DESCRIPCION,
--SUM(GA.GPV_PARTICIPACION_GASTO) AS SUMA,
CRA.DD_CRA_DESCRIPCION,
GPV.USUARIOMODIFICAR,
GPV.USUARIOCREAR, 
GPV.FECHACREAR,
GGE.GGE_FECHA_ENVIO_PRPTRIO,
GPV.FECHAMODIFICAR,
EAH.DD_EAH_DESCRIPCION,
PRO.PRO_DOCIDENTIF,PRO.PRO_NOMBRE,
GDE.GDE_IMPORTE_TOTAL
)
WHERE SUMA > 101 
  ) ';

DBMS_OUTPUT.PUT_LINE('[INFO] '||V_ESQUEMA_1||'.'||V_TABLA2||' CREADA');  


IF V_ESQUEMA_2 != V_ESQUEMA_1 THEN

	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA1||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA1||' OTORGADOS A '||V_ESQUEMA_2||''); 
	
	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA2||'" TO "'||V_ESQUEMA_2||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA2||' OTORGADOS A '||V_ESQUEMA_2||''); 

END IF;


IF V_ESQUEMA_4 != V_ESQUEMA_1 THEN
	
	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA1||'" TO "'||V_ESQUEMA_4||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA1||' OTORGADOS A '||V_ESQUEMA_4||''); 
	
	EXECUTE IMMEDIATE 'GRANT ALL ON "'||V_ESQUEMA_1||'"."'||V_TABLA2||'" TO "'||V_ESQUEMA_4||'" WITH GRANT OPTION';
	DBMS_OUTPUT.PUT_LINE('[INFO] PERMISOS SOBRE LA TABLA '||V_ESQUEMA_1||'.'||V_TABLA2||' OTORGADOS A '||V_ESQUEMA_4||''); 
	

END IF;

	COMMIT;

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/

EXIT;
