WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

	V_TABLA_TMP VARCHAR2(100 CHAR) := 'TMP_FECHA_INGRESO_CHEQUE_CAJAMAR'; -- Variable para tabla de salida para el borrado
	V_TABLA VARCHAr(100 char) := 'ECO_EXPEDIENTE_COMERCIAL';
	V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01';-- '#ESQUEMA#'; -- Configuracion Esquema
	V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER';-- '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	ERR_NUM NUMBER;-- Numero de errores
	ERR_MSG VARCHAR2(2048);-- Mensaje de error
	V_SQL VARCHAR2(4000 CHAR);
	PL_OUTPUT VARCHAR2(32000 CHAR);
	V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-6683';
	

BEGIN
	
	DBMS_OUTPUT.PUT_LINE('[INICIO]'||CHR(10));
	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.'||V_TABLA||' ECO
		USING 
		(select ECO_ID,PRINEX_FECHA_CONTABLE_VENTA, RN  
			from 
				(SELECT ECO1.ECO_ID ECO_ID ,PRINEX_FECHA_CONTABLE_VENTA
				,ROW_NUMBER() OVER(PARTITION BY ECO1.ECO_ID ORDER BY PRINEX_FECHA_CONTABLE_VENTA DESC) RN
				FROM rem01.TMP_FECHA_INGRESO_CHEQUE_CAJAMAR TMP
				JOIN REM01.OFR_OFERTAS OFR ON OFR.OFR_NUM_OFERTA = TMP.NUM_OFERTA_REM and ofr.borrado = 0
				JOIN REM01.ECO_EXPEDIENTE_COMERCIAL ECO1 ON ECO1.OFR_ID = OFR.OFR_ID and eco1.borrado = 0
				group by ECO1.ECO_ID ,PRINEX_FECHA_CONTABLE_VENTA)
			where RN = 1
		)AUX
    	ON (AUX.ECO_ID = ECO.ECO_ID )
    	WHEN MATCHED THEN UPDATE 
			SET
            ECO.ECO_FECHA_CONT_PROPIETARIO = TO_DATE(AUX.PRINEX_FECHA_CONTABLE_VENTA, ''dd/mm/yyyy''),
			ECO.USUARIOMODIFICAR = '''||V_USUARIO||''',
			ECO.FECHAMODIFICAR = SYSDATE
			';
		--DBMS_OUTPUT.PUT_LINE(V_SQL);

		EXECUTE IMMEDIATE V_SQL;
		DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualzado en total '||SQL%ROWCOUNT||' registros en la tabla '||V_ESQUEMA||'.'||V_TABLA_TMP||'');

	COMMIT;

	PL_OUTPUT := PL_OUTPUT || '[FIN]'||CHR(10);
	DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);

EXCEPTION
    WHEN OTHERS THEN
      PL_OUTPUT := PL_OUTPUT ||'[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(SQLCODE)||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||'-----------------------------------------------------------'||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||SQLERRM||CHR(10);
      PL_OUTPUT := PL_OUTPUT ||V_SQL||CHR(10);
      DBMS_OUTPUT.PUT_LINE(PL_OUTPUT);
      ROLLBACK;
      RAISE;
END;
/
EXIT;
