WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;
DECLARE
    V_MSQL VARCHAR2(4000 CHAR);
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.
    err_num NUMBER; -- Numero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_USR VARCHAR2(30 CHAR) := 'REMVIP-6688'; -- USUARIOCREAR/USUARIOMODIFICAR.

BEGIN
	
	  DBMS_OUTPUT.PUT_LINE('[INICIO] ');
  
    	V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST T1 
		    USING (SELECT GEE.GEH_ID FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEE
			INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAC ON GEE.GEH_ID = GAC.GEH_ID 
			INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.DD_CRA_ID = 2
			WHERE GEE.DD_TGE_ID = 362 AND GEE.GEH_FECHA_HASTA IS NULL AND GEE.USUARIOCREAR = ''ALT_BANKIA'' AND GAC.ACT_ID IN 
				(SELECT GAC.ACT_ID FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEE
				INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAC ON GAC.GEH_ID = GEE.GEH_ID
				WHERE GEE.USU_ID = 87960 AND TRUNC(GEE.FECHACREAR) = TO_DATE(''13/03/20'',''DD/MM/YY'')
				)
				  ) T2
		    ON (T1.GEH_ID = T2.GEH_ID)
		  WHEN MATCHED THEN UPDATE SET 
			       T1.USUARIOBORRAR = ''REMVIP-6688_GEH'',
			       T1.FECHABORRAR = SYSDATE,
			       T1.BORRADO = 1		
         ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de GEH_GESTOR_ENTIDAD_HIST.');

	V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD T1 
		    USING (SELECT GEE.GEE_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
			INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GEE.GEE_ID = GAC.GEE_ID 
			INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.DD_CRA_ID = 2
			WHERE GEE.DD_TGE_ID = 362 
			AND GEE.USUARIOCREAR = ''ALT_BANKIA'' 
			AND TRUNC(GEE.FECHACREAR) = TO_DATE(''13/03/20'',''dd/mm/yy'')
			AND GAC.ACT_ID IN (
				SELECT GAC.ACT_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
				INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
				WHERE GEE.USUARIOBORRAR = ''HREOS-9179'' AND GEE.BORRADO = 1)
				  ) T2
		    ON (T1.GEE_ID = T2.GEE_ID)
		  WHEN MATCHED THEN UPDATE SET 
			       T1.USUARIOBORRAR = ''REMVIP-6688_GEE1'',
			       T1.FECHABORRAR = SYSDATE,
			       T1.BORRADO = 1		
         ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de GEE_GESTOR_ENTIDAD.');

	V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD T1 
		    USING (SELECT GEE.GEE_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
			INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GEE.GEE_ID = GAC.GEE_ID 
			INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.DD_CRA_ID = 2
			WHERE GEE.DD_TGE_ID = 362 AND GEE.USUARIOBORRAR = ''HREOS-9179'' AND GAC.ACT_ID IN (
				SELECT GAC.ACT_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
				INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
				WHERE GEE.USUARIOBORRAR = ''HREOS-9179'' AND GEE.BORRADO = 1)
				  ) T2
		    ON (T1.GEE_ID = T2.GEE_ID)
		  WHEN MATCHED THEN UPDATE SET 
			       T1.USUARIOMODIFICAR = ''REMVIP-6688_GEE2'',
			       T1.FECHAMODIFICAR = SYSDATE,
			       T1.BORRADO = 0		
         ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de GEE_GESTOR_ENTIDAD.');

	DBMS_OUTPUT.PUT_LINE('[FIN] ');

	COMMIT;

EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;

