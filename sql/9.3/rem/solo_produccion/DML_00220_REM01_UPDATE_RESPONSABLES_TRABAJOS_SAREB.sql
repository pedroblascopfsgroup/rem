WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;
DECLARE
    V_MSQL VARCHAR2(4000 CHAR);
    V_SQL VARCHAR2(4000 CHAR);
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.
    err_num NUMBER; -- Numero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_USR VARCHAR2(30 CHAR) := 'REMVIP-6688'; -- USUARIOCREAR/USUARIOMODIFICAR.
    V_COUNT_UPDATE_1 NUMBER(16):= 0; -- Vble. para contar updates
    TBJ_ID NUMBER(16);
    USU_ID NUMBER(16);
    V_NUM_FILAS NUMBER(16); -- Vble. para validar la existencia de un registro.

TYPE T_JBV IS TABLE OF VARCHAR2(32000);
    TYPE T_ARRAY_JBV IS TABLE OF T_JBV; 
	
	--TBJ_ID, USU_ID

	V_JBV T_ARRAY_JBV := T_ARRAY_JBV(
		T_JBV(462221,	30038),
		T_JBV(401937,	30043),
		T_JBV(419321,	30043),
		T_JBV(463290,	29751),
		T_JBV(455474,	29751),
		T_JBV(448956,	29476),
		T_JBV(450388,	30038),
		T_JBV(451732,	30038),
		T_JBV(451762,	29476),
		T_JBV(464752,	29751),
		T_JBV(435114,	29476),
		T_JBV(460677,	29476),
		T_JBV(464614,	30043),
		T_JBV(450637,	30038),
		T_JBV(468649,	30043)

		); 
	V_TMP_JBV T_JBV;

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INICIO] ACTUALIZACION RESPONSABLES TRABAJOS');

	FOR I IN V_JBV.FIRST .. V_JBV.LAST
	
	LOOP
 
	V_TMP_JBV := V_JBV(I);
	
  	TBJ_ID := TRIM(V_TMP_JBV(1));
  	
  	USU_ID := TRIM(V_TMP_JBV(2));

	--COMPROBAMOS SI EXISTE TRABAJO

	V_SQL := 'SELECT COUNT(*) FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO WHERE TBJ_ID = '||TBJ_ID;
	
	EXECUTE IMMEDIATE V_SQL INTO V_NUM_FILAS;
	
	IF V_NUM_FILAS > 0 THEN 

		V_MSQL := 'UPDATE '||V_ESQUEMA||'.ACT_TBJ_TRABAJO 
					   SET TBJ_RESPONSABLE_TRABAJO = '||USU_ID||',
					   USUARIOMODIFICAR = '''||V_USR||''',
					   FECHAMODIFICAR = SYSDATE 
					   WHERE TBJ_ID = '||TBJ_ID||'';
	

		EXECUTE IMMEDIATE V_MSQL;
    
		DBMS_OUTPUT.PUT_LINE('[INFO] REGISTRO CON TBJ_ID: '||TBJ_ID||' ACTUALIZADO');
	
		V_COUNT_UPDATE_1 := V_COUNT_UPDATE_1 + 1;
	
	ELSE
	
		DBMS_OUTPUT.PUT_LINE('[INFO] REGISTRO NO EXISTE');
	
	END IF;

	END LOOP;

	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||V_COUNT_UPDATE_1||' registros en la tabla de ACT_TBJ_TRABAJO.');

	DBMS_OUTPUT.PUT_LINE('[INICIO] ASIGNAMOS RESPONSABLES TRABAJOS');
  
    	V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.ACT_TBJ_TRABAJO T1 
		    USING (SELECT DISTINCT TBJ.TBJ_ID, USU2.USU_ID
			FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO TBJ
			INNER JOIN '||V_ESQUEMA||'.ACT_TBJ ACTTBJ ON ACTTBJ.TBJ_ID = TBJ.TBJ_ID
			INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = ACTTBJ.ACT_ID
			INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.ACT_ID = ACT.ACT_ID 
			INNER JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GAC.GEE_ID AND GEE.DD_TGE_ID = 362 
			INNER JOIN REMMASTER.USU_USUARIOS USU2 ON USU2.USU_ID = GEE.USU_ID AND GEE.USU_ID <> 87960
			WHERE TBJ.TBJ_RESPONSABLE_TRABAJO = (SELECT USU_ID FROM REMMASTER.USU_USUARIOS WHERE USU_USERNAME = ''grupgact'')
			AND ACT.DD_CRA_ID = 2 AND TBJ.BORRADO = 0 AND TBJ_GESTOR_ACTIVO_RESPONSABLE IS NULL
			AND TBJ.DD_TTR_ID <> 23 AND GEE.BORRADO = 0 
				  ) T2
		    ON (T1.TBJ_ID = T2.TBJ_ID)
		  WHEN MATCHED THEN
		    UPDATE SET T1.USUARIOMODIFICAR = ''REMVIP-6688'',
			       T1.FECHAMODIFICAR = SYSDATE,
			       T1.TBJ_RESPONSABLE_TRABAJO = T2.USU_ID 	 		
         ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de ACT_TBJ_TRABAJO.');

	V_MSQL := '
		MERGE INTO '||V_ESQUEMA||'.ACT_TBJ_TRABAJO T1 
		    USING (SELECT DISTINCT TBJ.TBJ_ID, TBJ.TBJ_GESTOR_ACTIVO_RESPONSABLE
			FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO TBJ
			INNER JOIN '||V_ESQUEMA||'.ACT_TBJ ACTTBJ ON ACTTBJ.TBJ_ID = TBJ.TBJ_ID
			INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = ACTTBJ.ACT_ID
			INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.ACT_ID = ACT.ACT_ID 
			INNER JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GAC.GEE_ID AND GEE.DD_TGE_ID = 362 
			INNER JOIN REMMASTER.USU_USUARIOS USU2 ON USU2.USU_ID = GEE.USU_ID AND GEE.USU_ID <> 87960
			WHERE TBJ.TBJ_RESPONSABLE_TRABAJO = (SELECT USU_ID FROM REMMASTER.USU_USUARIOS WHERE USU_USERNAME = ''grupgact'')
			AND ACT.DD_CRA_ID = 2 AND TBJ.BORRADO = 0 AND TBJ_GESTOR_ACTIVO_RESPONSABLE IS NOT NULL
			AND TBJ.DD_TTR_ID <> 23 AND GEE.BORRADO = 0 
				  ) T2
		    ON (T1.TBJ_ID = T2.TBJ_ID)
		  WHEN MATCHED THEN
		    UPDATE SET T1.USUARIOMODIFICAR = ''REMVIP-6688'',
			       T1.FECHAMODIFICAR = SYSDATE,
			       T1.TBJ_RESPONSABLE_TRABAJO = T2.TBJ_GESTOR_ACTIVO_RESPONSABLE 	 		
         ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de ACT_TBJ_TRABAJO.');

	DBMS_OUTPUT.PUT_LINE('[FIN] ');

	COMMIT;

EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;

/

EXIT;
