WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_SQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_USUARIO VARCHAR2(25 CHAR):= 'REMVIP-6922'; -- Incidencia Link
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

BEGIN
    
    DBMS_OUTPUT.put_line('[INICIO]');

    V_SQL := 'MERGE INTO '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC USING(
		SELECT TAR.TAR_ID
		FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO TBJ
		INNER JOIN '||V_ESQUEMA||'.ACT_TRA_TRAMITE TRA ON TRA.TBJ_ID = TBJ.TBJ_ID
		INNER JOIN '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC ON TAC.TRA_ID = TRA.TRA_ID
		INNER JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.TAR_ID = TAC.TAR_ID
		WHERE TBJ.DD_EST_ID IN ((SELECT DD_EST_ID FROM '||V_ESQUEMA||'.DD_EST_ESTADO_TRABAJO WHERE DD_EST_CODIGO = ''02''),
		(SELECT DD_EST_ID FROM '||V_ESQUEMA||'.DD_EST_ESTADO_TRABAJO WHERE DD_EST_CODIGO = ''03''))
		AND TBJ.USUARIOCREAR = ''MIG_DIVARIAN'' AND TAR.TAR_TAREA_FINALIZADA = 0
		) AUX
		ON (TAC.TAR_ID = AUX.TAR_ID)
		WHEN MATCHED THEN UPDATE SET
		TAC.BORRADO = 1,
		TAC.USUARIOBORRAR = ''MIG_DIVARIAN'',
		TAC.FECHABORRAR = SYSDATE';
    EXECUTE IMMEDIATE V_SQL;
    
    DBMS_OUTPUT.put_line('[INFO] Se han borrado '||SQL%ROWCOUNT||' tareas de trabajos de Divarian.');

    V_SQL := 'MERGE INTO '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR USING(
		SELECT TAR.TAR_ID
		FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO TBJ
		INNER JOIN '||V_ESQUEMA||'.ACT_TRA_TRAMITE TRA ON TRA.TBJ_ID = TBJ.TBJ_ID
		INNER JOIN '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC ON TAC.TRA_ID = TRA.TRA_ID
		INNER JOIN '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR ON TAR.TAR_ID = TAC.TAR_ID
		WHERE TBJ.DD_EST_ID IN ((SELECT DD_EST_ID FROM '||V_ESQUEMA||'.DD_EST_ESTADO_TRABAJO WHERE DD_EST_CODIGO = ''02''),
		(SELECT DD_EST_ID FROM '||V_ESQUEMA||'.DD_EST_ESTADO_TRABAJO WHERE DD_EST_CODIGO = ''03''))
		AND TBJ.USUARIOCREAR = ''MIG_DIVARIAN'' AND TAR.TAR_TAREA_FINALIZADA = 0
		) AUX
		ON (TAR.TAR_ID = AUX.TAR_ID)
		WHEN MATCHED THEN UPDATE SET
		TAR.BORRADO = 1,
		TAR.TAR_FECHA_FIN = SYSDATE,
		TAR.TAR_TAREA_FINALIZADA = 1,
		TAR.USUARIOBORRAR = ''REMVIP-6922'',
		TAR.FECHABORRAR = SYSDATE';
    EXECUTE IMMEDIATE V_SQL;
    
    DBMS_OUTPUT.put_line('[INFO] Se han borrado '||SQL%ROWCOUNT||' tareas de trabajos de Divarian.');

COMMIT;
    
    DBMS_OUTPUT.put_line('[FIN]');
 
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          DBMS_OUTPUT.put_line(V_SQL);
          ROLLBACK;
          RAISE;   
END;
/
EXIT;
