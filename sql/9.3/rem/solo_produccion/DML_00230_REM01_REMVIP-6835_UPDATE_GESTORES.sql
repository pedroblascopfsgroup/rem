--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
	V_MSQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
 	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
 	V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_USU VARCHAR2(30 CHAR):= 'REMVIP-6835';
	
BEGIN
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');
		
	--INSERTAMOS TANTOS REGISTROS COMO ACTIVOS EN LA GEE
	V_MSQL := '
	INSERT INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD
		(gee_id,
		usu_id,
		dd_tge_id, 
		USUARIOCREAR, 
		FECHACREAR,
		USUARIOMODIFICAR,
		BORRADO)
	SELECT  '||V_ESQUEMA||'.S_GEE_GESTOR_ENTIDAD.nextval,
		(SELECT USU_ID FROM '||V_ESQUEMA_M||'.usu_usuarios WHERE USU_USERNAME = ''mgutierrezmo''),
		(SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = ''SFORM''),
		'''||V_USU||''',
		sysdate,
		TO_CHAR(ACT.ACT_ID),
	 	0
	FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
		INNER JOIN '||V_ESQUEMA||'.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID
		INNER JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID =  ACT.DD_SCR_ID 
	WHERE CRA.DD_CRA_CODIGO IN (''07'')
		AND SCR.DD_SCR_CODIGO IN (''138'')';
	
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS EN GEE:  '|| SQL%ROWCOUNT ||' REGISTROS');
	
	--INSERTAMOS EN LA GAC
	V_MSQL := '
		INSERT INTO '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC
		(GEE_ID, ACT_ID)
		SELECT UNIQUE GEE.GEE_ID, 
		TO_NUMBER(GEE.USUARIOMODIFICAR)
		FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
		WHERE GEE.USUARIOCREAR =	'''||V_USU||'''';		
		
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS EN GAC:  '|| SQL%ROWCOUNT ||' REGISTROS ');
		
	V_MSQL:='
		UPDATE '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD
		SET USUARIOMODIFICAR = NULL
		WHERE USUARIOCREAR = '''||V_USU||'''';
		
	EXECUTE IMMEDIATE V_MSQL;	
		
		--INSERTAMOS EN LA GEH
	V_MSQL := '
		INSERT INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST
		(GEH_ID,
		USU_ID,
		DD_TGE_ID, 
		GEH_FECHA_DESDE,
		USUARIOCREAR, 
		FECHACREAR, 
		USUARIOMODIFICAR,
		BORRADO)
		SELECT  '||V_ESQUEMA||'.S_GEH_GESTOR_ENTIDAD_HIST.nextval,
		  	(SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = ''mgutierrezmo''),
		  	(SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = ''SFORM''),
			 SYSDATE,
			'''||V_USU||''',
		  	SYSDATE,
		  	TO_CHAR(ACT.ACT_ID),
		  	0
		FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
			INNER JOIN '||V_ESQUEMA||'.DD_CRA_CARTERA CRA ON ACT.DD_CRA_ID = CRA.DD_CRA_ID
			INNER JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = ACT.DD_SCR_ID
		WHERE CRA.DD_CRA_CODIGO IN (''07'')
			AND SCR.DD_SCR_CODIGO IN (''138'')';
	
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS EN GEH:  '|| SQL%ROWCOUNT ||' REGISTROS ');
		
		--INSERTAMOS EN LA GAH
	V_MSQL := '
		INSERT INTO '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH
		(GEH_ID, ACT_ID) 
		SELECT UNIQUE GEH.GEH_ID, 
		TO_NUMBER(GEH.USUARIOMODIFICAR )
		FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH		 
		WHERE GEH.USUARIOCREAR = '''||V_USU||'''';
	
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS EN GAH:  '|| SQL%ROWCOUNT ||' REGISTROS ');
		
	V_MSQL:='
		UPDATE '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST 
		SET USUARIOMODIFICAR = NULL 
		WHERE USUARIOCREAR = '''||V_USU||'''';
	
	EXECUTE IMMEDIATE V_MSQL;
	
	
	COMMIT;
	DBMS_OUTPUT.PUT_LINE('[FIN] ');
	
	EXCEPTION
	
	    WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------');
		DBMS_OUTPUT.PUT_LINE(SQLERRM);
		DBMS_OUTPUT.PUT_LINE(V_MSQL);
		ROLLBACK;
		RAISE;
END;
/
EXIT;
