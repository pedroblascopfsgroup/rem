WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_SQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_USUARIO VARCHAR2(25 CHAR):= 'REMVIP-6921'; -- Incidencia Link
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

BEGIN
    
    DBMS_OUTPUT.put_line('[INICIO] Cuadrar checks APU con el estado de publicación');

    --PUBLICADO VENTA

	V_SQL := 'UPDATE REM01.ACT_APU_ACTIVO_PUBLICACION T1 
				SET APU_CHECK_PUBLICAR_V = 1
				    ,USUARIOMODIFICAR = '''||V_USUARIO||'''
				    ,FECHAMODIFICAR = SYSDATE
				WHERE EXISTS (
				    SELECT ACT.ACT_ID 
				    FROM REM01.ACT_ACTIVO ACT
				    JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = ''07''
				    JOIN REM01.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = ACT.DD_SCR_ID AND SCR.DD_SCR_CODIGO IN (''151'', ''152'')
				    JOIN REM01.ACT_APU_ACTIVO_PUBLICACION APU ON APU.ACT_ID = ACT.ACT_ID
				    WHERE APU.DD_EPV_ID = 3 AND APU.APU_CHECK_PUBLICAR_V = 0
				    AND T1.ACT_ID = ACT.ACT_ID
				)';
	EXECUTE IMMEDIATE V_SQL;
    DBMS_OUTPUT.put_line('	[INFO] Activos Publicados Venta actualizados: '||SQL%ROWCOUNT||'.');

	--OCULTO VENTA

	V_SQL := 'UPDATE REM01.ACT_APU_ACTIVO_PUBLICACION T1 
				SET APU_CHECK_PUBLICAR_V = 1
				    ,APU_CHECK_OCULTAR_V = 1
				    ,USUARIOMODIFICAR = '''||V_USUARIO||'''
				    ,FECHAMODIFICAR = SYSDATE
				WHERE EXISTS (
				    SELECT ACT.ACT_ID 
				    FROM REM01.ACT_ACTIVO ACT
				    JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = ''07''
				    JOIN REM01.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = ACT.DD_SCR_ID AND SCR.DD_SCR_CODIGO IN (''151'', ''152'')
				    JOIN REM01.ACT_APU_ACTIVO_PUBLICACION APU ON APU.ACT_ID = ACT.ACT_ID
				    WHERE APU.DD_EPV_ID = 4 AND (APU.APU_CHECK_PUBLICAR_V = 0 OR APU.APU_CHECK_OCULTAR_V = 0)
				    AND T1.ACT_ID = ACT.ACT_ID
				)';
	EXECUTE IMMEDIATE V_SQL;
    DBMS_OUTPUT.put_line('	[INFO] Activos Ocultos Venta actualizados: '||SQL%ROWCOUNT||'.');

	--PUBLICADO ALQUILER

	V_SQL := 'UPDATE REM01.ACT_APU_ACTIVO_PUBLICACION T1 
				SET APU_CHECK_PUBLICAR_A = 1
				    ,USUARIOMODIFICAR = '''||V_USUARIO||'''
				    ,FECHAMODIFICAR = SYSDATE
				WHERE EXISTS (
				    SELECT ACT.ACT_ID 
				    FROM REM01.ACT_ACTIVO ACT
				    JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = ''07''
				    JOIN REM01.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = ACT.DD_SCR_ID AND SCR.DD_SCR_CODIGO IN (''151'', ''152'')
				    JOIN REM01.ACT_APU_ACTIVO_PUBLICACION APU ON APU.ACT_ID = ACT.ACT_ID
				    WHERE APU.DD_EPA_ID = 3 AND APU.APU_CHECK_PUBLICAR_A = 0
				    AND T1.ACT_ID = ACT.ACT_ID
				)';
	EXECUTE IMMEDIATE V_SQL;
    DBMS_OUTPUT.put_line('	[INFO] Activos Publicados Alquiler actualizados: '||SQL%ROWCOUNT||'.');

	--OCULTO ALQUILER

	V_SQL := 'UPDATE REM01.ACT_APU_ACTIVO_PUBLICACION T1 
				SET APU_CHECK_PUBLICAR_A = 1
				    ,APU_CHECK_OCULTAR_A = 1
				    ,USUARIOMODIFICAR = '''||V_USUARIO||'''
				    ,FECHAMODIFICAR = SYSDATE
				WHERE EXISTS (
				    SELECT ACT.ACT_ID 
				    FROM REM01.ACT_ACTIVO ACT
				    JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = ''07''
				    JOIN REM01.DD_SCR_SUBCARTERA SCR ON SCR.DD_SCR_ID = ACT.DD_SCR_ID AND SCR.DD_SCR_CODIGO IN (''151'', ''152'')
				    JOIN REM01.ACT_APU_ACTIVO_PUBLICACION APU ON APU.ACT_ID = ACT.ACT_ID
				    WHERE APU.DD_EPA_ID = 4 AND (APU.APU_CHECK_PUBLICAR_A = 0 OR APU.APU_CHECK_OCULTAR_A = 0)
				    AND T1.ACT_ID = ACT.ACT_ID
				)';
	EXECUTE IMMEDIATE V_SQL;
    DBMS_OUTPUT.put_line('	[INFO] Activos Ocultos Alquiler actualizados: '||SQL%ROWCOUNT||'.');

    COMMIT;
    DBMS_OUTPUT.put_line('[FIN]');
 
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;
/
EXIT;