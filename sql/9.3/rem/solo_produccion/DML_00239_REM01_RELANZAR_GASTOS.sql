--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE
	
    err_num NUMBER; -- Numero de error.
    err_msg VARCHAR2(2048); -- Mensaje de error.
    V_ESQUEMA VARCHAR2(25 CHAR):= 'REM01'; -- Configuracion Esquemas.
    V_ESQUEMA_M VARCHAR2(25 CHAR):= 'REMMASTER'; -- Configuracion Esquemas.
    V_USUARIOMODIFICAR VARCHAR(100 CHAR):= 'REMVIP-6836';
    V_SQL VARCHAR2(32000 CHAR);

BEGIN			
			
	DBMS_OUTPUT.PUT_LINE('[INICIO] Actualiza cuentas y partidas gastos ');			
 

	V_SQL := ' MERGE INTO REM01.GGE_GASTOS_GESTION T1 USING
		(
		    SELECT GPV.GPV_ID
		    FROM REM01.GPV_GASTOS_PROVEEDOR GPV
		    JOIN REM01.GGE_GASTOS_GESTION GGE ON GGE.GPV_ID = GPV.GPV_ID
		    WHERE TRUNC(GGE.FECHAMODIFICAR) = TRUNC(SYSDATE) AND GGE.USUARIOMODIFICAR = ''gen_fich_gastos_ra''
		)T2 ON (T1.GPV_ID = T2.GPV_ID)
		WHEN MATCHED THEN
		UPDATE
		SET T1.DD_EAH_ID = (SELECT DD_EAH_ID FROM  REM01.DD_EAH_ESTADOS_AUTORIZ_HAYA WHERE DD_EAH_CODIGO = ''03''),
		    T1.GGE_FECHA_EAH = SYSDATE,
		    T1.DD_EAP_ID = (SELECT DD_EAP_ID FROM  REM01.DD_EAP_ESTADOS_AUTORIZ_PROP WHERE DD_EAP_CODIGO = ''01''),
		    T1.GGE_FECHA_EAP = NULL,
		    T1.GGE_MOTIVO_RECHAZO_PROP = NULL,
		    T1.GGE_FECHA_ENVIO_PRPTRIO = NULL,
		    T1.USUARIOMODIFICAR = ''REMVIP-6836'',
		    T1.FECHAMODIFICAR = SYSDATE';

	EXECUTE IMMEDIATE V_SQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en GGE_GASTOS_GESTION');  

	V_SQL := ' MERGE INTO REM01.GPV_GASTOS_PROVEEDOR T1 USING
		(
		    SELECT GPV.GPV_ID
		    FROM REM01.GPV_GASTOS_PROVEEDOR GPV
		    JOIN REM01.GGE_GASTOS_GESTION GGE ON GGE.GPV_ID = GPV.GPV_ID
		    WHERE TRUNC(GPV.FECHAMODIFICAR) = TRUNC(SYSDATE) AND GPV.USUARIOMODIFICAR = ''gen_fich_gastos_ra''
		)T2 ON (T1.GPV_ID = T2.GPV_ID)
		WHEN MATCHED THEN
			UPDATE
			SET T1.DD_EGA_ID = (SELECT DD_EGA_ID FROM REM01.DD_EGA_ESTADOS_GASTO WHERE DD_EGA_CODIGO = ''03''),
			    T1.PRG_ID = NULL,
		    	    T1.USUARIOMODIFICAR = ''REMVIP-6836'',
			    T1.FECHAMODIFICAR = SYSDATE';

	EXECUTE IMMEDIATE V_SQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en GPV_GASTOS_PROVEEDOR');  

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN] Proceso realizado');
	

EXCEPTION

    WHEN OTHERS THEN
        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------');
        DBMS_OUTPUT.put_line(SQLERRM);
        ROLLBACK;
        RAISE;
END;
/
EXIT
