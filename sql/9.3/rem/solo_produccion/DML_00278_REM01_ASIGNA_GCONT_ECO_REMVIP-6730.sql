--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE


WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_ID NUMBER(16);
    V_ID2 NUMBER(16);
    V_TEXT_TABLA VARCHAR2(2400 CHAR) := 'TMP_GCO_GCH'; -- Vble. auxiliar para almacenar el nombre de la tabla de ref.
    V_INCIDENCIA VARCHAR2(25 CHAR) := 'REMVIP-6730';
    
BEGIN	
	
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');
      
    V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TMP_GCO_GCH (ID, ECO_ID) 
    SELECT ROWNUM, ECO.ECO_ID
    FROM '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO
    JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON ECO.OFR_ID = OFR.OFR_ID AND OFR.BORRADO = 0
    JOIN (
    SELECT DISTINCT ACT_OFR.OFR_ID
    FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
    JOIN '||V_ESQUEMA||'.ACT_OFR ON ACT_OFR.ACT_ID = ACT.ACT_ID
    LEFT JOIN '||V_ESQUEMA||'.ACT_ABA_ACTIVO_BANCARIO ABA ON ACT.ACT_ID = ABA.ACT_ID AND ABA.BORRADO = 0
    LEFT JOIN '||V_ESQUEMA||'.DD_CLA_CLASE_ACTIVO CLA ON ABA.DD_CLA_ID = CLA.DD_CLA_ID AND CLA.BORRADO = 0
    LEFT JOIN '||V_ESQUEMA||'.DD_CRA_CARTERA CRA ON ACT.DD_CRA_ID = CRA.DD_CRA_ID AND CRA.BORRADO = 0
    LEFT JOIN '||V_ESQUEMA||'.DD_SCR_SUBCARTERA SCR ON ACT.DD_SCR_ID = SCR.DD_SCR_ID AND SCR.BORRADO = 0
    WHERE (CRA.DD_CRA_CODIGO IN (''01'',''02'',''03'',''11'') OR 
    SCR.DD_SCR_CODIGO IN (''65'',''151'',''152'') OR CLA.DD_CLA_CODIGO = ''01'') 
    ) CRA ON CRA.OFR_ID = OFR.OFR_ID
    LEFT JOIN '||V_ESQUEMA||'.DD_EEC_EST_EXP_COMERCIAL EEC ON ECO.DD_EEC_ID = EEC.DD_EEC_ID AND EEC.BORRADO = 0
    LEFT JOIN '||V_ESQUEMA||'.DD_TOF_TIPOS_OFERTA TOF ON TOF.DD_TOF_ID = OFR.DD_TOF_ID AND TOF.BORRADO = 0
    WHERE ECO.BORRADO = 0 AND EEC.DD_EEC_CODIGO NOT IN (''02'',''17'',''16'',''08'') AND TOF.DD_TOF_CODIGO = ''01'' 
    AND NOT EXISTS (
    SELECT 1 FROM  '||V_ESQUEMA||'.GCO_GESTOR_ADD_ECO GCO
    JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GCO.GEE_ID
    JOIN '||V_ESQUEMA_M||'.USU_USUARIOS USU ON GEE.USU_ID = USU.USU_ID AND USU.BORRADO = 0
    LEFT JOIN '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR TGE ON GEE.DD_TGE_ID = TGE.DD_TGE_ID AND TGE.BORRADO = 0
    WHERE TGE.DD_TGE_CODIGO IN (''GCONT'') AND GCO.ECO_ID = ECO.ECO_ID)';

    EXECUTE IMMEDIATE V_MSQL;
    
    
    
    V_MSQL := 'SELECT COUNT(*) FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA;
  	EXECUTE IMMEDIATE V_MSQL INTO V_NUM_TABLAS;
	 
    -- LOOP para insertar los valores en TMP_GCO_GCH -----------------------------------------------------------------
    DBMS_OUTPUT.PUT_LINE('[INFO]: INSERCION EN TMP_GCO_GCH ');
    FOR I IN 1..V_NUM_TABLAS
      LOOP

          V_MSQL := 'SELECT '||V_ESQUEMA||'.S_GEE_GESTOR_ENTIDAD.NEXTVAL FROM DUAL';
          EXECUTE IMMEDIATE V_MSQL INTO V_ID;	
          
          V_MSQL := 'SELECT '||V_ESQUEMA||'.S_GEH_GESTOR_ENTIDAD_HIST.NEXTVAL FROM DUAL';
          EXECUTE IMMEDIATE V_MSQL INTO V_ID2;
          
          V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD (GEE_ID, USU_ID, DD_TGE_ID, VERSION, USUARIOCREAR, FECHACREAR, BORRADO)
                      SELECT '|| V_ID || '
                      , (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = ''grucontroller'')
                      , (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = ''GCONT'')
                      , 0
                      , '''||V_INCIDENCIA||'''
                      , SYSDATE
                      , 0 FROM DUAL';
          EXECUTE IMMEDIATE V_MSQL;          	
          
          V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST (GEH_ID, USU_ID, DD_TGE_ID, GEH_FECHA_DESDE, VERSION, USUARIOCREAR, FECHACREAR, BORRADO)
                      SELECT '|| V_ID2 || '
                      , (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = ''grucontroller'')
                      , (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = ''GCONT'')
                      , SYSDATE
                      , 0
                      , '''||V_INCIDENCIA||'''
                      , SYSDATE
                      , 0 FROM DUAL';
          EXECUTE IMMEDIATE V_MSQL;  

          V_MSQL := 'UPDATE '||V_ESQUEMA||'.'||V_TEXT_TABLA||'
          SET GEE_ID = '||V_ID||', GEH_ID = '||V_ID2||' WHERE ID = '||I;  
          EXECUTE IMMEDIATE V_MSQL;

    END LOOP;
    
 	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GCO_GESTOR_ADD_ECO (ECO_ID, GEE_ID)
	SELECT ECO_ID, GEE_ID FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA;
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO] REGISTROS INSERTADOS CORRECTAMENTE EN GCO_GESTOR_ADD_ECO: ' || sql%rowcount);
	
	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GCH_GESTOR_ECO_HISTORICO (ECO_ID, GEH_ID)
	SELECT ECO_ID, GEH_ID FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA;
	EXECUTE IMMEDIATE V_MSQL;
	DBMS_OUTPUT.PUT_LINE('[INFO]: REGISTROS INSERTADO CORRECTAMENTE');
	DBMS_OUTPUT.PUT_LINE('[INFO] REGISTROS INSERTADOS CORRECTAMENTE EN GCH_GESTOR_ECO_HISTORICO: ' || sql%rowcount);
	
	V_MSQL := 'UPDATE '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS SET USU_ID = (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = ''grucontroller''),
			USUARIOMODIFICAR = '''||V_INCIDENCIA||''', FECHAMODIFICAR = SYSDATE 
			WHERE TAR_ID IN (SELECT DISTINCT TAC.TAR_ID FROM '||V_ESQUEMA||'.TAR_TAREAS_NOTIFICACIONES TAR 
				INNER JOIN '||V_ESQUEMA||'.TEX_TAREA_EXTERNA TEX ON TEX.TAR_ID = TAR.TAR_ID AND TEX.BORRADO = 0
				INNER JOIN '||V_ESQUEMA||'.TAP_TAREA_PROCEDIMIENTO TAP ON TAP.TAP_ID = TEX.TAP_ID AND TAP.BORRADO = 0
				INNER JOIN '||V_ESQUEMA||'.TAC_TAREAS_ACTIVOS TAC ON TAC.TAR_ID = TAR.TAR_ID AND TAC.BORRADO = 0
				INNER JOIN '||V_ESQUEMA||'.ACT_TRA_TRAMITE TRA ON TAC.TRA_ID = TRA.TRA_ID AND TRA.BORRADO = 0
				INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.TBJ_ID = TRA.TBJ_ID AND ECO.BORRADO = 0
				INNER JOIN '||V_ESQUEMA||'.'||V_TEXT_TABLA||' TMP ON TMP.ECO_ID = ECO.ECO_ID 
				WHERE TAR.TAR_TAREA_FINALIZADA = 0 AND TAR.BORRADO = 0 AND TAP.TAP_CODIGO IN (''T013_CierreEconomico'',''T017_CierreEconomico''))';
				
	EXECUTE IMMEDIATE V_MSQL;
	V_MSQL := 'DROP TABLE '||V_ESQUEMA||'.TMP_GCO_GCH';
	EXECUTE IMMEDIATE V_MSQL;
    COMMIT;   

EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;

          DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------'); 
          DBMS_OUTPUT.PUT_LINE(ERR_MSG);

          ROLLBACK;
          RAISE;          

END;

/

EXIT
