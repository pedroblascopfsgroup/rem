--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- #ESQUEMA# Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- #ESQUEMA_MASTER# Configuracion Esquema
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	PL_OUTPUT VARCHAR2(32000 CHAR);

	V_COUNT NUMBER(25);
	
BEGIN

	DBMS_OUTPUT.PUT_LINE('[INFO] Se van a actualizar ECO_FECHA_ENVIO_ADVISORY_NOTE desde AUX_CAMBIO_FECHAS_REMVIP_.');

	execute immediate 'MERGE INTO '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL T1 USING (
	    SELECT DISTINCT
	    ECO.ECO_ID, TO_DATE(AUX.FECHA,''DD/MM/YYYY'') ECO_FECHA_ENVIO_ADVISORY_NOTE
	    FROM '||V_ESQUEMA||'.AUX_CAMBIO_FECHAS_REMVIP_7172 AUX
	    INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_COD_DIVARIAN = AUX.OFERTA 
	    INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = OFR.OFR_ID 
	    WHERE AUX.TIPO_FECHA = ''ECO_FECHA_ENVIO_ADVISORY_NOTE''
	) T2
	ON (T1.ECO_ID = T2.ECO_ID)
	WHEN MATCHED THEN UPDATE SET
	T1.ECO_FECHA_ENVIO_ADVISORY_NOTE = T2.ECO_FECHA_ENVIO_ADVISORY_NOTE,
	T1.USUARIOMODIFICAR = ''REMVIP-7172'',
	T1.FECHAMODIFICAR = SYSDATE';

	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en ECO_EXPEDIENTE_COMERCIAL. Deberian ser 4.959.');  

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[INFO] Se van a actualizar ECO_FECHA_RECOMENDACION_CES desde AUX_CAMBIO_FECHAS_REMVIP_.');

	execute immediate 'MERGE INTO '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL T1 USING (
	    SELECT DISTINCT
	    ECO.ECO_ID, TO_DATE(AUX.FECHA,''DD/MM/YYYY'') ECO_FECHA_RECOMENDACION_CES
	    FROM '||V_ESQUEMA||'.AUX_CAMBIO_FECHAS_REMVIP_7172 AUX
	    INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_COD_DIVARIAN = AUX.OFERTA 
	    INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = OFR.OFR_ID 
	    WHERE AUX.TIPO_FECHA = ''ECO_FECHA_RECOMENDACION_CES''
	) T2
	ON (T1.ECO_ID = T2.ECO_ID)
	WHEN MATCHED THEN UPDATE SET
	T1.ECO_FECHA_RECOMENDACION_CES = T2.ECO_FECHA_RECOMENDACION_CES,
	T1.USUARIOMODIFICAR = ''REMVIP-7172'',
	T1.FECHAMODIFICAR = SYSDATE';

	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en ECO_EXPEDIENTE_COMERCIAL. Deberian ser 5.669.');  

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[INFO] Se van a actualizar OFR_FECHA_APROBACION_PRO_MANZANA desde AUX_CAMBIO_FECHAS_REMVIP_.');

	execute immediate 'MERGE INTO '||V_ESQUEMA||'.OFR_OFERTAS T1 USING (
	    SELECT DISTINCT
	    OFR.OFR_ID, TO_DATE(AUX.FECHA,''DD/MM/YYYY'') OFR_FECHA_APROBACION_PRO_MANZANA
	    FROM '||V_ESQUEMA||'.AUX_CAMBIO_FECHAS_REMVIP_7172 AUX
	    INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_COD_DIVARIAN = AUX.OFERTA 
	    WHERE AUX.TIPO_FECHA = ''OFR_FECHA_APROBACION_PRO_MANZANA''
	) T2
	ON (T1.OFR_ID = T2.OFR_ID)
	WHEN MATCHED THEN UPDATE SET
	T1.OFR_FECHA_APROBACION_PRO_MANZANA = T2.OFR_FECHA_APROBACION_PRO_MANZANA,
	T1.USUARIOMODIFICAR = ''REMVIP-7172'',
	T1.FECHAMODIFICAR = SYSDATE';

	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en OFR_OFERTAS. Deberian ser 5.559.');  

	COMMIT;
    
    DBMS_OUTPUT.PUT_LINE('[FIN]');

EXCEPTION
  WHEN OTHERS THEN 
    DBMS_OUTPUT.PUT_LINE('KO!');
    ERR_NUM := SQLCODE;
    ERR_MSG := SQLERRM;    
    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
    DBMS_OUTPUT.put_line(ERR_MSG);    
    ROLLBACK;
    RAISE;        

END;
/
EXIT;
