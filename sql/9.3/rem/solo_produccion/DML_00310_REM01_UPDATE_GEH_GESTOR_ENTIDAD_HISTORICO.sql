WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE
  V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
  V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
  V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
  ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
  ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
  V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
  V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
  V_USUARIO VARCHAR2(100 CHAR) := 'REMVIP-7325';
  USU_ID  NUMBER(16);
  TBJ_ID  NUMBER(16);
	
  
BEGIN	
	
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');


	
	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD T1 USING(
			    SELECT 
			    GEE.GEE_ID,
			    ROW_NUMBER() OVER (PARTITION BY ACT.ACT_ID,GEE.DD_TGE_ID ORDER BY GEE.GEE_ID DESC) AS RN,
			    FIRST_VALUE(GEE.FECHACREAR) OVER (PARTITION BY ACT.ACT_ID,GEE.DD_TGE_ID ORDER BY GEE.GEE_ID DESC) AS ULTIMA_FECHA,
			    FIRST_VALUE(GEE.USUARIOCREAR) OVER (PARTITION BY ACT.ACT_ID,GEE.DD_TGE_ID ORDER BY GEE.GEE_ID DESC) AS ULTIMO_USUARIO
			    FROM '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC 
			    INNER JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GAC.GEE_ID AND GEE.BORRADO = 0
			    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.BORRADO = 0
			) T2 ON (T1.GEE_ID = T2.GEE_ID AND T2.RN > 1)
			WHEN MATCHED THEN UPDATE SET
			T1.BORRADO = 1, T1.USUARIOBORRAR = T2.ULTIMO_USUARIO, T1.FECHABORRAR = T2.ULTIMA_FECHA,
			T1.USUARIOMODIFICAR = '''||V_USUARIO||''', T1.FECHAMODIFICAR = SYSDATE';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en GEE_GESTOR_ENTIDAD');
	
	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST T1 USING(
			    SELECT 
			    GEH.GEH_ID,
			    ROW_NUMBER() OVER (PARTITION BY ACT.ACT_ID,GEH.DD_TGE_ID ORDER BY GEH.GEH_ID DESC) AS RN,
			    FIRST_VALUE(GEH.GEH_FECHA_DESDE) OVER (PARTITION BY ACT.ACT_ID,GEH.DD_TGE_ID ORDER BY GEH.GEH_ID DESC) AS ULTIMA_FECHA
			    FROM '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH 
			    INNER JOIN '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH ON GEH.GEH_ID = GAH.GEH_ID AND GEH.BORRADO = 0 AND GEH.GEH_FECHA_HASTA IS NULL
			    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAH.ACT_ID AND ACT.BORRADO = 0
			) T2 ON (T1.GEH_ID = T2.GEH_ID AND T2.RN > 1)
			WHEN MATCHED THEN UPDATE SET
			T1.GEH_FECHA_HASTA = T2.ULTIMA_FECHA, T1.USUARIOMODIFICAR = '''||V_USUARIO||''', T1.FECHAMODIFICAR = SYSDATE';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros en GEH_GESTOR_ENTIDAD_HIST');  
		
    COMMIT;

   	DBMS_OUTPUT.PUT_LINE('[FIN] ');

EXCEPTION

   WHEN OTHERS THEN
        err_num := SQLCODE;
        err_msg := SQLERRM;

        DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
        DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
        DBMS_OUTPUT.put_line(err_msg);

        ROLLBACK;
        RAISE;          

END;

