			DBMS_OUTPUT.PUT_LINE('[INFO] NO EXISTE REGISTRO EN LA BIE_LOCALIZACION');
		END IF;		
	
    	END LOOP; 

	DBMS_OUTPUT.PUT_LINE('[FIN] Registros actualizado correctamente.');	
	
	COMMIT;

