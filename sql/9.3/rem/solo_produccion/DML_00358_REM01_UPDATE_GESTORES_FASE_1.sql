WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;
DECLARE
    V_MSQL VARCHAR2(4000 CHAR);
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.
    err_num NUMBER; -- Numero de errores
    err_msg VARCHAR2(2048); -- Mensaje de error
    V_USR VARCHAR2(30 CHAR) := 'REMVIP-7676-1'; -- USUARIOCREAR/USUARIOMODIFICAR.

BEGIN
	
	  DBMS_OUTPUT.PUT_LINE('[INICIO] ');
  
    	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST T1
		   USING (
		    SELECT GEE.GEH_ID FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEE
		    INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAC ON GEE.GEH_ID = GAC.GEH_ID 
		    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID 
		    INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_7676_FASE_1 AUX ON ACT.ACT_NUM_ACTIVO = AUX.NUM_ACTIVO
		    WHERE GEE.DD_TGE_ID = 369 
		    AND GEE.BORRADO = 0 
		    AND GEE.GEH_FECHA_HASTA IS NULL
		    ) T2
		        ON (T1.GEH_ID = T2.GEH_ID)
		        WHEN MATCHED THEN UPDATE SET 
		        T1.GEH_FECHA_HASTA = SYSDATE, 
		        T1.USUARIOMODIFICAR = ''REMVIP-7676-1'', 
		        T1.FECHAMODIFICAR = SYSDATE
				 ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de GEH_GESTOR_ENTIDAD_HIST.');

	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD T1
		   USING (
		    SELECT GEE.GEE_ID, USU.USU_ID
		    FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE
		    INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GEE.GEE_ID = GAC.GEE_ID 
		    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID 
		    INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_7676_FASE_1 AUX ON ACT.ACT_NUM_ACTIVO = AUX.NUM_ACTIVO 
		    INNER JOIN '||V_ESQUEMA_M||'.USU_USUARIOS USU ON USU.USU_USERNAME = AUX.NOMBRE_GESTORIA
		    WHERE GEE.DD_TGE_ID = 369
		    AND GEE.BORRADO = 0
		)T2
		    ON (T1.GEE_ID = T2.GEE_ID)
		    WHEN MATCHED THEN UPDATE SET
		    T1.USU_ID = T2.USU_ID,
		    T1.USUARIOMODIFICAR = ''REMVIP-7676-1'', 
		    T1.FECHAMODIFICAR = SYSDATE
				 ';
		
	EXECUTE IMMEDIATE V_MSQL;  

	DBMS_OUTPUT.PUT_LINE('[INFO] Se han actualizado en total '||SQL%ROWCOUNT||' registros en la tabla de GEE_GESTOR_ENTIDAD.');


	V_MSQL := 'DELETE FROM '||V_ESQUEMA||'.TMP_GEST_GCOM_SCOM';

  	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] TABLA TMP_GEST_GCOM_SCOM BORRADA');

	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TMP_GEST_GCOM_SCOM (
		        ACT_ID,
		        USU_ID,
		        GEH_ID,
		        TIPO_GESTOR
		    )
		    SELECT 
		          ACT.ACT_ID
		        , USU.USU_ID
		        , '||V_ESQUEMA||'.S_GEH_GESTOR_ENTIDAD_HIST.NEXTVAL GEH_ID
		        , 369
		    FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
		    INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_7676_FASE_1 AUX ON ACT.ACT_NUM_ACTIVO = AUX.NUM_ACTIVO
		    INNER JOIN '||V_ESQUEMA_M||'.USU_USUARIOS USU ON USU.USU_USERNAME = AUX.NOMBRE_GESTORIA
				 ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han INSERTADO en total '||SQL%ROWCOUNT||' registros en la tabla de TMP_GEST_GCOM_SCOM.');


	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST (
			    GEH_ID,
			    USU_ID,
			    DD_TGE_ID,
			    GEH_FECHA_DESDE,
			    GEH_FECHA_HASTA,
			    VERSION,
			    USUARIOCREAR,
			    FECHACREAR,
			    BORRADO)
			SELECT
			    TMP.GEH_ID,
			    TMP.USU_ID,
			    TMP.TIPO_GESTOR,
			    SYSDATE AS GEH_FECHA_DESDE,
			    NULL AS GEH_FECHA_HASTA,
			    1 AS VERSION,
			    ''REMVIP-7676-1'' AS USUARIOCREAR,
			    SYSDATE AS FECHACREAR,
			    0 AS BORRADO
			FROM '||V_ESQUEMA||'.TMP_GEST_GCOM_SCOM TMP
					 ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han INSERTADO en total '||SQL%ROWCOUNT||' registros en la tabla de GEH_GESTOR_ENTIDAD_HIST.');

	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO (
			    GEH_ID,
			    ACT_ID)
			SELECT
			    TMP.GEH_ID,
			    TMP.ACT_ID
			FROM '||V_ESQUEMA||'.TMP_GEST_GCOM_SCOM TMP
					 ';
		
	EXECUTE IMMEDIATE V_MSQL;  
	
	DBMS_OUTPUT.PUT_LINE('[INFO] Se han INSERTADO en total '||SQL%ROWCOUNT||' registros en la tabla de GAH_GESTOR_ACTIVO_HISTORICO.');

	DBMS_OUTPUT.PUT_LINE('[FIN] ');

	COMMIT;

EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;

/

EXIT;
