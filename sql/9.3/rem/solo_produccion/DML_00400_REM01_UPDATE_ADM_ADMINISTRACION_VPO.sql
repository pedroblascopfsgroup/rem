WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;
ALTER SESSION SET NLS_NUMERIC_CHARACTERS = ',.';


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.

    V_USR VARCHAR2(30 CHAR) := 'REMVIP-7806'; -- USUARIOCREAR/USUARIOMODIFICAR

BEGIN

	DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZAR VPO EN ACTIVOS');

	V_MSQL :=  'MERGE INTO '||V_ESQUEMA||'.ACT_ACTIVO T1 USING 
		    (SELECT ACT.ACT_NUM_ACTIVO FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT 
		     INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_7806 AUX ON ACT.ACT_NUM_ACTIVO = AUX.NUM_ACTIVO) T2
		     ON (T1.ACT_NUM_ACTIVO = T2.NUM_ACTIVO)
		     WHEN MATCHED THEN UPDATE SET 
		     T1.ACT_VPO = 1,
		     T1.USUARIOMODIFICAR = ''REMVIP-7806'',
		     T1.FECHAMODIFICAR = SYSDATE';

	DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros '); 

	DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZAR REGISTROS EN ACT_ADM_INF_ADMINISTRATIVA');


	V_MSQL :=   'MERGE INTO REM01.ACT_ADM_INF_ADMINISTRATIVA T1 USING 
		    (SELECT ADM.ACT_ID, AUX.ADM_DESCALIFICADO, AUX.DD_TPV_ID, AUX.ADM_FECHA_CALIFICAION, AUX.ADM_VIGENCIA, AUX.ADM_NUM_ECPEDIENTE, AUX.ADM_MAX_PRECIO_VENTA
		     FROM '||V_ESQUEMA||'.ACT_ADM_INF_ADMINISTRATIVA ADM 
		     INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = ADM.ACT_ID
		     INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_7806 AUX ON ACT.ACT_NUM_ACTIVO = AUX.NUM_ACTIVO
		     ) T2
		     ON (T1.ACT_ID = T2.ACT_ID)
		     WHEN MATCHED THEN UPDATE SET  
		     T1.ADM_DESCALIFICADO = T2.ADM_DESCALIFICADO,
		     T1.DD_TVP_ID = T2.DD_TPV_ID,
		     T1.ADM_FECHA_CALIFICACION = NVL2(T2.ADM_FECHA_CALIFICAION, TO_DATE(T2.ADM_FECHA_CALIFICAION, ''DD/MM/YYYY''), NULL),
		     T1.ADM_VIGENCIA = NVL2(T2.ADM_VIGENCIA, TO_DATE(T2.ADM_VIGENCIA, ''DD/MM/YYYY''), NULL),
		     T1.ADM_NUM_EXPEDIENTE = NVL2(T2.ADM_NUM_ECPEDIENTE,T2.ADM_NUM_ECPEDIENTE, NULL),
		     T1.ADM_MAX_PRECIO_VENTA = NVL2(T2.ADM_MAX_PRECIO_VENTA,T2.ADM_MAX_PRECIO_VENTA, NULL),
		     T1.FECHAMODIFICAR = SYSDATE,
		     T1.USUARIOMODIFICAR = ''REMVIP-7806''';

        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros '); 

        COMMIT;

EXCEPTION
    WHEN OTHERS THEN
    err_num := SQLCODE;
    err_msg := SQLERRM;

    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------');
    DBMS_OUTPUT.put_line(err_msg);
    DBMS_OUTPUT.put_line(V_MSQL);
    ROLLBACK;
    RAISE;

END;

