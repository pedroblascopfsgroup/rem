WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_SQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
    V_NUM_TABLAS NUMBER(16); -- Vble. para validar la existencia de una tabla.   
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_ID NUMBER(16);
    V_TEXT_TABLA VARCHAR2(2400 CHAR) := 'TMP_GCO_GCH_HREOS_11815'; -- Vble. auxiliar para almacenar el nombre de la tabla de ref.
    V_INCIDENCIA VARCHAR2(25 CHAR) := 'HREOS-11815';
    
BEGIN	
	
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');
      
    V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TMP_GCO_GCH_HREOS_11815 (ID, ECO_ID,USERNAME) 
SELECT ROWNUM, ECO_ID, ''usuario'' FROM(                        
SELECT DISTINCT ECO.ECO_ID FROM REM01.ECO_EXPEDIENTE_COMERCIAL ECO
JOIN REM01.OFR_OFERTAS OFR ON ECO.OFR_ID = OFR.OFR_ID 
JOIN REM01.ACT_OFR AOFR ON OFR.OFR_ID=AOFR.OFR_ID
JOIN REM01.ACT_ACTIVO ACT ON AOFR.ACT_ID = ACT.ACT_ID 
LEFT JOIN REM01.DD_EEC_EST_EXP_COMERCIAL EEC ON ECO.DD_EEC_ID = EEC.DD_EEC_ID AND EEC.BORRADO=0
LEFT JOIN REM01.DD_EOF_ESTADOS_OFERTA EOF ON OFR.DD_EOF_ID = EOF.DD_EOF_ID AND EOF.BORRADO=0
INNER JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = 16    
WHERE  EEC.DD_EEC_CODIGO != ''02'' AND EOF.DD_EOF_CODIGO != ''02'' AND ECO.ECO_NUM_EXPEDIENTE NOT IN(
    SELECT  DISTINCT ECO.ECO_NUM_EXPEDIENTE FROM REM01.ECO_EXPEDIENTE_COMERCIAL ECO
    JOIN REM01.OFR_OFERTAS OFR ON ECO.OFR_ID = OFR.OFR_ID 
    JOIN REM01.ACT_OFR AOFR ON OFR.OFR_ID=AOFR.OFR_ID
    JOIN REM01.ACT_ACTIVO ACT ON AOFR.ACT_ID = ACT.ACT_ID 
    LEFT JOIN REM01.DD_EEC_EST_EXP_COMERCIAL EEC ON ECO.DD_EEC_ID = EEC.DD_EEC_ID AND EEC.BORRADO=0
    LEFT JOIN REM01.DD_EOF_ESTADOS_OFERTA EOF ON OFR.DD_EOF_ID = EOF.DD_EOF_ID AND EOF.BORRADO=0
    INNER JOIN REM01.DD_CRA_CARTERA CRA ON CRA.DD_CRA_ID = ACT.DD_CRA_ID AND CRA.DD_CRA_CODIGO = 16
    INNER JOIN REM01.GCH_GESTOR_ECO_HISTORICO GCH ON GCH.ECO_ID = ECO.ECO_ID
    INNER JOIN REM01.GCO_GESTOR_ADD_ECO GCO ON GCO.ECO_ID = ECO.ECO_ID
    INNER JOIN REM01.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GCO.GEE_ID AND GEE.DD_TGE_ID = (SELECT DD_TGE_ID FROM REMMASTER.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = ''GFORM'') AND GEE.BORRADO = 0
    INNER JOIN REM01.GEH_GESTOR_ENTIDAD_HIST GEH ON GEH.GEH_ID = GCH.GEH_ID AND GEH.DD_TGE_ID = (SELECT DD_TGE_ID FROM REMMASTER.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = ''GFORM'') AND GEH.GEH_FECHA_HASTA IS NULL AND GEH.BORRADO = 0
    INNER JOIN REMMASTER.USU_USUARIOS USU2 ON GEE.USU_ID = USU2.USU_ID
    WHERE  EEC.DD_EEC_CODIGO != ''02'' AND EOF.DD_EOF_CODIGO != ''02''))';

    EXECUTE IMMEDIATE V_MSQL;
    DBMS_OUTPUT.PUT_LINE('[INFO] Actualizados '||SQL%ROWCOUNT||' registros '); 

    COMMIT;
   

EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;

          DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------'); 
          DBMS_OUTPUT.PUT_LINE(ERR_MSG);

          ROLLBACK;
          RAISE;          

END;

