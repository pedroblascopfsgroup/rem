WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master    
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_TABLA_GESTORES VARCHAR( 100 CHAR ) := 'ACT_GES_DIST_GESTORES';
    V_USUARIO VARCHAR2(30 CHAR) := 'REMVIP-8208'; -- USUARIOCREAR/USUARIOMODIFICAR

    V_USERNAME VARCHAR2(50 CHAR):= 'tecnotra01';
    V_USU_ID NUMBER(16);

BEGIN

    DBMS_OUTPUT.PUT_LINE('[INICIO]');

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINAR CONFIGURACION TECNOTRAMIT');

    V_MSQL := 'DELETE '||V_ESQUEMA||'.'||V_TABLA_GESTORES||' WHERE USERNAME = '''||V_USERNAME||''' AND COD_CARTERA = ''7''
                AND TIPO_GESTOR = ''GGADM'' AND COD_SUBCARTERA IS NULL AND BORRADO = 1';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINADOS '||SQL%ROWCOUNT||' REGISTROS'); 

    V_MSQL :=   'SELECT USU_ID FROM '|| V_ESQUEMA_M ||'.USU_USUARIOS WHERE USU_USERNAME = '''||V_USERNAME||''' AND BORRADO = 0';
    EXECUTE IMMEDIATE V_MSQL INTO V_USU_ID;

    DBMS_OUTPUT.PUT_LINE('[INFO] QUITAR FECHA FIN  ¡NO TECNOTRAMIT!  DE GESTORIA ADMISION PARA DIVARIAN'); 

    V_MSQL :=   'MERGE INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST T1 USING(
                    SELECT GEH.GEH_ID FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH 
                    INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH ON GAH.GEH_ID = GEH.GEH_ID
                    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAH.ACT_ID AND ACT.BORRADO = 0
                    WHERE ACT.DD_SCR_ID IN (443,444) AND GEH.DD_TGE_ID = 369 AND GEH.BORRADO = 0 
                    AND GEH.GEH_FECHA_HASTA IS NOT NULL AND GEH.USU_ID != '||V_USU_ID||'
                    AND GEH.USUARIOMODIFICAR = '''||V_USUARIO||''') T2
                ON (T1.GEH_ID = T2.GEH_ID)
                WHEN MATCHED THEN UPDATE SET
                T1.GEH_FECHA_HASTA = NULL,		     
                T1.FECHAMODIFICAR = SYSDATE,
                T1.USUARIOMODIFICAR = '''||V_USUARIO||'''';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADOS '||SQL%ROWCOUNT||' REGISTROS');  

    DBMS_OUTPUT.PUT_LINE('[INFO] REVIVIR REGISTRO ¡NO TECNOTRAMIT! DE GESTORIA ADMISION PARA DIVARIAN'); 

    V_MSQL :=   'MERGE INTO '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD T1 USING(
                    SELECT GEE.GEE_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE 
                    INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
                    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.BORRADO = 0
                    WHERE ACT.DD_SCR_ID IN (443,444) AND GEE.DD_TGE_ID = 369 AND GEE.BORRADO = 1
                    AND GEE.USU_ID != '||V_USU_ID||' AND GEE.USUARIOBORRAR = '''||V_USUARIO||''') T2
                ON (T1.GEE_ID = T2.GEE_ID)
                WHEN MATCHED THEN UPDATE SET
                T1.BORRADO = 0,	     
                T1.FECHABORRAR = NULL,
                T1.USUARIOBORRAR = NULL';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] REVIVIDOS '||SQL%ROWCOUNT||' REGISTROS');  

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINAR REGISTROS CON FECHA POSTERIOR A LA REVIVIDA'); 

    V_MSQL :=   'DELETE '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO WHERE GEH_ID IN (
                    SELECT GEH.GEH_ID FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH 
                    INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH ON GAH.GEH_ID = GEH.GEH_ID
                    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAH.ACT_ID AND ACT.BORRADO = 0
                    WHERE ACT.DD_SCR_ID IN (443,444) AND GEH.DD_TGE_ID = 369 AND GEH.BORRADO = 0 
                    AND GEH.GEH_FECHA_HASTA IS NULL AND GEH.USU_ID = '||V_USU_ID||')';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINADOS '||SQL%ROWCOUNT||' REGISTROS GAH');

    V_MSQL :=   'DELETE '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST WHERE GEH_ID IN (
                    SELECT GEH.GEH_ID FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH 
                    INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH ON GAH.GEH_ID = GEH.GEH_ID
                    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAH.ACT_ID AND ACT.BORRADO = 0
                    WHERE ACT.DD_SCR_ID IN (443,444) AND GEH.DD_TGE_ID = 369 AND GEH.BORRADO = 0 
                    AND GEH.GEH_FECHA_HASTA IS NULL AND GEH.USU_ID = '||V_USU_ID||')';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINADOS '||SQL%ROWCOUNT||' REGISTROS GEH');

    V_MSQL :=   'DELETE '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO WHERE GEE_ID IN (
                    SELECT GEE.GEE_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE 
                    INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
                    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.BORRADO = 0
                    WHERE ACT.DD_SCR_ID IN (443,444) AND GEE.DD_TGE_ID = 369 AND GEE.BORRADO = 0
                    AND GEE.USU_ID = '||V_USU_ID||')';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINADOS '||SQL%ROWCOUNT||' REGISTROS GAC');

    V_MSQL :=   'DELETE '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD WHERE GEE_ID IN (
                    SELECT GEE.GEE_ID FROM '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE 
                    INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.GEE_ID = GEE.GEE_ID
                    INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON ACT.ACT_ID = GAC.ACT_ID AND ACT.BORRADO = 0
                    WHERE ACT.DD_SCR_ID IN (443,444) AND GEE.DD_TGE_ID = 369 AND GEE.BORRADO = 0
                    AND GEE.USU_ID = '||V_USU_ID||')';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] ELIMINADOS '||SQL%ROWCOUNT||' REGISTROS GEE');

    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
    err_num := SQLCODE;
    err_msg := SQLERRM;

    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------');
    DBMS_OUTPUT.put_line(err_msg);
    DBMS_OUTPUT.put_line(V_MSQL);
    ROLLBACK;
    RAISE;

END;

/

EXIT
