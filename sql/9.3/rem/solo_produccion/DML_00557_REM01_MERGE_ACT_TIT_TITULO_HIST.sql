WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master    
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_TABLA_AUXILIAR VARCHAR( 100 CHAR ) := 'AUX_REMVIP_8421';
    V_USUARIO VARCHAR2(30 CHAR) := 'REMVIP-8421'; -- USUARIOCREAR/USUARIOMODIFICAR

BEGIN

    DBMS_OUTPUT.PUT_LINE('[INICIO]');

    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_TIT_TITULO T1 USING(
                SELECT TIT.TIT_ID,ACT.ACT_ID, AUX.FECHA_PRESENTACION, AUX.FECHA_INSCRIPCION, AUX.ESTADO_INSCRIPCION FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
                INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8421 AUX ON AUX.ACT_NUM_ACTIVO = ACT.ACT_NUM_ACTIVO
                LEFT JOIN '||V_ESQUEMA||'.ACT_TIT_TITULO TIT ON TIT.ACT_ID = ACT.ACT_ID AND TIT.BORRADO = 0
                LEFT JOIN '||V_ESQUEMA||'.ACT_AHT_HIST_TRAM_TITULO AHT ON AHT.TIT_ID = TIT.TIT_ID AND AHT.BORRADO = 0
                WHERE ACT.BORRADO = 0)T2
                ON (T1.TIT_ID = T2.TIT_ID)
                WHEN MATCHED THEN UPDATE SET
                T1.DD_ETI_ID = (SELECT DD_ETI_ID FROM '||V_ESQUEMA||'.DD_ETI_ESTADO_TITULO WHERE DD_ETI_DESCRIPCION = T2.ESTADO_INSCRIPCION),
                T1.TIT_FECHA_PRESENT1_REG = T2.FECHA_PRESENTACION,
                T1.TIT_FECHA_INSC_REG = T2.FECHA_INSCRIPCION, T1.USUARIOMODIFICAR = '''||V_USUARIO||''',
                T1.FECHAMODIFICAR = SYSDATE
                WHEN NOT MATCHED THEN INSERT (TIT_ID,ACT_ID,DD_ETI_ID,TIT_FECHA_PRESENT1_REG,TIT_FECHA_INSC_REG,USUARIOCREAR,FECHACREAR)
                VALUES ('||V_ESQUEMA||'.S_ACT_TIT_TITULO.NEXTVAL ,T2.ACT_ID, (SELECT DD_ETI_ID FROM '||V_ESQUEMA||'.DD_ETI_ESTADO_TITULO WHERE DD_ETI_DESCRIPCION = T2.ESTADO_INSCRIPCION),
                T2.FECHA_PRESENTACION,T2.FECHA_INSCRIPCION,'''||V_USUARIO||''',SYSDATE)';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] '||SQL%ROWCOUNT||' REGISTROS EN ACT_TIT_TITULO');  

    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_AHT_HIST_TRAM_TITULO T1 USING(
                SELECT AHT.AHT_ID,TIT.TIT_ID, AUX.FECHA_PRESENTACION, AUX.FECHA_INSCRIPCION, AUX.ESTADO_INSCRIPCION FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
                INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8421 AUX ON AUX.ACT_NUM_ACTIVO = ACT.ACT_NUM_ACTIVO
                LEFT JOIN '||V_ESQUEMA||'.ACT_TIT_TITULO TIT ON TIT.ACT_ID = ACT.ACT_ID AND TIT.BORRADO = 0
                LEFT JOIN '||V_ESQUEMA||'.ACT_AHT_HIST_TRAM_TITULO AHT ON AHT.TIT_ID = TIT.TIT_ID AND AHT.BORRADO = 0
                WHERE ACT.BORRADO = 0)T2
                ON (T1.AHT_ID = T2.AHT_ID)
                WHEN MATCHED THEN UPDATE SET
                T1.DD_ESP_ID = (SELECT DD_ESP_ID FROM '||V_ESQUEMA||'.DD_ESP_ESTADO_PRESENTACION WHERE DD_ESP_DESCRIPCION = T2.ESTADO_INSCRIPCION),
                T1.AHT_FECHA_INSCRIPCION = T2.FECHA_INSCRIPCION,
                T1.AHT_FECHA_PRES_REGISTRO = T2.FECHA_PRESENTACION,
                T1.USUARIOMODIFICAR = '''||V_USUARIO||''', T1.FECHAMODIFICAR = SYSDATE
                WHEN NOT MATCHED THEN INSERT (AHT_ID,TIT_ID,AHT_FECHA_PRES_REGISTRO,AHT_FECHA_INSCRIPCION,DD_ESP_ID,USUARIOCREAR,FECHACREAR)
                VALUES ('||V_ESQUEMA||'.S_ACT_AHT_HIST_TRAM_TITULO.NEXTVAL ,T2.TIT_ID,T2.FECHA_PRESENTACION,T2.FECHA_INSCRIPCION,
                (SELECT DD_ESP_ID FROM '||V_ESQUEMA||'.DD_ESP_ESTADO_PRESENTACION WHERE DD_ESP_DESCRIPCION = T2.ESTADO_INSCRIPCION),
                '''||V_USUARIO||''',SYSDATE)';
    EXECUTE IMMEDIATE V_MSQL;

    DBMS_OUTPUT.PUT_LINE('[INFO] '||SQL%ROWCOUNT||' REGISTROS EN ACT_AHT_HIST_TRAM_TITULO');  

    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
    err_num := SQLCODE;
    err_msg := SQLERRM;

    DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
    DBMS_OUTPUT.put_line('-----------------------------------------------------------');
    DBMS_OUTPUT.put_line(err_msg);
    DBMS_OUTPUT.put_line(V_MSQL);
    ROLLBACK;
    RAISE;

END;

/

EXIT
