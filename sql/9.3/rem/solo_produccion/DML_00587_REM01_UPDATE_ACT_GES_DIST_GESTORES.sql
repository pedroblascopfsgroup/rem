--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;

DECLARE
    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar     
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
    V_COUNT NUMBER(16);
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_USUARIO VARCHAR2(100 CHAR):='REMVIP-8504'; --Vble. auxiliar para almacenar el usuario

    V_USERNAME VARCHAR2(50 CHAR) := 'garsa03cajamar';
	V_PERFIL VARCHAR2(50 CHAR) := 'GIAFORM';

BEGIN	
	
    DBMS_OUTPUT.PUT_LINE('[INICIO] ');

    V_MSQL := 'SELECT COUNT(1) FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||V_USERNAME||'''';
	EXECUTE IMMEDIATE V_MSQL INTO V_COUNT;

	IF V_COUNT = 1 THEN
    
        V_MSQL := ' UPDATE '|| V_ESQUEMA ||'.ACT_GES_DIST_GESTORES SET 
                    USERNAME =  '''||V_USERNAME||''',
                    USUARIOMODIFICAR = '''||V_USUARIO||''',
                    FECHAMODIFICAR = SYSDATE
                    WHERE TIPO_GESTOR = '''||V_PERFIL||''' AND COD_CARTERA = 1
                    AND USERNAME = ''garsa03''';

        EXECUTE IMMEDIATE V_MSQL;
        
        DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADOS '||SQL%ROWCOUNT||' REGISTROS');

        -- ACTIVOS
        V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.AUX_REMVIP_8504_ACT (ACT_ID,ACT_NUM_ACTIVO,GEH_ID,DD_TGE_BY_GEH_ID,GEE_ID,DD_TGE_BY_GEE_ID,USU_ID_BY_GEE_ID)
                        SELECT ACT.ACT_ID, ACT.ACT_NUM_ACTIVO,GEH.GEH_ID,GEH.DD_TGE_ID,GEE.GEE_ID,GEE.DD_TGE_ID,GEE.USU_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
                        INNER JOIN '||V_ESQUEMA||'.GAC_GESTOR_ADD_ACTIVO GAC ON GAC.ACT_ID = ACT.ACT_ID
                        INNER JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GAC.GEE_ID
                            AND GEE.DD_TGE_ID = (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = '''||V_PERFIL||''')
                            AND GEE.BORRADO = 0
                        INNER JOIN '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO GAH ON GAH.ACT_ID = ACT.ACT_ID
                        INNER JOIN '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH ON GEH.GEH_ID = GAH.GEH_ID
                            AND GEH.DD_TGE_ID = (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = '''||V_PERFIL||''') AND GEH.GEH_FECHA_HASTA IS NULL 
                            AND GEH.BORRADO = 0
                        INNER JOIN '||V_ESQUEMA_M||'.USU_USUARIOS USU ON USU.USU_ID = GEH.USU_ID AND USU.USU_ID = GEE.USU_ID
                        AND ACT.DD_CRA_ID = 1 AND USU.USU_ID = 39241
                        AND ACT.BORRADO = 0';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN AUX_REMVIP_8504_ACT');

        -- EXPEDIENTES
        V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.AUX_REMVIP_8504_EXP (ECO_ID,ECO_NUM_EXPEDIENTE,GEH_ID,DD_TGE_BY_GEH_ID,GEE_ID,DD_TGE_BY_GEE_ID,USU_ID_BY_GEE_ID)
                        SELECT ECO.ECO_ID, ECO.ECO_NUM_EXPEDIENTE,GEH.GEH_ID,GEH.DD_TGE_ID,GEE.GEE_ID,GEE.DD_TGE_ID,GEE.USU_ID FROM '||V_ESQUEMA||'.ACT_ACTIVO ACT
                        INNER JOIN '||V_ESQUEMA||'.ACT_OFR AOFR ON AOFR.ACT_ID = ACT.ACT_ID
                        INNER JOIN '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO ON ECO.OFR_ID = AOFR.OFR_ID AND ECO.BORRADO = 0
                        INNER JOIN '||V_ESQUEMA||'.ofr_ofertas ofr on eco.ofr_id = ofr.ofr_id AND OFR.BORRADO=0
                        INNER JOIN '||V_ESQUEMA||'.GCO_GESTOR_ADD_ECO GCO ON GCO.ECO_ID = ECO.ECO_ID
                        INNER JOIN '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE ON GEE.GEE_ID = GCO.GEE_ID
                            AND GEE.DD_TGE_ID = (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = '''||V_PERFIL||''')
                            AND GEE.BORRADO = 0
                        INNER JOIN '||V_ESQUEMA||'.GCH_GESTOR_ECO_HISTORICO GCH ON GCH.ECO_ID = ECO.ECO_ID
                        INNER JOIN '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH ON GEH.GEH_ID = GCH.GEH_ID
                            AND GEH.DD_TGE_ID = (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = '''||V_PERFIL||''') AND GEH.GEH_FECHA_HASTA IS NULL 
                            AND GEH.BORRADO = 0
                        INNER JOIN '||V_ESQUEMA_M||'.USU_USUARIOS USU ON USU.USU_ID = GEH.USU_ID AND USU.USU_ID = GEE.USU_ID
                        AND ACT.DD_CRA_ID = 1 AND USU.USU_ID = 39241
                        AND ACT.BORRADO = 0 AND ECO.DD_EEC_ID != 2 AND ECO.DD_EEC_ID != 8 AND OFR.DD_EOF_ID != 2';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] INSERTADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN AUX_REMVIP_8504_EXP');

        COMMIT;

        V_MSQL := 'UPDATE '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH SET 
                GEH_FECHA_HASTA = TO_DATE(SYSDATE, ''DD/MM/YY''),
                USUARIOMODIFICAR = '''||V_USUARIO||''',
                FECHAMODIFICAR = SYSDATE
                WHERE GEH_ID IN (SELECT GEH_ID FROM '||V_ESQUEMA||'.AUX_REMVIP_8504_ACT)';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN GEH_GESTOR_ENTIDAD_HIST DE ACTIVOS');

        V_MSQL := 'UPDATE '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST GEH SET 
                GEH_FECHA_HASTA = TO_DATE(SYSDATE, ''DD/MM/YY''),
                USUARIOMODIFICAR = '''||V_USUARIO||''',
                FECHAMODIFICAR = SYSDATE
                WHERE GEH_ID IN (SELECT GEH_ID FROM '||V_ESQUEMA||'.AUX_REMVIP_8504_EXP)';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN GEH_GESTOR_ENTIDAD_HIST DE EXPEDIENTES');

        V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST (GEH_ID,USU_ID,DD_TGE_ID, GEH_FECHA_DESDE, USUARIOCREAR, FECHACREAR, USUARIOMODIFICAR, USUARIOBORRAR)
                    SELECT '||V_ESQUEMA||'.S_GEH_GESTOR_ENTIDAD_HIST.NEXTVAL,
                    (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||V_USERNAME||''' AND BORRADO = 0),
                    (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0),
                    SYSDATE, '''||V_USUARIO||''',SYSDATE, TO_CHAR(AUX.ACT_ID), ''ACT'' FROM '||V_ESQUEMA||'.AUX_REMVIP_8504_ACT AUX';
        EXECUTE IMMEDIATE V_MSQL;  

        DBMS_OUTPUT.PUT_LINE('[INFO]: REGISTROS INSERTADOS DE ACTIVOS EN GEH_GESTOR_ENTIDAD_HIST: ' ||sql%rowcount);

        V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST (GEH_ID,USU_ID,DD_TGE_ID, GEH_FECHA_DESDE, USUARIOCREAR, FECHACREAR, USUARIOMODIFICAR, USUARIOBORRAR)
                    SELECT '||V_ESQUEMA||'.S_GEH_GESTOR_ENTIDAD_HIST.NEXTVAL,
                    (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||V_USERNAME||''' AND BORRADO = 0),
                    (SELECT DD_TGE_ID FROM '||V_ESQUEMA_M||'.DD_TGE_TIPO_GESTOR WHERE DD_TGE_CODIGO = '''||V_PERFIL||''' AND BORRADO = 0),
                    SYSDATE, '''||V_USUARIO||''',SYSDATE, TO_CHAR(AUX.ECO_ID), ''EXP'' FROM '||V_ESQUEMA||'.AUX_REMVIP_8504_EXP AUX';
        EXECUTE IMMEDIATE V_MSQL;  

        DBMS_OUTPUT.PUT_LINE('[INFO]: REGISTROS INSERTADOS DE EXPEDIENTES EN GEH_GESTOR_ENTIDAD_HIST: ' ||sql%rowcount);

        V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GAH_GESTOR_ACTIVO_HISTORICO (GEH_ID, ACT_ID)
                    SELECT GEH_ID, TO_NUMBER(USUARIOMODIFICAR) FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST WHERE USUARIOCREAR = '''||V_USUARIO||'''
                    AND USUARIOMODIFICAR IS NOT NULL AND USUARIOBORRAR = ''ACT''';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO]: REGISTROS INSERTADOS EN GAH_GESTOR_ACTIVO_HISTORICO: ' ||sql%rowcount);

        V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.GCH_GESTOR_ECO_HISTORICO (GEH_ID, ECO_ID)
                    SELECT GEH_ID, TO_NUMBER(USUARIOMODIFICAR) FROM '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST WHERE USUARIOCREAR = '''||V_USUARIO||'''
                    AND USUARIOMODIFICAR IS NOT NULL AND USUARIOBORRAR = ''EXP''';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO]: REGISTROS INSERTADOS EN GCH_GESTOR_ECO_HISTORICO: ' ||sql%rowcount);

        V_MSQL:='UPDATE '||V_ESQUEMA||'.GEH_GESTOR_ENTIDAD_HIST 
                    SET USUARIOMODIFICAR = NULL,
                    USUARIOBORRAR = NULL
                    WHERE USUARIOCREAR = '''||V_USUARIO||''' AND USUARIOMODIFICAR IS NOT NULL AND USUARIOBORRAR IS NOT NULL';
        EXECUTE IMMEDIATE V_MSQL;

        V_MSQL := 'UPDATE '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE SET 
                USU_ID = (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||V_USERNAME||''' AND BORRADO = 0),
                USUARIOMODIFICAR = '''||V_USUARIO||''',
                FECHAMODIFICAR = SYSDATE
                WHERE GEE_ID IN (SELECT GEE_ID FROM '||V_ESQUEMA||'.AUX_REMVIP_8504_ACT)';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN GEE_GESTOR_ENTIDAD DE ACTIVOS');

        V_MSQL := 'UPDATE '||V_ESQUEMA||'.GEE_GESTOR_ENTIDAD GEE SET 
                USU_ID = (SELECT USU_ID FROM '||V_ESQUEMA_M||'.USU_USUARIOS WHERE USU_USERNAME = '''||V_USERNAME||''' AND BORRADO = 0),
                USUARIOMODIFICAR = '''||V_USUARIO||''',
                FECHAMODIFICAR = SYSDATE
                WHERE GEE_ID IN (SELECT GEE_ID FROM '||V_ESQUEMA||'.AUX_REMVIP_8504_EXP)';
        EXECUTE IMMEDIATE V_MSQL;

        DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN GEE_GESTOR_ENTIDAD DE EXPEDIENTES');
		
	ELSE 
	
		DBMS_OUTPUT.PUT_LINE('[INFO] NO EXISTE USUARIO '''||V_USERNAME||'''');
	
	END IF;

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('[FIN]');
    
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;

          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);

          ROLLBACK;
          RAISE;          

END;
/
EXIT