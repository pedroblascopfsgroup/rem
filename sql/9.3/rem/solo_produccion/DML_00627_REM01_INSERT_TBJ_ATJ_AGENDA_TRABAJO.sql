WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF; 
DECLARE

	V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejECVtar  
	V_SQL VARCHAR2(32000 CHAR); -- Sentencia a ejECVtar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_NUM_FILAS NUMBER(16);
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	V_USU VARCHAR2(30 CHAR) := 'REMVIP-8747'; -- USUARIOCREAR/USUARIOMODIFICAR.

BEGIN	

	DBMS_OUTPUT.PUT_LINE('[INICIO] TABLA TBJ_ATJ_AGENDA_TRABAJO');

	V_SQL := 'MERGE INTO '||V_ESQUEMA||'.TBJ_ATJ_AGENDA_TRABAJO T1
		USING (
		    SELECT TBJ_ID, USU_ID, TBO_OBSERVACION, TBO_FECHA, VERSION, USUARIOCREAR, FECHACREAR, USUARIOMODIFICAR, FECHAMODIFICAR, USUARIOBORRAR, FECHABORRAR, BORRADO
		    FROM '||V_ESQUEMA||'.ACT_TBO_TRABAJO_OBS
		) T2
		ON (T1.TBJ_ID = T2.TBJ_ID)
		WHEN NOT MATCHED THEN
		    INSERT
		    (TBJ_ATJ_ID, TBJ_ID, USU_ID, TBJ_ATJ_FECHA, DD_TAT_ID, TBJ_ATJ_OBSERVACION, VERSION, USUARIOCREAR, FECHACREAR, USUARIOMODIFICAR, FECHAMODIFICAR, USUARIOBORRAR, FECHABORRAR, BORRADO)
    		VALUES (
			'||V_ESQUEMA||'.S_TBJ_ATJ_AGENDA_TRABAJO.NEXTVAL,
			T2.TBJ_ID, 
			T2.USU_ID,
			T2.TBO_FECHA, 
			1,
			NVL(TO_CLOB(T2.TBO_OBSERVACION), ''-''),
			T2.VERSION, 
			T2.USUARIOCREAR,
			T2.FECHACREAR, 
			T2.USUARIOMODIFICAR,
			T2.FECHAMODIFICAR,
			T2.USUARIOBORRAR,
			T2.FECHABORRAR,
			T2.BORRADO
			 )';
		
	EXECUTE IMMEDIATE V_SQL;
		
	DBMS_OUTPUT.PUT_LINE('[INFO] SE HAN INSERTADO CORRECTAMENTE '||SQL%ROWCOUNT||' REGISTROS');   

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN] REGISTROS MIGRADOS DE ACT_TBO_TRABAJO_OBS A TBJ_ATJ_AGENDA_TRABAJO');
 
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;
/
EXIT;
