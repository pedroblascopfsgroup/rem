WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF; 
DECLARE

	V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejECVtar  
	V_SQL VARCHAR2(32000 CHAR); -- Sentencia a ejECVtar    
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_NUM_FILAS NUMBER(16);
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
	V_USU VARCHAR2(30 CHAR) := 'REMVIP-8747'; -- USUARIOCREAR/USUARIOMODIFICAR.

BEGIN	

	DBMS_OUTPUT.PUT_LINE('[INICIO] TABLA TBJ_ATJ_AGENDA_TRABAJO');

	V_MSQL := 'INSERT INTO '||V_ESQUEMA||'.TBJ_ATJ_AGENDA_TRABAJO (TBJ_ATJ_ID, TBJ_ID, USU_ID, TBJ_ATJ_FECHA, DD_TAT_ID, TBJ_ATJ_OBSERVACION, VERSION,
		USUARIOCREAR, FECHACREAR, USUARIOMODIFICAR, FECHAMODIFICAR, USUARIOBORRAR, FECHABORRAR, BORRADO) 
		SELECT S_TBJ_ATJ_AGENDA_TRABAJO.NEXTVAL, OBS.TBJ_ID, OBS.USU_ID, OBS.TBO_FECHA, 1, NVL(TO_CLOB(OBS.TBO_OBSERVACION), ''-''), OBS.VERSION,
		OBS.USUARIOCREAR, OBS.FECHACREAR, OBS.USUARIOMODIFICAR, OBS.FECHAMODIFICAR, OBS.USUARIOBORRAR, OBS.FECHABORRAR, OBS.BORRADO 
		FROM '||V_ESQUEMA||'.TBJ_ATJ_AGENDA_TRABAJO ATJ
		INNER JOIN '||V_ESQUEMA||'.ACT_TBO_TRABAJO_OBS OBS on ATJ.TBJ_ID = OBS.TBJ_ID
		WHERE ATJ.TBJ_ATJ_FECHA > TO_DATE(''15/01/2021'', ''DD/MM/YYYY'') AND ATJ.TBJ_ATJ_ID < 2328
		';

	EXECUTE IMMEDIATE V_MSQL;
    
	DBMS_OUTPUT.PUT_LINE('[INFO] SE HAN INSERTADO '||SQL%ROWCOUNT||' REGISTROS');   

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN] REGISTROS MIGRADOS DE ACT_TBO_TRABAJO_OBS A TBJ_ATJ_AGENDA_TRABAJO');
 
