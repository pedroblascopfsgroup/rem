--Para permitir la visualización de texto en un bloque PL/SQL utilizando DBMS_OUTPUT.PUT_LINE

WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON;
SET DEFINE OFF;


DECLARE
	V_MSQL VARCHAR2(4000 CHAR); -- Vble. para consulta que valida la existencia de una tabla.
 	V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema
 	V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master
	V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-8971'; -- USUARIO CREAR/MODIFICAR
	V_COUNT NUMBER(16); -- Vble. para comprobar
	err_num NUMBER; -- Numero de errores
	err_msg VARCHAR2(2048); -- Mensaje de error
	
BEGIN
	DBMS_OUTPUT.PUT_LINE('[INICIO] ');

	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_TBJ_TRABAJO T1 USING (
					SELECT DISTINCT TBJ.TBJ_ID, AUX.FECHA_TOPE FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO TBJ
					INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8971 AUX ON AUX.TBJ_NUM_TRABAJO = TBJ.TBJ_NUM_TRABAJO) T2
				ON (T1.TBJ_ID = T2.TBJ_ID)
				WHEN MATCHED THEN UPDATE SET
				TBJ_FECHA_TOPE = T2.FECHA_TOPE,
				USUARIOMODIFICAR = '''||V_USUARIO||''',
				FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN ACT_TBJ_TRABAJO PONIENDO FECHA TOPE');

	V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.ACT_TBJ_TRABAJO T1 USING (
					SELECT DISTINCT TBJ.TBJ_ID, AUX.FECHA_TOPE, AUX.FECHA_CONCRETA FROM '||V_ESQUEMA||'.ACT_TBJ_TRABAJO TBJ
					INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8971_2 AUX ON AUX.TBJ_NUM_TRABAJO = TBJ.TBJ_NUM_TRABAJO) T2
				ON (T1.TBJ_ID = T2.TBJ_ID)
				WHEN MATCHED THEN UPDATE SET
				TBJ_FECHA_TOPE = T2.FECHA_TOPE,
				TBJ_FECHA_HORA_CONCRETA = TO_TIMESTAMP(CONCAT(T2.FECHA_CONCRETA, '' 08:00:00''), ''DD/MM/YYYY HH:MI:SS''),
				USUARIOMODIFICAR = '''||V_USUARIO||''',
				FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS  '|| SQL%ROWCOUNT ||' REGISTROS EN ACT_TBJ_TRABAJO PONIENDO FECHA TOPE Y FECHA CONCRETA');

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN] ');
	
	EXCEPTION
	
	    WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('[ERROR] Se ha producido un error en la ejecucion:'||TO_CHAR(SQLCODE));
		DBMS_OUTPUT.PUT_LINE('-----------------------------------------------------------');
		DBMS_OUTPUT.PUT_LINE(SQLERRM);
		DBMS_OUTPUT.PUT_LINE(V_MSQL);
		ROLLBACK;
		RAISE;
END;
/
EXIT;