WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF; 

DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar   
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquemas
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master 
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_USUARIO VARCHAR2(50 CHAR) := 'REMVIP-8813';
	V_COUNT NUMBER(16); -- Vble. para comprobar
    V_ID_CONTABILIZADO NUMBER(16);
    V_ID_PAGADO NUMBER(16);

BEGIN
	DBMS_OUTPUT.PUT_LINE('[INICIO]');

    V_MSQL := 'SELECT DD_EGA_ID FROM '||V_ESQUEMA||'.DD_EGA_ESTADOS_GASTO WHERE DD_EGA_CODIGO = ''05''';
    EXECUTE IMMEDIATE V_MSQL INTO V_ID_PAGADO;

    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GDE_GASTOS_DETALLE_ECONOMICO T1 USING (
					SELECT DISTINCT GDE.GDE_ID, AUX.FECHA_PAGO FROM '||V_ESQUEMA||'.GPV_GASTOS_PROVEEDOR GPV
                    INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8813 AUX ON GPV.GPV_NUM_GASTO_HAYA = AUX.GPV_NUM_GASTO_HAYA
                    INNER JOIN '||V_ESQUEMA||'.GDE_GASTOS_DETALLE_ECONOMICO GDE ON GPV.GPV_ID = GDE.GPV_ID
                    WHERE GPV.BORRADO = 0 AND GPV.DD_EGA_ID = '||V_ID_PAGADO||' AND AUX.FECHA_PAGO LIKE ''%/19'') T2
				ON (T1.GDE_ID = T2.GDE_ID)
				WHEN MATCHED THEN UPDATE SET
				T1.GDE_FECHA_PAGO = T2.FECHA_PAGO,
				T1.USUARIOMODIFICAR = '''||V_USUARIO||''',
				T1.FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS  '|| SQL%ROWCOUNT ||' REGISTROS DE 2019 EN GDE_GASTOS_DETALLE_ECONOMICO');  
            
    COMMIT;

    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GDE_GASTOS_DETALLE_ECONOMICO T1 USING (
					SELECT DISTINCT GDE.GDE_ID, AUX.FECHA_PAGO FROM '||V_ESQUEMA||'.GPV_GASTOS_PROVEEDOR GPV
                    INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8813 AUX ON GPV.GPV_NUM_GASTO_HAYA = AUX.GPV_NUM_GASTO_HAYA
                    INNER JOIN '||V_ESQUEMA||'.GDE_GASTOS_DETALLE_ECONOMICO GDE ON GPV.GPV_ID = GDE.GPV_ID
                    WHERE GPV.BORRADO = 0 AND GPV.DD_EGA_ID = '||V_ID_PAGADO||' AND AUX.FECHA_PAGO LIKE ''%/20'') T2
				ON (T1.GDE_ID = T2.GDE_ID)
				WHEN MATCHED THEN UPDATE SET
				T1.GDE_FECHA_PAGO = T2.FECHA_PAGO,
				T1.USUARIOMODIFICAR = '''||V_USUARIO||''',
				T1.FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS  '|| SQL%ROWCOUNT ||' REGISTROS DE 2020 EN GDE_GASTOS_DETALLE_ECONOMICO');  
            
    COMMIT;

    V_MSQL := 'MERGE INTO '||V_ESQUEMA||'.GDE_GASTOS_DETALLE_ECONOMICO T1 USING (
					SELECT DISTINCT GDE.GDE_ID, AUX.FECHA_PAGO FROM '||V_ESQUEMA||'.GPV_GASTOS_PROVEEDOR GPV
                    INNER JOIN '||V_ESQUEMA||'.AUX_REMVIP_8813 AUX ON GPV.GPV_NUM_GASTO_HAYA = AUX.GPV_NUM_GASTO_HAYA
                    INNER JOIN '||V_ESQUEMA||'.GDE_GASTOS_DETALLE_ECONOMICO GDE ON GPV.GPV_ID = GDE.GPV_ID
                    WHERE GPV.BORRADO = 0 AND GPV.DD_EGA_ID = '||V_ID_PAGADO||' AND AUX.FECHA_PAGO LIKE ''%/21'') T2
				ON (T1.GDE_ID = T2.GDE_ID)
				WHEN MATCHED THEN UPDATE SET
				T1.GDE_FECHA_PAGO = T2.FECHA_PAGO,
				T1.USUARIOMODIFICAR = '''||V_USUARIO||''',
				T1.FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS  '|| SQL%ROWCOUNT ||' REGISTROS DE 2021 EN GDE_GASTOS_DETALLE_ECONOMICO');  
            
    COMMIT;

    DBMS_OUTPUT.PUT_LINE('[FIN]');
 
EXCEPTION
     WHEN OTHERS THEN
          ERR_NUM := SQLCODE;
          ERR_MSG := SQLERRM;
          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(ERR_NUM));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(ERR_MSG);
          ROLLBACK;
          RAISE;   
END;
