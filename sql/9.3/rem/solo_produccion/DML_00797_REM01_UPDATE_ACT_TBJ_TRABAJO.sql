WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar.
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema.
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_TEXT_TABLA VARCHAR2(27 CHAR) := 'ACT_TBJ_TRABAJO'; -- Vble. auxiliar para almacenar el nombre de la tabla de ref.
	V_TEXT_TABLA_AUX VARCHAR2(27 CHAR) := 'AUX_REMVIP_9427';
    V_USU VARCHAR2(30 CHAR) := 'REMVIP-9427'; -- Vble. auxiliar para almacenar el nombre de usuario que modifica los registros.
	V_COUNT NUMBER(16);
    
BEGIN		

	DBMS_OUTPUT.PUT_LINE('[INICIO]');

	--PREPARAR AREA PETICIONARIA DE LOS TRABAJOS

	V_MSQL:= 'UPDATE '||V_ESQUEMA||'.'||V_TEXT_TABLA_AUX||' SET AREA_PETICIONARIA = 2
						WHERE AREA_PETICIONARIA = ''REAM-Mantenimiento''';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS '|| SQL%ROWCOUNT ||' REGISTROS');

	V_MSQL:= 'UPDATE '||V_ESQUEMA||'.'||V_TEXT_TABLA_AUX||' SET AREA_PETICIONARIA = 4
						WHERE AREA_PETICIONARIA = ''REAM-Seguridad''';
	EXECUTE IMMEDIATE V_MSQL;

	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS '|| SQL%ROWCOUNT ||' REGISTROS');

	--MERGE PARA ACTUALIZAR AREA PETICIONARIA (LA TENGAN O NO)
	--MERGE PARA ACTUALIZAR ESTADO A VALIDADO
	--MERGE PARA PONER FECHA VALIDACION Y CAMBIO ESTADO A FECHA VALIDACION INDICADA (LA TENGAN O NO)

	V_MSQL:= 'MERGE INTO '||V_ESQUEMA||'.'||V_TEXT_TABLA||' T1 USING (
					SELECT DISTINCT TBJ.TBJ_ID, AUX.AREA_PETICIONARIA, AUX.FECHA_VALIDACION FROM '||V_ESQUEMA||'.'||V_TEXT_TABLA||' TBJ
					JOIN '||V_ESQUEMA||'.'||V_TEXT_TABLA_AUX||' AUX ON AUX.TBJ_NUM_TRABAJO = TBJ.TBJ_NUM_TRABAJO
					AND TBJ.BORRADO = 0 
				) T2
				ON (T1.TBJ_ID = T2.TBJ_ID)
				WHEN MATCHED THEN UPDATE SET
				T1.DD_IRE_ID = T2.AREA_PETICIONARIA,
				T1.DD_EST_ID = 23,
				T1.TBJ_FECHA_VALIDACION = TO_DATE(T2.FECHA_VALIDACION,''DD/MM/YYYY''),
				T1.TBJ_FECHA_CAMBIO_ESTADO = TO_DATE(T2.FECHA_VALIDACION,''DD/MM/YYYY''),
				USUARIOMODIFICAR = '''||V_USU||''',
				FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] MODIFICADOS '|| SQL%ROWCOUNT ||' REGISTROS EN '||V_TEXT_TABLA);

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN]');


EXCEPTION
		WHEN OTHERS THEN
			err_num := SQLCODE;
			err_msg := SQLERRM;

			DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
			DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
			DBMS_OUTPUT.put_line(err_msg);

			ROLLBACK;
			RAISE;          

END;

/

EXIT