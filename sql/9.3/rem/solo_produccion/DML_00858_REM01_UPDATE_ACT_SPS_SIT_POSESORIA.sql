WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar.
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema.
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_USU VARCHAR2(30 CHAR) := 'REMVIP-9742_2'; -- Vble. auxiliar para almacenar el nombre de usuario que modifica los registros.
	V_COUNT NUMBER(16);
    
BEGIN		

	DBMS_OUTPUT.PUT_LINE('[INICIO]');

	V_MSQL:= 'MERGE INTO '||V_ESQUEMA||'.ACT_SPS_SIT_POSESORIA T1 USING (
					SELECT DISTINCT ACT.ACT_ID, AUX.SPS_ALARMA, AUX.SPS_FECHA_INSTALA_ALARMA, AUX.SPS_FECHA_DESINSTALA_ALARMA, AUX.SPS_VIGILANCIA, AUX.SPS_FECHA_INSTALA_VIGILANCIA, AUX.SPS_FECHA_DESINSTALA_VIGIL
					FROM '||V_ESQUEMA||'.AUX_REMVIP_9742_2 AUX
					INNER JOIN '||V_ESQUEMA||'.ACT_ACTIVO ACT ON AUX.ACT_NUM_ACTIVO = ACT.ACT_NUM_ACTIVO) T2
				ON (T1.ACT_ID = T2.ACT_ID)
				WHEN MATCHED THEN UPDATE SET
					SPS_ALARMA = T2.SPS_ALARMA,
					SPS_FECHA_INSTALA_ALARMA = T2.SPS_FECHA_INSTALA_ALARMA,
					SPS_FECHA_DESINSTALA_ALARMA = T2.SPS_FECHA_DESINSTALA_ALARMA,
					SPS_VIGILANCIA = T2.SPS_VIGILANCIA,
					SPS_FECHA_INSTALA_VIGILANCIA = T2.SPS_FECHA_INSTALA_VIGILANCIA,
					SPS_FECHA_DESINSTALA_VIGILANCIA = T2.SPS_FECHA_DESINSTALA_VIGIL,
					T1.USUARIOMODIFICAR = '''||V_USU||''',
					T1.FECHAMODIFICAR = SYSDATE';
	EXECUTE IMMEDIATE V_MSQL;
	
	DBMS_OUTPUT.PUT_LINE('[INFO] ACTUALIZADAS '||SQL%ROWCOUNT||' SITUACIONES POSESORIAS');

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN]');

EXCEPTION
		WHEN OTHERS THEN
			err_num := SQLCODE;
			err_msg := SQLERRM;

			DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
			DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
			DBMS_OUTPUT.put_line(err_msg);

			ROLLBACK;
			RAISE;          

END;

/

EXIT
