		DBMS_OUTPUT.PUT_LINE('[INFO]: YA EXISTE REGISTRO EN '||V_TEXT_TABLA||' PARA PROVEEDOR PVE_COD_REM -> '||V_TMP_TIPO_DATA(4)||' EN CÓDIGO PROVINCIA -> '||V_TMP_TIPO_DATA(5)||'');

	END IF;
        
	END LOOP;

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('[FIN]: TABLA '||V_TEXT_TABLA||' ACTUALIZADA CORRECTAMENTE ');

EXCEPTION
     WHEN OTHERS THEN 
          err_num := SQLCODE;
          err_msg := SQLERRM;

          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejECVción:'||TO_CHAR(err_num));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(err_msg);

          ROLLBACK;
          RAISE;          

END;

