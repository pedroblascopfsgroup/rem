WHENEVER SQLERROR EXIT SQL.SQLCODE;
SET SERVEROUTPUT ON; 
SET DEFINE OFF;


DECLARE

    V_MSQL VARCHAR2(32000 CHAR); -- Sentencia a ejecutar.
    V_ESQUEMA VARCHAR2(25 CHAR):= '#ESQUEMA#'; -- Configuracion Esquema.
    V_ESQUEMA_M VARCHAR2(25 CHAR):= '#ESQUEMA_MASTER#'; -- Configuracion Esquema Master.
    ERR_NUM NUMBER(25);  -- Vble. auxiliar para registrar errores en el script.
    ERR_MSG VARCHAR2(1024 CHAR); -- Vble. auxiliar para registrar errores en el script.
    V_USU VARCHAR2(30 CHAR) := 'REMVIP-100094'; -- Vble. auxiliar para almacenar el nombre de usuario que modifica los registros.
	V_COUNT NUMBER(16);
	V_SCR_ID NUMBER(16);
	V_PRO_ID NUMBER(16);
	V_STG_ID NUMBER(16);

	TYPE T_TIPO_DATA IS TABLE OF VARCHAR2(150);
    TYPE T_ARRAY_DATA IS TABLE OF T_TIPO_DATA;
    V_TIPO_DATA T_ARRAY_DATA := T_ARRAY_DATA(
		T_TIPO_DATA('138','10101010','6291100000'),
		T_TIPO_DATA('138','101010','6291100000'),
		T_TIPO_DATA('151','6060','6295000'),
		T_TIPO_DATA('152','6060','6295000'),
		T_TIPO_DATA('151','6061','6295000'),
		T_TIPO_DATA('152','6061','6295000'),
		T_TIPO_DATA('151','6062','6295000'),
		T_TIPO_DATA('152','6062','6295000'),
		T_TIPO_DATA('151','6063','6295000'),
		T_TIPO_DATA('152','6063','6295000'),
		T_TIPO_DATA('151','6064','6295000'),
		T_TIPO_DATA('152','6064','6295000')

		); 
    V_TMP_TIPO_DATA T_TIPO_DATA;
    
BEGIN		

	DBMS_OUTPUT.PUT_LINE('[INICIO]');

	V_MSQL:= 'SELECT DD_STG_ID FROM '||V_ESQUEMA||'.DD_STG_SUBTIPOS_GASTO WHERE DD_STG_CODIGO = ''82''';
	EXECUTE IMMEDIATE V_MSQL INTO V_STG_ID;
	
	FOR I IN V_TIPO_DATA.FIRST .. V_TIPO_DATA.LAST
	LOOP
	V_TMP_TIPO_DATA := V_TIPO_DATA(I);

		V_MSQL:= 'SELECT DD_SCR_ID FROM '||V_ESQUEMA||'.DD_SCR_SUBCARTERA WHERE DD_SCR_CODIGO = '||V_TMP_TIPO_DATA(1)||'';
		EXECUTE IMMEDIATE V_MSQL INTO V_SCR_ID;

		V_MSQL:= 'SELECT PRO_ID FROM '||V_ESQUEMA||'.ACT_PRO_PROPIETARIO WHERE PRO_CODIGO_UVEM = '||V_TMP_TIPO_DATA(2)||'';
		EXECUTE IMMEDIATE V_MSQL INTO V_PRO_ID;

		V_MSQL:= 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.ACT_CONFIG_PTDAS_PREP WHERE DD_STG_ID = '||V_STG_ID||' AND DD_SCR_ID = '||V_SCR_ID||' AND PRO_ID = '||V_PRO_ID||' AND EJE_ID IN (70, 71, 72)';
		EXECUTE IMMEDIATE V_MSQL INTO V_COUNT;

		IF V_COUNT > 1 THEN

			V_MSQL:= 'UPDATE '||V_ESQUEMA||'.ACT_CONFIG_PTDAS_PREP SET
				CPP_PARTIDA_PRESUPUESTARIA = ''PP092'',
				USUARIOMODIFICAR = '''||V_USU||''',
				FECHAMODIFICAR = SYSDATE
				WHERE DD_STG_ID = '||V_STG_ID||' AND DD_SCR_ID = '||V_SCR_ID||' AND PRO_ID = '||V_PRO_ID||' AND EJE_ID IN (70, 71, 72)';
			EXECUTE IMMEDIATE V_MSQL;

			DBMS_OUTPUT.PUT_LINE('[INFO]: PARTIDA PRESUPUESTARIA PARA PROPIETARIO '||V_TMP_TIPO_DATA(2)||' Y SUBCARTERA '||V_TMP_TIPO_DATA(1)||' DE SUBTIPO COLOCACIÓN PUERTA ANTIOCUPA ACTUALIZADA');

		ELSE

			DBMS_OUTPUT.PUT_LINE('[INFO]: PARTIDA PRESUPUESTARIA PARA PROPIETARIO '||V_TMP_TIPO_DATA(2)||' Y SUBCARTERA '||V_TMP_TIPO_DATA(1)||' DE SUBTIPO COLOCACIÓN PUERTA ANTIOCUPA NO EXISTE');

		END IF;

		V_MSQL:= 'SELECT COUNT(1) FROM '||V_ESQUEMA||'.ACT_CONFIG_CTAS_CONTABLES WHERE DD_STG_ID = '||V_STG_ID||' AND DD_SCR_ID = '||V_SCR_ID||' AND PRO_ID = '||V_PRO_ID||' AND EJE_ID IN (70, 71, 72)';
		EXECUTE IMMEDIATE V_MSQL INTO V_COUNT;

		IF V_COUNT > 1 THEN

			V_MSQL:= 'UPDATE '||V_ESQUEMA||'.ACT_CONFIG_CTAS_CONTABLES SET
				CCC_CUENTA_CONTABLE = '''||V_TMP_TIPO_DATA(3)||''',
				USUARIOMODIFICAR = '''||V_USU||''',
				FECHAMODIFICAR = SYSDATE
				WHERE DD_STG_ID = '||V_STG_ID||' AND DD_SCR_ID = '||V_SCR_ID||' AND PRO_ID = '||V_PRO_ID||' AND EJE_ID IN (70, 71, 72)';
			EXECUTE IMMEDIATE V_MSQL;

			DBMS_OUTPUT.PUT_LINE('[INFO]: CUENTA CONTABLE PARA PROPIETARIO '||V_TMP_TIPO_DATA(2)||' Y SUBCARTERA '||V_TMP_TIPO_DATA(1)||' DE SUBTIPO COLOCACIÓN PUERTA ANTIOCUPA ACTUALIZADA');

		ELSE

			DBMS_OUTPUT.PUT_LINE('[INFO]: CUENTA CONTABLE PARA PROPIETARIO '||V_TMP_TIPO_DATA(2)||' Y SUBCARTERA '||V_TMP_TIPO_DATA(1)||' DE SUBTIPO COLOCACIÓN PUERTA ANTIOCUPA NO EXISTE');

		END IF;

	END LOOP;

	COMMIT;

	DBMS_OUTPUT.PUT_LINE('[FIN]');


EXCEPTION
		WHEN OTHERS THEN
			err_num := SQLCODE;
			err_msg := SQLERRM;

			DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejecución:'||TO_CHAR(err_num));
			DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
			DBMS_OUTPUT.put_line(err_msg);

			ROLLBACK;
			RAISE;          

END;

