BEGIN

	 -- LOOP para insertar los valores --
    DBMS_OUTPUT.PUT_LINE('[INFO]: UPDATE EN ECO_EXPEDIENTE_COMERCIAL');
    FOR I IN V_TIPO_DATA.FIRST .. V_TIPO_DATA.LAST

    LOOP
	V_TMP_TIPO_DATA := V_TIPO_DATA(I);

		V_MSQL := 'SELECT COUNT(*) 
					FROM '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO
					INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_ID = ECO.OFR_ID
					WHERE OFR.OFR_NUM_OFERTA = '||V_TMP_TIPO_DATA(1)||' AND OFR.BORRADO =0';
		EXECUTE IMMEDIATE V_MSQL INTO V_COUNT;

		IF V_COUNT > 0 THEN	

			V_MSQL := 'SELECT ECO.ECO_ID 
						FROM '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL ECO
						INNER JOIN '||V_ESQUEMA||'.OFR_OFERTAS OFR ON OFR.OFR_ID = ECO.OFR_ID
						WHERE OFR.OFR_NUM_OFERTA = '||V_TMP_TIPO_DATA(1)||' AND OFR.BORRADO =0';
			EXECUTE IMMEDIATE V_MSQL INTO V_ID;	

			V_MSQL:= 'UPDATE '||V_ESQUEMA||'.ECO_EXPEDIENTE_COMERCIAL SET 
						DD_EEB_ID = (SELECT DD_EEB_ID FROM '||V_ESQUEMA||'.DD_EEB_ESTADO_EXPEDIENTE_BC WHERE DD_EEB_CODIGO_C4C = ''150'' AND BORRADO = 0),
						USUARIOMODIFICAR = '''||V_USU||''',
						FECHAMODIFICAR = SYSDATE
						WHERE ECO_ID = '||V_ID||'';
			EXECUTE IMMEDIATE V_MSQL;
			
			DBMS_OUTPUT.PUT_LINE('[INFO] EXPEDIENTE '||V_TMP_TIPO_DATA(1)||' MODIFICADO');

		ELSE 

			DBMS_OUTPUT.PUT_LINE('[INFO] NO EXISTE EL EXPEDIENTE '||V_TMP_TIPO_DATA(1)||'');

		END IF;
        
	END LOOP;

    COMMIT;

EXCEPTION
     WHEN OTHERS THEN 
          err_num := SQLCODE;
          err_msg := SQLERRM;

          DBMS_OUTPUT.put_line('[ERROR] Se ha producido un error en la ejECVción:'||TO_CHAR(err_num));
          DBMS_OUTPUT.put_line('-----------------------------------------------------------'); 
          DBMS_OUTPUT.put_line(err_msg);

          ROLLBACK;
          RAISE;          

