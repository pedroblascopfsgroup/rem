prompt Cambiando los NLS_PARAMETERS a AMERICAN

set linesize 300
set pagesize 50000

ALTER SYSTEM SET NLS_LANGUAGE=AMERICAN SCOPE=SPFILE;
ALTER SYSTEM SET NLS_TERRITORY=AMERICA SCOPE=SPFILE;
ALTER SYSTEM SET NLS_CURRENCY='$' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_ISO_CURRENCY=AMERICA SCOPE=SPFILE;
ALTER SYSTEM SET NLS_NUMERIC_CHARACTERS='.,' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_DATE_FORMAT='DD-MON-RR' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_DATE_LANGUAGE=AMERICAN SCOPE=SPFILE;
ALTER SYSTEM SET NLS_SORT=BINARY SCOPE=SPFILE;
ALTER SYSTEM SET NLS_TIME_FORMAT='HH.MI.SSXFF AM' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_TIMESTAMP_FORMAT='DD-MON-RR HH.MI.SSXFF AM' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_TIME_TZ_FORMAT='HH.MI.SSXFF AM TZR' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_TIMESTAMP_TZ_FORMAT='DD-MON-RR HH.MI.SSXFF AM TZR' SCOPE=SPFILE;
ALTER SYSTEM SET NLS_DUAL_CURRENCY='$' SCOPE=SPFILE;

SHUTDOWN IMMEDIATE;
STARTUP MOUNT;
ALTER SYSTEM ENABLE RESTRICTED SESSION;
ALTER SYSTEM SET JOB_QUEUE_PROCESSES=0;
ALTER SYSTEM SET AQ_TM_PROCESSES=0;
ALTER DATABASE OPEN; 

--NLS_CHARACTERSET
ALTER DATABASE CHARACTER SET AL32UTF8;

--NLS_NCHAR_CHARACTERSET
ALTER DATABASE NATIONAL CHARACTER SET INTERNAL_USE AL16UTF16;

prompt Reiniciando la BD

shutdown immediate;

startup;

exit;
