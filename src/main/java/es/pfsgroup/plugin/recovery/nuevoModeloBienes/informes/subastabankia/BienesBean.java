package es.pfsgroup.plugin.recovery.nuevoModeloBienes.informes.subastabankia;


public class BienesBean {

	String operacion;
	String activo;
	String finca;
	Float sinPostores;
	Float desdePostores;
	Float hastaPostores;
	
	
	public String getOperacion() {
		return operacion;
	}
	public void setOperacion(String operacion) {
		this.operacion = operacion;
	}	
	public String getActivo() {
		return activo;
	}
	public void setActivo(String activo) {
		this.activo = activo;
	}
	public String getFinca() {
		return finca;
	}
	public void setFinca(String finca) {
		this.finca = finca;
	}
	public Float getSinPostores() {
		return sinPostores;
	}
	public void setSinPostores(Float sinPostores) {
		this.sinPostores = sinPostores;
	}
	public Float getDesdePostores() {
		return desdePostores;
	}
	public void setDesdePostores(Float desdePostores) {
		this.desdePostores = desdePostores;
	}
	public Float getHastaPostores() {
		return hastaPostores;
	}
	public void setHastaPostores(Float hastaPostores) {
		this.hastaPostores = hastaPostores;
	}
	
	
}
