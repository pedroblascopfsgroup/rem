-- tabla de resoluciones
DROP SEQUENCE S_RES_RESOLUCIONES_MASIVO;
CREATE SEQUENCE S_RES_RESOLUCIONES_MASIVO;

DROP TABLE RES_RESOLUCIONES_MASIVO;
CREATE TABLE RES_RESOLUCIONES_MASIVO  (
   RES_ID		 			NUMBER(16) NOT NULL,
   RES_TJU_ID				NUMBER(16),
   RES_TRE_ID				NUMBER(16),
   RES_EPF_ID				NUMBER(16) NOT NULL,
   RES_NOMBRE_FICHERO		VARCHAR2(250 BYTE),
   RES_CONTENIDO_FICH		BLOB,
   RES_ASU_ID				NUMBER(16),
   RES_PLAZA				VARCHAR2(250 BYTE),
   RES_JUZGADO				VARCHAR2(250 BYTE),
   RES_AUTOS				VARCHAR2(250 BYTE),
   VERSION					INTEGER         DEFAULT 0  	NOT NULL,
   USUARIOCREAR				VARCHAR2(10 BYTE)   				NOT NULL,
   FECHACREAR				TIMESTAMP(6)        				NOT NULL,
   USUARIOMODIFICAR			VARCHAR2(10 BYTE),
   FECHAMODIFICAR			TIMESTAMP(6),
   USUARIOBORRAR			VARCHAR2(10 BYTE),
   FECHABORRAR				TIMESTAMP(6),
   BORRADO					NUMBER(1)       DEFAULT 0		NOT NULL,   
   CONSTRAINT PK_RES_RESOLUCIONES_MASIVO PRIMARY KEY (RES_ID),
   CONSTRAINT FK_RES_TJU_ID FOREIGN KEY (RES_TJU_ID) REFERENCES DD_TJ_TIPO_JUICIO (DD_TJ_ID),
   CONSTRAINT FK_RES_TRE_ID FOREIGN KEY (RES_TRE_ID) REFERENCES DD_TR_TIPOS_RESOLUCION (DD_TR_ID),
   CONSTRAINT FK_RES_EPF_ID FOREIGN KEY (RES_EPF_ID) REFERENCES DD_EPF_ESTADO_PROCES_FICH (DD_EPF_ID),
   CONSTRAINT FK_RES_ASU_ID FOREIGN KEY (RES_ASU_ID) REFERENCES ASU_ASUNTOS (ASU_ID)

);