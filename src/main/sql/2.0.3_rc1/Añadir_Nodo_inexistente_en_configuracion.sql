   SET bpm_tpi_node_exc =
          REPLACE (CONCAT (bpm_tpi_node_exc, ',NONEXISTENT'),
                   'NONE,NONEXISTENT', 'NONEXISTENT'
                  )
 WHERE NOT (bpm_dd_tin_id = (SELECT TIN.BPM_DD_TIN_ID FROM BPM_DD_TIN_TIPO_INPUT TIN WHERE TIN.BPM_DD_TIN_CODIGO = 'CONSIGNA_PARCIAL_CPROC')
            OR bpm_dd_tin_id = (SELECT TIN.BPM_DD_TIN_ID FROM BPM_DD_TIN_TIPO_INPUT TIN WHERE TIN.BPM_DD_TIN_CODIGO = 'CONSIGNA_PARCIAL_SPROC')
            OR bpm_dd_tin_id = (SELECT TIN.BPM_DD_TIN_ID FROM BPM_DD_TIN_TIPO_INPUT TIN WHERE TIN.BPM_DD_TIN_CODIGO = 'CONSIGNA_TOTAL_CPROC')
            OR bpm_dd_tin_id = (SELECT TIN.BPM_DD_TIN_ID FROM BPM_DD_TIN_TIPO_INPUT TIN WHERE TIN.BPM_DD_TIN_CODIGO = 'CONSIGNA_TOTAL_SPROC')
           )
   AND bpm_tpi_node_inc = 'ALL';