MERGE INTO TMP_REC_EXP_REPARTO_AGENCIAS REP
USING 
(SELECT * FROM TMP_REC_EXP_REPARTO_AGENCIAS_B
) R
ON (REP.EXP_ID = R.EXP_ID)
WHEN MATCHED THEN UPDATE
SET REP.RCF_SCA_ID = R.RCF_SCA_ID
  , REP.RCF_AGE_ID = R.RCF_AGE_ID
  , REP.CAR_ID = R.CAR_ID;

COMMIT;

DROP TABLE TMP_REC_EXP_REPARTO_AGENCIAS_B;

UPDATE CRE_CICLO_RECOBRO_EXP 
SET CRE_FECHA_BAJA = NULL
    , USUARIOMODIFICAR = NULL
    , FECHAMODIFICAR = NULL
    , DD_MOB_ID = NULL
WHERE CRE_FECHA_BAJA IS NOT NULL;


UPDATE CRC_CICLO_RECOBRO_CNT 
SET CRC_FECHA_BAJA = NULL
    , USUARIOMODIFICAR = NULL
    , FECHAMODIFICAR = NULL
    , DD_MOB_ID = NULL
WHERE CRC_FECHA_BAJA IS NOT NULL;

UPDATE CRP_CICLO_RECOBRO_PER 
SET CRP_FECHA_BAJA = NULL
    , USUARIOMODIFICAR = NULL
    , FECHAMODIFICAR = NULL
    , DD_MOB_ID = NULL
WHERE CRP_FECHA_BAJA IS NOT NULL;

DELETE FROM TMP_REC_EXP_ROTACION;

DELETE FROM TMP_REC_EXP_REARQUETIPADO;

COMMIT;